	</h1>
	
		<p>
		История Беларуси за последнюю 1000 лет кратко в интерактивном изложении.
		Статьи в разделах <a href="pages/times/_times_intro.php">Время</a>, <a href="pages/events/_events_intro.php">События</a>, 
		<a href="pages/figures/_figures_intro.php">Люди</a>, <a href="pages/terms/_terms_intro.php">Термины</a> иллюстрированы историческими картами и документами 

		<p>
		Последняя часть канала , которая должна была бы соединить новый торговый путь с курляндским портом Виндава (ныне Вентспилс), не была построена — с 1837 года стали строиться железные дороги, и от продолжения строительства отказались.
 		</p>
		<p>
		 Законченная часть канала сохранила значение внутреннего водного пути, служащего для коммерческого судоходства и транспортировки древесины из Вислы в Неман и обратно.
		</p>
		<p>
		Между мировыми войнами канал впервые стал туристическим объектом. Таковым он остается и сегодня. 
		Главную роль в пропаганде достоинств Августовского канала сыграла книга «Черная Ганьча», написанная в 1931 г. Вандой Милошевской. 
		Первыми туристами-байдарочниками были варшавские скауты.
		</p>
		

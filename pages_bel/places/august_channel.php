		<p>
		Августовский канал (от г. Августов, Польша)— судоходный канал в Польше и <a href="../terms/belarus.php">Беларуси</a>, соединяет реки Вислу и Неман, памятник гидротехнического зодчества, 
		расположен в особо охраняемой зоне ЮНЕСКО. 
		</p>
		<p>
		В свое время Канал был технологическим чудом. Он стал первым искусственным водным путём в Европе, 
		связывающим напрямую две большие реки — Вислу и Неман и обеспечивал связь с Чёрным морем на юге через <a href="#">Огинский канал</a>, 
		Днепр, Березинскую водную систему и Двину.
		</p>
		<p>
		Его длина 101,2 км, в том числе 22 км на территории Беларуси, около 79 км на территории Польши. Включает ряд шлюзов и разводных мостов. 
		 В мире существует только два аналога этого водного пути: в Швеции — Gota и в Великобритании — Kalendonski.
		</p>
		<p>
		Канал был построен в 1824-39 по политико-экономическим причинам. В 1821 году Пруссия в одностороннем порядке ввела запрет на транзит 
		товаров через свою территорию, практически заблокировав доступ к морю торговцам. Появилась острая необходимость транспортного коридора 
		в обход прусской территории из центра Польского царства (в составе Российской Империи) к портам в Курляндии. 
		В 1824 году проект канала общей длиной в 395 км был представлен императору Александру I.
		</p>
		</p>

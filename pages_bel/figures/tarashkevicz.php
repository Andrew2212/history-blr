		<p>
		Бронислав Адамович Тарашкевич (20 января 1892 - 29 ноября 1938) — языковед, переводчик, публицист. Академик АН БССР.	
		</p>		
		<p>
		Наверно, лучше всего его характеризуют строки из устава ТБШ — "расширять и помогать просвещению в белорусском национальном, 
		человеческом и христианском духе". 
		Подготовил и издал в Вильне (1918) в типографии Мартина Кухты первый учебник "Белорусская грамматика для школ" (латиницей и кириллицей), 
		  заложив основы норм современного литературного языка.
		Стремился к объединению страны и народа, разорванного Рижским договором 1921 года между СССР и Польшей.	
		</p>		
		<p>
		Основатель "Таварыства беларускай школы" ТБШ (1921-1936) — культурно-просветительской организации в Западной Беларуси. Насчитывала около 30 тыс. членов. 
		Перевел на белорусский язык "Илиаду" Гомера и поэму "Пан Тадеуш" А. Мицкевича.
		</p>		
		<p>
		Окончил с серебряной медалью Виленскую гимназию. Историк-филолог (Петербургский университет) и 
		приват-доцент древнегреческого и латинского языков. Был одним из лидеров Беларускай Сацыялістычнай Грамады 
		— первой белорусской национальной политической партии (Вильно, Минск, Петербург). 
		Преподавал белорусский и греческий языки в Минском педагогическом институте, был членом масонских лож в Вильно и Варшаве.
		Возглавлял белорусский сектор департамента просвещения Срединной Литвы.
		</p>						
		
		<p>
		После раздела белорусских земель по Рижскому договору между СССР и Польшей (1921) жил в Западной Беларуси. 
		</br>Был членом Центрального белорусского избиркома, директором Виленской белорусской гимназии, депутатом польского сейма, Председателем Белорусского посольского клуба.
		</p>
		

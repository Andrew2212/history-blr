		<h4>Обмен на границе: Тарашкевич-Алехнович</h4>	
		<p>
		6 сентября 1933 происходит обмен на советско-польской границе: Франтишек Олехнович возвращается из советской тюрьмы в Западную Беларусь, 
		а поляки передают советам Бронислава Тарашкевича.
		</p>		
		<p>
		Бронислав Тарашкевич после обмена работал заведующим отдела Польши и Прибалтики в Международном аграрном институте.
		</br>В 1937 году арестован, в январе 1938 года приговорён к высшей мере наказания; 29 ноября 1938 года — расстрелян.
		</p>
		

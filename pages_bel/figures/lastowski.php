			Вацлав Устинович Ластовский (8 ноября 1883 - 23 января 1938) — белорусский писатель, общественный и политический деятель, 
			академик НАН РБ, историк, филолог, Премьер-Министр <a href="../terms/bnr.php">Белорусской Народной Республики</a>, директор 
			Национального Исторического Музея РБ. 
		</p>
		<p>
			В 1930 обвинён по делу о "Союзе за освобождение Белоруссии", приговорён к высылке. 
			</br>В 1938 расстрелян.		
		</p>
		<p>
		Был одним из создателей <a href="../terms/blr_museum.php">Белорусского музея</a> имени И. Луцкевича в Вильно.
		</br>Во время своего первого визита в Вильну <a href="bogdanowich.php">Максим Богданович</a> остановился у Ластовского, 
		и тот показал ему свое собрание белорусских артефактов. Поэт воскликнул:
		</br><i>— Гэта ёсць фундамент нашага адраджэння! Гэта i за тысячу гадоў будзе сведчыць аб нас!</i>
		</p>
		<p>
		[ <small>Даже в годы Первой мировой войны, в частности в 1916 году, в редактируемой Ластовским газете "Гоман" можно было увидеть объявления: 
		<i>"Купляю ўсякiе старасьвецкiе рэчы: абразы, гравюры, мiнiатюры, рукапiсы i кнiжкi XV i XVI в.; порцэляну, шкло, тканiны. В. Ластоўскi. 
		Завальная вулiца № 7. "Беларуская кнiгарня"</i>.
		</small> ]
		</p>
		<p>
		С 1922 был среди первых работников Института белорусской культуры (ИнБелКульт), который в 1928 году был преобразован в Белорусскую Академию Наук.
		</p>
		<p>
		В 1927-30 работал директором Белорусского государственного музея (ныне Национальный Исторический). Благодаря усилиям Ластовского была найдена и перевезена в Минск национальная святыня белорусов 
		— <a href="../terms/efrosinia_rood.php">крест Евфросинии</a> Полоцкой. С 1928 года Вацлав Ластовский — академик и секретарь Белорусской АН.
		</p>
		<p>
		В. Ластовский выступал против советской власти и польской оккупации за независимую и целостную <a href="../terms/belarus.php">Беларусь</a>.
		</p>
		<p>
		У 1916 — Ластоўскi адзін з аўтараў "Мемарандума прадстаўнікоў Беларусі", у якім адстойваў права беларускага народа на нацыянальна-дзяржаўнае развіццё 
		і які быў прадстаўлены на міжнароднай канферэнцыі ў Лазане (Швейцарыя).

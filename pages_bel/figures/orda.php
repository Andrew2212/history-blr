		<p>
		Наполеон Матеуш Тадеуш Орда (11 февраля 1807 - 26 апреля 1883) — 
		беларуский и польский литератор и композитор, музыкант, художник.
		</p>
		<p>
		Происходил из <a href="../terms/shliahta.php">Пинской шляхты</a>, родился в родовом имении Вороцевичи Минской губернии. 
		При крещении получил имя Наполеон, модное в то время — уже который год французский император вел победоносные войны. 
		Белорусская шляхта надеялась, что после победы над Российской Империей Бонапарт возродит <a href="../terms/wkl.php">ВКЛ</a>.
		</p>
		<p>
		То, что сделал этот человек для <a href="../terms/belarus.php">Беларуси</a>, иначе как подвигом не назовешь. Несколько тысяч гравюр, с документальной точностью показывающих 
		состояние памятников истории — именно по его гравюрам они реставрируются сегодня. К сожалению, двух третей объектов, изображенных на рисунках, уже не существует.
		</p>
		<p>
		Окончил гимназию в Свислочи (позже там будет учиться <a href="../figures/kalinowski.php">К. Калиновский</a>). В 1823 году поступил в Виленский университет, где изучал математику. 
		Был арестован за участие в деятельности нелегального студенческого общества "Зоряне". 
		</p>
		<p>
		Наполеон Орда принял активное участие в <a href="../events/uprising_1830.php">восстании 1830-1831 гг</a>. 
		После его подавления, опасаясь репрессий, был вынужден под чужим именем выехать за границу и обосноваться в Париже.
		 </p>
		<p>
		В это время определяются его приоритеты в творчестве - занятия музыкой и живописью. Наполеон Орда познакомился с выдающимися деятелями европейской культуры — 
		<a href="../figures/mickiewicz.php">А. Мицкевичем</a>, Ф. Шопеном, Ф. Листом, Дж. Россини, Дж. Верди, Ж. Гуно, Г. Берлиозом, О. Бальзаком, А. Стендалем, П. Виардо, И. Тургеневым.
		</p>
		<p>
		1856 После объявления российским императором Александром ІІ амнистии эмигрантам Н. Орда оставил семью, обеспеченную жизнь и успешную карьеру в Париже 
		и в 49-летнем возрасте вернулся на родину.
		</p>
		<p>
		1866  Арестован по подозрению в участии в <a href="../events/uprising_1863.php">восстании 1863 года</a> и посажен в Кобринский тюремный замок. Приговорен к высылке в отдаленные губернии России. 
		Благодаря стараниям жены и помощи посла Франции приговор отменили.
		</p>
		

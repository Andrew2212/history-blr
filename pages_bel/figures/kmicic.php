		<p>
		Военную службу Самуил Кмитич начал поручиком в 1654 году — в самом начале <a href="../events/blood_potop.php">"Кровавого Потопа"</a>. 
		Из шляхты Полоцкого воеводства, Витебского и Оршанского поветов создал хоругвь, которая вошла в полк князя 

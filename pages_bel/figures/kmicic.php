		Возглавлял полк в дивизии Павла <a href="../figures/sapegi.php">Сапеги</a>, принимал участие в битвах со шведами.
		</p>
		<p>
		В сентябре 1658 году во главе 12 хоругвей воевал в тылу московских войск в белорусском Поднепровье. 
		Во главе полка воевал со шведами в 1659 в Курляндии, в 1660 — в Подляшье. Самуил Кмитич был ярым противником короля Яна II Казимира, 
		принимал активное участие в бунте войск в интересах гетмана Сапеги.
		</p>
		<p>
		В апреле 1660 года дивизия Сапеги объявила конфедерацию и назначила Кмитича "генеральным полковником". 
		Участвовал в <a href="../events/battle_polonka.php">битве под Полонкой</a>, позднее участвовал в действиях против московских войск на востоке ВКЛ. 
		В 1663 осуществил глубокий рейд за <a href="../places/smolensk.php">Смоленск</a>. В 1664 участвовал в походе на Рославль и Брянск.
		</p>
		

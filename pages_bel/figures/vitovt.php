		<h4>Хронология событий</h4>
		<p>
		Отец Витовта Кейстут и его дядя Ольгерд правили совместно и не боролись за власть между собой. 
		Ольгерд был великим князем и занимался восточными и южными делами, Кейстут вёл борьбу с тевтонскими рыцарями на северо-западе.
		</p>
		<p>
		1377 Умирает в. кн. Ольгерд. Новым великим князем становится его сын  Ягайло. 
		</p>
		<p>
		1381-84 Первый этап <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> за власть в ВКЛ — Витовт и Кейстут vs Ягайло. 
		Кейстут, ненадолго взявший власть (1381-82), погибает в плену в <a href="../places/krevo.php">Кревском замке</a>. Ягайло остается в. князем Литовским.
		</p>
		<p>
		1385 <a href="../events/unia_krewo.php">Кревская уния</a> — Ягайло, оставаясь в. внязем Литовским, становится королем Польши. 
		Витовт становится наместником Ягайло в Литве и стремится стать независимым от Короны князем ВКЛ.
		</p>
		<p>
		1389-92 Второй этап <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> — Витовт vs Ягайло.
		</p>
		<p>
		1392 Островское соглашение — итог Гражданской войны. Витовт признан Великим князем Литовским.
		</p>
		<p>
		1392 Гродненский замок становится одной из главных резиденций Витовта, который в 1398 году вместо деревянной крепости построил каменный готический замок с пятью башнями. Там же он впоследствии будет собирать войска 
		перед Грюнвальдской битвой.
		</p>
		<p>
		1398 Витовт принимает к себе хана Тохтамыша — потомка Чингизхана, потерпевего поражение в борьбе за власть в Орде — и расселяет в ВКЛ его войско.
		</p>
		<p>
		1399 <a href="../events/battle_worskla.php">Битва на Ворскле</a> — поражение Витовта от войск Золотой Орды. Конец экспансии ВКЛ на юго-восток. Ослабление влияния Витовта.
		</p>
		<p>
		1401 <a href="../events/unia_vilno.php">Виленско-Радомская уния</a> — Витовт, признанный пожизненным Великим князем правителем ВКЛ, приносит присягу верности Ягайло.
		</p>
		<p>
		1410 <a href="../events/grunwald.php">Битва под Грюнвальдом</a> — разгром Тевтонского Ордена. Как итог — в 1411 <a href="../places/samogitia.php">Самогития</a> была окончательно присоеденена к ВКЛ. 
		[ <small> Именно Витовт стал главным героем картины Я. Матейко "Битва под Грюнвальдом"</small> ] 
		</p>
		<p>
		1413 Городельская уния — введен наследственный институт великого князя литовского и положено начало 
		<a href="../download/blr_heraldic.php">современной белорусской геральдике</a>.
		</p>
		<p>
		1419 После смерти чешского короля Вацлава гуситы провозглашают Витовта королем.
		</p>
		<p>
		1421  Чешский сейм объявил Витовта утратившим чешский престол "из-за неявки".
		</p>
		<p>
		1422 Витовт отправляет на помощь гуситам пятитысячное литовское войско во главе с князем Жигимонтом Корибутовичем, которое вместе с гуситами 
		отразило четыре крестовых похода императорских войск.
		</p>
		<p>
		1429 Съезд в Луцке — 
		согласие о коронации Витовта. При участии короля Германии (Римского короля) и будущего императора Священной Римской империи Сигизмунда, Витовта, Ягайло, легата папы римского, князей рязанских, одоевских, новгородских, псковских, а также посланников великого князя московского и тверского князя, Тевтонского ордена, Золотой Орды, Княжества молдавского, датского короля, византийского императора.
		</p>
		<p>
		1430 Не дожив до коронации Витовт умирает. Новым Великим князем становится мятежный <a href="../figures/svidrigailo.php">Свидригайло</a>, сражавшийся за этот титул с Витовтом почти 40 лет.
		</p>
		

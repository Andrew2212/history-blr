		<p>
		Зигмунд Минейко (1840-1925) — родился  в деревне Болванишки (ныне Зеленый Бор) на Ошмянщине в <a href="../terms/shliahta.php">шляхетской</a> семье. 
		Учился в Виленской гимназии (вместе c Ф. Богушевичем), Инженерном училище в Санкт-Петербурге, в военной школе в Генуе, основанной знаменитым Гарибальди. 
		</p>
		<p>
		В 1863 году, узнав о готовящемся <a href="../events/uprising_1863.php">восстании</a>, приезжает в <a href="../places/wilno.php">Вильно</a> и, как человек, обучавшийся в военной школе, становится командиром отряда  
		повстанцев Ошмянского уезда.
		</p>
		<p>
		После подавления восстания приговорен к смертной казни. Через подкуп чиновника казнь была заменена каторгой. По дороге на каторгу в 
		Сибирь умирает его товарищ, и Зигмунд под его документами обосновался в Томске, откуда бежит, и в итоге оказывается во Франции, 
		где заканчивает Академию  генерального штаба французской армии.
		</p>
		<p>
		У него было обостренноре чувство справедливости. Так, после окончания академии ему предложили стать капитаном легиона и "усмирять" Алжир, 
		но Зигмунд отказался. Зато взял оружие в руки, чтобы защищать Францию от прусского нашествия. 
		</p>
		<p>
		В середине 70-х годов XIX века Минейко перебирается в Грецию и начинает раскопки легендарного храма Зевса в Додоне, 
		попытки найти который археологи предпринимали в течение двухсот лет. Инженерные знания позволили ему определить место, 
		где следовало искать храм. В результате — сенсационное археологическое открытие и мировая слава.
		</p>
		<p>
		Но археология оставалась только увлечением. В Греции Минейко трудится главным инженером провинций Эпир и Фессалия, 
		строит железные дороги и мосты, возглавляет Департамент публичных работ в Афинах. А когда  Греция готовилась принимать первые возрожденные 
		Олимпийские игры в 1896, Зигмунд Минейко возглавил реставрацию Панатинаикоса — древнегреческого Мраморного стадиона.
		</br>Минейко спроектировал и построил первую железную дорогу в Болгарии (тогда еще турецкой провинции), за что получил высший орден Турции.
		</p>
		<p>
		Его участие в Играх не ограничилось проектировкой амфитеатра. На них проявился еще один талант этого неординарного человека — публицистический. 
		Из Афин он писал бойкие и красочные репортажи сразу в несколько газет: краковский "Час", "Дзённік польскі", "Львовскую газету". 
		По сути, Зигмунд Минейко стал родоначальником спортивной журналистики в мире.
		</p>
		<p>
		 В 1910 году вопрос о вкладе Зигмунда в развитие экономики, культуры, создание вооруженных сил был внесен на рассмотрение греческого парламента. 
		 Редкий случай: в знак признания выдающихся заслуг перед страной парламент присвоил иноземцу звание почетного гражданина Греции.
		</p>
		<p>		
		Во время греко-турецкой войны 1912-13 проявил себя блестящим фортификатором. За штурм Салоников и крепости Янина Минейко 
		наградили Золотым крестом — самой высокой воинской наградой Греции.
		</p>

<p>
Грекулов Е. Ф. — Православная инквизиция в России
</br>Академия наук СССР. Научно-популярная серия.
</br>Издательство "Наука". М.: 1964 г.
</p>
<p>
В России еретиков судили по "градскому" закону, но это — по словам историка Н. М. Карамзина — было лишь одной "пристойностью". Хотя православная церковь 
не располагала таким инквизиционным аппаратом, какой имела католическая церковь, но в борьбе с ересями, прикрывавшими, как и на Западе, движение против феодального гнета,
 она применяла настоящие инквизиционные методы расправы - розыск и допрос под пытками и другие способы "познания истины". Она вела часто весь судебный процесс, 
 добиваясь угодного ей приговора и наказания. Православная церковь посылала на костры еретиков и ослушников собственной властью, светская же власть была лишь 
 исполнительницей ее требований и приговоров. Один из основателей православной инквизиции Иосиф Волоцкий провозгласил, что царь, не желавший бороться против ересей, не слуга божий, а дьявол...
</p>
<p>
Свою инквизиторскую деятельность православная церковь осуществляла через судебные органы, находившиеся в распоряжении епархиальных архиереев, через патриарший суд и церковные соборы. Она располагала и специальными органами, созданными для расследования дел против религии и церкви — Приказом духовных дел, Приказом инквизиторских дел, Раскольнической и Новокрещенской конторами и др. В духовном приказе рассматривались дела о богохульстве, еретичестве, волшебстве, святотатстве. Дела против веры и церкви вело также организованное при Синоде в 1721 г. тиунское управление. Приказ духовных дел наблюдал за "чистотой" православия, расправлялся с раскольниками и еретиками. Приказ инквизиторских дел вел следствие по делам "интересным" и "безгласным". Он имел большой штат инквизиторов как в Москве, так и на местах; возглавлял его протоинквизитор, архимандрит московского Данилова монастыря Пафнутий. В распоряжении Приказа инквизиторских дел были свои подьячие, своя охрана и собственная тюрьма. Настоящим инквизиторским застенком являлась и Раскольническая контора, существовавшая до 1764 г...
</p>
<p>
Особенно жестоко расправлялись соборы с церковными врагами в XIV-XVII вв…
</p>
<p>
Суровым и непримиримым гонителем был игумен Волоколамского монастыря Иосиф Санин (Волоцкий). Он был представителем воинствующей церкви, сторонником сильной светской власти, создателем теории божественного происхождения царской власти… Жестокие "градские" законы [предписывали карать смертной казнью за переход из православия в другую религию], преследовавшие свободу совести, Иосиф называл "божественным писанием".. После расправы с еретиками на соборе 1504 г Иосиф стал знаменем воинствующей церкви. За заслуги перед светской властью и церковью Иосиф в 1591 г. был объявлен общерусским "святым"...
</p>
<p>
Московских еретиков судил церковный собор 1504 г. По настоянию собора наиболее активных еретиков — Ивана Волка, Михаила Коноплева и Ивана Максимова сожгли в клетке в Москве, а Некраса Рукавова - в Новгороде, предварительно отрезав ему язык. Духовные инквизиторы во главе с митрополитом Симоном настояли еще на сожжении юрьевского архимандрита Кассиана…
</p>
<p>
Требования кровавой расправы над церковными противниками отразились и на законодательстве XVII в. В Соборном уложении 1649 г. критика церкви и ее догматов рассматривалась как богохульство и каралась сожжением на костре. Так же наказывались "без вся кого милосердия" и другие выступления против религии и церкви — переход в "басурманскую" веру, святотатство и др. По свидетельству иностранца Петрея, врагов церкви, "святотатцев", сажали на кол, после смерти тело снимали с кола, выносили за городские ворота и сжигали, а пепел засыпали землей15. По указу 1684 г. смертной казни через сожжение предавались еретики, которые "отшатнулись от церкви и чинят соблазн и мятеж", а также "перекрещенцы"...

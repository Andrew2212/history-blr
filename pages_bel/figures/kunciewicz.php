		В 1604 пострижен в монашество с именем Иосафат. Пишет сочинения о необходимости восстановления единства Западной и Восточной Церкви.
		В 1609 году посвящён в священники, а в 1614 стал архимандритом виленского монастыря Святой Троицы. 
		</br>[ <small>При Троицком монастыре  митрополит Киевский, Галицкий и всея Руси Ипатий Потий основал в 1601 году первую духовную семинарию Руской униатской церкви.</small> ]

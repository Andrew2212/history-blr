		</p>
		<p>
		Кастусь Калиновский (2 февраля 1838 - 22 марта 1864) — лидер национально-освободительного 
		<a href="../events/uprising_1863.php">восстания 1863 года</a> в Беларуси.
		</p>
		<p>
		Будущий лидер повстанцев родился в имении Мостовляны на Гродненщине в семье безземельной <a href="../terms/shliahta.php">шляхты</a>. 
		Окончил Свислочскую гимназии, учился на юридическом факультете Петербургского университета, активно участвовал в деятельности 
		студенческих землячеств и в революционных кружках.
		</p>
		<p>
		Создавал революционные гурткі 
		в Гродненской и <a href="../places/wilno.php">Виленской</a> губерниях. 
		В 1862 он стал во главе Комитета движения, позже ставшего Литовским провинциальным комитетом восстания. 
		Восстание поддерживали 
		такие знаковые для Беларуси фигуры, как <a href="../figures/bogushewicz.php">Ф. Богушевич</a>, 
		<a href="../figures/dunin-marzinkewicz.php">В. Дунин-Марцинкевич</a>, 
		<a href="../figures/orda.php">Н. Орда</a>, <a href="../figures/mineyko.php">З. Минейко</a>. 
		</p>
		</p>

		<p>
		В 1862-1863 руководил изданием и распространением "Мужыцкай праўды" — первой нелегальной революционной газеты на белорусском языке. Под именем 
		<i>Jasko Haspadar s pad Wilni</i> писал воззвания к населению (<a href="../terms/blr_mova.php">белорусская латинка</a>).
		</p>
		<p>
		Во время восстания 1863 года <i>«далеко деятельнее, энергичнее и способнее всех воеводских комиссаров 
		для революционного дела оказался Калиновский»</i>
		</br>
		<i>"Константин Калиновский с настроением герценовской школы во главе честолюбивейших личностей из красных литвинов настойчиво проводил идею 
		о самостоятельности Литвы"</i>, — писали о нем в донесениях виленскому генерал-губернатору.
		</p>
		

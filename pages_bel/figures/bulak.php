		<p>
		С 1920 года Булак-Балахович в составе РНДА сражается с большевиками на территории Беларуси. 
		 По соглашению с <a href="../terms/bnr.php">БНР</a> был признан главнокомандующим вооруженных сил Беларуси и приказал сформировать (отдельно от 
		 РНДА) Белорусскую Народную Армию — в составе Крестьянской дивизии атамана Искры, отрядов <a href="../events/war_green_oak.php">«Зеленого дуба»</a>, 
		 Особого белорусского батальона 2-й пехотной дивизии. Поддерживал <a href="../events/uprising_sluzk.php">Слуцкi збройны чын</a> 1920 г.
		</p>
		
		<p><small>
		[ РНДА — Русская народная добровольческая армия — «белая» армия, действовавшая осенью 1920 года на территории Беларуси, 
		подчинена Русскому политическому комитету в Польше во главе с Б. В. Савинковым (руководитель Боевой организации партии эсеров).
		Образована на основе отряда С. Н. Булак-Балаховича, действовала против Красной армии совместно с польскими войсками. 
		В ноябре 1920 года после неудачного похода в Полесье армия отступила на территорию Польши, где была интернирована и расформирована. ]

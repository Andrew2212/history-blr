		<p>
		[ <small>
		С Кейстутом связано первое упоминание "литовского" (lithwanice) языка в исторических источниках (<a href="../download/chronicles_dubnica.php">Дубницкая хроника</a>) 
		</small>
		</p>
		<p>
		<small>
		В 1351 году войско <a href="../terms/litwiny.php">литвинов</a> из Бреста, Дорогичина, Гродно и Трок во главе с Кейстутом вело войну на венгерской границе.
		В итоге договорились о перемирии с венгерским королём. Кейстут убил быка (rogacina) и при свидетелях прокричал клятву 
		"Рогацина — розни наши! Господ з нами!", что было громко повторено литвинами (Lithwani).
		</small> 
		</p>
		<p>
		<small>
		Эта фраза была фонетически дословно записана очевидцем "rogachina rozne nachy gospanany" и вольно пересказана в латыни, как:  
		"Бог на нас и души рогатого посмотри, обещеная нами клятва сегодня свершается"
		</small> ]
		</p>
		

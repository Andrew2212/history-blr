		После смерти Ольгерда, во время <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны 1381-1384</a> за власть в ВКЛ, Кейстут, воевавший на стороне своего сына Витовта, пленил <a href="jagiello.php">Ягайло</a>, 
		и был признан <a href="../terms/dukes_wkl.php">Великим князем Литовским</a> (1381-82).  Однако, освободив Ягайло, надолго примириться с ним не удалось. 
		В ходе мирных переговоров Кейстут с сыном были пленены и перевезены в родовой замок Ягайло — <a href="../places/krevo.php">Крево</a>. Через неделю Кейстут скончался.

		<p>
		Кейстут (1297-1382) — сын великого князя литовского <a href="gedimin.php">Гедемина</a>, брат и фактический соправитель <a href="olgerd.php">Ольгерда</a>, 
		отец <a href="vitovt.php">Витовта</a>.
		 Еще при жизни отца получил в удел <a href="../places/samogitia.php">Жмудь</a>, Троки, Гродно и Берестье 
		— узкую и длинную полосу вдоль западного рубежа <a href="../terms/wkl.php">ВКЛ</a>, прилегавшую к владениям Тевтонского ордена и Мазовии.
		</p>
		<p>
		<i>"Ольгерд и Кейстутий были в великой любви, милосце и ласке".</i>
		</br>В течение четырёх лет после смерти Гедимина (1341—1345) централизованная власть в ВКЛ, распавшейся на самостоятельные уделы, фактически отсутствовала.
		В 1345 году Кейстут, по предварительному уговору с братом, занял <a href="../places/wilno.php">Вильну</a>, где сидел младший сын Гедимина, Евнутий, 
		и пригласил на великое княжение Ольгерда. С тех пор до самой смерти Ольгерда братья действовали солидарно.
		</p>
		<p>
		О достаточной самостоятельности Кейстута в правлении свидетельствует договор от его имени с Англией (1342), 
		обеспечивавший свободное право въезда англичан в его владения.
		</p>
		<p>
		На долю Кейстута выпала охрана западной границы Литовского государства от натиска крестоносцев,
		 и эту задачу он выполнял около полстолетия с большим успехом. Ниже приведены выдержки из 
		 <a target="_blank" href="http://www.sedmitza.ru/lib/text/719959/">"Монографии по истории Западной и Юго-Западной России"</a> Антонович В. Б. 1885 г.
		</p>
		 

		<p>
		<h3>Агульны ход бітвы</h3>
		— арміі супернікаў размясціліся адзін супраць адной на адлегласці ў 20 стадыяў (3700 метраў);
		</br>— армія ВКЛ уступае ў бой першай, у той час, як польскія атрады Ягайла пакідае ў рэзерве;
		</br>— армія Вітаўта нясе вялікія страты, паколькі супраць іх выстаўлены лепшыя рыцары Ордэна;
		</br>— напал бітвы спадае з прычыны стомленасці ваяроў;
		</br>— Ягайла ўводзіць у бой рэзервовыя польскія харугвы і абарачвае ва ўцёкі крыжакоў;
		</br>— у бітве гіне колер Ордэна, улучаючы вялікага магістра, чальцоў вярхоўнага капітула і камтураў. 
		</p>
		<p>
		Завяршыў Вялікую вайну Торуньскі мір 1 лютага 1411 года: Ордэн страціў Жмудзь на карысць Вялікага княства, Добжыньскую зямлю на карысць Польшчы і выплачваў кантрыбуцыю. 
		Неўзабаве пасля гэтага, падчас Трынаццацігадовай вайны, Ордэн быў даканцова разгромлены Польшчай ужо без удзелу ВКЛ і прызнаў сваю васальную залежнасць ад яе.
		</p>
		

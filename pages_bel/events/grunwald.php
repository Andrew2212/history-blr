		<p>
		Артыкул напісаны па матэрыялах Р. Б. Гагуа, 
		</br><small>дацэнта па спецыяльнасці "Гісторыя" (тэма дысертацыі: "Вялікая вайна з Тэўтонскім ордэнам 1409—1411")</small>	
		</p>
		<p>
		Грунвальдская бітва 15 ліпеня 1410 года — вырашальнае змаганне <a href="../events/war_1409-11.php">Вялікай вайны 1409-1411</a>, якое адбылося паміж 
		хаўрусным польска-літоўскім і тэўтонскім войскамі. 
		</p>
		<p>
		<a href="../terms/wkl.php">Вялікае княства Літоўскае</a> і Польшча з 1385 года былі злучаны <a href="../events/unia_krewo.php">Крэўскай уніяй</a> і баранілі агульныя цікавасці супольна.
		</p>
		<p>
		Вялікая вайна 1409-1411 гадоў — вайсковы канфлікт паміж рыцарамі Тэўтонскага ордэна з аднаго боку і ВКЛ у звязе з Польшчай з іншага. 
		Паўстаў праз канфлікт цікавасцяў — ВКЛ імкнулася атрымаць 
		<a href="../places/samogitia.php">Жмудзь (Самагітыю)</a>, Польшча жадала атрымаць землі, згубленыя па выніках папярэдняй вайны, і выйсце да мора, Ордэн хацеў дамінаваць у Прыбалтыцы.
		</p>
		<p>[ 
		З часоў <a href="../events/war_civil_1381-84_1389-92.php">Грамадзянскай вайны ў ВКЛ</a> (Вітаўт супраць Ягайлы) перадача Жмудзі Ордэну была замацавана 
		чатырма дамовамі — Дубіскай (1382,  Ягайла; не ратыфікаваны), Кёнігсбергскай (1384, Вітаўт), Лікскай (1390, Вітаўт), Салінскай (1398, Вітаўт).
		 ]
		</p>
		

		<p>
		Люблінская унія — дзяржаўны звяз паміж Каралеўствам Польскім і <a href="../terms/wkl.php">Вялікім княствам Літоўскім</a>, што паклаў пачатак адзінай федэратыўнай 
		дзяржаве — <a href="../terms/rzeczpospolita.php">Рэчы Паспалітай</a>. Была створана дзяржава, якая аказвала значную ролю на сусветнай арэне цягам наступных 200 год. 
		</p>
		<p>
		Акт уніі быў складзены 28 чэрвеня 1569 года і 1 ліпеня гэтага года зацверджаны паасобна дэпутатамі польскага і літоўскага соймаў на агульным сойме, скліканым у Любліне. 
		4 ліпеня унія была ратыфікавана каралём польскім і вялікім князем літоўскім <a href="../terms/dukes_wkl.php">Жыгімонтам II Аўгустам</a> 
		— апошнім з <a href="../figures/jagellony.php">Ягелонаў</a>.
		</p>
		<p>
		Унія не столькі ўмацавала ўладу манарха (чаго і жадаў Жыгімонт), колькі ўзмацніла ўплыў 
		<a href="../terms/shliahta.php">шляхты</a>, павялічыўшы заразом і яе колькасць. Абсалютная манархія, характэрная для XVI стагоддзя так і не склалася (яна прыйдзе на гэтыя землі толькі напрыканцы XIX стагоддзя на рускіх багнетах).
		</p>
		<p>
		Паводле уніі обедзве дзяржавы мелі аднаго васпана, якога штораз павінны былі абіраць разам (без спадчыннага пераемства ўлады). Быў агульны сойм і сенат ("паны рада"), які загадваў агульнымі ўнутранымі справамі і палітыкай. 
		Мытныя межы паміж Польшчай і Літвой ліквідаваліся. Да Польшчы адышлі <a href="../download/zemie_ruskie.php">Землі Рускія</a> (Украіна).
		</p>
		<p>
		За ВКЛ захоўвалася дзяржаўнасць у выглядзе ўласнага права (<a href="../terms/statut.php">Статут</a>) 
		і суда, адміністрацыі, войска, скарбу, а таксама дзяржаўнай <a href="../terms/blr_mova.php">беларускай</a> мовы.
		Польшча і Літва мелі ўласныя арміі, аднак ядналі сілы абароны агульнай дзяржавы.
		</p>
		<p>
		Рэлігійныя супярэчнасці ў новай дзяржаве былі ўрэгуляваны Актам <a href="../events/confederacia_warshawa.php">Варшаўскай канфедэрацыі</a> 1573 года.
		</p>

		<p>
		Унія прымалася ва ўмовах <a href="war_livonia.php">Лівонскай вайны</a> з Масковіяй 1558-83.
		Разгляд пытання пра Люблінскую унію пачаўся ў студзені 1569 года на агульным сойме. З-за канфлікту інтарэсаў — дапамога ў вайне ў памен на землі —
		сойм працягваўся з перапынкамі да жніўня 1569 года. Знакаміты <a href="../download/map_wkl.php">Атлас Меркатара</a> (1595) дае ўяўленне пра межы ВКЛ да падпісання уніі.
		</p>
		<p>
		1558 маскоўскія войскі ўварваліся ў Лівонію
		</p>
		<p>

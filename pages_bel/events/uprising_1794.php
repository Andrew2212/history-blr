		<p>
		Ідэю паўстання горача падтрымаў У. І. Ленін:
		</br><i>"Ніхто не вінаваты ў тым, што ён нарадзіўся рабом; але раб, які не толькі цураецца імкненняў да сваёй волі, 
		але апраўдвае і прыхарошвае сваё рабства (напрыклад, называе задушванне Польшчы, Украіны і г.д. "абаронай бацькаўшчыны" велікаросаў), 
		такі раб выклікае законнае пачуццё абурэння, пагарды і агіды, халуй і хам".
		</i>
		</br><small>У. Ленін <a target="_blank" href="http://leninism.su/index.php?option=com_content&view=article&id=2098:o-naczionalnoj-gordosti-velikorossov&catid=65:tom-26&Itemid=53">
		"Пра нацыянальны гонар велікаросаў"</a> ПСС т. 26, стр. 106-110</small>
		</p>
		<p>
		Храналогія падзей:
		</br>1772 — Першы падзел Рэчы Паспалітай
		</br>1789 — Вялікая французская рэвалюцыя, што зрынула "феадальную арыстакратыю" і ўзяцце Бастыліі
		</br>1791 — Прыняцце <a href="../events/constitution_3may.php">Канстытуцыі 3 траўня</a>, якая абвясціла ўзмацненне цэнтральнай улады і выклікала незадаволенасць сярод магнатаў і шляхты Рэчы Паспалітай. 
		</br>1792 — Створана Таргавіцкая канфедэрацыя пад пратэктаратам Расіі супраць рэформ, Канстытуцыі 3 траўня і караля
		<a href="../figures/poniatowski.php">Станіслава Панятоўскага</a>
		</br>1793 — Другі падзел Рэчы Паспалітай. Месцаванне расійскіх войскаў на астатніх тэрыторыях Польшчы і ВКЛ.

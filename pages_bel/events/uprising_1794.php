		<p>
		16 марта 1794 года в Кракове был провозглашен Акт восстания. Тадеуш Костюшко был объявлен диктатором республики и принес публичную присягу. 
		Акт восстания провозглашал Тадеуша Костюшко верховным главнокомандующим национальными вооруженными силами и предоставлял 
		ему всю полноту власти в стране.
		</p>
		<p>
		Генеральным командующим повстанческими войсками в Великом княжестве Литовском был Якуб Ясинский, лидер радикального "якобинского" крыла 
		повстанцев, сторонник идей французской революции. Во время восстания был комендантом <a href="../places/wilno.php">Вильны</a>. Погиб при обороне Варшавы.
		</p>
		<p>
		7 мая Костюшко издает Поланецкий Универсал — акт, провозглашавший опеку правительства над крестьянами и объявлявший их лично свободными. 
		Универсал позволял крестьянам вступать в повстанческую армию.
		</p>
		

		<p>
		12 марта - 25 октября 1794 года. Восстание под предводительством <a href="../figures/kostiushko.php">Тадеуша Костюшко</a>, 
		направленное против <a href="../events/razbor_rechi.php">разделов Речи Посполитой</a> и за ее независимость.	
		</p>
		<p>
		Идею восстания горячо поддерживал В. И. Ленин:
		</br><i>"Никто не повинен в том, если он родился рабом; но раб, который не только чуждается стремлений к своей свободе, 
		но оправдывает и прикрашивает свое рабство (например, называет удушение Польши, Украины и т. д. “защитой отечества” великороссов), 
		такой раб есть вызывающий законное чувство негодования, презрения и омерзения холуй и хам".
		</i>
		</br><small>В. Ленин <a target="_blank" href="http://leninism.su/index.php?option=com_content&view=article&id=2098:o-naczionalnoj-gordosti-velikorossov&catid=65:tom-26&Itemid=53">
		"О национальной гордости великороссов"</a> ПСС т. 26, стр. 106-110</small>
		</p>
		<p>
		Хронология событий:
		</br>1772 — Первый раздел речи Посполитой
		</br>1789 — Великая французская революция, свергнувшая  "феодальную аристократию" и Взятие Бастилии
		</br>1791 — принятие <a href="../events/constitution_3may.php">Конституции 3 мая</a>, провозгласившей усиление центральной власти и вызвавшей недовольство среди магнатов и шляхты Речи Посполитой. 
		</br>1792 — создана Тарговицкая конфедерация под протекторатом России против реформ, Конституции 3 мая и короля 
		<a href="../figures/poniatowski.php">Станислава Понятовского</a>
		</br>1793 — Второй раздел Речи Посполитой. Размещение российских войск на оставшихся территориях Польши и ВКЛ.
		</br>1794 — Восстание Костюшко
		</br>1795 — Третий раздел Речи Посполитой
		</p>
		

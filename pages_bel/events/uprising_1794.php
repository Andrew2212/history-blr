		<p>
		16 сакавіка ў Кракаве быў абвешчаны Акт паўстання. Тадэвуш Касцюшка быў абвешчаны дыктатарам рэспублікі і прынёс публічную прысягу. 
		Акт паўстання абвяшчаў Тадэвуша Касцюшка вярхоўным галоўнакамандуючым нацыянальнымі ўзброенымі сіламі і надаваў 
		яму ўсю поўнасць улады ў краіне.
		</p>
		<p>
		Генеральным камандуючым паўстанцкімі войскамі ў Вялікім княстве Літоўскім быў Якуб Ясінскі, лідар радыкальнага "якабінскага" крыла 
		паўстанцаў, прыхільнік ідэі французскай рэвалюцыі. Падчас паўстання быў камендантам <a href="../places/wilno.php">Вільні</a>. Загінуў пры абароне Варшавы.
		</p>
		<p>
		7 траўня Касцюшка выдае Паланецкі Універсал — акт, што абвяшчаў апеку ўрада над сялянамі і аб'яўляў іх асабіста вольнымі. 
		Універсал дазваляў сялянам уступаць у паўстанцкае войска.
		</p>
		

<p>
У выданні рэдакцыі "Рэха з-за Сяню", 1885, з тыпаграфіі С. П. П'янткевіча
</br>Пераклад ад <a target="_blank" href="http://worldconstitutions.ru/?p=367">Інтэрнэт-бібліятэка канстытуцый Рамана Пашкова</a>

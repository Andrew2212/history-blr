		<p>
		Бітва пад Палонкай 28 чэрвеня 1660 года — бітва вайны з Масковіяй 1654-1667, якая адбылася каля беларускага сяла Палонка.  
		Гэта паваротны момант <a href="blood_potop.php">"Крывавага Патопу"</a>, што захліснуў
		 <a href="../terms/rzeczpospolita_treh_narodov.php">Рэч Паспалітую Трох Народаў</a>. 
		 </p>		
		<p>
		 Паваротны момант у самай крывавай вайне за ўсю гісторыю Беларусі (па ўсходніх ваяводствах людскія страты склалі да 70%). Пасля гэтай перамогі пачалося вызваленне захопленых <a href="../download/blr_people.php">беларускіх зямель</a>.
		</p>		
		<p>
		Войска Рэчы Паспалітай — ВКЛ (8000 чал.) і Польшчы (4000 чал.), якое ўзначальвалі вялікі гетман Літоўскі <a href="../figures/sapegi.php">Павел Ян Сапега</a> і 
		ваявода Рускі (кіраўнік Рускага ваяводства ў Каралеўстве Польскім) 
		Стафан Чарнецкі, разграміла 20-ці тысячную маскоўскую армію.
		</p>
		<p>		
		У бітве пад Палонкай прымаў удзел легендарны шляхціц Аршанскі — <a href="../figures/kmicic.php">Самуіл Кміціч</a>, "генеральны палкоўнік" дывізіі Сапегі, 
		які з'яўляўся правобразам галоўнага героя рамана Г. Сянкевіча "Патоп"
		</p>
		<p>		
		Становішча на франтах вайны да пачатку бітвы — маскоўскае войска захапіла ўсю тэрыторыю <a href="../terms/wkl.php">ВКЛ</a>,
		акрамя Слуцка і <a href="battle_liahowiczy.php">Ляхавіч</a>.
		</p>		
		<p>		
		Вынікі бітвы — поўны разгром маскоўскага войска. Страты арміі Сапегі 300 чалавек, супраціўніка — 3500. Вызвалены заходнія землі ВКЛ. Маскоўскія войска ў Навагрудку і некаторых іншых гарадах капітулявалі адразу, 
		а ў Берасце, Гародні і <a href="../places/wilno.php">Вільне</a> — пасля адна-двухгадовых аблог. Рух польска-літоўскай арміі быў спынены толькі пад Барысавам.
		</p>
		<p>		
		Вайна да поўнага вызвалення краіны працягвалася яшчэ доўгіх сем год.
		</p>
		

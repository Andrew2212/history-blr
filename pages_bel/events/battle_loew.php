		<h3>Бітва пад Лоевам, 1649 год</h3>
		
		<p>
		Януш <a href="../figures/radziwilly.php">Радзівіл</a>, ваявода Віленскі і польны гетман Літоўскі, на чале 7-тысячнай арміі 
		разбіў 20-тысячнае казацкае войска Сцяпана Падабайлы і Міхала Крычэўскага.		
		</p>
		<p>
		Казацкая армія на чале са Сцяпанам Падабайлам ўзяла Лоеў напрыканцы ліпеня 1649 года. Горад стаў выкарыстоўвацца казакамі 
		ў якасці асноўнай базы для распаўсюджвання ўлады Войска Запарожскага на найбліжэйшыя губерні. 
		</p>
		<p>
		У адказ на гэта, у ліпені таго ж года, Януш Радзівіл, сабраўшы войска колькасцю 7000 чалавек, 
		(820 <a href="../terms/husaria.php">гусараў</a>, 1660 драгунаў, 2000 <a href="../terms/zemiane.php">панцырных баяраў</a>, 1500 нямецкай пяхоты), рушыў іх на Лоеў.
		</p>
		<p>
		У выніку бітвы горад вярнуўся пад кантроль ВКЛ. Пасля перамогі Радзівіл сабраў каля сарака казацкіх сцягоў на поле бою.
		</p>
		<p>
		На гравюры 1720 года падрабязна паказаны і апісаны ў легендзе ход бітвы, размяшчэнне войскаў, лагер Радзівіла, казацкі табар-вагенбург.
		</p>
		

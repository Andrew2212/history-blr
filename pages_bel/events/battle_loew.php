		<h3>Бітва пад Лоевам, 1651 год</h3>
		
		<p>
		Праз два гады, 6 ліпеня 1651 года на тым жа месцы адбылася другая бітва пад Лоевам з Войскам Запарожскім. 
		</br>Гэтым разам, колькасная перавага была ў арміі княства Літоўскага. 
		І зноў Януш Радзівіл на чале 40-тысячнага войска, узмоцненага <a href="../places/sea_herodotus.php">палескім</a> апалчэннем, разбіў 20-тысячнае казацкае войска атаманаў Мартына Нябабы і Сцяпана Падабайлы.
		</p>
		

		<h3>1941-1945</h3>
		<p>
		Да 1941 года СССР і Германія ўсталявалі агульную мяжу, прыблізна адпаведную дамоўленасці.
		</p>
		<p>
		22 чэрвеня 1941 года Германія напала на СССР. З гэтага дня савецкая гісторыяграфія вядзе пачатак Вялікай Айчыннай вайны.
		</p>
		<p>
		Баявыя дзеянні вяліся на пяці "тэатрах вайсковых дзеянняў":
		Заходнееўрапейскі, Усходнееўрапейскі, Міжземнаморскі, Афрыканскі, Ціхаакіянскі.
		</p>
		<p>
		7 траўня 1945 года быў падпісаны "Акт пра безумоўную капітуляцыю Германскіх узброеных сіл"
		</p>
		<p>
		8 траўня 1945 года капітуляцыя Германіі набыла моц. Гэта дата ў Еўропе стала святкавацца як Дзень Перамогі.
		</p>
		<p>
		9 траўня — гэта дата ў СССР стала святкавацца як Дзень Перамогі.
		</p>
		
		<h4>1945 травень-верасень</h4>
		<p>
		Працягвалася вайна з Японіяй — удзельніцай Берлінскага пакта (дамова пра ўсталяванне новага сусветнага парадку, падпісанная Германіяй, Італіяй
		і Японіяй у 1940). Да гэтага часу вайну Японіі абвясцілі каля 60 краін. 
		Аднак, нягледзячы на ўзніклае становішча, японцы не збіраліся капітуляваць і абвясцілі пра вядзенне вайны да пераможнага канца.
		</p>
		<p>
		6 жніўня 1945 года ЗША скінулі на Хірасіму атамную бомбу «Little Boy». 
		</p>
		<p>
		8 жніўня 1945 года СССР абвясціў вайну Японіі.
		</p>
		<p>
		9 жніўня 1945 года ЗША скінулі на Нагасакі атамную бомбу «Fat Man». 
		</p>
		<p>
		2 верасня 1945 года Японія на борце амерыканскага лінкара «Місуры» падпісала Акт пра капітуляцыю. 
		</p>
		
		

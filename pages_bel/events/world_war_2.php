		<h1>Другая сусветная вайна</h1>
		<p>
		<i>
		Мой таварыш, у смяротнай агоніі
		</br>Не кліч дарэмна сяброў. 
		</br>Дай-ка лепш угрэю далоні я 
		</br>Над дымлівай крывёю тваёй.
		</p>
		<p>
		Ты не плач, не стагні, не маленькі,
		</br>Ты не ранены, проста забіты. 
		</br>Дай на памяць здыму з цябе валёнкі. 
		</br>Нам яшчэ наступаць мае быць.
		</i>
		</br><small>Іёна Дэген</small>
		</p>
		<p>
		Другая сусветная вайна (1 верасня 1939 - 2 верасня 1945) 
		— вайна двух сусветных вайскова-палітычных кааліцый, што стала найбуйным узброеным канфліктам у гісторыі чалавецтва. 
		</p>
		<p>
		У ёй бралі ўдзел 62 дзяржавы з 73 існававалых на той момант (80% насельніцтва зямнога шара). 
		Баявыя дзеянні вяліся на тэрыторыі трох кантынентаў і ў водах чатырох акіянаў. 
		Гэта адзіны канфлікт, у якім была ўжыта ядзерная зброя.
		</p>
		<h3>1939-1941</h3>
		<p>
		1 верасня 1939 — пачатак вайны. 
		Войскі Германіі нападам на Польшчу з захада пачалі баявыя дзеянні ў Еўропе.
		</p>
		<p>
		3 верасня Англія, Францыя, Аўстралія і Новая Зеландыя абвясцілі Германіі вайну.
		</p>
		<p>
		17 верасня 1939 года ў Польшчу з усходу ўварваліся войскі СССР.
		</p>
		<p>
		22 верасня 1939 года падчас перадачы занятага Германіяй Брэста савецкаму боку, у Брэсце быў праведзены супольны парад войскаў вермахта і СССР.
		</p>
		<p>
		28 верасня 1939 года паміж СССР і Германіяй была падпісана "дамова пра сяброўства і мяжу".
		</p>
		<p>
		30 лістапада 1939 года СССР урываецца ў Фінляндыю. Да 12 сакавіка 1940 года ідзе Савецка-Фінская вайна.
		</p>
		

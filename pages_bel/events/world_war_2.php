			<p>
			За час Другой сусветнай вайны хаўруснікі па антыгітлераўскай кааліцыі страцілі каля 16 млн. салдатаў і 30 млн. грамадзянскіх. 
			</p>
			<p>
			За час Другой сусветнай вайны Германія і яе хаўруснікі страцілі каля 8 млн. салдатаў і 9 млн. грамадзянскіх.
			</p>
			<p>
			Страты СССР ацэньваюцца, як 8,6 млн. салдатаў і 13,7 млн. грамадзянскіх. 
			Баявыя страты СССР перавышаюць сумарныя страты Германіі і ўсіх яе хаўруснікаў, якія ваявалі на ўсіх франтах — ад Афрыкі да Нарвегіі.
			</p>
			<p>
			Гэтыя лічбы робяць лозунг "Можам паўтарыць" асабліва брыдотным.
			</p>
			<p>
			Памяць пра тых, хто змагаўся пад сягам СССР ушанавана ў мностве музеяў і мемарыялаў. Памяць пра тых, хто
			бачыў будучыню <a href="../terms/belarus.php">Беларусі</a>, як незалежнай вольнай краіны ці проста воляю лёсу ўзяў удзел 
			у Другой сусветнай не з 1941, а з 1939 — старанна выкрасляецца з культурна-гістарычнага кантэксту грамадства.
			</p>

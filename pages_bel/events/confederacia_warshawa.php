		Сярод тых, хто падпісаў Акт канфедэрацыі былі Астафій <a href="../figures/volovicz.php">Валовіч</a> — падканцлер Літоўскі, 
		Павал <a href="../figures/pacy.php">Пац</a> — кашталян Віцебскі. 

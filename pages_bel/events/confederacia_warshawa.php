	<h2>Генрыхавы артыкулы
	</br><small>першы выбарны кароль і яго клятва</small>
	</h2>
	<p>
		Генрых Валуа — апошні кароль Францыі з дынастыі Валуа, вялікі князь Літоўскі і першы выбарны кароль Польшчы (са студзеня па чэрвень 1574). 
		Кіраванне яго было нядоўгім — пазнаўшы пра магчымаць заняць пасад Францыі, ён адмовіўся ад дынастычнага шлюбу з Ганнай Ягелонкай і вярнуўся на радзіму.
	</p>
	
	<p>
		Для таго, каб акт Варшаўскай канфедэрацыі атрымаў сілу закона, неабходна было, каб яго зацвердзіў кароль. 
		Калі ўлічыць тое, што Генрых Валуа і яго маці, Кацярына Медычы, выславіліся "Варфаламеевай ноччу" (масавая разаніна гугенотаў каталікамі) 
		— гэта было няпроста.
	</p>
	<p>
		Па патрабаванні дысідэнтаў (некаталікоў) былі складзены 
		<a target="_blank" href="https://pl.wikisource.org/wiki/Literae_confirmationis_articulorum_Henrico_Regi_antea_oblatorum">Генрыхавы артыкулы</a>, 
		якімі абмяжоўвалася ўлада караля і забяспечвалася рэлігійная вольнасць, а непарушнасць гэтых артыкулаў пацвярджалася клятвай:
		<i> "У выніку ж, калі мы (ад чаго, Божа, захавай!) не выканаем гэтых артыкулаў ці ўмоў, ці ўчынім штосьці насуперак ім, 
		законам і вольнасцям, то ўсіх жыхароў каралеўства і вялікага княства аб'яўляем вольнымі ад належнага нам падпарадкавання і вернасці".
		</i>
		</br>[ <small>&sect;17 </i>...A ieslibysmy (czego Boze uchoway) co przeciw prawom, wolnosciom, artykulom, kondycyom wykroczyli, abo czego nie wypelnili: 
		tedy obywatele Koronne oboyga narodu, od posluszenstwa y wiary Nam powinney, wolne czyniemy."</i></small> ]
	</p>
	<p>
		Усе біскупы, акрамя кракаўскага Францыска Красінскага, былі супраць прыняцця акта канфедэрацыі і рэлігійных вольнасцяў. 
		 І, калі каталікі святкавалі па выпадку абрання Генрыха Валуа, дысідэнты,
		у ліку якіх быў кіеўскі ваявода Астрожскі (сын вялікага гетмана <a href="../figures/ostrogski.php">Канстанціна Астрожскага</a>) з праваслаўнай шляхтай,
		 сышлі з поля абрання. 
		Звада пагражала кровапраліццём, якое пужала ўсіх. 
		І супернікі прыйшлі да каспрамісу: дысідэнты згадзіліся на абранне Генрыха, а каталікі прынялі цалкам Генрыхавы артыкулы.
	</p>
	<p>
		На сойме 20 траўня 1573 года "Артыкулы" падпісаў прадстаўнік новаабранага караля Генрыха Валуа.
		Дарэчы — менавіта вестка пра абранне Генрыха каралём Польшчы спыніла аблогу Ла-Рашэлі і выратавала трох мушкяцёраў.
	</p>
	
	<p class="img-float-center">
		<a target="_blank" href="../../images/img-events/confederacia_warshawa/memory.jpg">
		<img alt="Варшаўская канфедэрацыя" src="../../images/img-events/confederacia_warshawa/memory_thumb.jpg" height="140">
		</br><small>Сусветная спадчына</small>
		</a>		
	</p>
	<p>
	Адрозненне ад выбарчых капітуляцый (pacta conventa), якія і раней падпісвалі пры ўступе на пасад Ягелоны, 
	складаецца з выбарнасці, а не спадчыннасці каралеўскай улады. Генрыхавы артыкулы пацвярджаліся ўсімі наступнымі кіраўнікамі 
	Польшчы і канчаткова ўзаконілі сістэму "шляхецкай дэмакратыі".
	</p>
	<p>
	Генрыхавы артыкулы ўвялі абмежаванні каралеўскай улады. Манарх не мог без згоды сойма ні аб'яўляць вайну, 
	ні заключаць мір, ні склікаць паспалітае рушэнне. У выпадку парушэння прывілегій шляхты з боку манарха, шляхта мела 
	права паўстаць супраць яго.
	</p>
	<p>
	З моманту падпісання артыкулаў у Рэчы Паспалітай пачала згасаць традыцыя спадчыннасці каралеўскай улады. 
	Ужо праз сто гадоў ніводны сын паспяховага караля <a href="../terms/dukes_wkl.php">Яна Сабескага</a> не мог прэтэндаваць на перайманне пасада.
	</p>
	<p>
	У 2003 годзе тэкст канфедэрацыі быў занесены ў спіс ЮНЭСКА "Памяць свету".
	</p>	
		

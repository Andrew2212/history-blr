		<p>
		Варшаўская канфедэрацыя 28 студзеня 1573 года заклала асновы рэлігійнай памяркоўнасці шляхты і вольных людзей у Рэчы Паспалітай.
		 Яна была прынята ў смутны час "бескаралеўя" — у 1572 памер <a href="../terms/dukes_wkl.php">Сігізмунд III Аўгуст</a>; 
		 з нім спынілася дынастыя Ягелонаў. <a href="../terms/dukes_wkl.php">Генрых Валуа</a> будзе каранаваны толькі ў 1574.
		 </p>
		 <p>
		Варшаўская канфедэрацыя мела мэтай пераадолець праблемы, якія паўсталі пасля заключэння Люблінскай уніі, 
		і ўтварэння Рэчы Паспалітай, якая мела адзінага караля (а ў той момант не мела і яго), 
		але не мела агульнай царквы. 
		І калі ў Польшчы дамінавалі каталікі, то <a href="../terms/religion_blr.php">рэлігійная палітра</a> ВКЛ была даволі пярэстай. На ўсходніх землях пераважала праваслаўе, на заходніх — каталіцызм. 
		На поўначы і захадзе шырыліся ідэі Рэфармацыі. З часоў Вітаўта ў ВКЛ жыла вялікая юдэйская суполка.
		</p>
		 </p>
		 Акт варшаўскай канфедэрацыі з'яўляецца першым падобным дакументам у Еўропе. 
		 Хоць акт і не вымінуў усіх канфліктаў на аснове рэлігіі, але зрабіў Рэч Паспалітую нашмат больш бяспечным і 
		 талерантным месцам, чым большая частка Еўропы, дзе ў той час бушавалі рэлігійныя войны.
		</p>
	

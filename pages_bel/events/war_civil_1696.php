		<p>
		Выбары новага караля пасля смерці Яна Сабескага раскалолі вялікалітоўскую шляхту: 
		Сапегі прынялі бок французскага прынца Луі дэ Конці, іх супернікі падтрымалі саксонскага курфюрста Фрыдрыха Аўгуста, 

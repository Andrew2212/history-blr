		Першая сусветная вайна (28 ліпеня 1914 - 11 лістапада 1918) — адзін з самых шырокамаштабных узброеных канфліктаў у гісторыі чалавецтва.
		</p>
		<p>
		Гэта назва зацвердзілася ў гісторыяграфіі толькі пасля пачатку Другой сусветнай вайны ў 1939 годзе. 

		Краіны-удзельніцы страцілі забітымі больш за 10 млн салдатаў і каля 12 млн мірных жыхароў, каля 55 млн чалавек былі паранены.
		</p>
		
            	<h3>Супернікі</h3>
		<h4>Траісты звяз - Цэнтральныя дзяржавы</h4>
		<p>
		Траісты звяз — вайскова-палітычны блок Германіі, Аўстра-Венгрыі і Італіі, які склаўся ў 1879-1882 гады, што паклаў пачатак падзелу Еўропы на варожыя лагеры.
		</p>
		<p>
		У 1915 годзе Італія выйшла з Траістага звязу і ўступіла ў вайны на боку яго супернікаў.
		</p>
		<p>
		Асманская імперыя і Балгарыя далучыліся да Германіі і Аўстра-Венгрыі ўжо падчас вайны. Асманская імперыя ўступіла ў вайну ў кастрычніку 1914, 
		Балгарыя — у кастрычніку 1915 года. 
		Створаны вайскова-палітычны блок называўся "Цэнтральныя дзяржавы" (Чацвярны звяз)
		</p>
		<h4>Антанта</h4>
		<p>
		Антанта (фр. entente - згода) — вайскова-палітычны блок Расіі, Англіі і Францыі, створаны ў якасці процівагі "Траістаму звязу": склаўся ў 1904-1907 гадах і завяршыў размежаванне вялікіх дзяржаў напярэдадні Першай сусветнай вайны. 
		</p>
		

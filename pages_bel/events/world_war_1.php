		<h3>Даты</h3>
		<p>
		28 чэрвеня 1914 Гаўрыла Прынцып, баснійскі серб, студэнт, чалец арганізацыі Млада Босна, забівае ў Сараева спадчынніка аўстрыйскага трона эрцгерцага Франца Фердынанда.
		</p>
		<p>
		28 ліпеня 1914 Аўстра-Венгрыя абвясціла вайну Сербіі — пачатак Першай сусветнай вайны.
		</p>
		<p>
		Вайна ў 1914 годзе разгортвалася на некалькіх тэатрах вайсковых дзеянняў — французскім і рускім, а таксама на Балканах (у Сербіі), 
		на Каўказе і Блізкім Усходзе, у калоніях - Афрыцы, Кітае, Акіяніі. У 1914 годзе ўсе ўдзельнікі вайны збіраліся скончыць вайну 
		за некалькі месяцаў шляхам наважнага наступу. Ніхто не чакаў, што вайна прыме зацяжны характар.
		</p>
		<p>
		14 верасня 1917 Крах Расійскай імперыі — Часовы ўрад абвясціў краіну рэспублікай.
		</p>
		<p>
		3 сакавіка 1918 заключаны Брэсцкі мір — сепаратная мірная дамова, падпісаная прадстаўнікамі Савецкай Расіі з аднаго боку 
		і Цэнтральных дзяржаў — з іншага. Адзначыў паразу і выйсце Савецкай Расіі з Першай сусветнай вайны.
		Краіны Антанты прынялі вырашэнне пра падтрымку сіл, што не прызналі ўладу новага рэжыму.
		</p>
		<p>
		9 лістапада 1918 года ў выніку Лістападаўскай рэвалюцыі ў Германіі зрынуты апошні германскі імператар і кароль Прусіі Вільгельм II.
		</p>
		<p>
		11 лістапада 1918 падпісана Камп'енскае перамір'е паміж Антантай і Германіяй. Хаўруснікі Германіі па Чацвярным звязе капітулявалі яшчэ раней. 
		Быў дадзены 101 залп артылерыйскага салюту нацый, што абвясціў сканчэнне Першай сусветнай вайны
		</p>
		<p>
		28 чэрвеня 1919 падпісана Версальская мірная дамова, якая афіцыяна завяршыла Першую сусветную вайну 1914-1918 гадоў.
		</p>
		

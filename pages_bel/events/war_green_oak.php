		<p>
		Паводле Статута, дзяржаўны лад незалежнай Беларусі і зямельнае пытанне павінны быў бы значыць Устаноўчы сход. Прадугледжвалася інтэнсіўная беларусізацыя, а таксама арыентацыя на Захад, Польшчу і Украіну. На пячатцы "Зялёнага дуба" змяшчалася выява "Пагоня" (вершнік з мячом), а таксама чэрап і скрыжаваныя косткі. У той жа час штаб арганізацыі і атаман Дзергачоў карысталіся пячаткай з выявай трох дубовых лісткоў.

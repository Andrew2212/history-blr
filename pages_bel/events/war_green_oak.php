		<p>
		Галоўны штаб "Зялёнага дуба" знаходзіўся ў Лунінцы, які ў той час уваходзіў у склад Польшчы. Напачатку ваяроў было ўсяго 400 чалавек, якія дзяліліся на "пяцёркі", што, у сваю чаргу, рабіліся ядром асобных паўстанцкіх атрадаў, цэнтрамі прыцягнення незадаволенага савецкім ладам грамадства. "Зялёнадубцы" вялі баявыя дзеянні ў Слуцкім, Мазырскім, Бабруйскім, Барусаўскім, Ігуменскім раёнах. Атаман Дзергачоў асабіста кіраваў атрадамі "Зялёнага дуба" ў Палессе.

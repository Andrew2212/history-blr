		<p>
		Гарадзельская унія 2 кастрычніка 1413 года — дамова вялікага князя літоўскага <a href="../figures/vitovt.php">Вітаўта</a> 
		з польскім каралём <a href="../figures/jagiello.php">Уладзіславам Ягайла</a>, 
		падпісаная ў Гарадлі на рацэ Заходні Буг. У мястэчку над Бугам сабраліся не толькі манархі, але і вярхі абедзвюх дзяржаў. 

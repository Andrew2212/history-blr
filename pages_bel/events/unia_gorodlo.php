		яно захоўвала права на існаванне і пасля смерці Вітаўта. Уводзіўся спадчынны інстытут. 
		<a href="../terms/dukes_wkl.php">вялікага князя Літоўскага</a>, што касавала тэзісы Віленска-Радамская уніі.
		</p>
		<p>
		Вялікага князя можна было выбраць толькі пасля ўзгаднення з Ягайлам і яго радай. У сваю чаргу, палякі бралі абавязанне не абіраць 
		караля пасля Ягайлы, не ўзгадніўшы кандыдатуры з вялікім княствам і яго баярствам.
		</p>

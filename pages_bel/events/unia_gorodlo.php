		<h3>Грамадзянская вайна і Трокскі прывілей </h3>
		<p>
		Аднак правы на гэтыя пасады і ўсе прывілеі, гарантаваныя Гарадзельскай уніяй, тычыліся толькі каталікоў. Адной з прычын адкрыта 
		каталіцкага характару Гарадзельскай уніі было імкненне Вітаўта набыць сімпатыі на Захадзе, пазбавіць заходняй падтрымкі Ордэн. 
		</p>
		<p>
		Выключна праваслаўныя землі — Віцебская, Полацкая, Смаленская — не бралі ўдзел у Гарадзельскім з'ездзе, і акты уніі іх не датычыліся. 
		Яны заставаліся ў дзяржаве аўтаномнымі тэрыторыямі.
		</p>
		<p>
		Акты Гарадзельскай уніі зрабілі каталіцтва прывілеяванай рэлігіяй. Канфесійнае супрацьстаянне шырока карысталася супернікамі 
		ў <a href="../events/war_civil_1432.php">Грамадзянскай вайне 1432-38</a> (<a href="../figures/svidrigailo.php">Свідрыгайла</a> vs Жыгімонт) і імі ж яно было ліквідавана. 
		</p>
		<p>		
		Так, каб прыцягнуць да сябе праваслаўных прыхільнікаў Свідрыгайлы, 6 траўня 1434 года Жыгімонт выдаў Трокскі прывілей, 
		які зраўнаваў іх у правах з каталікамі. 

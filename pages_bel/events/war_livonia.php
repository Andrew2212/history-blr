		<p>
		1579 год. 30 жніўня пасля 20-ці дзённай аблогі войска Рэчы Паспалітай вызвалілі Полацк. Войскі Стэфана Баторыя дайшлі да Пскова і аблажылі горад. 
		Шведы ўзялі Нарву і аблажылі Арэшак.
		</p>
		<p>
		1582 Вайна скончылася падпісаннем Ям-Запольскага (1582 год) і Плюскага (1583 год) перамір'яў. Масковія пазбаўлялася ўсіх заваёваў, 
		зробленых у выніку вайны, а таксама зямель на мяжы з Рэччу Паспалітай і прыморскіх балтыйскіх гарадоў (Капор'я, Яма, Івангорада).
		 Тэрыторыя Лівонскай канфедэрацыі была падзелена паміж Рэччу Паспалітай, Швецыяй і Даніяй.

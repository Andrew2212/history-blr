		<p>
		Лівонская (Інфлянцкая) вайна (1558-1583) — спроба Маскоўскага цара Івана IV Жахлівага захапіць Лівонію. У вайне бралі ўдзел Лівонская канфедэрацыя, 
		Маскоўскае царства, <a href="../terms/wkl.php">Вялікае княства Літоўскае</a>, Шведскае і Дацкае каралеўствы. 
		Баявыя дзеянні вяліся галоўным чынам на тэрыторыі сучасных Эстоніі, Латвіі, <a href="../terms/belarus.php">Беларусі</a> і Паўночна-Заходняй Расіі.
		</p>
		<p>
		1558 Напад <a href="../terms/ross_empire.php">Маскоўскага царства</a> на Лівонію і захоп Нарвы, Дэрпта і шэрага іншых гарадоў.
		</p>
		<p>
		1561 па Віленскім дамовам Лівонская канфедэрацыя была ліквідавана, частка яе тэрыторыі ператваралася ў васальнае 
		 ў адносінах да ВКЛ герцагства Курляндыя і Семігалія, 
		іншая ж наўпроста ўвайшла ў склад Вялікага княства Літоўскага.
		 </p>
		<p>
		З гэтага часу вайна вялася паміж ВКЛ і Маскоўскім царствам.
		</p>
		<p> 
		1563 Маскоўскім войскам быў захоплены Полацк — нуйбуйны і найбагаты горад ВКЛ. Спусташэнне горада стала канцом велічы Полацка, 
		пасля якога ён больш ніколі не адрадзіўся. 
		Колькасць палонных жыхароў склала ад 15 000 да 60 000 чалавек, большасць з іх былі прададзены ў рабства.
		</p>
		<p>
		1564 год. 27 студзеня ў <a href="battle_ula.php">бітве на Уле</a> 6-ці тысячнае войска Мікалая 
		<a href="../figures/radziwilly.php">Радзівіла</a> Рудага і Рыгора <a href="../figures/hodkewich.php">Хадкевіча</a> 
		разграміла 20-ці тысячнае маскоўскае войска. Маскоўскі паход на беларускія землі быў спынены.
		</p>
		<p>
		1569 У кіраванне апошняга <a href="../figures/jagellony.php">Ягелона</a> — <a href="../terms/dukes_wkl.php">Жыгімонта II Аўгуста</a> 
		— ВКЛ па <a href="../events/unia_liublin.php">Люблінскай уніі</a> з'ядналася з Каралеўствам Польскім 
		у адзіную Рэч Паспалітую для абароны ад вонкавай агрэсіі.
		</p>

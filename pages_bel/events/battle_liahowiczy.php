		<p>
		— горад на рацэ Ведзьме ў Берасцейскай вобласці. Вядомы з XV стагоддзя ў ВКЛ як цэнтр аднайменнай воласці.
 		</p>		
		<p>
		 У 1572 годзе Сігізмунд II Аўгуст перадаў гарадок кашталяну віленскаму Яну Іяранімовічу Хадкевічу. 
		 Яго сын, гетман вялікі літоўскі <a href="../figures/hodkewich.php">Ян Кароль Хадкевіч</a> пабудаваў на месце невялікага драўлянага замка новы, каменны, бастыённага тыпу 
		 па самым лепшым тагачасным еўрапейскім узорам.  
		 У 1635 годзе замак і горад перайшлі ў валоданне Сапег.
		 </br>Пасля другога падзелу <a href="../terms/rzeczpospolita.php">Рэчы Паспалітай</a> замак дастаўся Расійскай імперыі і ў далейшым паступова разбураўся.
		</p>		

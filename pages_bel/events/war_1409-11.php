		<p>
		Вялікая вайна 1409-1411 — вайна паміж <a href="../terms/wkl.php">Вялікім княствам Літоўскім</a> у звязе з Каралеўствам Польскім з аднаго боку і 
		рыцарамі Тэўтонскага ордэна з іншага. На боку ВКЛ і Польшчы ваявалі Мазавецкае і Малдаўскае княствы і татары Джэлал ад-Дзіны. 
		Вайна пачалася з-за імкнення ВКЛ далучыць <a href="../places/samogitia.php">Самагітыю</a> і імкнення Ордэна да экспансіі ў прымежных польска-літоўскіх землях. 
		Стала вынікам зацяжнога канфлікту, што цягнуўся з часоў <a href="../figures/mindowg.php">Міндоўга</a>.
		</p>
		<p>
		Да XIV стагоддзя нямецкія рыцарскія ордэны занялі выйсці да Балтыйскага мора асноўных транспартных артэрый — Нёмана (Тэўтонскі ордэн) і Заходняй Дзвіны  
		(Лівонскі Ордэн Мечнікаў). Спрэчнай зямлёй заставалася самагітыя — трохкутнік паміж ВКЛ і Ордэнамі, 
		што не мае выйсця да мора і які служыў прадметам гандлю ў канфліктных сітуацыях.
		</p>
		<p>
		1226 "Залатая була Рыміні" — паклала пачатак экспаніі Тэўтонскага Ордэна ў Прыбалтыцы. Гэты дазвол, які імператар Святой Рымскай імперыі 
		Фрыдрых II выдаў Тэўтонскаму ордэну для вольнасці дзеянняў у Прусіі. 
		Була падала аўтаномію (фактычна, незалежнасць) рыцарам Ордэна, перадаўшы ім ва ўласнасць усе ажыццёўленыя 
		ці патэнцыйныя заваёвы на землях <a href="../terms/yotving.php">прусаў</a>. Була імператара была падпісана ў італьянскім горадзе Рыміні і замацавана залатой пячаткаю. 
		Паступова ўся Прусія трапіла пад уладу Тэўтонскага ордэна.
 		</p>
		<p>
		 У 1237 годзе Тэўтонскім ордэн увабраў у сябе рэшткі вайсковага братэрства рыцараў Мечнікаў (рыцараў Хрыста), тым самым далучыўшы Лівонію. 
		 На захопленыя землі прыбывалі нямецкія асаднікі. Да гэтага часу было захоплена Усходняе Памор'е. 
		 Такім чынам, да канца XIII стагоддзя ордэн, фактычна стаўшы дзяржавай, працягвае палітыку экспансіі на Усход. 
		 У 1309 годзе сталіцай Тэўтонскіх рыцараў стаў горад Марыенбург (ням. "Замак Марыі"; польск. Мальбарк).
		</p>
		<p>
		У XIV стагоддзі орэн здзейсніў звыш за сотню паходаў у межы Літвы.
		</p>
		<p>
		У час <a href="../events/war_civil_1381-84_1389-92.php">грамадзянскай вайны 1381-1384</a> (<a href="../figures/jagiello.php">Ягайла</a> vs <a href="../figures/vitovt.php">Вітаўт</a>) 
		Ордэну была перададзена Самагітыя, як плата за падтрымку ў канфлікце. 
		</p>
		<p>
		Пасля занятку Ягайлам польскага пасада па ўмовах <a href="unia_krewo.php">Крэўскай уніі</a>, і пасля ўрэгулявання адносін паміж 
		Ягайлам і Вітаўтам па <a href="unia_vilno.php">Астроўскаму пагадненню</a> 
		склаліся ўмовы для супольных дзеянняў Польшчы і ВКЛ супраць Тэўтонскага ордэна. 
		</p>
		<h3>1410 Грунвальдская бітва</h3>
		<p>
		Вырашальнай бітвай "Вялікай вайны" стала <a href="grunwald.php">Грунвальдская бітва</a> 1410. Звяз Каралеўства Польскага і Вялікага княства Літоўскага пад кіраўніцтвам 
		караля Уладзіслава II Ягайлы і вялікага князя літоўскага Вітаўта здабыў вырашальную перамогу над войскам Тэўтонскага ордэна. 
		Большасць рыцараў ордэна была забіта ці ўзята ў палон.
		</p>
		

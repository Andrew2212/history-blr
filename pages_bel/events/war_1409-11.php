		<p>
		Вынік вайны — у лютым 1411 года Польшча і ВКЛ склалі з Тэўтонскім ордэнам мірную дамову ў горадзе Торунь (Таруньскі мір), 
		па якой Ордэн вяртаў усе занятыя раней у Польшчы і Літвы тэрыторыі і выплачваў кантрыбуцыю, за ВКЛ замацоўвалася Самагітыя (Жамойць). 
		па наступнаму Таруньскаму міру 1466 Польшча вярнула частку сваіх зямель і атрымала выхад да Балтыйскага мора.

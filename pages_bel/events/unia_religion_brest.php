		<p>
		Пра прагрэсіўны характар уніі кажа жыццяпіс Мялеція Сматрыцкага (1577-1633) — публіцыста, філолага, архібіскупа Полацкага, Віцебскага, Амсціслаўскага, 
		заўзятага абаронцы праваслаўя, зацятага супраціўніка уніі, стваральніка "Граматыкі" — асновы царкоўнаславянскай граматычнай навукі на наступныя 200 гадоў. 
		У 50-летнім узросце ён прымае ўніяцтва.
		</p>
		<p>
		Нараду з магчымасцю рэлігійнага замірэння, унія падняла на якасна новы ўзровень усеагульную адукацыю — па ўсёй краіне адкрываліся школы і калегіумы 
		<a href="../terms/jesuity.php">езуітаў</a> і <a href="../terms/religion_blr.php">базыльянаў</a> (уніятаў), якія прынялі езуіцкія стандарты навучання. У іх прымалі да навучання ўсіх, незалежна ад рэлігійнай прыналежнасці. 
		Пра розніцу паміж уніяй і РПЦ шмат сказана ў класічнай працы А. Кіркора
		<a href="../download/kirkor.php">"Маляўнічая Расія. Беларускае і Літоўскае Палессе"</a> 1882 

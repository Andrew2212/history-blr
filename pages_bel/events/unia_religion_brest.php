		<p>
		Унія царкоўная (лат. злучэнне) — зліццё спавяданняў праваслаўнага і каталіцкага, прытым, з аднаго боку, 
		прызнаецца вяршэнства папы, чысцілішча, знаходжанне Святога Духу і ад Сына, 
		з іншай — дапушчаецца шлюб белага духавенства і набажэнства на роднай мове, з захаваннем усходніх абрадаў.	
		</p>
		

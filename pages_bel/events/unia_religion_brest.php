		<p>
		1573 Акт <a href="confederacia_warshawa.php">Варшаўскай канфедэрацыі</a> — абвяшчэнне роўнасці ўсіх рэлігій.
		</p>
		
		<p>
		1596 Берасцейская царкоўная унія — прынята для змяншэння межканфесійных супярэчнасцяў і зніжэння ўплыву Масквы на ўнутраныя справы дзяржавы 
		— пераход праваслаўных прыходаў у грэка-каталіцкія (уніяцкія). Як і ў 1439 годзе былі незадаволеныя. Пакутніцкай смерцю загінуў
		<a href="../figures/kunciewicz.php">св. Іасафат Кунцэвіч</a> — "апостал яднання", архібіскуп Полацкі, прапаведнік уніі.
		</p>
		
		<h2>Уніяцтва — "беларуская вера"</h2>
		<p>
		У маскоўскіх дакументах пачатку XVI стагоддзя — ужо праз 30 гадоў пасля Берасцейскай уніі — уніяцтва называецца "беларускай верай". Уніяты складалі 
		~40% насельніцтва, каталікі ~35%, вернікі РПЦ ~6%.
		</p>
		<p>
		Пераход ва ўніяцтва — <i>"Гэта неправільная фармуліроўка. Праваслаўныя заставаліся праваслаўнымі, проста біскупы дамовіліся, што цяпер яны пераходзяць 
		(ці вяртаюцца, у каталіцкім разуменні) у еднасць з Рымам."</i> — выправіў рэдакцыю прадстаўнік Рымска-каталіцкага Касцёла ў Беларусі.
		</p>
		<p>
		Уніяцтва было не столькі рэлігійным выбарам, колькі выбарам цывілізаванай мадэлі. Догмы і абрады праваслаўных, якія прынялі унію, заставаліся ранейшымі. 
		Аднак уплыў Маскоўскага патрыярхату, які жыў па Саборнаму Укладу (асноўнаму закону Маскоўскага царства), больш не аказваў ціск 
		на ўнутраныя справы ВКЛ.
		</p>
		

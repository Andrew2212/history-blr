		<p>
		<small>Энцыклапедычны слоўнік Бракгаўза і Яфрона, 1890</small> :
		</p>
		
		<i>		
		<p>
		"
		Унія не была выпадковай з'явай; яна не была і вынікам толькі асабістых разлікаў і свавольных дзеянняў асобных прадстаўнікоў праваслаўнай 
		царкоўнай іерархіі. Яна была абумоўлена гістарычным жыццём рускай праваслаўнай царквы ў 
		<a href="../terms/wkl.php">Літве</a> і з'явілася яе непасрэдным вынікам.
		</p>
		<p>
		Галоўная перашкода да ўводзін уніі палягала ў тым, што заходне-руская царква была падначалена маскоўскаму мітрапаліту, 
		які заўсёды быў дбайным супраціўнікам не толькі якога злучэння, а нават збліжэння з каталіцкай царквой.
		</p>
		<p>
		 Каля 1414 г. у Літве былі вельмі незадаволены маскоўскім мітрапалітам Фоціем.<a href="../figures/vitovt.php">Вітаўт</a>, які кіраваў у той час Літвой, вырашыў заснаваць 
		 самастойную літоўскую мітраполію. У 1415 г. у <a href="../places/nowogrudok.php">Навагрудку</a> быў скліканы сабор заходне-рускіх праваслаўных біскупаў, 
		 і мітрапалітам быў абраны балгарын Рыгор Цамбалак.
		</p>
		<p> 
		 Пасля смерці Вітаўта думка пра унію не знікала ў Літве. З нагоды злучэння цэркваў уваходзіў у зносіны з папай мітрапаліт Герасім, 
		 пастаўлены заходне-рускім мітрапалітам у 1433 г. з смаленскіх біскупаў. 
		 </br>Калі ў 1439 г. была заключана Фларэнтыйская унія, літоўскі ўрад прызнаў яе і права каталіцкага духавенства былі зраўнаваны 
		 з правамі праваслаўнага.
		 </p>
		<p> 
		 Падчас кіравання <a href="../terms/dukes_wkl.php">Аляксандра Ягелона</a> была створана новая спроба ўвесці ў Літве унію. У 1498 на кіеўскую мітраполію быў прызначаны смаленскі біскуп Язэп Балгарынавіч, які адразу пачаў дзейнічаць на карысць уніі з Рымам. 
		 Ён звярнуўся да папы Аляксандра VI з лістом, у якім спавядаў веру па ўзоры лацінскаму, прызнаваў фларэнтыйскі сабор 
		 і аддаваў сябе і сваю царкву пад заступніцтва папы.
		 </p>
		<p> 

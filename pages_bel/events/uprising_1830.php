		<h1>1830 Лістападаўскае паўстанне</h1>
		
		<p>
		<i>
		"Ідэі, якія ляжаць у аснове нашага сучаснага свету — мерытакратыя, роўнасць перад законам, права ўласнасці, 
		рэлігійная памяркоўнасць, сучасная секулярная адукацыя, здаровыя фінансы, і гэтай далей — былі ўзяты пад абарону, 
		кансалідаваны, кадыфікаваны і геаграфічна распаўсюджаны Напалеонам. Да іх ён дадаў рацыянальную і эфектыўную мясцовую адміністрацыю, канец вясковага бандытызму, заахвочанне навук і мастацтваў, 
		адмену феадалізму і найбуйнейшую кадыфікацыю законаў з часоў заняпадку Рымскай імперыі."
		</i>
		</br><small>A. Roberts, Visiting Professor at the Department of War Studies, King's College London</small>
		</p>
		

		<p>
		Ліпеньская рэвалюцыя 1830 года ў Францыі выступіла каталізатарам. У Царстве Польскім 29 лістапада 1830 года выбухнула паўстанне. 
		У кароткі тэрмін уся яго тэрыторыя была вызвалена ад рускай улады. 
		</p>
		<p>[ <small>
		Ліпеньская рэвалюцыя ці Французская рэвалюцыя 1830 года —  Другая французская рэвалюцыя,— паўстанне ў ліпені 1830 года ў Францыі, 
		што прывяло да скідання Карла Х і ўзвядзенню на пасад Луі-Піліпа. Ліберальная буржуазія з'ядналася з ніжнімі пластамі грамадства, 
		якія ўпершыню з 1795 года зноў атрымалі магчымасць уплываць на палітыку. Яна адзначыла ўрачыстасць прынцыпу народнага 
		суверэнітэту над прынцыпам боскага права караля, а таксама ўсталяванне ліберальнага рэжыму і канчатковая ўрачыстасць 
		буржуазіі над зямельнай арыстакратыяй.
		</small>
		</p>		
		<p><small>
		Ліпеньская рэвалюцыя мела ўплыў на ўсю Еўропу. Ліберальныя плыні паўсюдна знайшлі ўпэненасць і рашучасць. 
		У дзяржавах Германскага звязу пачаліся хваляванні, што выклікалі перагляд дзейных канстытуцый. 
		Хваляванні пачаліся і ў італьянскіх дзяржавах, у тым ліку, і ў Папскай вобласці. Найвялікі эфект Ліпеньская рэвалюцыя здзейсніла 
		на тэрыторыі Польшчы, выклікаўшы паўстанне 1830 года.
		</small> ]
		</p>
		<p>
		У Беларусі і Літве дзейнічала каля 30 паўстанцкіх атрадаў, якія налічвалі 12 тыс. чалавек. 
		Сярод інсургентаў былі такія людзі, як <a href="../figures/orda.php">Напалеон Орда</a>, <a href="../figures/domeyko.php">Ігнат Дамейка</a>, Эмілія Плятэр. 
		Старшынёй Часовага ўрада Літвы быў Тадэвуш Тышкевіч — <i>"граф на Лагойску і Бярдзічаве"</i>.		
		</p>
		

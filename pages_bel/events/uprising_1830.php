		<p>
		Паўстанне 1830-1831 гадоў — нацыянальна-вызваленчае паўстанне супраць улады Расійская імперыі 
		на тэрыторыі Царства Польскага, Літвы, Беларусі і Правабярэжнай Украіны.
		</p>
		
		<p>
		У гады Напалеонаўскіх войнаў на землях Польскіх было створана Герцагства Варшаўскае 
		 (пазней царства Польскае), Напалеон падтрымліваў стварэнне Часовага ўрада ВКЛ. 
		 Напалеонаўскія войны выклікалі ўздым патрыятычных настрояў на землях Рэчы Паспалітай і ў Расійскай імперыі (Паўстанне дзекабрыстаў 1825 года).
		</p>
		<p>[ <small>
		Царства Польскае. Вялікае Герцагства Варшаўскае — дзяржава, утвораная ў 1807 годзе па Тыльзіцкім міры з польскіх тэрыторый, 
		якія адышлі падчас Падзелаў Рэчы Паспалітай да Прусіі і Аўстрыйскай імперыі. Герцагства Варшаўскае з'яўлялася пратэктаратам напалеонаўскай
		Францыі і праіснавала да 1813 года, калі большая частка герцагства была далучана да Расійскай імперыі як аўтаномнае Царства Польскае.
		</small> ]
		</p>
		<p>
		Усведамленне страты дзяржаўнасці, якое падсілкоўвалася сістэмай адукацыі, арыентаванай на польска-літвінскую культуру 
		(у Віленскім універсітэце, Полацкай езуіцкай Акадэміі, большасці школ выкладанне вялося на польскай), фармавала патрыятычныя 
		настроі ў асяроддзі <a href="../terms/shliahta.php">мясцовай шляхты</a>, выклікала пратэст супраць расійскага панавання, спараджала імкненне да змагання
		за аднаўленне Рэчы Паспалітай.
		</p>
		<p>
		Ствараліся патрыятычныя супольнасці — <a href="../terms/filomaty.php">філаматы</a> ў Вільні, Патрыятычнае Грамадства ў Польшчы. У Царстве Польскім, якое мела свой сойм і войска,  
		рух ахапляў многіх армейскіх афіцэраў, шляхту, студэнцтва.  
		</p>
		

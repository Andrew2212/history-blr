		<p>
		На пачатку жніўня 1831 года на ўсёй тэрыторыі Беларусі паўстанне было падаўлена. Многія актыўныя паўстанцы, у тым ліку з тэрыторыі сучаснай Беларусі, пакінулі радзіму і пазней удзельнічалі ў барацьбе за свабоду ў розных краінах Еўропы.
		</p>		
		<p>
		Сваіх маёнткаў пазбавіліся буйныя магнаты — <a href="../figures/oginskie.php">Агінскія</a>, Плятэры, 
		<a href="../figures/radziwilly.php">Радзівілы</a>, <a href="../figures/sapegi.php">Сапегі</a>, Чартарыйскія і інш. Увогуле да следства за ўдзел у паўстанні 
		ў беларуска-літоўскіх губернях было прыцягнута 2878 чалавек. пачалася "дэпаланізація" (русіфікацыя) беларускіх земляў.
		</p>		

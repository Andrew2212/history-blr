		<p>
		У 1398 годзе хан Тахтамыш, нашчадак Чынгісхана, які змагаўся за аб'яднанне Залатой Арды пад сваёй аднаасобнай уладай, сутыкнуўся з моцным і небяспечным супернікам — ханам Цімурам Кутлугай. 
		Пацярпеўшы паразу, Тахтамыш бег разам з сям'ёй да Вітаўта, быў прыняты і паселены ў г. Ліда.
		 </p>
		<p>
		Услед за ім прыйшлі татары з сем'ямі і маёмасцю — у краіну, дзе ім абяцалі зямлю і свабоднае вызнанне мусульманскай веры. 
		Татары, якія мелі статус <a href="../terms/shliahta.php">шляхты</a>, захавалі яго і ў ВКЛ. На працягу цэлага стагоддзя пасля перасялення, 
		яны мелі магчымасць атрымаць дакументы ад крымскіх ханаў пра сваіх продкаў.
		</p>
		<p>
		У пярэстую <a href="../terms/religion_blr.php">рэлігійную карціну Беларусі</a> дадаўся іслам.
		</p>
		<p>
		З цягам часу для татар стала роднай <a href="../terms/blr_mova.php">беларуская мова</a>, а свая ж была паступова забытая. 
		Вельмі яскравая і ўнікальная з'ява — кітабы — татарскія кнігі, якія былі напісаны
		на беларускай мове арабскім вязьмом.

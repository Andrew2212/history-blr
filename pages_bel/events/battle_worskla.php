		Бітва на Ворсклі 12 жніўня 1399 — бітва за ўладу над землямі Залатой Арды. 
		Адна з найбуйнейшых бітваў XIV стагоддзя ва Усходняй Еўропе. Завяршылася паразай літоўскага войска.

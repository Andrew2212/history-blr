		Хоцінская бітва 1673 года	
		</h3>
		<p>
		Хоцінская бітва 11 лістапада 1673 года — бітва падчас польска-турэцкай вайны 1672-1676 гадоў за ўладу над Правабярэжнай Украінай. 
		</br>Аб'яднанае польска-літоўскае войска пад камандаваннем кароннага гетмана 
		Яна Сабескага  разграміла турэцкую армію на чале з Гусэйн-пашой (каля 35 тысяч чалавек), 
		якая ўмацавалася ў старым лагеры часоў першай Хоцінскай бітвы (1621).
		</p>
		<p>
		Разгром турак быў поўны. Толькі некалькім тысячам з 35-тысячнага войска ўдалося сысці ў заняты туркамі Камянец.
		Рэшткі турэцкая арміі былі знішчаныя ці ўзятыя ў палон.
		</br>Перамога моцна ўмацавала прэстыж гетмана Яна Сабескага, які атрымаў мянушку "Хоцінскі леў", і дапамагла яму пры абранні на польскі трон.

		Армія Рэчы Паспалітай ўзмацнілася трыма тысячамі наймітаў, якія перайшлі на службу да <a href="../terms/dukes_wkl.php"> Сігізмунда III</a>, 
		і васьмітысячным рускім войскам Валуева, які прысягнуў каралевічу Уладзіславу пасля паразы. 

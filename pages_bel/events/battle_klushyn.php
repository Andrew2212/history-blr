		які прысягнуў Уладзіславу (сыну Сігізмунда III) і ўпусціў у Маскву войска Рэчы Паспалітай. 
		Такім чынам, перамога пры Клушыне на некаторы час прывяла да ліквідацыі Маскоўскай дзяржавы.
		</p>
		

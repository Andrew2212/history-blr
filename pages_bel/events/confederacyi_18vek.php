		<h1>Шляхецкія канфедэрацыі XVIII стагоддзя
		</br><small>часовыя палітычныя звязы <a href="../terms/shliahta.php">шляхты</a></small>
		</h1>
		<p>
		У  <a href="../times/18_vek.php">XVIII стагоддзі</a> краіна, якая перажыла <a href="blood_potop.php">"Крывавы Патоп"</a>
		 і <a href="war_civil_1696.php">грамадзянскую вайну</a>, якая ўгразла ў <a href="war_great_north.php">Вялікай Паўночнай вайне</a>,
		 адчувала крызіс дзяржаўнай улады. 
		 </br>Змаганне "шляхецкай дэмакратыі" з каралеўскай уладай, міжканфесійныя супярэчнасці і 
		 асабістыя цікавасці магнацкіх родаў (чыя ўлада на землях <a href="../terms/wkl.php">ВКЛ</a> была супастаўная з каралеўскай) спараджалі анархію.
		 Для пошуку шляхоў захавання дзяржавы збіраліся шляхецкія канфедэрацыі. 
		</p>
		
		<h4><small>1696-1702</small> Адбываецца <a href="../events/war_civil_1696.php">Грамадзянская вайна ВКЛ</a></h4>
		
		<h4><small>1700-1721</small> Адбываецца <a href="../events/war_great_north.php">Вялікая Паўночная вайна</a></h4>
		
		<h3><small>1715</small> Тарнаградская канфедэрацыя</h3> 
		<p>
		— аб'яднанне польскай шляхты, якая выступіла супраць прапанаванага Аўгустам II абсалютызму. 
		Не жадаючы ўзмацнення ўлады Аўгуста II у Рэчы Паспалітай, гэтую канфедэрацыю падтрымаў рускі цар Пётр I. 
		Дзейнасць канфедэрацыі была накіравана асабіста супраць караля, які спрабаваў скарыстаць уведзеныя ў 
		Рэч Паспалітую з Саксоніі нямецкія войскі з мэтай далейшага ўзмацнення сваёй улады.
		</p>
		
		<h3><small>1716</small> Віленская канфедэрацыя</h3>
		<p> 
		— аб'яднанне вялікалітоўскай шляхты, аналагічнае Тарнаградскай канфедэрацыі польскай шляхты.
		</p>
		
		<h3><small>1767</small> Слуцкая канфедэрацыя</h3>
		<p>  
		— звяз пратэстанцкай (кальвіністаў, лютэранаў) і праваслаўнай шляхты ВКЛ, які быў створаны з мэтай раўнання ў правах 
		з каталіцкай шляхтай. Расійская імператрыца Кацярына II прыняла канфедэрацыю пад "імператарскую пратэкцыю" і даслала ёй у падтрымку 20-тысячнае войска.
		</p>
		
		<h3><small>1767</small> Торуньская канфедэрацыя</h3> 
		<p> 
		— утворана ў адзін дзень са Слуцкай канфедэрацыяй на тэрыторыі Польшчы, таксама з мэтай падтрымкі правоў дысідэнтаў у Рэчы Паспалітай.
		</p>
		
		<h3><small>1767</small> Радамская канфедэрацыя</h3>
		<p>  
		— утворана ў Рэчы Паспалітай супраць спробаў караля 
		Станіслава Панятоўскага і яго асяроддзя ўзмацніць цэнтральную ўладу. Утваралася пры падтрымцы расійскага пасла 
		М. В. Рэпніна і пад абаронай расійскіх войскаў. Узмацніла ўплыў Расіі ў Рэчы Паспалітай.
		</p>
		
		<h3><small>1768</small> Барская канфедэрацыя</h3> 
		<p> 
		— утворана ў Рэчы Паспалітай у процівагу Слуцкай, Торуньскай і Радамскай канфедэрацыям 
		для абароны ўнутранай і вонкавай самастойнасці Рэчы Паспалітай ад ціску Расіі і супраць дарадцаў караля, 
		якія жадалі абмежаваць уладу магнатаў.
		</p>
		<p>
		Вайсковыя падзеі Барскай канфедэрацыі на тэрыторыі ВКЛ праходзілі пад кіраўніцтвам <a href="../figures/radziwilly.php">Караля Радзівіла</a> (1768), 
		<a href="../figures/sapegi.php">Сапегі</a> і <a href="../figures/pacy.php">Паца</a> (1769), 
		<a href="../figures/oginskie.php">Агінскага</a> і Красінскага (1770-1771). Расійскімі войскамі камандаваў А. Сувораў.  
		</p>
		<p>
		У сакавіку 1772 быў раззброены ў Рэчыцкім павеце апошні партызанскі атрад. 5455 удзельнікаў канфедэрацыі, 70 % якіх складалі сяляне, 
		былі сасланы ў Сібір на катаргу. 
		</p>
		<p>
		Барская канфедэрацыя існавала 4 гады і была разгромлена расійскімі войскамі і адданымі каралю атрадамі Рэчы Паспалітай.
	    </p>

		<h2>Храналогія падзей</h2>						
		
		<h3>Грамадзянская вайна ў ВКЛ 1381-1384 гадоў</h3>
		<p>
		 — першы эпізод змагання за ўладу паміж стрыечнікамі: вялікім князем літоўскім Ягайлам і князем Вітаўтам.
		</p>
		
		<p>
		1377 Памірае вялікі князь Альгерд. Па тэстаменце, у абыход традыцыйнага ўспадкоўвання, новым вялікім князем робіцца Ягайла. Кейстут, брат Альгерда, які досыць аўтаномна кіраваў у Троках, адсунуты на другі план. 
		</p>
		<p>
		Ягайле было 30 гадоў і ён шукаў шляхі мадэрнізацыі краіны, быў гатовы да прыняцця хрысціянства. Кейстуту было 80 гадоў і ён быў наважным супернікам хрышчэння.
		</p>
		<p>
		1378 Тэўтонскі Ордэн арганізаваў буйную вайсковую кампанію супраць Літвы. Крыжакі дайшлі да Бярэсця і выйшлі да Прыпяці. 
		Лівонскі Ордэн уварваўся ў Упіцкую зямлю. Чарговая кампанія пагражала сталіцы дзяржавы — Вільне.
		</p>
		<p>1380 Ягайла без узгаднення з Кейстутам заключыў перамір'е з Лівонскім Ордэнам для абароны сваіх земляў у ВКЛ. З Тэўтонскім Ордэнам ён падпісаў Давыдзішкаўскую дамову пра супольны ненапад.
		</p>
		<p>
		1381 Тэўтонскі ордэн зноў уварваўся ў землі Кейстута і апавясціў яго пра сваю дамову з Ягайлам. Кейстут вырашае выступіць супраць Ягайлы.
		</p>
		

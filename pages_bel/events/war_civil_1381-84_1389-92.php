		<h3>Грамадзянская вайна ў ВКЛ 1389-1392 годов</h3>
		<p>
		 — другі эпізод змагання за ўладу паміж стрыечнікамі: Ягайлам і Вітаўтам.
		</p>
		
		
		<p>
		1389 ведаючы пра хваляванні ў Вялікім княстве, Ягайла паслаў у Вільню польскі гарнізон і паспрабаваў мірам уладзіць нарастальны канфлікт паміж Вітаўтам і Скіргайлам. Вітаўт быў змушаны падпісаць дамову, па якой абавязаўся быць лаяльным да Скіргайлы і падтрымваць яго.
		</p>
		<p>
		1390 Вітаўт зноў звяртаецца за дапамогай да Тэўтонскага Ордэна — заключае <i>Лікскую дамову</i>, якае перадае Самагітыю Ордэну за падтрымку ў змаганні з Ягайлам.
		</p>
		<p>
		1390 Супернікі здзейснілі некалькі баявых паходаў. Ягайла заняў Гародню, Вітаўт разбурыў Крывой замак пад Вільняй.
		</p>
		<p>
		1391 Дачка Вітаўта Сафія выходзіць замуж за князя Маскоўскага Васіля I. Вітаўт атрымвае падтрымку на ўсходзе краіны.
		</p>
		<p>
		1392 Магістр Ордэна Конрад фон Валенрод, прыхільнік Вітаўта, сабраў еўрапейскіх наймітаў і арганізаваў паход на Вільню. 
		У Коўне войска зладзілі багаты баль, апісаны <a href="../figures/mickiewicz.php">Адамам Міцкевічам</a> у паэме "Конрад Валенрод". Яны спустошылі блізкія гарады — 
		<a href="../figures/kesgajla.php">Вількомір</a> (Укмергэ) і Майшаголу, 
		пры гэтым замак ў апошнім горадзе быў спалены і больш не быў адноўлены. 
		</p>
		<p>
		1392 Уладзіслаў, князь Апольскі і Галіцкі, заснавальнік Чанстахоўскага манастыра ў Яснай Гары, прадаў стратэгічна важную крэпасць Златорыя Тэўтонскаму Ордэну 
		і прапанаваў план па падзелу Польшчы паміж Ордэнам, Свяшчэннай Рымскай імперыяй, Сілезіяй і Венгрыяй.
		Ягайла паспрабаваў захапіць яго княства, але патрываў паразу.
		</p>
		

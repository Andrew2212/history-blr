		<h4>Гражданская война в ВКЛ 1389-1392 годов</h4>
		<p>
		 — второй эпизод борьбы за власть между двоюродными братьями: Ягайло и Витовтом.
		</p>
		
		
		<p>
		1389 зная о волнениях в Великом княжестве, Ягайло послал в Вильну польский гарнизон и попытался миром уладить нарастающий конфликт между Витовтом и Скиргайло. Витовт был вынужден подписать договор, по которому обязался быть лояльным к Скиргайло и поддерживать его.
		</p>
		<p>
		1390 Витовт вновь обращается за помощью к Тевтонскому Ордену — заключает <i>Ликский договор</i>, передающий Самогитию Ордену за поддержку в борьбе с Ягайло.
		</p>
		<p>
		1390 Противники совершили несколько боевых походов. Ягайло занял Гродно, Витовт разрушил Кривой замок под Вильней.
		</p>
		<p>
		1391 Дочь Витовта София выходит замуж за князя Московского Василия I. Витовт получает поддержку на востоке страны.
		</p>
		<p>
		1392 Магистр Ордена Конрад фон Валленрод, сторонник Витовта, собрал европейских наемников  и организовал поход на Вильну. 
		В Ковно войска устроили богатый пир, описанный <a href="../figures/mickiewicz.php">Адамом Мицкевичем</a> в поэме "Конрад Валленрод". Они разорили близлежащие города — 
		<a href="../figures/kesgajla.php">Вилькомир</a> (Укмерге) и Майшаголу, 
		при этом замок в последнем городе был сожжен и более не был восстановлен. 
		</p>
		<p>
		1392 Владислав, князь  Опольский и Галицкий, основатель Ченстоховского монастыря в Ясной Горе, продал стратегически важную крепость Златория Тевтонскому Ордену. 
		и предложил план по разделу Польши между Орденом, Священной Римской империей, Силезией и Венгрией.
		Ягайло попытался захватить его княжество, но потерпел поражение.
		</p>
		

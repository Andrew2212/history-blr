		<p>
		1381 Кейстут заняў Вільню, і ўзяў Ягайлу ў палон. Пасля таго, як Ягайла пісьмова прызнае Кейстута вялікім князем, яму вяртаюць волю.
		</p>
		<p>
		1382 У Вільні выбухнула паўстанне прыхільнікаў Ягайлы. Купецтва было незадаволенае антынямецкай палітыкай Кейстута, што перашкаджала гандлю.
		</p>
		<p>
		1382 Войскі Кейстута і Вітаўта з аднаго боку, Ягайлы і Лівонскага ордэна з іншай, сустрэліся каля Трок. 
		Кейстут, бачачы перавагу супернікаў, пайшоў на перамовы. У лагеры Ягайлы абодва князя — Кейстут і Вітаўт — былі схоплены і зняволены ў Крэўскі замак.
		</p>
		<p>
		1382 У Крэўскім замку Кейстут памірае (ці быў забіты). Вітаўту шанцуе бегчы са зняволення.
		</p>
		<p>
		1382 Ягайла рыхтуе з Тэўтонскім ордэнам <i>Дубіскую дамову</i> пра сяброўства, абяцаючы наўзамен Самагітыю (дамова так і не была падпісана)
		</p>
		<p>
		1382 Вітаўт едзе ў землі Тэўтонскага Ордэна, прымае хрышчэнне па каталіцкім абрадзе 
		</p>
		<p>
		1384 Вітаўт падпісвае з Тэўтонскім Ордэнам  <i>Кёнігсбергскую дамову</i>, па якой прызнае сябе васалам Ордэна і перадае ім Самагітыю
		</p>
		<p>
		1384 Ягайла рыхтуецца да дынастычнага звязу (шлюбу) спачатку з Масквой, потым з Польшчай. Яму патрэбен мір ў краіне і ён злагоджваецца з Вітаўтам. 
		Разам яны ідуць вайной у землі Ордэна і спальваюць свежазбудаваную крэпасць у Коўна.
		</p>
		<p>
		1384 Ягайла падаруе Вітаўту землі на Валыні (Луцк). Дзеля кіравання ў праваслаўнай Валыні Вітаўт прымае праваслаўе.
		</p>
		
		<h3>1385 Крэўская унія </h3>
		<p>
		— пагадненне пра дынастычны звяз ВКЛ і Польшчы праз шлюб вялікага князя Ягайла з каралевай Ядвігай. 
		Ягайла, застаючыся вялікім князем Літоўскім, робіцца каралём Польшчы. 
		</p>
		<p>
		1387 Ягайла вяртаецца ў Вільню і супольна з Вітаўтам праводзіць актыўнае "хрышчэнне Літвы і Жмудзі" ў каталіцкую веру.
		</p>
		<p>
		Пасля каранавання Ягайлы становішча Літвы змянілася. Ён кіраваў абедзвюма дзяржавамі з Кракава і тытулаваўся каралём Польшчы і вялікім князем Літоўскім. 
		Намеснікам Ягайлы ў Вільні быў яго брат Скіргайла. У ВКЛ сфарміравался апазіцыя кракаўскаму кіраванню на чале з Вітаўтам. 
		</p>
		
		

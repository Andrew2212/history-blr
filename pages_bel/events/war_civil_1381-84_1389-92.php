		правление Ягайло вызывает недовольство в ВКЛ, и война за власть возобновляется. 
		Противостояние не выявило явного победителя, что принудило стороны идти на компромисс. 
		По условиям заключенного в 1392 году Островского соглашения, Витовт признал Ягайло верховным сюзереном Литвы, а сам стал Великим князем Литовским.
		</p>

		<h3>Хронология событий</h3>						
		
		<h4>Гражданская война в ВКЛ 1381-1384 годов</h4>
		<p>
		 — первый эпизод борьбы за власть между двоюродными братьями: великим князем литовским Ягайло и князем Витовтом.
		</p>
		
		<p>
		1377 Умирает в. князь Ольгерд. По завещанию, в обход традиционного наследования, новым в. князем становится Ягайло. Кейстут, брат Ольгерда, достаточно автономно правивший в Троках, отодвинут на второй план. 
		</p>
		<p>
		Ягайло было 30 лет и он искал пути модернизации страны, был готов к принятию христианства. Кейстуту  было 80 лет и он был решительным противником крещения.
		</p>
		<p>
		1378 Тевтонский Орден организовал крупную военную кампанию против Литвы. Крестоносцы дошли до Берестья и вышли к Припяти. 
		Ливонский Орден вторгся в Упитскую землю. Очередная кампания угрожала столице государства — Вильне
		</p>
		<p>1380 Ягайло без согласования с Кейстутом заключил перемирие с Ливонским Орденом для защиты своих земель в ВКЛ. С Тевтонским Орденом он подписал Довидишковский договор о совместном ненападении.
		</p>
		<p>
		1381 Тевтонский Орден снова вторгся в земли Кейстута и известил его о своем договоре с Ягайло. Кейстут решает выступить против Ягайло.
		</p>
		

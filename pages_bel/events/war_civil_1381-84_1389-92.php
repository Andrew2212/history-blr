		<h2>Гражданская война в ВКЛ 1381-84 и 1389-92
		</br><small>Витовт и Кейстут vs Ягайло</small>
		</h2>
		
		<p>
		Война за власть в <a href="../terms/wkl.php">Великом княжестве Литовском</a> после смерти в. кн. <a href="../figures/olgerd.php">Ольгерда</a> и избрания новым великим князем его сына 
		<a href="../figures/jagiello.php">Ягайло</a>. 
		За княжеский титул с Ягайло боролись брат Ольгерда <a href="../figures/kejstut.php">Кейстут</a> и его сын <a href="../figures/vitovt.php">Витовт</a>.
		</p>
		<p>
		На короткий период (1381-82) Кейстуту удалось стать правителем ВКЛ, однако после его смерти в плену в <a href="../places/krevo.php">Кревском замке</a>
		 титул вернулся к Ягайло.
		Борьбу за титул продолжил Витовт. В период войны оба противника поочередно обращались за помощью к Тевтонскому Ордену, 
		обменивая <a href="../places/samogitia.php">Самогитию</a> на военную и политическую помощь.	
		</p>
		

		И для Ягайло, и для Витовта война шла не слишком удачно, в то время как земли ВКЛ разорялись все более и более. 
		Польская шляхта всё больше разочаровывалась в войне: Кревская уния должна была усилить их влияние в Галиции, Молдавии и Валахии, 
		а привела к новым проблемам на севере. Ягайло начинает мирные переговоры с Витовтом, закончившиеся Островецким соглашением.

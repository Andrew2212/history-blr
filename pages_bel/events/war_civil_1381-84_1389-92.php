		<h1>1381-92 Грамадзянская вайна
		</br><small>Вітаўт і Кейстут vs Ягайла</small>
		</h1>
		
		<p>
		Вайна за ўладу ў <a href="../terms/wkl.php">Вялікім княстве Літоўскім</a> пасля смерці вялікага князя <a href="../figures/olgerd.php">Альгерда</a> і абрання новым вялікім князем яго сына 
		<a href="../figures/jagiello.php">Ягайла</a>. 
		За княскі тытул з Ягайлам змагаліся брат Альгерда <a href="../figures/kejstut.php">Кейстут</a> і яго сын <a href="../figures/vitovt.php">Вітаўт</a>.
		</p>
		<p>
		На кароткі перыяд (1381-82) Кейстуту ўдалося стаць уладцам ВКЛ, аднак пасля яго смерці ў палоне ў <a href="../places/krevo.php">Крэўскім замку</a>
		 тытул вярнуўся да Ягайлы.
		Барацьбу за тытул працягнуў Вітаўт. У перыяд вайны абодва суперніка па чарзе звярталіся за дапамогай да Тэўтонскага ордэна, 
		мяняючы <a href="../places/samogitia.php">Самагітыю</a> на вайсковую і палітычную дапамогу.	
		</p>
		

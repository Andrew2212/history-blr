		Супрацьстаянне не выявіла выяўнага пераможца, што прымусіла бакі ісці на кампраміс. 
		Па ўмовах заключанага ў 1392 Астроўскага пагаднення, Вітаўт прызнаў Ягайлу вярхоўным сюзэрэнам Літвы, а сам стаў Вялікім князем Літоўскім.

		<p>
		1381 Кейстут занял Вильню, и взял Ягайло в плен. После того, как Ягайло письменно признает Кейстута великим князем, ему возвращают свободу.
		</p>
		<p>
		1382 В Вильне вспыхнуло восстание сторонников Ягайло. Купечество было недовольно антинемецкой политикой Кейстута, препятствовавшей торговле.
		</p>
		<p>
		1382 Армии Кейстута и Витовта с одной стороны, Ягайло и Ливонского ордена  с другой, встретились около Трок. 
		Кейстут, видя преимущество противников, пошел на переговоры. В лагере Ягайло оба князя — Кейстут и Витовт — были схвачены и заключены в Кревский замок.
		</p>
		<p>
		1382 В Кревском замке Кейстут умирает (или был убит). Витовту удается бежать из заключения.
		</p>
		<p>
		1382 Ягайло готовит с Тевтонским Орденом <i>Дубисский договор</i> о дружбе, обещая взамен Самогитию (договор так и не был подписан)
		</p>
		<p>
		1382 Витовт едет в земли Тевтонского Ордена, принимает крещение по католическому обряду 
		</p>
		<p>
		1384 Витовт подписывает с Тевтонским Орденом  <i>Кёнигсбергский договор</i>, по которому признает себя вассалом Ордена и передает им Самогитию
		</p>
		<p>
		1384 Ягайло готовится к династическому союзу (браку) сначала с Москвой, потом с Польшей. Ему нужен мир в стране и он замиряется с Витовтом. 
		Вместе они идут войной в земли Ордена и сжигают свежепостроенную крепость в Ковно.
		</p>
		<p>
		1384 Ягайло дарует Витовту земли на Волыни (Луцк). Ради правления в православной Волыни Витовт принимает православие.
		</p>
		
		<h4>1385 Кревская уния </h4>
		<p>
		— соглашение о династическом союзе ВКЛ и Польши через брак в. кн. Ягайло с королевой Ядвигой. 
		Ягайло, оставаясь в. князем Литовским, становится королем Польши. 
		</p>
		<p>
		1387 Ягайло возвращается в Вильню и совместно с Витовтом проводит активное "крещение Литвы и Жмуди" в католическую веру.
		</p>
		<p>
		После коронации Ягайло положение Литвы изменилось. Он управлял обоими государствами из Кракова и титуловался королём Польским и великим князем Литовским. 
		Наместником Ягайло в Вильне был его брат Скиргайло. В ВКЛ сформировалась оппозиция краковскому правлению во главе с Витовтом. 
		</p>
		
		

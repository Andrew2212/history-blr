		<p>
		Бітва каля Кірхгольма (зараз Саласпілс, 18 км ад Рыгі) — 
		адна з найбуйнейшых бітваў <a href="war_1600.php">вайны 1600-1611</a> са Швецыяй за Эстонію. 
		</br>Войска ВКЛ пад камандаваннем вялікага гетмана Літоўскага 
		<a href="../figures/hodkewich.php">Яна Караля Хадкевіча</a> змагаліся з утрая пераважаючымі
		па колькасці шведамі.
		</p>
		<p>
		Бітва скончылася вырашальнай перамогай войска <a href="../terms/wkl.php">ВКЛ</a> і запомнілася як адзін з найвялікшых трыўмфаў кавалерыі 
		<a href="../terms/rzeczpospolita.php">Рэчы Паспалітай</a>.
		Зыход бітвы прадвызначыў вырашальны ўдар <a href="../terms/husaria.php">крылатых гусараў</a>. 
		</p>
		<p>
		Гетман Хадкевіч валодаў сіламі, якія налічвалі 4500 чалавек: 
		2500 <a href="../terms/husaria.php">крылатых гусараў</a>, 1300 пяхотнікаў, татарскія вершнікі, якія складалі авангард.
		</p>
		<p>
		Сілы Карла IX Шведскага былі колькасна пераўзыходзячымі - 12 500 чалавек: 11 000 пяхоты (8500 капейшчыкаў, 2500 мушкяцёраў), 2500 кавалярыстаў і 11 гармат. Шведская армія ўключала некалькі тысяч нямецкіх і галандскіх наймітаў, а таксама некалькі соцен шатландцаў.
		</p>
		
		<p>
		Паколькі вусце Дзвіны з часоў <a href="../terms/polotsk_principality.php">Полацкага княства</a> было зонай цікавасці беларускіх князёў, удзел польскіх войскаў у гэтай бітве Рэчы Паспалітай было 
		сціплым — усяго 200 гусар з 2500 змагароў (ВКЛ і Польшча ў Рэчы Паспалітай мелі ўласныя казну і войска) 
		</p>

		<p>
		Галоўнай падставай для бітвы была шведская аблога Рыгі, распачатая 23 верасня 1605 года. Яе ўзяццё дазволіла б шведам амаль бесперашкодна заняць Ліфляндыю цалкам.
		</p>
		<p>
		Вайна са Швецыяй 1600-1611 гадоў — працяг серыі ваенных канфліктаў па падзелу Лівоніі (Liefland & Curland), распачатых у XVI стагоддзі. 
		</br>
		Іншай прычынай вайны была барацьба за шведскі прастол паміж герцагам Карлам і Сігізмундам III Вазай.
		</p>
		<p>
		Вытокі канфлікту паміж Рэччу Паспалітай і Швецыяй узыходзяць да падзей шведскай грамадзянскай вайны за трон (1597-1599), калі Сігізмунд III Ваза, які адначасовы быў каралём і Рэчы Паспалітай, і Швецыі, пацярпеў паразу.
		</p>
		

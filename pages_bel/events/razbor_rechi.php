		<p>
		 1767 Кацярына II ініцыюе прыняцце "кардынальных правоў", якія ліквідавалі вынікі пачатых раней прагрэсіўных рэформаў 1764 года. 
		</p>
		<p>
		 1768 Расійскія войскі бяруць удзел у заглушэнні <a href="confederacyi_18vek.php">Барскай канфедэрацыі</a>, 
		 незадаволенай умяшаннем Расіі.
		</p>
		<p>
		1772 Першы падзел. Расія, Аўстрыя і Прусія падпісалі ў Вене канвенцыю пра падзел Рэч Паспалітай. Іх войскі адначасова ўвайшлі на тэрыторыю 
		краіны і занялі вобласці, размеркаваныя паміж імі па пагадненні. 
		</p>
		<p>
		Не маючы магчымасці абараніць цэласць краіны ўзброеным шляхам, <a href="../terms/shliahta.php">шляхта</a> спрабавала не дапусціць зацверджання яго юрыдычна. 
		Супольна з іншымі шляхцічамі, сярод якіх былі Самуэль Карсак (Наваградак) і Станіслаў Багушэвіч (Мінск), 
		<a href="../figures/rejtan.php">Тадэвуш Рэйтан</a> спрабаваў сарваць сойм, каб не дапусціць зацверджання Перашага падзелу Рэчы Паспалітай. 
		Калі ўсе фармальныя юрыдычныя сродкі былі вычарпаны, Тадэвуш Рэйтан, дамагаючыся ад Сойма пратэсту супраць падзелу, 
		лёг перад выхадам са словамі: "Забіце мяне, не забівайце Айчыну!"
		</p>
		<p>
		1791 Прыняцце <a href="constitution_3may.php">Канстытуцыі 3 траўня</a> пацягнула за сабой умяшанне з боку Расіі, якая асцерагалася аднаўлення Рэчы Паспалітай у межах 1772 года.
		</p>
		<p>
		1793 Другі падзел. Прусія і Расія падпісалі канвенцыю пра другі падзел Рэчы Паспалітай.
		</p>
		<p>
		1794-95 <a href="uprising_1794.php">Паўстанне Касцюшкі</a>
		</p>
		<p>
		1795 Трэці падзел. Кароль <a href="../figures/poniatowski.php">Станіслаў Аўгуст ІІ Панятоўскі</a> злажыў свае паўнамоцтвы 25 лістапада. 
		</p>
		<p>
 		XIX стагоддзе — <a href="../download/blr_people.php">беларускія землі</a> ўпершыню ўвайшлі ў склад маскоўскай дзяржавы. 
		 </p>
		<p>
		1839 Ліквідаваны <a href="unia_religion_brest.php">Берасцейская царкоўная ўнія</a> і агульнаадукацыйная сістэма. 
		 </p>
		<p>
		1840 Спыніў сваё дзеянне <a href="../terms/statut.php">Статут ВКЛ</a> на беларускіх землях ВКЛ. 

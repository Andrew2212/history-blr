		<p>
		Падзелы <a href="../terms/rzeczpospolita.php">Рэчы Паспалітай</a> — падзел тэрыторыі дзяржавы паміж Прускім каралеўствам, 
		<a href="../terms/ross_empire.php">Расійскай імперыяй</a> і Аўстрыйскай манархіяй у канцы XVIII стагоддзя (1772-1795).
		</p>
		<p>
		Першы падзел адбыўся ў 1772 годзе, другі — 23 студзеня 1793 года, трэці — 24 кастрычніка 1795 года.
		</p>
		
		<p>
		XVIII стагоддзе было ў Рэчы Паспалітай стагоддзем магнацкай анархіі. Неабмежаваная <a href="../terms/shliahta.php">шляхецкая дэмакратыя</a> прывяла да паслаблення цэнтральнай 
		каралеўскай улады і ўзмацненню магнацкіх кланаў. 
		</p>
		<p>XVIII стагоддзе пачалося з <a href="war_civil_1696.php">грамадзянскай вайны</a> за пераразмеркаванне ўлады ў ВКЛ (кароль у ёй нават не браў удзел), 
		працягнулася ўзброенным змаганнем <a href="confederacyi_18vek.php">шляхецкіх канфедэрацый</a>, спробай зацвердзіць 
		новую <a href="constitution_3may.php">Канстытуцыю</a> і скончылася падзеламі дзяржавы.
		</p>
		<p>
		Бліжэй да сярэдзіны XVIII стагоддзя Рэч Паспалітая ўжо не была ў поўнай меры незалежнай. Расійскія імператары аказвалі 
		непасрэдны ўплыў на яе ўнутраныя справы, падмацоўваючы палітычны ціск змесцаванымі ў краіне войскамі. 
 		</p>
 		

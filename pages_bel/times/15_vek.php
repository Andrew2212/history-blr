</br></br>• 1411 Торуньский мир (г. Торунь, Польша) между Польшей и ВКЛ, с одной стороны, и Тевтонским орденом — с другой, которым завершена "Великая война".
</br></br>• 1413 Городельская уния — союз Великого княжества Литовского с Польским Королевством (принята на общем польско-литовском сейме в г. Городле, Польша).
</br></br>• 1415 Договор ВКЛ с Молдавским княжеством об основах взаимоотношений (аналогичные договоры заключались и подтверждались в 1431, 1437, 1442, 1496, 1499, 1518, 1551, 1554 гг.).
</br></br>• 1422 Самогития включена в состав ВКЛ.
</br></br>• 1427 Великое княжество Литовское подчинило себе верховские княжества (в верховьях р.Ока).
</br></br>• 1430 После смерти Витовта на съезде бояр в Вильно великим князем ВКЛ избран Свидригайло (сын Ольгерда, младший брат Ягайлы). Княжил в Великом княжестве Литовском до сентября 1432 г.; расторгнул унию с Польшей.
</br></br>• 1431 Свидригайло разрывает унию с Поьшей. Луцкая Война между ВКЛ и Польшей. 
</br></br>• 1432 В результате интриг свергнут Свидригайло.
</br></br>• 1432 Княжение Жигимонта Кейстутовича (сын Кейстута, младший брат Витовта).
</br></br>• 1432 Гродненская уния с Польшей (Ягайло и Сигизмунд).
</br></br>• 1432-1435 Гражданская война между сторонниками Жигимонта и Свидригайло.
</br></br>• 1432 Выступление православных феодалов ВКЛ под руководством Свидригайло за расширение своих экономических и политических прав
</br></br>• 1432, 1434 Жигимонт Кейстутович издал привилеи (грамоты) об уравнении персональных и имущественных прав католиков и православных.
</br></br>• 1434 Войска Свидригайло заняли города Минск, Витебск, Полоцк.
</br></br>• 1435 Битва под Вилькомиром между войсками Свидригайло Ольгердовича и Жигимонта Кейстутовича; поражение Свидригайло.
</br></br>• 1440 В результате сговора убит великий князь Жигимонт Кейстутович.
</br></br>• 1440 Паны-рада ВКЛ избрали князем Казимира Ягеллончика (младший сын Ягайлы и киевской княжны).
</br></br>• 1440-1492 Княжение в Великом княжестве Литовском Казимира Ягеллончика (с 1447 г. король Польши под именем Казимир IV).
</br></br>• 1468 Издание Судебника Казимира Ягеллончика — первого сборника законов ВКЛ, кодекса уголовного и процессуального права.
</br></br>• 1470-е Нападения крымских татар на южные земли ВКЛ; набеги участились, когда крымские ханы стали вассалами Турецкой империи (1475).
</br></br>• 1490-1551 Жизнь и деятельность Франциска Скорины
</br></br>• 1492 Паны-рада ВКЛ избрали князем Александра (сына Казимира Ягеллончика, внука Ягайлы. С 1501 г. король Польши).
	Издан привилей, которым ограничена власть великого князя и повышена роль панов-рады, определены основы гражданского и уголовного права.
</br></br>• 1492-1494 Война ВКЛ с Великим княжеством Московским, к Москве отошли Вяземское княжество и большинство верховских княжеств.
</br></br>• 1499 Краковско-Виленская уния с Польшей
</br></br>• 1500-1503 Война ВКЛ с Великим княжеством Московским, московские войска заняли Смоленскую, Оршанскую, Мстиславльс-кую, Витебскую, Полоцкую и др. земли.
</br></br>• 1500 Битва с Московией на реке Ведрошь — потеряно примерно треть территории ВКЛ.


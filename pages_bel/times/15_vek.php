	<h2><a href="../terms/belarus.php">Беларусь</a>
	<small>
	</br><a href="../terms/blr_etnos.php">этнос</a>, <a href="../terms/religion_blr.php">религия</a>, государственный язык — <a href="../terms/blr_mova.php">старобелорусский</a>
	</br><a href="../terms/wkl.php">Великое княжество Литовское</a> — начало "Золотого века" (1410-1569) 
	</br>• Луцкая война с Польшей • Гражданская (религиозная) война — Свидригайло vs Жигимонт • 
	Грюнвальд и завершение "Великой войны" с Орденом • Предоставление Магдебургского права городам ВКЛ • Формирование белорусского этноса •
	</small>
	</h2>

<p>
• 1392-1430 правление Витовта. Начало "Золотого века" (1410-1569) ВКЛ-Беларуси. 
</br>— расцвет государства и закрепления своих политических позиций на европейской арене
</br>— увеличение количества шляхты и создания привилегий для нее
</br>— победа под Грюнвальдом 15 июля 1410 года
</br>— создание сильной централизованной власти и стабилизация отношений с Короной Польской 
</br>— ВКЛ становится крупнейшей державой Европы. 
</p>
<p>
• 1401 Виленско-Радомская уния — Витовт, признанный пожизненным Великим князем правителем ВКЛ, приносит присягу верности Ягайло.
</p>
<p>
• 1413 Городельская уния — введен наследственный институт великого князя литовского и положено начало современной белорусской геральдике.
</p>
<p>
• 1430 После смерти Витовта шляхта ВКЛ избирает великим князем Свидригайло. Он разрывает унию с Польшей.
</p>
<p>
• 1431 Луцкая война с Польшей. Согласно условию перемирия, В. кн. Свидригайло сохранил в составе ВКЛ Восточную Подолию (города Брацлав и Винницу) 
и Волынь. Король Владислав II Ягайло удерживал Западную Подолию. 
</p>
<p>
• 1432 Свидригайло свергнут с княжения Сигизмундом и посажен на "княжение руское" в Полоцк. 
</p>
<p>
• 1432 Гродненская уния — возобновление унии с Польшей (Ягайло и Сигизмунд). 
</p>
<p>
• 1432-1435 Гражданскя война в ВКЛ Свидригайло vs Сигизмунд — конфессиональное противостояние.
</br>В войне за княжеский титул претендентов поддерживали католические (Жигимонта) и православные (Свидригайло) феодалы.
Чтобы увеличить число своих сторонников, Сигизмунд расширил все права, данные феодалам-католикам, на всех феодалов ВКЛ независимо от их вероисповедания.  
В битве под Вилькомиром Свидригайло потерпел окончательное поражение.
</p>
<p>
• 1440 Сигизмунд убит заговорщиками в Трокском замке. Новым в. князем становится Казимир 
Ягеллон — сын короля польского Владислава II Ягайло и Софьи Гольшанской.
</p>
<p>
• 1487-1537 Войны с Московским царством, определившие современную восточную границу Беларуси
</p>
<p>
• 1499 Краковско-Виленская уния — <i>"ни великий князь литовский не мог быть избран без согласия Польши, ни польский трон не мог быть замещён без согласия литовского дворянства"</i>
</p>
<p>
• XV век исследователи считают временем окончательного формирования белорусского этноса 
</p>



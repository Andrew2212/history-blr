	 <h4>Польша</h4>
		<p>
		Королевство Польша.
		</br>Сын Ягайло Владислав III стал одновременно королём Венгрии и Польши, но погиб в битве с турками под Варной. 
		После этого польско-венгерская уния прекратилась, но зато восстановилась (прекратившаяся было) польско-литовская уния, 
		благодаря избранию на польский престол брата Владислава — Великого князя ВКЛ Казимира Ягеллончика (Казимир IV).
		</br>В 1454 году по Нешавским статутам Польша превратилась в республику, где высшая власть принадлежала сейму. 
		</br>Тринадцатилетняя война с Тевтонским орденом. Присоединение Померании с Гданьском — выход к Балтийскому морю.
		</br>Луцкая война с ВКЛ. Признание независимости ВКЛ от короны.  
		</br>Крестовый поход Венгрии и Польши против Османской империи закончился поражением в битве при Варне.
		</p>
		

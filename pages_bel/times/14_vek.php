	 <h4>Польша</h4>
		<p>
		В 1320 году Куявский князь Владислав Локетек, присоединив к своим владениям Великую Польшу, короновался в Кракове польским королём.
		</br>В 1349 году к Польше была присоединена Галиция. В 1370 году королём Польши стал племянник Казимира — король Венгрии Людовик (Лайош) I,
		 из Анжуйской династии — первый король-иноземец на польском престоле. 
		 Не имея прочной опоры в стране, он издал в 1374 году Кошицкий привилей, согласно которому магнаты и шляхта были освобождены от всех повинностей, 
		 кроме военной службы.
		 </br>После смерти Людовика I, в 1384 году королевой Польши стала Ядвига. Магнаты начали подыскивать Ядвиге мужа, который мог бы быть полноценным польским монархом, 
		 и нашли такового в лице Великого князя ВКЛ Ягайло. 
		 В 1385 году была подписана Кревская уния, согласно которой Ягайло крестился по католическому обряду, 
		 ввёл католичество в качестве государственной религии в ВКЛ, женился на Ядвиге и вступил на польский престол под именем Владислава II.
		</p>
		

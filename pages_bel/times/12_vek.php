	 <h4>Польша</h4>
		<p>
		 При Болеславе III Кривоустом (1102—1138) Древнепольское государство достигло последнего расцвета. 
		 Болеслав в 1109 году отразил нашествие немецкого императора, в 1122 году присоединил к Польше практически всё Поморье. 
		 </br>Однако после его смерти, как в те же годы на Руси — после смерти Владимира Мономаха, 
		 в Польше началась феодальная раздробленность. 
		 Согласно «Статуту Болеслава Кривоустого» (1138), Польша была разделена между четырьмя сыновьями с титулом великого князя и великокняжеским уделом 
		 (часть Великой Польши с Гнезном и Малая Польша с Краковом) за старшим. 
		 </br>Образовался ряд княжеств: Куявия, Мазовия, Силезия, Поморье, Сандомир и т. д.
         </br>Начался немецкий «Натиск на восток». В 1181 году князь Западного Поморья признал себя вассалом германского императора.
		</p>
		

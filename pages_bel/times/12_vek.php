	<h2><a href="../terms/belarus.php">Беларусь</a>
	<small>
	</br><a href="../terms/blr_etnos.php">этнос</a>, <a href="../terms/religion_blr.php">религия</a>
	</br> • Феодальная раздробленность — <i>Полоцк, Минское, Витебское, Друцкое, Изяславское, Логойское, Стрежевское, Городцовское, Гродненское, Мстиславское, 
	Смоленское княжества, Туровский и Клецкий уделы, Литва •</i> 
	</small>
	</h2>
	
<p>
• Дробление Полоцкого княжества на уделы сыновьями Всеслава Чародея
</br>После смерти Всеслава Чародея Полоцкое княжество 
(охватывало примерно половину современной Беларуси и достигало берегов Рижского залива) было разделено
 на Минское, Витебское, Друцкое, Изяславское, Логойское, Стрежевское и Городцовское княжества между его сыновьями. 
</br>Междуусобные войны. Войны с Киевом.
</br>В 1129 году князь киевский Мстислав высылает в Византию шестерых захваченных Рогволодовичей. 
 За время их отсутствия в Полоцке сильно возросла роль веча. Через 10 лет князья возвращаются, 
но с этого времени князей в Полоцке выбирало вече.
</br>• Распад Туровского княжества на Туровский и Клецкий уделы.
</p>
<p>
• Северо-восточные белорусские земли входят в раздробленное Полоцкое княжество
</br>• На северо-западе (Новогрудок-Вильно-Минск-Слоним) под наплывом полабских славян формируется Литва (еще не государство)
</br>• На западе белорусских земель образуется Гродненское княжество.
</br>• Восточные белорусские земли входят в княжество Смоленское.
</br>• На юге часть Полесья входит в Волынское и Черниговское княжества, часть — в Туровское княжество
</p>
<p>
• На территории Беларуси известно около 30 городов:
 Полоцк, Витебск, Заславль, Туров, Усвят, Копысь, Минск, Орша, Друцк, Логойск, Лукомль, Брест, Пинск, Голотическ, 
 Борисов, Браслав, Стрежев (Стреглав), Слуцк, Неколоч, Еменец, Одрск, Городец, Гродно, Клецк, Мядель, Кривич-Город, 
 а также вассальные города-крепости в нижнем Подвинье Герцике, Кукенойс и др. (ныне территория Латвии). 
 </p>
 

		<p>
		Источники «краевой концепции» на землях, входивших когда-то в состав <a href="../terms/wkl.php">ВКЛ</a>, находятся глубже, чем перелом XIX—XX веков. 
		"Краёвость" вытекала из памяти коренных дворян и интеллигенции о бывшем единстве этой территории 
		(шести северо-западных губерний) во времена ВКЛ и стремления сохранить и укрепить это единство и дальше. 
		</p>
		<p>
		Краевасьць продолжала традиции <a href="../terms/filomaty.php">филоматов</a> — поэтов и романтиков 1817-23 годов — общества, давшего национальных героев Беларуси, Польше, Летуве, Чили.
		</p>
		<p>
		<i>
			"Краёвасць не звязаная з ніводнай нацыянальнасцю. Краёўцам можа быць як тутэйшы паляк, так і літовец, беларус, нават габрэй, 
			калі ён разумее і адчувае дязржаўныя традыцыі Вялікага Княства Літоўскага. 
			І наадварот - усе яны не будуць краёўцамі, калі інтарэсы сваёй нацыянальнасці ставяць вышэй за агульныя, краёвыя інтарэсы. 
		</i>
		</p>
		<p>
		<i>	
			Розніца толькі ў тым, што літоўцы і беларусы ў любым выпадку з'яўляюцца бясспрэчным матэрыялам для краёўцаў, 
			у той час як палякі і габрэі, маючы апору па-за межамі краю, могуць складаць чужы і нават варожы элемент адносна ідэі кансалідацыі 
			ўсіх нацыянальных чыннікаў на тэрыторыі былога Вялікага Княства Літоўскага.".
		</i>

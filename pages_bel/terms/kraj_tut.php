		<p>
		"Краевая идея" была идеей местной средней и заможной <a href="../terms/shliahta.php">шляхты</a>  объединить силы сторонников автономии и развития всего Северо-Западного края. 
		В реализации этой идеи выделялись лидеры Минского общества сельского хозяйства — <a href="../figures/woinillovicz.php">Эдвард Войнилович</a>,
		 Александр и <a href="../figures/skirmunt.php">Роман Скирмунт</a> и др.
		</p>		
		<p>
		Краевасьць в 1912-1915 гг. стала составной частью идеологии белорусского национально-демократического движения. 
		Ее поддердживали лидеры "Белорусскогой христианской демократии" — Адам Станкевич, <a href="../figures/godlewski.php">Винцент Годлевский</a>. 
		Принципы "краевой идеологии" отражались в статьях газет местных демократических движений — "Наша нива", "Kurier Krajowy". 
		Издание "Нашей нивы" под редакцией <a href="../figures/luckiewicz_anton.php">А. Луцкевича</a>, так же, как стихов <a href="../figures/bogdanowich.php">Богдановича</a>
		 поддерживала Мария Магдалена Радзивилл из рода <a href="../figures/zawisza.php">Завишей</a>. 		
		</p>		
		<p>
		Тутэйшая краевасьць легла в основу <a href="../download/blr_nationalists_1957.php">белорусского национализма</a> XX века — от Войниловича до наших дней.
		</p>		
		<p>
		<i>
			"Войниловичи не пришли ни с Востока, ни с Запада — они коренные, местные, кость от кости, кровь от крови того народа, 
			который когда-то хоронил своих предков в этих курганах (сегодня — на сельских кладбищах) 
			и родную белорусскую землю сохой пашет".
		</i>

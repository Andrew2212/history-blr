		<p>
		Утвержден 25 марта 1917 года в Минске на Первом Белорусском конгрессе.
		</br>Государственный флаг <a href="bnr.php">Белорусской Народной Республики</a>, 1918-1919
		</br>Государственный флаг Республики беларусь, 1991-1995
		</p>
	</div>
	<div class="col-md-12">
	<p>	
		<i>Глядзіце ў будучыню смела!
		</br>Настаў доўгачаканы час,
		</br>Пад сцягам бел-чырвона-белым
		</br>Чакае перамога нас.</i>
		</br><small>"Ты чуеш, брат…" 1930, М. Танк — Народный поэт БССР, Герой Социалистического Труда , Лауреат Ленинской и Сталинской премии , Лауреат Государственной премии БССР им. Янки Купалы ,  Академик АН БССР, Председатель Верховного Совета БССР (1965-1971)</small>
	</p>
	</div>
		
		<h4>Происхождение </h4>
		
		<p>
		Белый и красный цвета характерны для белорусской культуры, например, красные элементы на белой основе считаются самым 
		распространённым сочетанием цветов в белорусском орнаменте. 
		Белый и красный — цвета национального герба <a href="blr_gerb.php">Погоня</a>.
		</p>
		
		<p class="img-float-left">

		</br><small>Woman, 1920</small>	
		</a>
		</p>
		
		<p class="img-float-right">
		<a href="../../images/img-terms/blr_flag/Siege_of_Polotsk_in_1579.jpg" class="highslide"
        onclick="return hs.expand(this)">

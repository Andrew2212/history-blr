		<p>		
		В 1918-1919 годах БЧБ флаг был официальным флагом <a href="bnr.php">БНР</a> — первого белорусского государства со времен 
		<a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>. 
		Наряду с Погоней он был символом <a href="../events/uprising_sluzk.php">Слуцкого восстания</a> в 1920 году.
		Целью восстания была объявлена защита независимости Белоруссии. 
		Под БЧБ флагом в 1920 г. воевал Белорусский особый батальон в составе армии Летувы.

		<p>
		<h2>Беларуская лацінка</h2>
		— беларускі лацінскі алфавіт ("bielaruskaja lacinka") — агульная назва некалькіх гістарычна існавалых сістэм запісу беларускага 
		(кірылаўскага) тэксту ў лацінскай сістэме пісьма. 
		</p>
		<p>
		Ей, разам з кірыліцай, карысталіся літвіны-беларусы XVII стагодзя (распіскі <a href="../download/blr_moscow.php">беларускіх разьбароў</a>
		 у Расейскім архіве старажытных спраў). 
		На ей друкавалася першая беларуская газэта "Мужыцкая праўда" ў 1862-63 гг. На ей звяртался да народа і пісаў свае 
		"Лісты з пад шыбеніцы" <a href="../figures/kalinowski.php">Кастусь Каліноўскі</a>.
		</p>
		<p>
		На сёння з'яўляецца афіцыйнай ліцінскай транслітарацыяй беларускай мовы. 
		</p>
		<p>
		Пастанова Дзяржаўнага камітэта па картаграфіі РБ ад 23 лістапада 2000 г. № 15 
		<a target="_blank" href="http://gki.gov.by/activity_branches/maps/mnorm/">"Пра трансітэрацыю геаграфічных назваў Рэспублікі Беларусь літарамі лацінскага алфавіта"</a>.
		Дакумент спампаваць можна <a target="_blank" href="../../images/img-articles/belarus_belarussia/instruction_23.10.2000.doc">ТУТ</a>. 

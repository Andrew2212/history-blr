		<p>
		<h2>Тарашкевіца 
		</br><small>класічны правапіс</small></h2>  
		— варыянт беларускай арфаграфіі 
		(у больш шырокім сэнсе — граматыкі ці моўнай нормы), заснаваны на літаратурнай норме сучаснай беларускай мовы, 
		першая нармалізацыя якога была выраблена <a href="../figures/tarashkevicz.php">Браніславам Тарашкевічам</a> у 1918 годзе і афіцыйна дзейнічала 
		да рэформы беларускага правапісу 1933 года.
		</br>У 2005 годзе з выхадам кнігі "Беларускі класічны правапіс. Збор правіл" была выраблена сучасная нармалізацыя тарашкевіцы.
		27 красавіка 2007 года IANA прысвоіла тарашкевіцы ўласны моўны падтэг "tarask" (поўнае пазначэнне: be-tarask)
		</p>
		
		<p>
		<h2>Наркамаўка</h2>
		Рэформа беларускай мовы была праведзена пастановай СНК БССР (апублікавана 26 жніўня 1993 года). 
		</br>Быў уведезны збор правіл граматыкі (выданы ў 1934 годзе), які дзейнічаў да 1959 года.
		</br>Сучасныя беларускія філолагі падкрэсліваюць той факт, што больш за 20 новых правіл, уведзеных рэформай 1933 года, 
		перакручвалі ўсталяваныя нормы беларускай літаратурнай мовы шляхам штучнага накладання на іх правілаў рускай.
		</p>
		

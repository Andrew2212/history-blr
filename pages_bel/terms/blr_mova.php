		<h2>Prehistory</h2>
		<p>
		Беларускі этнас склаўся ў XIV-XV стагоддзях, калі ўсе <a href="../download/blr_people.php">беларускія землі</a>
		 былі сабраны ў адзінае <a href="../terms/wkl.php">Вялікае княства Літоўскае</a>. Даследнікі адзначаюць, што
		 на фоне <a href="../terms/religion_blr.php">пярэстай рэлігійнай карціны</a> яднальным фактарам служыла культурная і моўная агульнасць. 
		 </p>
		 <p>
		 Аднім з першых згадванняў пра вуснае выкарыстанне старабеларускай мовы з'яўляецца <a href="../figures/kejstut.php">"клятва Кейстута"</a> — 
		складаючы мір з вугорскім каралём, Кейстут забіў быка (рагатую скаціну, rogacina) і пры сведках пракрычаў 
		 <i>"Рагаціна — розні нашы! Гасподзь з намі!"</i>, што было гучна паўторана 
		 <a href="../terms/litwiny.php">літвінамі</a> (Lithwani). 
		 Гэтыя словы фанетэчна даслоўна запісаны ў <a href="../download/chronicles_dubnica.php">Дубніцкай хроніцы</a> 1351.	 
		</p>
		

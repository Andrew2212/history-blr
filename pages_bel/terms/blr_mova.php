			<h2>Дзяржаўная мова
			</br><small>беларуская, польская, руская</small>
			</h2>
			Беларуская ці старабеларуская. Не будзем спрачацца пра тэрміны — можна зваць яе літоўскай (ад <a href="litwiny.php">літвінаў</a>) 
			ці стара-<a href="../download/zemie_ruskie.php">рускай</a> (стара-украінскай) — гэта справа лінгвістаў. 
			Гэта мова, на якой казаў і пісаў свае законы народ <a href="../download/blr_people.php">земляў беларускіх</a>.
		</p>
		<p>	
			Яна была мовай <a href="../terms/polotsk_principality.php">Полацкага княства</a> і дзяржаўнай мовай ВКЛ да <a href="../times/18_vek.php">XVIII стагоддзя</a>. 
			На гэтай мове напісаны <a href="statut.php">Статут ВКЛ</a>, усе <a href="../download/metrica_litwa.php">Метрыкі Літоўскія</a>, 
			княскія і каралеўскія прывілеі (гл. <a target="_blank" href="http://niab.by/unikdocs/">Нацыянальны гістарычны архіў</a>)
		</p>
		<p>	
			700 гадоў — з X стагоддзя да 1696 года — дзяржаўнай мовай на беларускіх землях была наша нацыянальная мова. 
			</br>[ <small>На ёй размаўлялі не толькі літвіны-беларусы.
			Яна стала роднай для тысяч татар, якія прыйшлі да Вітаўта з Тахтамышам: на ёй пісалі кітабы 
			— тэксты на беларускай мове арабскім пісаннем.</small> ]
		</p>
		<p>
			168 гадоў — з 1696 па 1864 — польская і латынь. 
			</br>[<small> У 1696 годзе Усеагульная канфедэрацыя саслоўяў (канфедэратыўны сойм) 
			Рэчы Паспалітай прыняла пастанову пра тое,
			што ў ВКЛ дзяржаўныя дакументы павінны пісацца не на беларускай, а на польскай і лацінскай мовах ]</small>
		</p>
		<p>	
			127 гадоў — з 1864 года, пасля <a href="../events/uprising_1863.php">паўстання Каліноўскага</a>, і да 1991 года — дзяржаўнай мовай была руская.
			</br>[<small> З 1863 года, пасля паўстання Каліноўскага, намаганнямі рускага ўрада 
			<i>"педагагічны персанал змяніўся і стаў выключна рускім і праваслаўным"</i>. Фармальна, дзяржаўных моў з 1919 года дзве 
			— беларуская і руская, аднак уся сістэма адукацыі падтрымлівае толькі адну. ]</small>	
		</p>
		

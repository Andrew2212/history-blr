	<h3>PS.</h3>
	<p>
	Герб Рэспублікі Беларусь "Пагоня" быў зацверджаны ў 1991 годзе. Герб Літоўскай Рэспублікі "Віціс" быў зацверджаны ў 1992 годзе. 

		</br>Людзі, якія абвясцілі незалежнасць Беларусі ў 1991 годзе, у якасці свайго арыенціра 
		бачылі менавіта Беларускую Народную Рэспубліку 1918 года. Гэты дзень — галоўнае свята Беларусі.

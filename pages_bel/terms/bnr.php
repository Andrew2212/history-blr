		<h3>Ці магла БНР утрымацца?</h3>

		<p>
		З <a href="../articles/bnr_article.php">артыкула</a> Алеся Чайчыца:</small>
		</p>
		
		<i>
		<p>
		Напачатку сакавіка 1918 года ў Брэсце расійскія бальшавікі пасля серыі вайсковых параз склалі мір з Германіяй. 
		Па ўмовах міру Расія адмаўлялася ад Прыбалтыкі, Беларусі і Украіны. 
		</p>
		<p>
		Выканаўчы камітэт Усебеларускага з'езда, 
		беларускага каардынацыйнага прадстаўнічага органа, зрэагаваў імгненна: Беларусь не павінна была застацца «нічыёй зямлёй», 
		яе лёсам павінны распараджацца самі беларусы, а не немцы ці бальшавікі. 
		Легітымнае самаарганізаванае мясцовае прадстаўніцтва ўзяло на сябе адказнасць за будучыню краіны, выдаўшы тры статутныя граматы, 
		у якіх абвяшчаўся дзяржаўны суверэнітэт Беларусі.
		</p>
		<p>
		Беларусь абвясціла пра стварэнне самастойнай дзяржавы праз лічаныя тыдні пасля Эстоніі і Літоўскай Рэспублікі 
		і на паўгода раней за Латвію.
		</p>
		<p>
		Каму атрымалася адарвацца ад імперыі, а каму не — гэта шмат у чым гісторыя пра шанцаванне і нешанцаванне.
		</p>
		<p>
		Скажам, дзяржавам Балтыі ў 1918 пашанцавала. У латышоў і эстонцаў не было лініі фронту пасярод краіны, не было прэтэндуючых на іх тэрыторыю 
		з аднаго боку савецкай Расіі, з іншага — магутнай адроджанай Польшчы. У іх былі з аднаго боку бальшавікі, а з іншага — Балтыйскае мора,
		 дзе сяброўскі брытанскі флот не толькі аказваў матэрыяльную падтрымку войскам новых краін, 
		 але і падаваў непасрэдную агнявую падтрымку. 
		</p>
		<p>
		Пашчасціла так далёка не ўсім. Па выніках грамадзянскай вайны ў былой Расійскай Імперыі не ўдалося ўтрымацца нават незалежнай Украіне 
		з яе, здавалася б, каласальнымі матэрыяльнымі рэсурсамі і масавым нацыянальным рухам. 
		</p>
		<p>
		Не ўтрымаліся дэмакратычныя Грузія, Арменія і Азербайджан, якія абвясцілі незалежнасць ад Расіі, 
		нягледзячы нават ні на якое выгаднае геаграфічнае становішча, ні на які горны ландшафт. 
		</p>
		</i>
			
		
		<h3>25 сакавіка — Дзень волі</h3>	

		<p>	 
			<h4><a href="../times/14_vek.php">XIV век</a></h4> 
				Все белорусские земли собраны в единое государство — Великое княжество Литовское.
			 </br>Княжеская династия <a href="../figures/rogvolod_rogneda.php">Рогволодовичей</a>, правившая в Полоцке, согласно "Воскресенской летописи", 
			 <a target="_blank" href="../../images/img-terms/belarus/kirkor_litwa_duces.jpg">дала начало</a> династии Гедыминовичей, правившей в ВКЛ.
			 </br>В 1323 году <a href="../figures/gedimin.php">Гедымин</a>, Gedemine Dei gratia Letphanorum Ruthenorumque rex, обращается к Папе Римскому Иоанну XXII, и западные белорусские 
			 земли принимают католичество. 			 				  			
		</p>
		<p>
			Так <a href="religion_blr.php">религия</a>, а именно разные конфессии христианства восточных и западных земель, начинает играть заметную роль в истории Беларуси.  
		</p>
	
		<p>
		<h4><a href="../times/15_vek.php">XV век</a></h4>
		ВКЛ достигло своего расцвета. 		
		Развитое европейское государство со сложной структурой власти 
		(Великие <a href="dukes_wkl.php">князья</a>, <a href="weliki_marshalok.php">маршалки</a>, <a href="weliki_kancler.php">канцлеры</a>, 
		<a href="weliki_hetman.php">гетманы</a> и тд.).
		 Первое межконфессиональное противостояние — <a href="../events/war_civil_1432.php">Гражданская война</a> 1432-1435 годов.
		 </br><a href="../events/grunwald.php">Битва под Грюнвальдом</a> — окончание войн с Тевтонским Орденом и 
		 присоединение <a href="../places/samogitia.php">Самогитии</a>.
		 </p>
		 
		 <p>
		 Наступил "Золотой век" (1410-1569) — время белорусского Ренессанса.
		 </br>Издан <a href="statut.php">Статут ВКЛ</a>, издаются труды белорусских гуманистов и просветителей.
		 </br>На <a href="../download/wkl_equal_etno.php">основе ВКЛ</a>
		 в XIII-XVI веках складывается <a href="blr_etnos.php">белорусский этнос</a>.
		</p>
		

		<p>
		У 1944 урадавай камісіяй фонды музея былі падзелены паміж ЛітССР і БССР. Улетку сорак пятага з Вільнюса ў Мінск прыбыла першая 
		значная партыя беларускіх музейных каштоўнасцяў. Большасць экспанатаў "да лепшых часоў" складвалі ў музеі гісторыі 
		Вялікай Айчыннай вайны.

		В 1944 правительственной комиссией фонды музея были поделены между ЛитССР и БССР. Летом сорок пятого из Вильнюса в Минск прибыла первая 
		значительная партия белорусских музейных ценностей. Большинство экспонатов "до лучших времен" складировали в музее истории 

		<p>
		Беларускі музей у <a href="../places/wilno.php">Вільні</a> 1921-44 — адна з галоўных навуковых і асьветніцкіх установаў у гісторыі 
		<a href="../terms/belarus.php">Беларусі</a>, даўно сталая легендай. 
		Знаходзіўся ў Вільні на вуліцы Вастрабрамская-9 (зараз вул. Аўшрос Варту). Сёння яго фонды захоўваюцца ў роўных дзяржаўных 
		зборах Вільнюса і Мінска.
		</p>
		<p>
		У яго падмурак былі пакладзеныя калекцыі <a href="../figures/luckiewicz_ivan.php">Яна Луцкевіча</a> і <a href="../figures/lastowski.php">Вацлава Ластоўскага</a>. 
		<i>"Купляю ўсякiе старасьвецкiе рэчы: абразы, гравюры, мiнiатюры, рукапiсы i кнiжкi XV i XVI в.; порцэляну, шкло, тканiны. В. Ластоўскi. 
		Завальная вулiца № 7. Беларуская кнiгарня"</i>.
		</p>
		<p>
		Ідэя стварэння нацыянальнага музея выкрышталізавалася ў Вільні ў 1912. У артыкуле пад праграмнай назвай "Збiрайце рэчы ў беларускi музэй" 
		Луцкевіч пісаў: 
		<i>"Мы дайшлi да таго, што нам патрэбна навуковае таварыство, пры каторым павiнна быць бiблiотэка i музэй. У музэi патрэбны рэчы з этнографii, 
		гiсторыi i штукарства (iскуства)…  Дзе хто знойдзе якiе паперы пiсаные, цi друкаваные па беларуску, дасылаць у рэдакцiю ["Наша нiва"]. 
		Усё прысланае не загiне, а будзе паложэна у беларускi музэй"</i>.
		</p>
		<p>
		У пяці экспазіцыйных залях былі выстаўлены калекцыі: археалагічная, сфрагістычная, нумізматычная, мэдалёў і крыжоў, старажытнага адзеньня, 
		у тым ліку і слуцкіх паясоў, старажытнай зброі, рукапісных кніг і старадукаў, царкоўных абразоў, карцінаў даўнейшых і сучасных мастакоў, 
		народных музычных інструментаў, твораў народных разьбяроў і многае другое. Паводле інвентара 1941 г. музэй налічваў 13 450 адзінак захаваньня. 
		Архіў музэя зьберагаў дакуманты ад 16-га стагоддзя да сярэдзіны 20-га (агулам — звыш 9000 адзінак захаваньня). Бібліятэка налічвала звыш 14000 тамоў.
		</p>
		<p>
		Да 1939 года Беларускі музей узначальваў брат Івана Луцкевіча — <a href="../figures/luckiewicz_anton.php">Антон Луцкевіч</a>, 
		Старшыня Рады Міністраў Найвышэйшай Рады <a href="../terms/bnr.php">БНР</a> (1918-20).
		</p>
		<p>
		Падчас акупацыі мясцовыя беларусы захоўвалі музей. У 1944 немцы занялі будынак музея пад шпіталь і каштоўны збор перанеслі ў будынак
		былога кляштара бернардынаў. Частку захоўваў у падзямеллях касцёла Св. Міхала ксёндз Адам Станкевіч.
		</p>
		<p>
		4 кастрычніка 1943 года Саўнаркам БССР прыняў пастанову № 76 "Пра аднаўленне працы гістарычнага музея БССР". 
		Аднак яно не было выканана: у Мінску ў 1944 годзе адкрыўся новы музей — Беларускі дзяржаўны музей гісторыі Вялікай Айчыннай вайны.
		</p>
		

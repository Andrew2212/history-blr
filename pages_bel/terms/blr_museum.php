		<p>
		Белорусский музей в <a href="../places/wilno.php">Вильно</a> 1921-44 — адна з галоўных навуковых і асьветніцкіх установаў у гісторыі 
		<a href="../terms/belarus.php">Беларусі</a>, давно ставшая легендой. 
		Знаходзіўся ў Вільні на вуліцы Вастрабрамская-9 (ныне ул. Аушрос Варту). Сегодня его фонды хранятся в разных государственных 
		собраниях Вильнюса и Минска.
		</p>
		<p>
		В его основу была положены коллекции <a href="../figures/luckiewicz_ivan.php">Яна Луцкевича</a> и <a href="../figures/lastowski.php">Вацлава Ластовского</a>. 
		<i>"Купляю ўсякiе старасьвецкiе рэчы: абразы, гравюры, мiнiатюры, рукапiсы i кнiжкi XV i XVI в.; порцэляну, шкло, тканiны. В. Ластоўскi. 
		Завальная вулiца № 7. Беларуская кнiгарня"</i>.
		</p>
		<p>
		Идея создания национального музея выкристаллизовалась в Вильно в 1912. В статье под программным названием "Збiрайце рэчы ў беларускi музэй" 
		Луцкевич писал: 
		<i>"Мы дайшлi да таго, што нам патрэбна навуковае таварыство, пры каторым павiнна быць бiблiотэка i музэй. У музэi патрэбны рэчы з этнографii, 
		гiсторыi i штукарства (iскуства)…  Дзе хто знойдзе якiе паперы пiсаные, цi друкаваные па беларуску, дасылаць у рэдакцiю ["Наша нiва"]. 
		Усё прысланае не загiне, а будзе паложэна у беларускi музэй"</i>.
		</p>
		<p>
		У пяці экспазіцыйных залях былі выстаўлены калекцыі: археалагічная, сфрагістычная, нумізматычная, мэдалёў і крыжоў, старажытнага адзеньня, 
		у тым ліку і слуцкіх паясоў, старажытнай зброі, рукапісных кніг і старадукаў, царкоўных абразоў, карцінаў даўнейшых і сучасных мастакоў, 
		народных музычных інструментаў, твораў народных разьбяроў і многае другое. Паводле інвентара 1941 г. музэй налічваў 13 450 адзінак захаваньня. 
		Архіў музэя зьберагаў дакуманты ад 16-га стагоддзя да сярэдзіны 20-га (агулам — звыш 9000 адзінак захаваньня). Бібліятэка налічвала звыш 14000 тамоў.
		</p>
		<p>
		До 1939 года Белорусский музей возглавлял брат Ивана Луцкевича — <a href="../figures/luckiewicz_anton.php">Антон Луцкевич</a>, 
		Старшыня Рады Міністраў Найвышэйшай Рады <a href="../terms/bnr.php">БНР</a> (1918-20).
		</p>
		<p>
		Во время оккупации местные белорусы сохраняли музей. В 1944 немцы заняли здание музея под госпиталь и ценное собрание перенесли в здание 
		бывшего монастыря бернардинцев. Часть хранил в подземельях костела Св. Михаила ксендз Адам Станкевич.
		</p>
		<p>
		4 октября 1943 года Совнарком БССР принял постановление № 76 "О возобновлении работы исторического музея БССР". 
		Однако оно не было выполнено: в Минске в 1944 году открылся новый музей — Белорусский государственный музей истории Великой Отечественной войны.
		</p>
		

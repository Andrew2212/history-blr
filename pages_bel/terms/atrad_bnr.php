		<h3>Асобны аддзел войскаў БНР</h3>
		<p>
			Асобны аддзел войскаў БНР у Балтыі, таксама вядомы як Асобны атрад БНР, Асобны атрад Балаховіча 
			— атрад войскаў БНР, які дзейнічаў на тэрыторыі Эстоніі й Латвіі пад камандаваньнем Станіслава Булак-Балаховіча ў канцы 1919-пач. 1920 гг
		</p>
		<p>	
			Да пераходу на бок Беларускай Народнай Рэспублікі атрад пад камандаваньнем Станіслава Булак-Балаховіча ўваходзіў у склад расейскай (белай) Паўночна-Заходняй арміі, 
			якая сумесна з эстонцамі вяла барацьбу з бальшавікамі ў раёне Пскову.
		</p>
		<p>	
			14 лістапада 1919 году Балаховіч падаў афіцыйную дэклярацыю аб беларускім грамадзянстве й прыняцьці яго на беларускую службу. 
			Кастусь Езавітаў 15 лістапада 1919 году ў адказ на заяву Балаховіча паведаміў яму: <i>"З гэтаго часу Вы з Вашым войскам лічыцесь на службе 
			ў Арміі Беларускай Народнай Рэспублікі, як Камандзер Асобнага Атрада Беларускай Народнай Рэспублікі ў Эстіі"</i>.
			 Атрад атрымаў афіцыйную назву "Асобны аддзел войскаў БНР у Балтыі". 
		</p>
		<p>
		У складзе Атрада Булак-Балаховіча большасць салдатаў і афіцэраў былі або выхадцамі, або жыхарамі беларускіх губерняў. 
		К. Езавітаў як афіцэр выдатна разумеў, што сувязь з роднымі і блізкімі падымае баявы дух салдатаў. 
		У перыяд Грамадзянскай вайны рэгулярнае паштовае паведамленне практычна адсутнічала. 
		Таму нядзіўна, што бакі арганізавалі ўласную паштовую сувязь.
		</p>
		

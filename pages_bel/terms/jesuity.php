		<p>
		Иезуиты, или Орден Иезуитов - мужской монашеский орден Римско-католической церкви, основанный в 1534 году Игнатием Лойолой и 
		утверждённый Павлом III в 1540 году. Официальное название - Общество Иисуса (лат. Societas Jesu), 
		также Орден св. Игнатия.
		</p>
		<p>
		Иезуиты сыграли большую роль в контрреформации, активно занимались наукой, образованием и миссионерской деятельностью. 
		</br>Члены Общества Иисуса наряду с тремя традиционными обетами (бедности, послушания и целомудрия) дают и четвёртый - послушания папе римскому "в вопросах миссий". 
		 Девизом ордена является фраза "Ad majorem Dei gloriam" ("К вящей славе Божией").
		</p>		
		<p>
		После роспуска ордена решением папы Климента XIV в 1773 году и изгнания их из большинства стран Европы, свои позиции они сохранили только на землях 
		ВКЛ-<a href="belarus.php">Беларуси</a> в составе <a href="ross_empire.php">Российской империи</a>. 
		</p>		
		<p>
		В течении 30-ти лет (из 40), пока орден был под запретом, генералами Ордена (генеральными викариями) были наши соотечественники: Станислав Черневич,
		Гавриил Ленкевич, Франциск Каре	(из семьи английских переселенцев в Орше).
		</p>
		<p class="img-float-center">
		<a target="_blank" href="../../images/img-terms/iesuity/jesuity_RI_1772-1820.pdf">

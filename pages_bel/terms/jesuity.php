		<p>
		Значительный след на пестром полотне <a href="religion_blr.php">религии в Беларуси</a> оставили в XVI-XIX вв. иезуиты. 
		Вместе с <a href="religion_blr.php">базилианами</a> (реформированию которых в просветительский орден иезуиты способствовали), 
		они подняли на новый, европейский уровень науку и систему образования в ВКЛ.

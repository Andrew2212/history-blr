		<p>
		28-29 кастрычніка 1937 года па абвінавачванні ва ўдзеле ў " антысавецкай з'яднанай шпіёнска-тэрарыстычнай нацыянал-фашысцкай арганізацыі" 
		у скляпах мінскай турмы НКУС было расстраляна больш за 100 беларускіх пісьменнікаў, паэтаў, навуковых, дзяржаўных дзеячаў.	
		</p>
		<p>
		Характэрная дэталь. Мінск у 20-30-я гады быў у поўным сэнсе слова мультыкультурным горадам. 
		Выходзілі кнігі і перыёдыка, ставіліся п'есы па-беларуску, па-польску, па-габрэйску, па-руску, па-літоўску. 
		Тут размаўлялі і стваралі на ўсіх узгаданых мовах. 
		</p>
		<p>
		Словам, Мінск нагадваў <a href="../places/wilno.php">Вільню</a> з яе сярэднявечнымі традыцыямі, месца зліцця розных культур. 
		Але мінскі мультыкультуралізм быў цалкам знішчаны ў 30-я. Пасля вайны існавалая тут традыцыя яшчэ не адрадзілася. 
		</p>		

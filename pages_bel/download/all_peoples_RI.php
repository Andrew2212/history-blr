		<h3>Часть IV
		</br><small>"О народах монгольских, ..., поляках и владычествующих россиянах"</small>
		</h3>

		
		<p>
			Описание интересно тем, что составлено между Вторым (1792) и Третьим (1795) <a href="../events/razbor_rechi.php">разделами речи Посполитой</a>. 
		</p>
		<p>
			Присоединенные к России части <a href="../terms/wkl.php">Великого княжества Литовского</a> — Могилевская, Полоцкая, Минская губернии населены "поляками", у которых 
			"роды дворянские породнились с коренными Польскими". 
			В этом контексте по-другому воспринимается термин <i>"польские восстания"</i> 
			<a href="../events/uprising_1794.php">1794</a>, <a href="../events/uprising_1830.php">1830</a> и <a href="../events/uprising_1863.php">1863</a> годов. 		
		</p>
		<p>
			"Древняя Литва" — оставшаяся неприсоединенной до 1795 года (Третий раздел РП) часть ВКЛ —  "Волыния, Подляхия, Самогития и часть России". Уже нет 
			"<a href="../terms/litwiny.php">литвинов</a>", но появляются 
			не очень внятные "литовцы суть древнее племя Руссов" (почему-то в Самогитии).			
		</p>
		<p>
			Наверно, первый и последний раз на белорусских землях употребляется термин "Чернь или Простолюдины Польские <a href="../terms/religion_blr.php">разного вероисповедания</a>".
			В отличие от последующих трудов, 
			тут <a href="../terms/shliahta.php">шляхта</a> еще не выделена в "отдельную этнографическую группу",
			нет еще деления на "белорусов" и "католиков" по национальности. "Бело-Руссия" — пока термин для обозначения 
			восточных и юго-восточных границ Литвы (в тексте почему-то <i>"западных"</i>). 
		</p>
		<p>	
			Пока еще нет причитаний о "горькой доле мужика-белоруса" — <i>"Разного скота и птицы почти каждый поселянин имеет для хозяйственного 
			своего обихода достаточное количество".</i>  
		</p>
		

		<p>
		Впервые гербы в современном понятии возникли во времена Крестовых походов XI-XIII вв. На <a href="blr_people.php">белорусских землях</a> такие гербы появились в XIV-XV вв. 
		По <a href="../events/unia_gorodlo.php">Городельской унии</a> 1413 года бояре <a href="../terms/wkl.php">Великого княжества Литовского</a> вошли в польское шляхетское гербовое братство — "акт об адопции".
		</p>
		
		<p>
		По классификации гербы разделяются на личные, родовые, корпоративные (цеховые, братские), городские, земельные, государственные. 
		Самыми многочисленными и сложными были <a href="../terms/shliahta.php">шляхетские</a> родовые и личные гербы. 
		</p>
		<p>
		Параллельно с гербами продолжали существовать личные эмблемы-клейма, некоторые из которых со временем становились гербами или их частью.
		</p>
		

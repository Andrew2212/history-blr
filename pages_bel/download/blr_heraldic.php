		<p>
		Старые белорусские городские гербы связаны с получением городом <a href="../terms/magdeburg_law.php">Магдебургского права</a>: "надаемъ имъ на гербъ до печати местъское…". 
		Герб свидетельствовал о независимости города от представителей феодальной иерархии. 
		Характерной чертой старых белорусских гербов является барочный "польский" щит (нехарактерный для собственно польских гербов).  
		Гербы, полученные при <a href="../terms/ross_empire.php">Российской империи</a> имеют ново-французский щит.

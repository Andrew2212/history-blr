		<h2>Захад. Польшча
		</br><small>У адзінай дзяржаве 1569-1917 (348 гадоў). У асабістай уніі 1385-1569 (184 гады)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Агульная мяжа XIV-XX стст.</h4>
		<p>
		Мяжа па лініі Падляшша-Брэст сфармавалася да сканчэння войн ВКЛ-Польшча за Галіцка-Валынскую спадчыну (1340-1385), якія былі спыненыя заключэннем 
		<a href="../events/unia_krewo.php">Крэўскай Уніі</a> 1385 года. Вынікі замацаваны пасля сканчэння 
		<a href="../events/war_civil_1381-84_1389-92.php">Грамадзянскай вайны ў ВКЛ</a> <i>Вітаўт VS Ягайла</i> (1381-92) Астравецкім пагадненнем.
		</p>
		<p>
		Мяжа, адзначаная на <a href="../download/map_mercator.php">мапе Меркатара,</a> без сур'ёзных змен існавала да 1917 года. 
		Канчатковы выгляд набыла ў 1945 годзе, пасля перадачы Падляшша ад БССР да Польшчы.
		</p>
		
		</div>
		
		<div class="col-md-12">
		
		<h4>Войны XIV-XX стст.
		</br><small>2 месяцы</small></h4>
		<p>
		Пасля заключэння Крэўскай уніі 1385 года войны былі немагчымыя — дзяржавы знаходзіліся ў асабістай уніі кіраўнікоў. Адзіная вайна 
		здарылася падчас кіравання вялікага князя <a href="../figures/svidrigailo.php">Свідрыгайла</a> — мяцежнага князя, які разарваў унію з 
		<a href="../figures/jagiello.php">Ягайла</a>. З 1569 года паводле <a href="../events/unia_liublin.php">Люблінскай уніі</a> ВКЛ аб'ядналася з Польшчай 
		у адзіную федэрацыйную дзяржаву <a href="../terms/rzeczpospolita.php">Рэч Паспалітую</a>.
		</p>
		<p>
		Луцкая вайна — з 25 чэрвеня 1431 па 26 жніўня 1431 года.
		</p>
		

		<h4>Агульныя героі</h4>
		<p>
		З часоў <a href="../events/unia_liublin.php">Люблінскай уніі</a> 1569 года польская мова пачынае дамінаваць у <a href="../terms/shliahta.php">шляхецкім асяроддзі</a>. 
		Тады ж <a href="../terms/blr_mova.php">беларуская мова</a> атрымоўвае (апроч кірыліцы і арабскай) яшчэ і
		лацінскае напісанне. У 1795 годзе польская робіцца дзяржаўнай мовай і, разам з латынню, мовай навучання, — мовай гараджан і арыстакратыі (як руская сёння). З гэтага 
		часу ўсе польскамоўныя выхадцы Беларусі залічаюцца ў польскі пантэон славы. Як 
		<a href="../terms/filomaty.php">філаматы</a> і <a href="../figures/mickiewicz.php">Міцкевіч</a> — 
		<a href="../terms/litwiny.php">літвін</a>, які ніколі не быў у Польшчы.
		</p>
		<p>
		Такім чынам, шматлікія нацыянальныя героі Беларусі шануюцца заходнімі суседзямі — 
		<a href="../figures/gusowsky.php">Гусоўскі</a>, <a href="../figures/kostiushko.php">Касцюшка</a>, <a href="../figures/domeyko.php">Дамейка</a>, 

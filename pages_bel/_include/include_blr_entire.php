	<h2>Поўдзень. Украіна
		</br><small>У адзінай дзяржаве 1362-1569 (207 гадоў). У складзе Рэчы Паспалітай 1569-1795 (226 гадоў). У складзе Расійскай імперыі 1795-1917 (122 гадоў)</small>
	</h2>
	
		<div class="col-md-12">
	
	<h4>Агульная мяжа XIV-XX стст.</h4>
		<p> 
		У адзіную дзяржаву былі аб'яднаныя вялікім князем Альгердам пасля <a href="../events/sinie_wody.php">бітвы на Сініх Водах</a> 1362 года паміж 
		войскамі ВКЛ і Залатой Ардой.
		</p>
		<p>
		Натуральнай мяжой паміж Беларуссю і Украінай з'яўляецца <a href="../places/sea_herodotus.php">Палессе</a>. 
		Яшчэ на мапах XVI стагоддзя яго адзначалі Сармацкім морам — <a href="../places/sea_herodotus.php">морам Герадота</a>, што падзяляла 
		<a href="../terms/shliahta.php">сарматаў</a> і скіфаў.
		</br>[ <small>		
		Па словах Герадота <i>"Бацькамі Таргітая (першага чалавека), як кажуць скіфы, быў Зеўс і дачка ракі Барысфена (Дняпра) багіня Апі"</i>
		</small> ]
		</p>
		<p>
		 Канчаткова лінія мяжы 
		сфармавалася ў 1569 годзе, згодна з Люблінскай уніяй, <a href="../download/zemie_ruskie.php">Землі Рускія</a> адышлі ад ВКЛ да Польшчы.

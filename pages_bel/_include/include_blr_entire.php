		<h2>Паўночны ўсход. Латвія
		</br><small>У адзінай дзяржаве (пратэктарат ВКЛ) 1565-1793 (228 гадоў). У складзе Расійскай імперыі 1793-1917 (124 гады)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Агульная мяжа XIV-XX ст.</h4>
		<p>
		З часоў заснавання Дынабурга (Даўгаўпілса) рыцарамі-мечаносцамі (Fratres militiae Christi de Livonia) у 1275 годзе засталася практычна нязменнай па сённяшні дзень. 
		Курляндскае і Задзвінскае герцагствы — Herzogtum Kurland und Semgallen & Ducatus Ultradunensis — з 1565 па 1795 — пратэктарат ВКЛ і Рэчы Паспалітай. 
		З 1796 — Курляндская губерня Расійскай імперыі.	
		</p>
		
		</div>
				
		<h2>Паўночны захад. Летува (Lietuva)
		</br><small>У адзінай дзяржаве (пасля Грунвальда) 1411-1917 (506 гадоў). Ад Гедыміна да Грунвальда 1341-1411 (70 гадоў)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Агульная мяжа XIV-XX ст.</h4>
		<p>
		Гістарычная мяжа паміж Самагітыяй (Жмуддзю) і <a href="../terms/wkl.php">Вялікім Княствам Літоўскім</a>. Амаль супадае з паўночнай мяжой рассялення беларускага этнасу і  
		з мяжой Lietuvos Respublika 1935 года. Гэта мяжа, якая была адзначана на ўсіх мапах, згадваецца яшчэ ў 
		<a href="../download/chronicles_peter.php">"Chronicon terrae Prussiae"</a> 1326 года. 
		Сучасны выгляд мяжа атрымала ў 1940 годзе. 	
		</p>
		
		</div>
		
		<div class="col-md-12">
		
		<h4>Войны XIV-XX ст. 
		</br><small>0 (нуль) гадоў</small>
		</h4>
		<p>
		Фармаванне этнасаў і дзяржаўнасці сучасных беларусаў і літоўцаў адбывалася ў межах адной дзяржавы. Дзяржаўных войнаў на 
		мяжы сучасных Беларусі і Летувы не вялося. У XIV-XV стагоддязх у Жмудзі-Самагітыі былі слаба задакументаваныя паўстанні супраць ВКЛ. 
		Віленскі край (губерня) па роднай мове да 1980-х гадоў палавініўся <i>мова/kalba</i> і войн на моўнай мяжы 
		ў разгляданы перыяд не было. 
		</p>
		<p>
		Самая працяглася і бескампрамісная вайна цягам больш за 20 год адбываецца (сёння — у сетцы) паміж <i>сьвядомымі змагарамі</i> 
		і <i>lietuviski nacionalistai</i> за права 
		зваць "сваім" культурна-гістарычную спадчыну агульнай краіны. Хутка гэта стане нацыянальным спортам. 
		</p>
		

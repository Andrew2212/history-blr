		<p>
		 Складанае пытанне. Мабыць, лягчэй пералічыць <i>НЕ</i> агульных — такіх, як <a href="../figures/budny.php">Сымон Будны</a> (1530-93) і Марцінас Мажвідас (1510-63), якія пашыралі друкарства на родных мовах. 

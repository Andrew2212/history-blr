		<p>		
		1920 (31 ліпеня) Стварэнне <a href="../terms/bssr.php">БССР</a> — у газеце "Савецкая Беларусь" была апублікавана "Дэкларацыя пра незалежнасць БССР".
		</p>
		<p>
		1920 (9 кастрычніка) Стварэнне Сярэдняй Літвы Litwa Srodkowa (1920-22) — 1-шая Літоўска-Беларуская дывізія генерала Жалігоўскага
		(куды ўваходзілі Віленскі, Мінскі, Наваградскі, Гарадзенскі стралковыя палкі 
		і Літоўска-Беларускі полк артылерыі) адбіла Вільню ў Літоўскай Рэспублікі. 
		<i>"Пакуль прынамсі адзін з іх у стане трымаць у руках зброю, магілы продкаў не захопіць літовец, бальшавік ці немец"</i>
		</p>		
		<p>
		1920 (27 лістапада) <a href="../events/uprising_sluzk.php">Слуцкі Збройны чын</a> — спроба Часовай Рады Случчыны аднавіць незалежнасць краю. 
		Цягам месяца пад зброю сталі звыш за 4000 чалавек. 
		</p>		
		<p>		
		1921 (18 сакавіка) Рыжскі мір — вынік савецка-польскай вайны — ізноў падзяліў Беларусь амаль па лініі 
		Другога <a href="../events/razbor_rechi.php">падзелу Рэчы Паспалітай</a>.
		</p>
		

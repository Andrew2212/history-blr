		1919 Параза БНР у змаганні з бальшавікамі. 1 студзеня ў <a href="../places/smolensk.php">Смаленску</a> была абвешчана Савецкая Сацыялістычная Рэспубліка Беларусь, 
		8 студзеня ўрад ССРБ пераехаў са Смаленска ў Мінск. 27 лютага абвешчана пра стварэнне Літоўска-Беларускай ССР.

	<h3>Храналогія падзей 1914-1991 у межах Беларусі</h3>
		<p>
		1914-1918 <a href="../events/world_war_1.php">Першая сусветная вайна</a>. 
		У 1916-18 лінія фронту праходзіць па мяжы Другога <a href="../events/razbor_rechi.php">падзелу Рэчы Паспалітай</a>.
		</p>
		<p>
		1917 Крах <a href="../terms/ross_empire.php">Расійскай імперыі</a> — у выніку Лютаўскай рэвалюцыі (перавароту) у межах імперыі абвешчана Расійская Рэспубліка. 
		Кастрычніцкім пераваротам (рэвалюцыяй) Часовы ўрад Рэспублікі зрынуты. 
		</p>
		<p>
		1918 (25 сакавіка) у гістарычных <a href="../download/blr_people.php">межах беларускага этнасу</a> 
		абвешчана <a href="../terms/bnr.php">Беларуская Народная Рэспубліка</a> (БНР).
		</p>
		
		

		<p>
		1990 (27 ліпеня) Прынята Дэкларацыя пра дзяржаўны суверэнітэт Беларускай ССР, 
		</p>
		<p>
		1991 (25 жніўня) Дэкларацыі пра суверэнітэт нададзены статус канстытуцыйнага закона. 19 верасня БССР была перайменавана ў «Рэспубліку Беларусь». 
		</p>
		<p>
		1991 (8 снежня) у сядзібе Віскулі (Беларусь) былі падпісаныя Белавежскія пагадненні пра спыненне існавання 
		Саюза Савецкіх Сацыялістычных Рэспублік як «суб'екта міжнароднага права і геапалітычнай рэальнасці» 
		і пра стварэнне Супольнасці Незалежных Дзяржаў (НСД)
		</p>
		<p>
		1991 (26 снежня) Савет Рэспублік Вярхоўнага Савета СССР прыняў дэкларацыю пра спыненне існавання СССР, 
		тым самым афіцыйна распусціўшы Саюз ССР і яго інстытуты ўлады. СССР спыніў сваё існаванне.
		</p>
		

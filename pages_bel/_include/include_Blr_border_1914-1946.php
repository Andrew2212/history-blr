		<p>
		1939-1945 <a href="../events/world_war_2.php">Другая сусветная вайна</a> (ДСВ). 
		</p>
		<p>		
		1939 (17 верасня) Польскі паход РСЧА і парад у Брэсце — <a href="../terms/sssr.php">Савецкі саюз (СССР)</a> уступіў у Другую сусветную вайну. 
		2 лістапада выдадзены "Закон СССР ад 02.11.1939 пра ўлучэнне Заходняй Беларусі ў склад СССР і ўз'яднанне яе з БССР".
		</p>
		<p>
		1946 па выніках Ялцінскай канферэнцыі 1945 (якая падводзіць вынікі ДСВ), Беластоцкая вобласць, а таксама невялікія ўчасткі Гарадзенскай 
		і Брэсцкай вобласці былі перададзены Польскай Народнай Рэспубліцы.
		</p>
	

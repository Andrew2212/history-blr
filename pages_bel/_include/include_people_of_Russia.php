		<h3>Часть IV
		</br><small>"Пра народы мангольскія, ..., паляках і расіан, якія ўладараць"</small>
		</h3>

		
		<p>
			Апісанне цікава тым, што складзена паміж Другім (1792) і Трэцім (1795) <a href="../events/razbor_rechi.php">падзеламі Рэчы Паспалітай</a>. 
		</p>
		<p>
			Далучаныя да Расіі часткі <a href="../terms/wkl.php">Вялікага княства Літоўскага</a> — Магілёўская, Полацкая, Мінская губерніі населены "палякамі", у якіх 
			"роды дваранскія парадніліся са спрадвечнымі Польскімі". 
			У гэтым кантэксце па-іншаму ўспрымаецца тэрмін <i>"польскія паўстанні"</i> 
			<a href="../events/uprising_1794.php">1794</a>, <a href="../events/uprising_1830.php">1830</a> і <a href="../events/uprising_1863.php">1863</a> гадоў. 		
		</p>
		<p>
			"Старажытная Літва" — апошняя недалучаная да 1795 года (Трэці падзел Рэчы Паспалітай) частка ВКЛ —  "Валынія, Падляшша, Самагітыя і частка Расіі". Ужо няма 
			"<a href="../terms/litwiny.php">літвінаў</a>", але з'яўляюцца 
			не вельмі зразумелыя "літоўцы старажытнага племя Русаў" (чамусьці ў Самагітыі).			
		</p>
		<p>
			Напэўна, першым і апошнім разам на беларускіх землях ужываецца тэрмін 
			"Чарналюд ці Прастачыны Польскія <a href="../terms/religion_blr.php">рознага веравызнання</a>".
			У адрозненне ад наступных прац, 
			тут <a href="../terms/shliahta.php">шляхта</a> яшчэ не вылучана у "асобную этнаграфічную групу",
			няма яшчэ падзялення на "беларусаў" і "каталікоў" па нацыянальнасці. "Бела-Русія" — пакуль тэрмін для пазначэння 
			ўсходніх і паўднёва-усходніх межаў Літвы (у тэксце чамусьці <i>"заходніх"</i>). 
		</p>
		<p>	
			Пакуль яшчэ няма лямантавання пра "горкі лёс мужыка-беларуса" — <i>"Рознай скаціны і птушкі амаль кожны селянін мае для гаспадарчага 
			свайго ўжытку дастатковую колькасць".</i>  
		</p>
		

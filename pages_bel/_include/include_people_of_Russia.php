		<h2>PS.</h2>

		<p>
		Артыкул не скіраваны супраць "рускіх" ці яшчэ каго-небудзь. Проста, напэўна, у гутарцы пра гісторыі варта выкарыстоўваць 
		тэрміны, сучасныя апісваным падзеям. Англію заваявалі "нарманы", а не "французы" ці "нарвежцы". 
		Пасажы <i>"Тэрыторыі, на якіх у XV стагоддзі жылі людзі, што звалі сябе рускімі",</i> проста рэжуць слых. 
		Гэтак жа, як <i>"беларусы і літоўцы ў бітве пад Грунвальдам"</i> ці <i>"бітва Ганібала з італьянцамі"</i>. 
		</p>
		<p>
		Нават у прыведзенай вышэй працы (канца XVIII стагоддзя) яшчэ няма такога тэрміна як "рускі" ці "беларус" у дачыненні да этнічнай групы. 
		</p>
		

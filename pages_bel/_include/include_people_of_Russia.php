		<h1>"Апісанне ўсіх народаў, што насяляюць Расійскую дзяржаву"
		</br><small>1799, Іаган Готліб Георгі, Імператарская Акадэмія навук</small>
		</h1>
		
		<div class="col-md-12">
		
			<p class="img-float-left">
			<a href="../../images/img-download/all_peoples_RI/book_title.jpg" class="highslide"

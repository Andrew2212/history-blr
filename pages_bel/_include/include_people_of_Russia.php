			<p>
			Іаган Готліб Георгі (Johann Gottlieb Georgi; 1729-1802) — нямецкі медык, хімік, натураліст, этнограф, вандроўнік, прафесар мінералогіі і акадэмік Імператарскай Акадэміі навук і майстэрстваў.
			</p>
			<p>
			Прыбыў у Расію па запрашэнні Імператарскай Акадэміі навук і майстэрстваў у 1770 годзе.
			</p>
			<p>
			Прымаў удзел ў "Фізічнай экспедыцыі" Паласа. У агульным, маршрут паласаўскага атрада склаў 27 264 вёрсты (каля 29 085 км). Па сваіх маштабах і складанасці такі экспедыцыйны праект і сёння выглядаў бы цяжка выканальным. 
			<i>"Палас, Пётр Сымон, — адзін з найболей выбітных прыродазнаўцаў усіх краін і часоў…"</i> (c)<small> Рускі біяграфічны слоўнік</small>.
			</p>
			<p>
			Вынікам этнаграфічных даследванняў Георгі стала падрабязнае ілюстраванае апісанне народнасцяў, што насяляюць Расію. 
			Гэта праца была апублікаваная ў Санкт-Пецярбургу на нямецкай (роднай мове імператрыцы Кацярыны II) пад назвай
			<i>"Апісанне ўсіх народаў Расійскай дзяржавы, іх побыту, веравызнання, звычаяў, жылля, адзення і астатніх адрозненняў".</i> 
			Часткова ён быў перакладзены на рускую.  
			</p>
			
		</div>
		

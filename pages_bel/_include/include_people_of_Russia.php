		<h3>Частка I
		</br><small>"Пра народы фінскага племя, вядомых пад імем Русаў"</small>
		</h3>
		
		<p>
			Няма яшчэ ні "велікарусаў", ні "рускіх" (але ж, ёсць "украінцы"). 
			Ёсць заблытаны пасаж <i>"Пра расіан, якія ўладараць"</i> (у IV частцы), што добра пераклікаецца з думкай Ё. Шафарыка "Пра славянаў". 
		</p>
		<p>
			За 60 гадоў — да <a href="../events/uprising_1863.php">1863 году</a> — усё <a href="../terms/sewero_zapadny_kraj.php">імкліва зменіцца</a>. Ідэалогія і этнаграфія знойдуць выразныя фармулёўкі. 
		</p>
		

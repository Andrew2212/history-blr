			<p>
			Исаак Абрамович Сербов (27 мая 1871, Могилёвская губ.- 1943) — этнограф, фольклорист и археолог. Действительный член Института белорусской культуры (ИнБелКульлт).
			</p>
			<p>
			Окончил Полоцкую учительскую семинарию. Работал учителем в Вильно, Минске, Гомеле, Троках, Столбцах, Хотимске. В 1910-1913 годах как член Северо-Западного отдела Русского Императорского Географического Общества организовывал этнографические экспедиции в Беларусь. 
			</p>
			<p>
			В 1918 году окончил Московский археологический институт. В 1919-1921 годах работал в отделах народного образования Могилевской и Гомельской губерний, возглавлял работы по сбору и охране памятников истории и искусства. Во второй половине 1920-х годов исследовал курганы дреговичей около Минска и Мозыря. С 1929 года научный секретарь кафедры этнографии и фольклора Института истории Академии наук БССР.
			</p>
			<p>
			В ходе экспедиций по <a href="../places/sea_herodotus.php">Полесью</a> 1911-12 годов — "по землям дреговичей" — собрал большую фототеку, на основе которой был издан альбом "Беларусы ў фотаздымках"
			</p>
			
			<p>
			Кроме этнографических типажей в альбоме собраны фотографии городов и местечек. Вроде, ничего нового. Но "День белой кветки" 
			(горожане покупали белые бумажные цветы, выручка шла на благотворительность),  
			строгие деревянные сельские церкви — смотришь, и становится неяк сумна.  
			
			</p>
			

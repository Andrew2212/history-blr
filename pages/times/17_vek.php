	 </br></br>• Правление Жигимонта III Вазы (1566-1632) "Божьей милостью король Польский, Великий князь Литовский, Русский, Прусский, Мазовецкий, Жмудский, Ливонский, а также наследный король Шведов, Готов и Венедов"
	 </br></br>• 1600-1611 Война Речи Посполитой со Швецией. Битва при Кирхгольме — один из величайших триумфов кавалерии Речи Посполитой.
	 </br></br>• 1609-1618 Война Речи Посполитой с Московией.
	 </br></br>• 1610 Битва при Клушине. Войска Речи Посполитой заняли Москву. 
	 </br></br>• 1612 2-е ополчение под руководством Минина и Пожарского освободило Москву от войск Речи Посполитой.
	 </br></br>• 1617-1618 Поход на Москву войск королевича Владислава Жигимонтовича. 
	 </br></br>• 1618 Деулинское перемирие (д.Деулино, теперь г. Сергиев Посад) с Московией сроком на 14,5 года. 
	 			По условиям перемирия к Великому княжеству Литовскому отошло 29 городов.
	 </br></br>• 1621 Войска Речи Посполитой разгромили турецкую армию в битве под Хотином.
	 </br></br>• 1623 Бунт горожан Витебска против введения Брестской церковной унии, убит архиепископ Иосафат Кунцевич (канонизирован в 1867).
	 </br></br>• 1629 Подписано перемирие между Речью Посполитой и Швецией, которым окончена война 1600-1629 гг.
	 </br></br>• 1632 Королем Польши и великим князем ВКЛ избран Владислав Ваза (сын Жигимонта III Вазы); правил до 1648 г.
	 </br></br>• 1632-1634 Война Речи Посполитой с Московией (в исторической литературе именуется Смоленской войной).
	 </br></br>• 1634 Поляновский мир с Московией, которым закончена война 1632-1634 гг.
	 </br></br>• 1648 Начало восстания украинских казаков под руководством Богдана Хмельницкого против Речи Посполитой.
	 </br></br>• 1648 , лето. На юге Беларуси появились посланные Б.Хмельницким казацкие отряды, которые разоряли шляхетские имения, костелы, монастыри; казаков поддержала часть крестьян и горожан; повстанцы захватили города Гомель, Лоев, Брагин, Мозырь, Туров, Пинск, Бобруйск, Речицу, осаждали Слуцк, Быхов, Минск.
	 </br></br>• 1648 , осень. Войска ВКЛ под командованием гетмана польного Януша Радзивилла, двигаясь от Бреста вдоль Припяти, выбили казаков из городов Туров, Мозырь, Бобруйск, Речица.
	 </br></br>• 1648 Королем Польши и великим князем ВКЛ избран Ян Казимир (брат короля Владислава Вазы; правил до 1668 г.).
	 </br></br>• 1648, 1649, 1651 Осада украинскими казаками г.Гомеля.
	 </br></br>• 1649 , весна. На юг Беларуси из Украины направлены новые казацкие отряды И.Голоты, С.Пободайло, Ф.Гаркуши, к которым присоединялись и местные крестьяне; казаки вновь заняли города Чечерск, Туров, Речицу.
	 </br></br>• 1649 , июль. Б.Хмельницкий направил на Полесье 15-тысячное казацкое войско с целью помешать войскам ВКЛ во главе с Янушем Радзивиллом вступить в пределы Украины.
	 </br></br>• 1649 Битва под Лоевом между казацким войском и войсками ВКЛ. Разбитые казацкие отряды частично вырвались из окружения.
	 </br></br>• 1651 Войска ВКЛ во главе с Янушем Радзивиллом направились на подавление восстания на Украине. 
	 По дороге возле Лоевских переправ разбито 20-тысячное казацкое войско Небабы (Небаба погиб) и занят Киев.
	 </br></br>• 1651 , сентябрь. Белоцерковский мирный договор между гетманом Украины Б.Хмельницким и представителями Речи Посполитой, 
				в соответствии с которым из Беларуси выведены все казацкие войска.
	 </br></br>• 1654 Переяславская рада утвердила решение Московского земского собора о присоединении Украины к Московии.
	 </br></br>• 1654-1667 Война Речи Посполитой с Московией
	 </br></br>• 1654 80-тысячная российская армия во главе с царем заняла Дорогобуж, Рославль, Мстиславль, Копысь, осадила Смоленск (взят через 3 месяца); 
	 казацкие войска заняли Гомель, Мозырь, Чечерск, Новый Быхов и осадили Старый Быхов (осада продолжалась более трех лет).
	 </br></br>• 1654 В результате 3-месячной осады 20-тысячная русская армия заняла г.Витебск.
	 </br></br>• 1654 , лето. Русская армия во главе с В.П.Шереметевым заняла города Полоцк, Невель, Усвят, Диену.
	 </br></br>• 1654 , сентябрь. Русские войска заняли город Могилев.
	 </br></br>• 1655 , лето. Русские войска и украинские казаки во главе с Золотаренко заняли города Дубровно, Оршу, Копысь, Борисов, Минск, Вильно.
	 </br></br>• 1655 Кейданский договор (г. Кейданы) об установлении унии  ВКЛ и Швеции.
	 </br></br>• 1655, 1660 , Русские войска в результате осады овладели Новогородским замком.
	 </br></br>• 1656 Виленское перемирие с Московией о приостановлении войны 
	 </br></br>• 1658 Гадячский договор (г. Гадяч) между гетманом Войска Запорожского Иваном Выговским и представителями Речи Посполитой. 
	 Согласно договора Великое княжество Русское в составе Киевского, Черниговского и Брацлавского воеводств Королевства Польского должно было стать третьим 
	 участником Речи Посполитой Трех Народов.
	 </br></br>• 1660 Русская армия И.А.Хованского захватила Брест
	 </br></br>• 1660 Поражение армии И.А.Хованского в бою под Ляховичами.
	 </br></br>• 1660 Войска Речи Посполитой освободили Минск и земли до р.Березины.
	 </br></br>• 1661 В Могилеве жителями уничтожен многочисленный русский гарнизон.
	 </br></br>• 1661 Русские войска во главе с И.А.Хованским разбиты возле д.Кушлики (под г. Вильно); освобожден г.Вильно.
	 </br></br>• 1667 Андрусовское перемирие между Речью Посполитой и Московией (подписано в д.Андросово возле Мстиславля). 
					По условиям перемирия к Московии отошли Смоленское воеводство и Левобережная Украина. 
	 </br></br>• 1668 Король Польши и великий князь ВКЛ Ян Казимир, разочарованный неудачами, отрекся от престола (через 4 года умер).
	 </br></br>• 1669 Королем Польши и великим князем ВКЛ избран Михаил Вишневецкий; правил до 10 ноября 1673 г.
	 </br></br>• 1672—1676 Война между Речью Посполитой и Османской империей за власть над Правобережной Украиной. 
	 			Окончилась Журавенским договором 1676 года. Этот договор вернул Речи Посполитой 2/3 Украины и 10 тысяч пленных-христиан.
	 </br></br>• 1674 Королем Польши и великим князем ВКЛ избран полководец Ян Собесский; правил в Речи Посполитой до 1696 г.
	 </br></br>• 1683-1699 Война Речи Посполитой и Священной Лиги против Крымского ханства и Оттоманской империи; война доведена до победы в 1699 г.
	 </br></br>• 1684 Папой Иннокентием XI снована Священная лига. Состояла из Священной Римской империи, Венецианской республики и Речи Посполитой. 
	 Альянс противостоял Оттоманской империи в Великой Турецкой войне.	 
	 </br></br>• 1686 Подписан "вечный мир" с Московией, к Московии отошли смоленские земли и Киев. 
				Московия вступает в Священную Лигу и перестает платить дань Крымскому Ханству.
	 </br></br>• 1696 Всеобщая конфедерация сословий (конфедеративный сейм) Речи Посполитой приняла постановление о том, что в ВКЛ государственные документы должны писаться не на белорусском, а на польском и латинском языках.
	 </br></br>• 1697 Королем Польши и великим князем ВКЛ избран саксонский курфюрст Август II Сильный; правил в Речи Посполитой до 1733 г.


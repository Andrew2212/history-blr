Войны с Россией
</br>"Кровавый Потоп" Великого княжества Литовского
</br>1609—1618 "Смутное время", осада Смоленска, взятие Москвы. Приглашение на московский пресол королевича Владислава.
</br>1632—1634 Смоленская война. Вторжение русских войск. Владислав отказался от московского престола. Смоленск остался в составе ВКЛ.
</br>1654—1667 "Кровавый Потоп". После присоединения к Московии запорожского казачества (Переяславская рада), Москва пытается захватить восточные земли ВКЛ. 
				Захвачены Левобережная Украина, Киев, Смоленск.
</p>
<p>
Война с Войском Запорожским (Гетманщиной)
</br>1648-1654 Восстание Хмельницкого в Русских Землях. Итог: Переяславская рада, начало новой войны с Московией.
</p>
<p>
Войны со Швецией
</br>"Кровавый (Шведский) Потоп" Королевства Польша
</br>1600—1611 Попытка Жигимонта III Ваза вернуть себе шведскую корону и занять Эстляндию. Границы между обоими государствами остались прежними.
</br>1617—1618 Попытка Швеции занять весь прибалтийский регион. Границы между обоими государствами остались прежними.
</br>1621—1626 Швеция захватила все земли ВКЛ в Ливонии, в том числе и Ригу.
</br>1626—1629 "Прусская война двух Ваза". За Швецией остались значительная часть Прибалтики с Ригой. 
</p>
<p>
<p>Войны с Турцией (Османской империей)

	 </br></br>• 1534-1537 Война ВКЛ с Великим княжеством Московским.
	 </br></br>• 1545 В ВКЛ начали свою деятельность протестантские проповедники. Проявление Реформации в различных ее течениях, что способствовало дальнейшему развитию просвещения и гуманизма.
	 </br></br>• 1548-1572 Княжение в ВКЛ Жигимонта II Августа; в годы его правления в государстве проведены важные военные, политические и экономические реформы.
	 </br></br>• 1553 Основание типографии в Бресте.
	 </br></br>• 1558-1583 Ливонская война - борьба между Россией, Швецией, Польшей и ВКЛ за Прибалтику.
	 </br></br>• 1559 Ливонский орден перешел под протекторат ВКЛ; к договору о протекторате присоединился и епископ рижский.
	 </br></br>• 1562 Основание в Несвиже типографии и издание в ней Сымоном Будным "Катехизиса".
	 </br></br>• 1562 Движение антитринитариев - сторонников радикальной реформации христианского учения, примыкавших к гуманистической и рационалистической традиции ренессансной культуры; их общины созданы в Клецке, Ивье, Любче, Новогрудке, Лоске и др. городах.
	 </br></br>• 1562 В Москве вышла первая печатная книга ("Апостол"), изданная Иваном Федоровым (Федоровичем) и Петром Мстиславцем.
	 </br></br>• 1566 Утвержден 2-й Статут ВКЛ.
	 </br></br>• 1569 Люблинская уния - объединение Королевства Польского (Корона) и ВКЛ (княжество) в Речь Посполитую.
	 </br></br>• 1569 В ВКЛ начал деятельность католический монашеский орден Общества Иисуса (орден иезуитов; действовал до 1820 г.)
	  Основной своей задачей считал борьбу с Реформацией, что стало началом Контрреформации.
	 </br></br>• 1573 Шляхта получала официальное право выбирать короля польского и великого князя ВКЛ.
	 </br></br>• 1575 Королем Польши и великим князем ВКЛ избран Стефан Баторий; правил в Речи Посполитой до 12 декабря 1586 г.
	 </br></br>• 1579 Преобразование Виленской иезуитской академии в Виленский университет - первый университет в Восточной Европе.
	 </br></br>• 1579 Осада и взятие Полоцка войсками Великого княжества Литовского.
	 </br></br>• 1581 Создан Главный литовский трибунал - высший судебный и апелляционный орган в ВКЛ.
	 </br></br>• 1581 Открытие в Полоцке иезуитского коллегиума (среднее учебное заведение классического типа).
	 </br></br>• 1582 Перемирие на 10 лет между Россией и Речью Посполитой, заключенное в г. Ям-Запольском.

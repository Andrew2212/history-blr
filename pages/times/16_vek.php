	 </br></br>• 1501 В. кн. ВКЛ Александр избран королем Польши — восстановлена персональная уния ВКЛ и Королевства Польского.
	 </br></br>• 1503 Заключено перемирие на 6 лет между ВКЛ и Великим княжеством Московским; к Москве отошло 29 городов и 70 волостей, в том числе Чернигов, Брянск, Стародуб, Дорогобуж, Гомель, Вельск, Мценск, Торопец и часть Мстиславльского княжества.

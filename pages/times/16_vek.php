<p>
• "Золотой век" (1410-1569) ВКЛ-Беларуси
</br>В XVI веке на землях ВКЛ развивались идеи европейского Возрождения. Активно печатались книги, расцветали наука, образование и искусство. 
Расширялись международные связи страны.
</p>
<p>
• 1558-1583 Ливонская война с Московией — попытка царя Ивана Грозного при поддержке Касимовского ханства захватить Полоцк и прибалтийские земли ВКЛ.
</p>
<p>
• 1659 В поисках помощи для борьбы с московской экспансией была заключена Люблинская уния с Польшей. (создание Речи Посполитой Обоих Народов).
</p>
<p>
• 1573 Для предотвращения религиозных конфликтов принимается акт Варшавской конфедерации — закладываются основы религиозной терпимости знати и свободных людей в Речи Посполитой.
</p>
<p>
• 1576-1586 Правление Стефана Батория — В. кн. Литовского и короля Польского. 
</br>В его правление был освобожден Полоцк, начаты образовательные реформы 
(основаны Виленская Академия, коллегиумы в Несвиже и Полоцке), упоядочена денежная система (введен единый польский грош). 
</br>Стефан Баторий планировал перенести столицу в Гродно, 
где жил последние годы и реконструировал Старый замок под новую королевскую резиденцию.
</p>
<p>
• 1596 Брестская уния — переход православных приходов в греко-католические (униатские). Принята для уменьшения межконфессиональных противоречий и 
снижения влияния Москвы и Московского патриархата РПЦ <small>[в XVI веке в Московии формируется концепция "Москва — третий Рим"]</small> на внутренние дела ВКЛ. 
</br>Базилиане (монахи монастырей, принявших Брестскую унию, члены Ордена Пресвятейшей Троицы — Ordo Sancti Basilii Magni) основой своей деятельности считали образование и воспитание молодёжи, на этом поприще базилиане соревновались с иезуитами 
— по всей стране открывались школы для "дворянских, мещанских и купеческих детей".
</p>



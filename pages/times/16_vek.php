	 </br></br>• 1506 Крымско-татарские набеги на на города Брагин, Мозырь, Стрешин, Клецк, Ошмяны, Волковыск, Друцк, Витебск, Полоцк; безуспешная осада города и замка Минска войсками перекопского хана Махмут-Гирея.
	 </br></br>• 1506 Клецкая битва - разгром 7-тысячным войском ВКЛ под командованием Михаила Глинского войск крымских татар под Клецком.
	 </br></br>• 1506 На княжение в ВКЛ панами-радой избран Жигимонт I Старый (8 декабря 1506 г. он избран и королем Польши); княжил до 1 апреля 1548 г.
	 </br></br>• 1507-1508 Война ВКЛ с Великим княжеством Московским. Войска русских осаждали Кричев (дважды), Оршу, Мстиславль (дважды), Полоцк и др.; война окончилась очередным "вечным миром".
	 </br></br>• 1508 Мятеж Глинских. Их войска захватили Туров, Мозырь, Бобруйск; с помощью московских войск безуспешно осаждали Слуцк и Минск. Спустя некоторое время мятежники перешли на службу к князю московскому Василию III.
	 </br></br>• 1512-1522 Война ВКЛ с Великим княжеством Московским.
	 </br></br>• 1514 Московские войска заняли г.Смоленск. 
	 </br></br>• 1514 Оршанская битва. Победа войск ВКЛ под командованием Константина Острожского над 80-тысячным войском московским под Оршей, на р.Крапивно.
	 </br></br>• 1517 Франциск Скорина издал в г.Прага первую белорусскую книгу; до 1519 г. им изданы 23 книги Библии.
	 </br></br>• 1521-1522 Белорусский поэт-латинист Николай Гусовский создал лироэпическую поэму "Песня о зубре" - гимн красоте родной земли (издана в 1523 г. в Кракове).
	 </br></br>• 1522 Франциск Скорина основал в Вильно типографию - первую на территории Восточной Европы; в 1522 г. в ней напечатана "Малая подорожная книжица", в 1525 г. - "Апостол".
	 </br></br>• 1529 Утвержден и введен в действие 1-й Статут ВКЛ.
	 </br></br>• 1529 На сейме в Вильно великим князем ВКЛ избран 9-летний Жигимонт II Август (сын Жигимонта I Старого); 

		<h3>PS.</h3>
		<p>
		В белорусском медийном пространстве приписывать Калюмны Трокскому воеводству так же неприлично, как писать Миндаугас или Витаутас.
		</p>
		<p>
		Единственый раз, когда Калюмны присутствовали в гербе этого воеводства — это в "Codex Bergshammar" XV в. на щите "копейщика". 
		Однако там он присутствует и как <i>отдельный герб</i>, а так же 
		стоит на <i>каждом</i> щите (у всадника Погони в том числе). Впоследствии у Трокского копейщика щит исчезает и Калюмны 
		на гербе воеводства более не появляются.
		</p>
		<p>
		 Кроме того, как сказано в "Herby rycerstwa polskiego" 1584, герб Калюмны просто старше города Троки.

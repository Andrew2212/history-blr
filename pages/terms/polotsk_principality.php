		<p>
			Полоцкое княжество — княжество кривичей на "Пути из варяг в греки".
			Первые летописные сведения о нем связаны со скандинавскими варягами. 
			Хроника "Деяния данов" (Gesta Danorum) рассказывает о походе на Полоцк легендарного короля Фроди I (V-VI в. н.э.).			
			 В "летописях руских" впервые упоминается в 862 году ("Повесть временных лет").
		</p>
		<p>	 
			 Подробный анализ сведений о "Полоцке времен Аттилы" можно посмотреть 

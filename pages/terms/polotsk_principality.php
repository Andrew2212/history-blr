		</p>
		<p>	 
			 Поскольку Полоцкие летописи исчезли вместе с разрушенным Софийским собором, о многих эпизодах истории мы сегодня знаем только из скандинавских хроник. 
			 Так София, княжна Минская — рускими летописями не упоминается, но хорошо известна из западных источников (сочинения Саксона Грамматика, "Саги о Кнютлингах", генеалогии датских королей) — она была королевой Датской, женой Вальдемара I Великого. 
		</p>
		<p>
		Столицу княжества — <a href="../places/polotsk.php">Полоцк</a> — скандинавские летописи  особо выделяли из остальных 8 городов руских 
		— Киев, Полоцк, Новгород, Изборск, Ростов, Муром, Ладога, Белоозеро, — упомянутых к 862 году. 		
		</p>
		<p>
		<i>"Особое тяготение варягов к Полоцку"</i> подчеркивал А. Н. Веселовский в своей работе 

		<h3>XII век</h3>
		
		<p>
		Век феодальной раздробленности. 
		</br>Крещение языческих капищь и первые христианские святые — <a href="../figures/efrosinia.php">Ефросиния Полоцкая</a> 
		и <a href="../figures/kirill_tur.php">Кирилл Туровский</a>.
		</p>
		<p>
		Полоцкое княжество разделено
 		на Минское, Витебское, Друцкое, Изяславское, Логойское, Стрежевское и Городцовское. 
		Начались междуусобные войны на фоне непрекращавшегося противостояния с Киевом. 
		</p>
		<p>
		В 1129 году князь киевский Мстислав высылает в Византию шестерых захваченных Рогволодовичей. 
		 За время их отсутствия в Полоцке сильно возросла роль веча. Через 10 лет князья возвращаются, 
		но с этого времени князей в Полоцке выбирало вече.
		</p>
	
		<h4>
		Язычество и христианство. Двинские камни.
		</h4>
		<p>
		В XII на территории Беларуси христианство не было доминирующей религией, скорее было довольно локальным.
		</p>
		<p>
		В первой половине XI века были построены Софийские соборы — в Киеве, Полоцке и Новгороде. 
		Киевские и новгородские правители того периода стали канонизированными 
		святыми — св. кн. Анна Новгородская, киевские св. кн. Ольга, св. кн. Владимир "креститель Руси" и  его сын Ярослав Мудрый, родной брат Изяслава. 
		</p>
		<p>
		Однако два Полоцких князя (Брячеслав и Всеслав), правившие весь XI век запомнились иначе — 
		Брячеслав "обращался к волхвам и сын его рожден от волхования", а Всеслава летописи описывали как оборотня-воўкалака и потомки дали ему имя колдун-Чарадзей.
		</p>
		<p>
		Так начинала складываться богатая <a href="../terms/religion_blr.php">палитра религий на белорусских землях</a>.
		</p>
		<p>
		Одним из значимых культовых артефактов, оставшихся от XII века являются 
		<a href="../terms/dwina_stouns.php">Двинские (Борисовы) камни</a> — огромные валуны с выбитыми на них христианскими символами. 
		Массовое "крещение" языческих капищь в Полоцком княжестве — так большинство иследователей 
		определяет назначение Двинских камней. 
		</p>
		<p>
		Двинские (Борисовы) камни связывают с именами первых князей Полоцких и Друцких, носивших по два имени (языческое и христианское) — 
		Рогволода-Бориса (сына Всеслава "Чародея") и его сына Рогволода-Василия. С именем Рогволода-Бориса также связан город Борисов — 
		<i>"ходил он на ятвяг и, победя их, возвратясь, поставил град во свое имя..."</i>
		</p>
		<p>
		Однако, в связи с раздробленностью Полоцкого княжества на уделы, "крестовый поход" на язычество затронул только саму Полоцкую землю (Витебская обл.).
		</p>
		<p>
		В XII  веке в Турово-Пинском княжестве писал свои труды <a href="../figures/kirill_tur.php">Кирилл Туровский</a> (1130-1182) — богослов, писатель и проповедник. 
		Одно из ярких имен XII века — внучка Всеслава "Чародея", св. <a href="../figures/efrosinia.php">Ефросиния Полоцкая</a> (1101-1167) — монахиня и просветительница, 
		легендарная переписчица Полоцкой летописи, основательница иконописных и ювелирных мастерских. Церковное почитание ее на землях от Полоцка до Киева 
		началось еще в XII веке — существовали церковная служба и «Житие» преподобной Евфросинии. 
		</p>
		<p>
		[ <small>Московская церковь до XIX века не имела к ней отношения — Макарьевские соборы XVI в., канонизировавшией русских святых, не считали ее таковой. 
		И хотя ее имя упоминается в "Степенной книге царского родословия" (написанной при Иване Грозном, временно захватившем Полоцк), 
		первая русская служба преподобной Ефросинии была составлена в 1893 году. 
		Поэтому довольно странно <a target="_blank" href="../../images/img-terms/polotsk_principality/RPZ.jpg">читать на православных порталах</a> о том, что 
		<i>"преподобная мати Евфросиния как воин Христов на страже охраняет крайний ЗАПАДНЫЙ рубеж РУССКОЙ земли".</i> Полоцк, в общем-то, находится на ВОСТОКЕ 
		земли, которая называется БЕЛАРУСЬ.
		
		</small> ]  
		</p>		
		
		
		

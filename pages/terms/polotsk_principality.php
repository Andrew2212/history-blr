		<h3>X век</h3>
		<p>
		Полоцкое княжество быстро отпало от предполагаемого "древнерусского государства".
		</p>
		<p>
		920-978 <a href="../figures/rogvolod_rogneda.php">Рогволод</a>. Первым известным князем Полоцка, о котором упоминают летописные источники, был Рогволод (ум. ок. 978). 
		Прямых наследников по мужской линии он не оставил — киевский князь Владимир ("Красно Солнышко", креститель Руси) убил его и его сыновей, 
		и насильно взял в жены дочь Рогнеду.  
		</p>
		<p>
		987 <a href="../figures/rogvolod_rogneda.php">Изяслав и Рогнеда</a>. Рогнеда пыталась отомстить и убить Владимира, но попытка была неудачной. На ее защиту с мечом стал старший сын — малолетний Изяслав. 
		По совету бояр: <i>"Не убивай её ради дитяти сего, но воздвигни отчину отца её, и отдай ей с сыном твоим"</i>, 
		Изяслав и Рогнеда были отправлены в Полоцкую землю,  
		город Заславль (Изяславль).		

		<p>
		<i>"И с тех пор взимают меч Рогволожи внуки против Ярославовых внуков."</i>
		</br>Дети от брака Владимира и Рогнеды положили начало родам Изяславичей ("Рогволожи внуки" — князья Полоцкие) от старшего Изяслава и Ярославичей 
		("Рюриковичи" — князья Киевские, Новгородские, Ростовские) от Ярослава Мудрого, младшего сына. 
		</p>
		<p>
		<i>"В вековой распре Ярославичей и Изяславичей есть что-то расовое"</i> — отмечал Веселовский. 
		</p>
		<p>
		В летописях и литературе князей Полоцких именуют Рогволодовыми внуками, Рогволодовичами, Изяславичами, Брячеславовичами (от сына Изяслава), 
		Всеславичами (от внука Изяслава), но не Рюриковичами или Владимировичами.
		</p>
		<p>
		986 <a href="../figures/torwald.php">Торвальд Кодранссон</a>. Крещение Полоцка, в отличие от остальной Руси, связывают с именем Торвальда Кодранссона (Торвальда Вандроўніка) — легендарного христианскиого миссионера и путешественника. 
		В Полоцке он основал монастырь святого Иоанна Крестителя и в Полоцке же похоронен.
		Согласно исландской саге "Прядь о Торвальде Путешественнике" Торвальд похоронен <i>"в горе у церкви Иоанна и почитается как святой"</i>.
		</p>
		

		<h4>1003-1044 княжение Брячеслава</h4>
		<p>
		Брячеслав, сына Изяслава. Присоединил земли между Западной Двиной и Десной, где основал город Браслав. 
		</p>
		<p>
		В его правление начато строительство Полоцкого <a href="../places/sophia_sobor.php">Софийского собора</a> (1030-60) — одного из трех (Киев, Полоцк, Новгород) Софийских соборов,
		построенных в одно время и по единому образу — образу патриаршего собора Св. Софии в Константинополе.
		</p>
		<p>
		Его княжение отмечено успешным походом на Новгород и борьбой с киевским князем Ярославом Мудрым. Это противостояние по-разному описано в "Саге об Эймунде" 
		и киевских летописях. Сага повествует о том, как Эймунд выступил на стороне Брячеслава, и "мир творит" Ингигерда (дочь короля Швеции, жена Ярослава).
		И Эймунду в удел отдается Полоцк, Брячеславу Киев, а Ярославу Новгород. Руские летописи рассказывают немного иначе. 
		</p>
		
		<p>
		<a target=_blank" href="http://norse.ulver.com/src/konung/eymund/ru2.html">"Сага об Эймунде"</a>

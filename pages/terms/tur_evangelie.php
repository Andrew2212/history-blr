		<h3>XI век. Исторический контекст</h3>
		<p>
		Туровское Евангелие — один из первых камней в пестрой мозаике <a href="../terms/religion_blr.php">традиционных религий нашего края</a>. 
		</p>
		<p class="img-float-left">
		<a href="../../images/img-terms/tur_evangelie/holy_people.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img alt="Туровское евангелие" src="../../images/img-terms/tur_evangelie/holy_people_thumb.jpg" height="120" >
			</br><small>Святые XI века</small>
		</a>
		</p>
		<p>
		Туровско-Пинские земли принадлежат к владениям Ярославичей. В Киевских и Новгородских землях уже третье поколение князей причисляется 
		к лику святых — к концу XI века их уже около 20
		(см. <a target="_blank" href="https://ru.wikipedia.org/wiki/Список_канонизированных_русских_князей_и_княгинь">полный список</a>). 
		И это не считая схимников и монахов — таких, как Преподобный Иларион схимник Печерский, в Дальних пещерах (1066).
		</p>
		<p>
		В <a href="../terms/polotsk_principality.php">Полоцком княжестве</a> весь век правят два князя-язычника, внук и правнук <a href="../figures/rogvolod_rogneda.php">Рогволода</a>
		 — <a href="../terms/polotsk_principality.php">Брячеслав</a> (советующийся с волхвами) и <a href="../figures/charadzej.php">Всеслав</a> (рожденный волхованием и прозванный колдуном-оборотнем).
		Идет непримиримая борьба Рогволодовичей и Ярославичей, <i>"примеров которой нет в нашей древней истории"</i>.
		</p>
		<p>
		Единственный человек, которого в Полоцких землях современники называют "святым" — <a href="../figures/torwald.php">Торвальд Кодранссон</a>, 
		исландский викинг-миссионер, креститель Полоцка. До массового крещения языческих капищь в Полоцком княжестве — 
		"крещения" <a href="../terms/dwina_stouns.php">Двинских камней</a> — еще более 100 лет.
		</p>
		<p>
		При этом Софийские соборы по образу Константинополького строятся в Киеве, Полоцке и Новгороде почти одновременно — в 1030-1050 годах. 
		</p>
		<p>
		Поэтому Туровское евангелие является действительно уникальным документом эпохи. Эпохи, когда христианство приходило на белорусскую землю.
		Христианство, еще не разделенное на конфессии — до раскола христианской церкви на католиков (латинян) и 
		<a href="../articles/ortodox.php">православных</a> (ортодоксов) в 1054 году. 
		<i>"А канфесійнае разуменне розніцы паміж хрысціянамі лацінскага і ўсходняга абрадаў прышло значна пазней."</i> — уточнил представитель Рыма-каталіцкага Касцёла ў Беларусі.
		</p>
		
		
		
		

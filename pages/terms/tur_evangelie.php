		<p>
			Туровское Евангелие — рукописное Евангелие-апракос, созданное в первой половине XI века (1030-1050 гг), текст написан кириллицей. Хранится в библиотеке Академии наук Литвы. 
			Туровское Евангелие является одним из древнейших памятников славянской письменности. Старейший памятник письменности на территории 
			<a href="../terms/belarus.php">Беларуси</a>.
		</p>
		<p>
		[ <small>Евангелие — повествование о божественной природе, учении и земной жизни Иисуса Христа.
		</br>Апракос (др.-греч. не-дельный, праздничный) — разновидность Евангелия или Апостола, в которой текст организован не в каноническом порядке, 
		а календарно, согласно с недельными церковными чтениями. </small> ]
		</p>
		<p>
		От рукописи сохранилось только 10 листов. Они были найдены в Турове в 1865 участниками организованной Виленским учебным округом 
		археографической экспедиции. В 1869 году в Вильне вышло исследование текста Туровского Евангелия, подготовленное П. А. Гильтебрандтом.
		</p>
		<p>
		Туров — город, впервые упомянутый в 980 году, столица Турово-Пинского княжества. В XI веке стал значимым центром христианства. 
		Родина <a href="../figures/kirill_tur.php">Кирилла Туровского</a> — богослова и теолога XII века.
		</p>
		


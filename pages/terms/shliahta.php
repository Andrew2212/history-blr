		<h3>Сарматизм</h3>
		<p>
		 — шляхетская идеология , доминировавшая в XVI - XIX вв. 
		Сарматизм возводил шляхту к древним сарматам , отделяя тем самым себя от массы простолюдинов.
		Сарматизм предопределил многие особенности культуры знати Речи Посполитой и её отличие от западноевропейской аристократии: 
		условно «восточный» стиль парадной одежды ( жупан , контуш , слуцкий пояс , сабля ), особые манеры, сарматские портреты и тд. 
		</p>
		<p>
		На картах Сарматия локализовалась вокруг <a href="../terms/yotving.php">венедов</a> 
		и <a href="../places/sea_herodotus.php">моря Геродота</a> (ныне Полесье).
		</p>
		<p>
		Эта традиция — определять шляхту как "отдельную этнографическую группу" — продолжена в академическом издании Императорского Русского Географического Общества
		<a href="../download/russia_blr.php">"Россия. Полное географическое описание"</a> 1905 года.
		</p>
		

		<p>
		Шляхта (от древневерхненемецкого slahta — род, либо нем. Schlacht — сражение) — привилегированное воинское сословие в Королевстве Польском и 
		<a href="wkl.php">Великом княжестве Литовском</a>, а также некоторых других государствах. 
		Играло большую роль в политической жизни страны, со временем сформировало концепцию «шляхетской нации» и утвердило свое право на выборную монархию.
		</p>
		<p>
		<i>"Шляхціч на загродзе роўны ваяводзе"</i>. Любой шляхтич, избранный депутатом сейса или сеймика, обладал правом Liberum Veto.
		<i>Liberum veto</i> — принцип парламентского устройства в Речи Посполитой, который позволял любому депутату сейма прекратить обсуждение вопроса 
		в сейме и работу сейма вообще, выступив против. Было принято как обязательное в 1589 году, в 1666 году было расширено на воеводские сеймики.		
		</p>
		

		<p>
		В отличие от соседних стран, где дворянство составляло ~3% населения, в ВКЛ шляхта составляла 10-15% (по разным воеводствам). 
		По <a href="../events/unia_gorodlo.php">Городельской унии</a> 1413 года бояре <a href="../terms/wkl.php">Великого княжества Литовского</a> 
		вошли в польское шляхетское гербовое братство — "акт об адопции". Это время принято за точку отсчета современной 
		<a href="../download/blr_heraldic.php">белорусской геральдики</a>.
		</p>
		
		<p>
		По имущественному положению шляхта делилась на:
		<br>— магнатов
		<br>— заможная шляхта (владение одной или несколькими деревнями)
		<br>— фольварковая шляхта (владение одним или несколькими фольварками /усадьбами/)
		<br>— застенковая (загродковая, околичная) шляхта (имела свое хозяйство, но не имела крестьян)
		<br>— шляхта-голота (безземельная)
		</p>
		
		<p>
		Нижний имущественный слой шляхты размыто смыкался с <a href="zemiane.php">земянами</a> и панцирными <a href="zemiane.php">боярами</a>.
		Шляхта, земяне, панцирные и путные бояре были воинским сословием (крестьне на службу не призывались). 
		До наших дней сохранился поименный <a href="../download/spis_voiska.php">"Спис войска Литовского"</a> 1528-67 гг., 
		где каждый может найти знакомые фамилии.
		</p>
		

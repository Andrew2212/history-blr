		<h3>Gente Lituane, natione Polonus</h3>
		<p>
			<i>" — большинство местного дворянства знало о своем литовском или белорусском этническом происхождении,
			  но воспринимало языковую и культурную полонизацию предков как акт их добровольного политического и цивилизационного выбора
			  </i>[ аналог национальных элит в СССР ]<i>."
			  </i>
			  <small>Юлиуш Бардах, доктор honoris causa Варшавского университета, Вильнюсского университета, Лодзинского университета</small>
		</p>
		
		<p>
		C 1696 года польский язык стал государственным в Речи Посполитой. Он стал языком горожан (аналог русскому сегодня), 
		наряду с латынью использовался в учебных заведениях (Виленский университет, Полоцкая иезуитская Академия и пр). 
		</p>
		<p>[ <small>
		Однако даже в XIX веке польскоязычные <a href="../terms/filomaty.php">филоматы</a> называли себя не поляками, а <a href="../terms/litwiny.php">литвинами</a>, 
		обращались в своих литературных 
		произведениях к образам "исторической Литвы" (ВКЛ), вводили в свои литературные произведения элементы "тутэйшага" языка ( "Dziady" Мицкевича).
		</p>
		<p>
		Позднее, <i>"<a href="../figures/domeyko.php">Игнаци Домейко</a> в дневниковых записях о <a href="../events/uprising_1830.php">восстании 1830-1831</a>
		 практически в каждом случае не забывает указать национальную принадлежность 
		того или иного повстанца. Он четко выделял «коронных» (поляков), «жмудинов» (современных литовцев) и «наших литвинов» (современных белорусов)."
		</i> (с) <a target="_blank" href="http://archives.gov.by/index.php?id=162773">Архивы Беларуси</a>

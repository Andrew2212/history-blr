	<h3>Краевасьць и национальность</h3>
	
		<p>
		Пожалуй, самое грустное, что сделали "русский поп и учитель" в Северо-Западном крае — это приделали название "белорусы" к православным крестьянам. 
		Не к жителям края. К отдельной социальной группе. 
		Крестьян-католиков часто записывали в поляки. Большинство шляхты и горожан были польскоязычными (польский был государственным языком и языком образования 
		— как сейчас русский). Шляхта была вынесена в отдельную "этнографическую группу". 
		</p>
		<p>	
		 Если с этнонимом <a href="../terms/litwiny.php">литвины</a> 
		издавна идентифицировали себя все слои населения, то "белорусами" местные "тутэйшыя" не называли себя даже в начале XX века. С этим наследием страна 
		пытается справится до сих пор. На этой земле идея <a href="../terms/kraj_tut.php">тутэйшай краевасьцi</a> всегда доминировала над этническим происхождением.
		</p>
		

		<p>
			Северо-Западный край — шесть губерний <a href="../download/west_russland.php">Западного края</a> Российской империи — Виленская, Ковенская, Гродненская, Минская, Могилёвская, Витебская. 
		</p>
		<p> 
			Были образованы на территории бывшего <a href="../terms/wkl.php">Великого Княжества Литовского</a>, захваченного Российской империей, 
			и отличались определенными особенностями общественно-правового положения. Центр управления Северо-Западным краем находился в <a href="../places/wilno.php">Вильне</a>.

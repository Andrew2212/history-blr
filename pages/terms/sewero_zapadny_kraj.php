		<p>
		Советская Историческая Энциклопедия забавно делит окраины Российской империи на колонии и "не колонии". Кавказ и Средняя Азия — колонии, 
		Западный край — нет. Возможно, потому, что цивилизационно он стоял выше метрополии. Так, парламент — сейм, государственный совет — в ВКЛ появился в XV 
		веке, а в империи только в XIX. Даже такая мелочь, как современный григорианский календарь: Минск на него перешел в 1582 
		(при <a href="../figures/batory.php">Стефане Батории</a>), а Москва — в 1918 году.
		</p>
		
		<h3>Паўстаньні і адукацыя</h3>
		<p>
		Самыми яркими событиями в истории края на протяжении всего XIX века были 
		<a href="../download/all_peoples_RI.php">"польские"</a> восстания 
		— <a href="../events/uprising_1794.php">1794</a>, 
		<a href="../events/uprising_1830.php">1830</a> и <a href="../events/uprising_1863.php">1863</a> годов.
		</p>
		<p>
		После подавления <a href="../events/uprising_1863.php">восстания Калиновского 1863 г.</a> интеграция Северо-Западного края в империю усилилась — к 1865 году <i>"весь учебно-педагогический состав 
		[ <small>учебных заведений</small> ] 
		духовно и нравственно преобразился и сделался исключительно русским и православным... единственно силою и властью М. Н. Муравьева"</i> <small> И. П. Корнилов, 1864</small> 
		</p>
		

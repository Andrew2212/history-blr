		<p>
		Филоматы (от греч. "стремящийся к знанию") — тайное патриотическое и просветительское объединение студентов Виленского университета, 
		действовавшее в 1817-23 годах, членами которого состояли впоследствии выдающиеся поэты, учёные, общественные деятели.
		</p>
		<p>
		Осенью 1817 г. по инициативе студентов университета — <a href="../figures/mickiewicz.php">Адама Мицкевича</a> из Новогрудка, 
		Томаша Зана из-под Молодечно и Яна Чечота из-под Кореличей
		— в <a href="../places/wilno.php">Вильно</a> было создано тайное Общество филоматов (“любителей наук”), одной из целей которого провозглашалась общественная работа 
		на благо угнетенной родины. Это были молодые люди 18-24 лет (1795-1802 г. р.). Самым юным участником общества был 
		<a href="../figures/domeyko.php">Игнаци Домейко</a> (1802 г.р.).
		</p>
		<p>
		 Первоначальными целями было самообразование и самоусовершенствование членов, связанных дружбой. 
		 Структура ориентировалась на научные и литературные общества. Деятельность состояла в чтении и критических разборах своих сочинений, 
		 дискуссиях о прочитанных книгах и т. п.
		</p>
		<p>
		В 1819 году программа общества приобрела отчетливую политическую и патриотическую окраску.
		</p>
		<p>
		Оно охватило своим влиянием виленское студенчество и гимназическую среду главным образом через свои дочерние организации, 
		филиалы — легальные и тайные кружки и общества: Союз друзей (1819), преобразованный в 1822 году в Союз филадельфистов (38 членов); 
		Кружок лучезарных (или "лучистых", "Promienistych", 1820), официально именуемым Обществом друзей полезного развлечения (168 членов); 
		общество филаретов (1820; 176 членов), Общество поэтов (1823; 17 членов)
		</p>
		<p>
		Филоматы заложили основы современной самоидентификации края — <a href="../terms/kraj_tut.php">тутэйшай краевасьцi</a>, которая станет основой 
		<a href="../download/blr_nationalists_1957.php">белорусского национализма</a> 1890-1930 годов.
		</p>
		<p>
		[ <small>
		Свои произведения они писали на польском — официальном языке обучения (Виленский университет, Полоцкая иезуитская Академия и пр) и языке горожан того времени 
		(аналог русскому сегодня). В то же время называли себя не поляками, а "литвинами", обращались в своих литературных 
		произведениях к образам "исторической Литвы" (ВКЛ), вводили в свои литературные произведения элементы "тутэйшага" языка ( "Dziady" Мицкевича).
		

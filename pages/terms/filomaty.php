		<h3>Филареты</h3>
		<p>
		Филареты (от греч. "любящий добродетель") — тайное патриотическое объединение студентов Виленского университета, действовавшее в 1820-23 годах. 
		Основана филоматами как одна из дочерних организаций. Имела целью взаимопомощь и самосовершенствование. Председателем общества филаретов был 
		Томаш Зан, один из основателей и руководителей общества филоматов. Одним из кружков филаретов руководил Ян Чечот.		
		</p>
		<p>
		Общество состояло из четырёх секций — физиков и математиков, юристов, литераторов, медиков. Секции делились на кружки, 
		носившие названия цветов радуги. Собрания секций проходили дважды в месяц.
		</p>
		<p>
		Новых членов принимали в общество только по рекомендациям его членов. Численность общества достигала 176 человек. 
		Деятельность общества была пресечена массовыми арестами студентов в 1823 году по делу филоматов.
		</p>
		
	

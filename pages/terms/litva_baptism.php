		<h3>*</h3>
		<p>
		* День пепла, пепельная среда (лат. Dies Cinerum) — день начала Великого поста в латинском обряде католической, англиканской и некоторых лютеранских церквей. Отмечается за 46 календарных дней (1,5 месяца) до праздника Пасхи. 
		 В православии соответствует "чистому понедельнику".
		</p>
		<p>
		На имше этого дня проводится обряд посыпания пеплом или наносится на лоб пеплом знак креста. 
		Пепельной среде предшествует "жирный вторник" — последний день периода карнавалов, 
		празднеств и развлечений перед Великим постом (Марди Гра).
		</p>
		
		
		<h2>Хронология событий</h2>
		<p>
		*** Первых католических монахов в Литву приглашал еще в. кн. <a href="../figures/gedimin.php">Гедимин</a> в своем письме 1323 года. Но языческие традиции в Литве 
		оставались сильны еще очень долго. 
		</p>
		<p>
		Языческое наследие, сохранившееся в консервативной сельской глубинке, отмечали этнографы Императорского Русского Географического Общества в 
		фундаментальных трудах  <a href="../download/russia_blr.php">"Россия — Полное географическое описание"</a> (1905) под редакцией 
		Семенова-Тяньшаньского и <a href="../download/kirkor.php">"Живописная Россия"</a> (1882) А. Киркора.
		</p>
		

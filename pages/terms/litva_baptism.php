		<p>
		Этот и последующие привилеи поддерживали принятие католичества жителями ВКЛ: 
		<i>— "Усялякі ж, хто, прыняўшы святую каталіцкую веру, ад яе праклятым чынам адступіцца, альбо адмовіцца яе прыняць, няхай не будзе ў поўнай меры цешыцца 
		вышэй апісанымі правамі."</i> 
		</p>
		<p>
		Через 50 лет, во второй <a href="../events/war_civil_1432.php">Гражданской войне 1432-38</a>, после смерти Витовта расколовшей страну во многом по конфессиональной принадлежности, линия противостояния 
		прошла по диагонали через всю Беларусь — от Браслава через Борисов до Мозыря 
		(<a href="../download/spis_swidrigailo.php">Спис городов Свидригайло</a> сохранился в архиве магистров Тевтонского ордена в Кёнигсберге). 
		Трокский привилей 1434 года уравнял в правах верующих римской и греческой церквей и во многом предопределил исход войны. 
		</p>
		<p>
		Самый старый (неперестроенный) костел Беларуси (1449 г.) заложен Немировичем в м. Ишколдь. Действует по сей день. И да, если кого-то тревожит "неруское" название Ишколдь, то стоит 
		напомнить — <i>вся</i> Беларусь лежит в ареале балтских топонимов, и <a href="../terms/slavs.php">славянство</a> — не генетика, а культурная парадигма, распространявшаяся вслед за централизацией государства.
		</p>
		

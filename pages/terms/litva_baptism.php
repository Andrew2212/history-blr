		<h1>Крещение Литвы</h1>
				
		<p><i>
		В основе статьи лежат работы Яна Длугоша (1415-1480), современника <a href="../figures/jagiello.php">Ягайло</a> (великий князь и король умер, когда Длугошу было 19 лет).
		</i></p>
		<h2>1387 год. День Пепла* <small>(20 февраля)</small></h2>
		<p>
		"По прибытии в Литву король Владислав (Ягайло) собирает в Вильно съезд в день Пепла [20 февраля]. Туда съехались по повелению короля 
		его братья, князья Скиргайло Трокский, <a href="../figures/vitovt.php">Витовт Гродненский</a>, Владимир Киевский, Корибут Новгородский, 
		и большое число рыцарей и простого люда; 
		</p>
		<p>
		и в течение многих дней Владислав, король польский, при содействии католических князей, прибывших с ним, прилагал много стараний к тому, 
		чтобы рыцари и простые люди, отвергнув ложных богов, которым они, обманутые тщетой языческих заблуждений, до тех пор поклонялись, 
		согласились почитать единого истинного бога и поклоняться ему и исповедовать христианскую веру."
		</p>
		<p>
		В этот день король и в.кн. Ягайло подписывает привилей <i>"што датычыцца пэўных вольнасцей, нададзеных жыхарам 
		<a href="../pages/terms/wkl.php">Вялікага княства Літоўскага</a> 
		па ўзору вольнасцей Каралеўства Польскага, дзе ўстанаўліваюцца і ўводзяцца суддзі ў рэгіёнах."</i>
		</p>
		<p> 
		В этом привилеи Ягайло дает ставшее классическим определение <a href="../terms/blr_gerb.php">Погони</a>:
		</p>
		<p><i>
		— "Калі будзе патрэба праследаваць якіх бы там ні было ворагаў, непрыяцеляў і самой зямлі нашай Літоўскай уцекачоў, то для праследавання такім спосабам, які традыцыйна завецца "пагоняй" (quod pogonia vulgo dicitur), 
		абавязаны выступаць не толькі людзі, хто носіць зброю, але і кожны мужчына, у якіх бы абставінах ці становішчы ён ні знаходзіўся, 
		толькі б мог насіць ваенную зброю."
		</i></p>
		

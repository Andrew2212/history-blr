		<p>
		1377 Умирает в. князь <a href="../figures/olgerd.php">Ольгерд</a>. По завещанию, в обход традиционного наследования, новым в. князем становится Ягайло. 
		<a href="../figures/kejstut.php">Кейстут</a>, брат Ольгерда, достаточно автономно правивший в Троках, отодвинут на второй план. 
		</p>
		<p>
		Ягайло было 30 лет и он искал пути модернизации страны, был готов к принятию христианства. 
		Кейстуту  было 80 лет и он был решительным противником крещения. 
		</p>
		<p>
		1381 Начинается <a href="../events/war_civil_1381-84_1389-92.php">Гражданская война в ВКЛ</a>, длившаяся с перерывами до 1392 года. Кейстут и Витовт против Ягайло. В ходе войны обе стороны 
		неоднократно обращаются за помощью к Ордену, предлагая в качестве платы за услуги <a href="../places/samogitia.php">Самогитию</a>.
		</p>
		<p>
		1385 Заключена <a href="../events/unia_krewo.php">Кревская уния</a> — соглашение о династическом союзе ВКЛ и Польши через брак в. кн. Ягайло с королевой Ядвигой. 
		Ягайло, оставаясь в. князем Литовским, становится королем Польши, управляя обоими государствами из Кракова. Согласно условиям союза, Ягайло обязуется обратить Литву в католичество. 
		</p>
		<p>
		1387 Ягайло возвращается в Вильню и проводит "крещение Литвы", 
		заложив первые фарные костелы в Вилькомире, Мейшаголе, Медниках, Неменчине, Крево, Гайне, Обольцах и Лиде(?). Чуть позже были заложены костелы в Быстрице (1390),
		Ошмянах (1391), <a href="../places/fara_grodno.php">Фара Витовта</a> в Гродно (1389) и костел св. Роха на золотой Горке в Минске (1390-е).
		</p>
		

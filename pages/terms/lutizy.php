		<p>
		Такие топонимы, как Волковыск, Вильча, Вильчицы, <a href="../places/wilno.php">Вильно</a>, <a href="../figures/kesgajla.php">Вилькомир</a>,
		<a href="../places/vowczyn.php">Волчин</a>, Литва, Люта, Лютца, Лютовичи в Виленской, Гродненской, Минской, Витебской 
		областях не выглядят случайными. 
		</p>
		
		<p>Имеются археологические и антропологические свидетельства того, что даже в Новгородской земле 
		расселилось несколько волн мигрантов с юго-западного балтийского побережья. 
		В частности, отмечаются сходства конструкции некоторых типов построек, а также параметров черепов из захоронений 
		у полабских славян и новгородских словен: 
		</br><i>"В противовес летописной версии некоторые лингвисты и археологи придерживаются гипотезы о происхождении словен из Польского Поморья."</i>
		</br><small>Археологический словарь. - М.: Прогресс. Уорвик Брей, Дэвид Трамп. Перевод с английского Г.А.Николаев. 1990.</small>

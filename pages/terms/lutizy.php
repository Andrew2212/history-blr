		<h3><a target="_blank" href="../../images/img-terms/lutizy/disappearance_of_Polabian_Slavs.pdf">"Венды в научной мысли"</a>
		</br><small>славяно-германский этнокультурный синтез в междуречье Эльбы и Одера</small>
		</h3>
		
		<p>
		<i>"Относительно исторической судьбы славянских племен за Эльбой в справочниках можно выделить две противоречивые точки зрения. 
		Согласно одной из них, все славянские племена, за исключением сохранившихся 
		серболужицких в Лаузитцу, полностью исчезли с этнолингвистической карты 
		Европы к началу ХVIII в. 
		</p>
		<p>
		О причинах исчезновения основной массы полабских славян авторы словарных статей не сообщают, указывая только на изгнание вендов или их бегство. 
		</p>
		<p>
		Другая точка зрения признает сохранение в местности, до сих пор называющейся Вендланд, остатков полабских славян древян, 
		которые еще во время написания лексиконов сохраняли свой язык, многие древние суеверия и языческие верования."
		</i>
		</br><small>Иванова-Бучатская Ю.В. 
		<a target="_blank" href="../../images/img-terms/lutizy/disappearance_of_Polabian_Slavs.pdf">Plattes Land: символы Северной Германии</a>
		 (славяно-германский этнокультурный синтез в междуречье Эльбы и Одера). СПб., 2006.</small>
		</p>
		<p>
		Достаточно много фактов (в археологии, топонимике, летописях) подтверждают миграцию части полабских славян в XI-XII вв. на восток 
		— на земли исторической <a href="../places/litwa.php">Литвы</a> (Lithuania Propria), Новгорода, Латвии.
		</p>
		<p>
		 <i>"В округах Виленском и Троцком до первой половины 13 века жили Вилчи и Велеты, потомки Невров и другие славяне... 
		 Вельты: так назывался сильный и в истории Средних веков больше прочих славян прославившийся народ, Велеты или Лютичи, прозванные Волками, в первый раз упоминаются 
		 Александрийским географом....
		 Их жилища....в губернии Виленской. Я их признаю предками последующих Кривичей". 
		 Полабские славяне [к ним относились и Велеты - Вильцы] так же мигрировали из тех земель, которые у Одера, за Вислу, на Западную Двину и Березену. 
		 Густо расселились через Одер до Эльбы, постепенно и в разное время."</i>
		 </br><small>П. Й. Шафарик, хранитель и директор библиотеки Пражского университета, XIX в.</small>
  		</p>
		<p>
		 <i>"....волками Невры (готский народ) называли Вильтов (Литвинов) или Вильцев... Живущие в верховьях Двины Кривичи, построили там Witepsk и Полоцк и в южной оконечности озера Псков или Oleskau и Изборск. 
		 Широким потоком первой иммиграци заполнили пространство между Двиной и Неманом, сохранив имя Литовцев...."</i>
		 </br><small>"Журнал этнологии" (нем.), Т. 1 (19 век)</small>
		 </p>
		<p>
		Большинство историков и хронистов разных времён и народов до 20 века были единодушны в теории происхождения <a href="../places/litwa.php">Литвы</a> и 
		<a href="litwiny.php">Литвинов</a> от народа 
		Лютичей (лютов, литвы) — Велетов-Вильцев. На ранних картах иногда название Литвы  пишется как LUTUANIA. 
		</p>
		<p> 		
		Имя Лютувера (Lutuver) — легендарного отца князей <a href="../figures/vyten.php">Витеня</a>, Воина и 
		<a href="../figures/gedimin.php">Гедимина</a> — многие исследователи так же выводили от 
		<a href="../terms/lutizy.php">лютичей</a>-велетов (lutizi-veltae-wilzians).
		<p>
		

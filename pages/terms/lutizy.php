		<p>

		<p>
		Лютичи — общее название для полабского союза племен, живших между Одером, Балтийским морем и Эльбой, в ареале лужицкой культуры.
		Самоназванием этого племенного союза было вильцы (нем. Wilzen, Wilsen, Wilciken, Wilkinen) или велеты.
		</br>В немецких источниках часто упоминаются как венеды или венды. К X веку велеты становятся известны как лютичи.
		</p>
		<p>
		Первые группы славян появляются в междуречье Заале и Одера (в почти полностью опустевшем в результате миграций германских племён в ходе 
		великого переселения народов) с VI века, продвигаясь на запад из первоначальной области своего обитания между Одером 
		и Днепром к северу от Карпатских гор.
		</p>
		<p>
		В конце VIII века Карл I Великий, король франков и император запада (Empereur d’Occident) предпринял поход через Эльбу к Одеру.
		 Начинаются вытеснение лютичей-велетов с Полабских земель и их миграция на восток.
		 Приблизительно к XII—XIII векам, все славянские земли Полабья вливаются в те или иные государственные немецкие образования 
		 в составе Священной Римской империи. 
		</p>
		<p>
		<i>"К 12 веку может быть отнесена полная германизация Лютичей-славян: политическая независимость навсегда потеряна, славянские обычаи забыты."</i>
		</br><small>Энциклопедический словарь <a target="_blank" href="http://bibliotekar.ru/bel2/316.htm">Брокгауза и Ефрона</a></small>
		</p>
		<p>
		В виде современного этноса сохранились  лужичане (сорбы) — народ на востоке Германии, единственный среди полабских славян, 
		сохранивший до настоящего времени родные язык и культуру.
		</p>
		<p>
		Культурно-генетическое наследие полабских славян на белорусских землях отмечали многочисленные исследователи 
		— от историка литературы профессора Веселовского в работе 
		"<a target="_blank" href="../../images/img-terms/polotsk_principality/Veselovskij_Rus_in_Wilkinasaga.djvu">Русские и вильтины в саге о Тидреке Бернском</a> (Wilkinasaga)" 1906, 
		до генетиков и этнографов О. Балановского и О. Тегако в труде <a href="../articles/dnk.php">"Генофонд белоруссов"</a> 2014.
		</p>
		
		

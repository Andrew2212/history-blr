		<h3>Иезуиты в ВКЛ</h3>
		<p>
		В <a href="wkl.php">ВКЛ</a> иезуиты появились при <a href="../figures/batory.php">Стефане Батории</a>.
		</br>В 1569 г. виленский епископ Валериан Протасевич пригласил в ВКЛ 13 представителей Ордена.
		 И уже через год их количество увеличилось до 300 человек. Среди них были миссионеры, богословы, проповедники, писатели.
		</p>		
		<p>
		Иезуиты создали самую густую сеть учебных учреждений на территории ВКЛ-Беларуси. Их опорные пункты состояли из коллегий, резиденций и миссий. 
		При этом коллегии и резиденции размещались только в городах, миссии же были в основном расположены в местечках и деревнях. 
		Каждой коллегии подчинялось несколько миссионерских пунктов. На всей территории <a href="rzeczpospolita.php">Речи Посполитой</a> иезуиты в течении XVI-XVIII вв. 
		создали 153 опорных пункта, свыше половины из них (79) находилось в Беларуси.
		</p>						
		<p>
		После себя иезуиты оставили в Беларуси не только иной взгляд на образование, но и богатое архитектурное наследие — здания костелов, коллегий, монастырей. 
		</p>
		

		<h3>1534 Создание Ордена иезуитов</h3>
		<p>
		Иезуиты, или Орден Иезуитов — мужской монашеский орден Римско-католической церкви, основанный в 1534 году Игнатием Лойолой и 
		утверждённый Павлом III в 1540 году. Официальное название — Общество Иисуса (лат. Societas Jesu), 
		также Орден св. Игнатия. 
		</p>
		<p>
		Орден создан в ходе Контрреформации (католическое движение против реформаторов церкви), 
		и его члены, наряду с тремя традиционными обетами (бедности, послушания и целомудрия), дают и четвёртый — послушания папе римскому "в вопросах миссий".
		</p>
		<p>[
		Реформация — религиозное и общественно-политическое движение в Западной и Центральной Европе XVI-XVII вв, 
		направленное на реформирование католического христианства в соответствии с Библией.
		]
		</p>
		<p>
		Иезуиты принадлежат к числу регулярных клириков — монашеского ордена, члены которого занимаются пастырской деятельностью, 
		образованием и делами милосердия. Они не находятся в строгом затворе, и ориентированы на общественную деятельность (образование, наука, миссионерство). 
		 Девизом ордена является фраза "Ad majorem Dei gloriam" ("К вящей славе Божией").
		</p>
		<p>
		Иезуитом является нынешний Папа Франциск (Хорхе Марио Бергольо) — 266-й Папа римский (первый Папа-иезуит).
		</p>

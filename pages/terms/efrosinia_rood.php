		<p>
			Прошло почти 200 лет с прибытия в Полоцк крестителя-Торвальда, но языческие традиции были еще сильны. Дед Ефросинии, Всеслав, 
			"был рожден волхованием" и почитался, как колдун-оборотень. Не был причислен к лиу святых несмотря даже на строительство Софийского собора.

		<p>
			Восьмиконечный крест в России до 20 века был, как свастика до Гитлера — ординарным символом, иногда употреблявшимся. Но не удостоившийся чести быть ни на 
			Исакиевском соборе, ни на Василия Блаженного, ни на шапке Мономаха, ни на императорской державе, ни на вершине "1000-летия России".
		</p>
		<p>	
			И только в новой России с приходом термина "Русский Мир" он обрел "сакральный смысл". Как свастика с приходом Гитлера. 
		</p>
		<p>	
			И сегодня, наряду с "золотыми цыбулинами" служит Русскому Миру в качестве метки "своих территорий". 
			Даже на эмблеме черносотенной организации "Союз Русского Народа" (основана в 1905, воссоздана в 2005) 
			он заменил более традиционный шестионечный крест.
			Его же используют в своей символике всяческие "казаки" и тп. 
		</p>
		<p>
			За последние годы количество золотых цыбулин (даже на шпилях виленского барокко) и "русских крестов" в Беларуси увеличилось в десятки раз.
			И это при том, что сам <a href="../articles/ortodox.php">термин "православие"</a> появился у нас только в XIX веке, после 
			<a href="../events/razbor_rechi.php">Разделов Речи Посполитой</a>. 
		</p>

		

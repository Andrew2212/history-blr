		</p>
		<p>
			Напрестольный крест, созданный в 1161 по ее заказу стал первой значимой христианской реликвией Края. Хранился в Полоцке, в результате войн XIII в. попал в Москву.
			Войска <a hrf="../figures/batory.php">Стефана Батория</a>, освобождавшие Полоцк в 1579, отбили у Ивана Грозного и город и крест, привезенный туда во время оккупации. 
			Утрачен во время эвакуации в 1941 году.
		</p>
		

		<p>
		Все апелляции литовской знати к "легенде о Палемоне", придуманной в XVI веке (римском происхождении и сходстве с латынью окончаний на калбе -ас и -ис),
		были вызваны стремлением к древнему происхождению и тягой к этно-экзотике. Так российское дворянство и царская семья носили горские папахи и бурки 
		("Кавказ - тоже часть России, это тоже наше")
		</p>
				
		<p>
		После <a href="../events/uprising_1830.php">1830 года</a> в изданиях Императорского Русского Географического общества и императорских указах термин "литвины" 
		активно заменяется словом "белорусы", во имя <a target="_blank" href="../../images/img-terms/slavs/brothers.jpg">мифа</a> о происходении <a href="../terms/blr_etnos.php">белорусского этноса</a> от триединого русского народа.
		</p>
		<p>
		<i>"Естественно, переродиться я никогда не сумею и надеюсь на Бога, что я — или в Кордильерах, или в Панарах (Вильня) — все равно умру литвином..."</i>, 
		— <a href="../figures/domeyko.php">Игнаци Домейко</a>.
		</p>

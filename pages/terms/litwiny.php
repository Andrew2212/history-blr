		<h2>Литвины</h2>
		<p>
		<i>Эндоэтноним — имя народа, которое он присваивает себе сам</i>
		</br><small>Wikipedia.ru</small>
		</br><i>Родился я литвином... и немного лет жизни мне остается, а сумерки еще покрывают будущую судьбу моей родины и многочисленных ее окраин.</i>
		</br><small><a href="../figures/kostiushko.php">Тадэуш Костюшко</a> </small>
		</p>
		<p>
		  Литвины — исторический эндоэтноним уроженцев <a href="../download/blr_people.php">белорусских земель</a>. Наряду с терминами 
		  <a href="../download/zemie_ruskie.php">русин</a> и <a href="../places/samogitia.php">жмудин</a> использовался в полном названии 
		  <a href="wkl.php">ВКЛ</a> — Великое княжество Литовское, Русское, Жемайтийское и иных — определяя титульную нацию и подчеркивая его мультикультурность. 
		</br><small>[Когда сегодня утверджают, что "литвины" — политоним, то "забывают", что в этом качестве термин использовали только 
		иностранцы: "...прибыл литвин, латыш N-ского уезда...". Аналогично, ныне для немцев и тувинец, и адыгеец — "русские" 
		(по титульной нации государства).]</small>
		</p>
		
		<p>
		<i>Так літвіны смела з крыкамі нясуцца
		</br>А пад імі коні бакамі б’юцца
		</br>Смела з літвай Вітаўт рэй вядзе-трымае
		</br>І натхняе крыкам, шэрагі раўняе</i>
		</br><small>Мацей Стрыйкоўскi, "Хроніка польская, літоўская, жамойцкая і ўсяе Русі", 1582 г. </small>
		</br>
		<a href="../download/spis_voiska.php">Перепись войска Литвского</a> 1528 года называет тех <i>литвинов </i> поименно — в ней, за редким исключением, 
		сплошь <a href="../articles/blr_family_names.php">белорусские фамилии</a>.
		</p>
		
		<p>
		«Kto jest litwin? — Ten, ktory kocha wolnosc i szanuje Statut litewski». <a href="statut.php">Статут</a> ВКЛ действовал только на белорусских и украинских землях 
		(для жмудинских земель был отдельный "<a target="_blank" href="../../images/img-places/samogitia/zmud_law.jpg">устав жмудский</a>").
		</p>
		
		<p>
		Литвинами называли себя <i>только</i> "беларускіх зямель ураджэнцы" — <a href="../figures/kostiushko.php">Тадэуш Костюшко</a>, 
		<a href="../figures/mickiewicz.php">Адам Мицкевич</a>, <a href="../figures/domeyko.php">Игнаци Домейко</a>, <a href="../figures/kalinowski.php">Кастусь Калиновский</a>,
		 <a href="../figures/simienowicz.php">Казимир Семенович</a>, <a href="../figures/budny.php">Сымон Будный</a>.		 
		</br>Акты ректорского суда Краковского университета называют «Литвинами» (Lithuanus, лат.) всех студентов с земель ВКЛ, 
		но без <a href="../places/samogitia.php">Жемайтии</a> и <a href="../download/zemie_ruskie.php">Руси</a> (за 1469-1536 годы проходит только один Жемайт и он не записан как Lithuanus).
		</br><small>[Кстати, слова "<a href="../places/litwa.php">Литва</a>" у жмудинов нет ни в языке, ни в <a href="../places/_places_intro.php">топонимах</a>] </small> 
		</p>
		
		<p>
		 В "<a href="../download/chronicles_peter.php">
		Chronicon terrae Prussiae von Peter von Dusburg</a>" от 1326 г. (хроника Тевтонского Ордена) можно видеть 
		<small>[см. <a target="_blank" href="http://www.vostlit.info/Texts/rus6/Dusburg/frametext33.htm">перевод</a>]</small>,
		что литвинами (Littouwin) называли <a href="../figures/gedimin.php">Гедымина</a>, его наместника 
		<a href="../figures/david.php">Давида Городенского</a> и воинов Гродно (Гарты). Эта же хроника отчетливо противопоставляет литвинов и 
		<a href="../places/samogitia.php">жмудинов</a> (Samaitin).
		</p>
		

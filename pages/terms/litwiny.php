		<p>
		  Литвины — исторический эндоэтноним уроженцев <a href="../download/blr_people.php">белорусских земель</a>. Наряду с терминами 
		  <a href="../download/zemie_ruskie.php">русин</a> и <a href="../places/samogitia.php">жмудин</a> использовался в полном названии 
		  <a href="wkl.php">ВКЛ</a> — Великое княжество Литовское, Русское, Жемайтийское и иных — определяя титульную нацию и подчеркивая его мультикультурность. 
		</p>
		<p>
		Эндоэтноним — имя народа, которое он присваивает себе сам.
		<small>[Когда сегодня утверджают, что "литвины" — политоним, то "забывают", что в этом качестве термин использовали только 
		иностранцы: "...прибыл литвин, латыш N-ского уезда...". Аналогично, ныне для немцев и тувинец, и адыгеец — "русские" 
		(по титульной нации государства).]</small>
		</p>
		
		<p>
		«Kto jest litwin? — Ten, ktory kocha wolnosc i szanuje Statut litewski». <a href="statut.php">Статут</a> ВКЛ действовал 
		только на белорусских и украинских землях — Литве и Руси. Для Самогитии был отдельный Устав Жмудский. В земском Привилеи для Самогитии 
		говорилось: <i>"Запрещается упрекать жмудинов, что присоединены силой, ибо добровольно они подчинилиь нам"</i>.
		</p>
		
		<p>
		Литвинами называли себя <i>только</i> "беларускіх зямель ураджэнцы" — <a href="../figures/kostiushko.php">Тадэуш Костюшко</a>, 
		<a href="../figures/mickiewicz.php">Адам Мицкевич</a>, <a href="../figures/domeyko.php">Игнаци Домейко</a>, <a href="../figures/kalinowski.php">Кастусь Калиновский</a>,
		 <a href="../figures/simienowicz.php">Казимир Семенович</a>, <a href="../figures/budny.php">Сымон Будный</a>.		 
		</br><small>[Кстати, слова "<a href="../places/litwa.php">Литва</a>"  у lietuviai нет ни в языке, ни в <a href="../places/_places_intro.php">топонимах</a>] </small> 
		</p>
		
		<p>
		 В "<a href="../download/chronicles_peter.php">
		Chronicon terrae Prussiae von Peter von Dusburg</a>" от 1326 г. (хроника Тевтонского Ордена) можно видеть,
		что литвинами (Littouwin) называли <a href="../figures/gedimin.php">Гедымина</a>, его наместника 
		<a href="../figures/david.php">Давида Городенского</a> и воинов Гродно (Гарты). Эта же хроника отчетливо противопоставляет литвинов и 
		<a href="../places/samogitia.php">жмудинов</a> (Samaitin).
		</p>
		

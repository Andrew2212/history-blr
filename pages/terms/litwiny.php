		<p>
		<br>Согласно <a href="statut.php">Статуту ВКЛ</a> 1566 г. (арт. 9, раздел 3)
		<br><i>
		"... Но на благородность [шляхетство-дворянство] и всякую должность духовную и светскую не может быть избран, 
		даже от Нас Государя ставленник, только издавна предков своих уроженец Великого Княжества Литовского Литвин и Русин ..."
		</i>
		<br>В списках Великих <a href="../terms/weliki_hetman.php">гетманов</a>, <a href="../terms/weliki_kancler.php">канцлеров</a> 
		и <a href="../terms/weliki_marshalok.php">маршалков</a> Литовских — только <a href="../terms/slavs.php">славянские</a> имена.
		</p>
		<p>
		Герберштейн в своем труде <a target="_blank" href="../http://www.vostlit.info/Texts/rus8/Gerberstein/frametext1.htm">"Записки о Московии"</a>
		 (1517—1526 гг.) писал: «Государь назначает туда [в Жемайтию] начальника [из Литвы], которого на своем языке они [литвины] называют „староста“ (Starosta)».
		</p>
		<p>
		В России к XVIII веку Белоруссией называлась Смоленская губерния, и в архивных документах уточнялась религиозная принадлежность литвинов: 
		"беларуской" (<a href="../events/unia_religion_brest.php">униаты</a>) или католической веры (православных в ВКЛ XVIII века было меньше 6%).
		</p>
		

	<h2>Язычество</h2>
		<p class="img-float-left">
		<a href="../../images/img-terms/religion_blr/blr_pogan.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img src="../../images/img-terms/religion_blr/blr_pogan_thumb.jpg" height="120">
		</br><small>Языческое наследие</small> 	
		</a>
		</p>
		<p class="img-float-right">
		<a href="../../images/img-terms/religion_blr/1460_rerumPrussicarum.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img src="../../images/img-terms/religion_blr/1460_rerumPrussicarum_thumb.jpg" height="120">
		</br><small>1460 rerum Prussicarum</small> 	
		</a>
		</p>
		<p>
		До X века на <a href="../download/blr_people.php">белорусских землях</a> единственной религией было язычество. 
		</br><i>"Гэта не зусім так. Асобныя супольнасці хрысціян і цэрквы ўжо існавалі." </i> — поправил редакцию представитель Рыма-каталіцкага Касцёла ў Беларусі.
		</p>
		<p>
		Так сложилось, что о языческом периоде больше известно из летописных христианских источников 
		и этнографических экспедиций Русского Императорского Географического общества.
		</p>
		<p>
		Языческое наследие, сохранившееся в консервативной сельской глубинке, отмечали этнографы Императорского Русского Географического Общества в 
		фундаментальных трудах  <a href="../download/russia_blr.php">"Россия — Полное географическое описание"</a> (1905) под редакцией 
		Семенова-Тяньшаньского и <a href="../download/kirkor.php">"Живописная Россия"</a> (1882) А. Киркора.
		</p>		
		<p>
		В конце X века из Византии приходит новая религия — христианство. В первой половине XI века были построены Софийские соборы — в Киеве, Полоцке и Новгороде. 
		Киевские и новгородские правители того периода стали канонизированными 
		святыми — св. кн. Анна Новгородская, киевские св. кн. Ольга, св. кн. Владимир "креститель Руси" и  его сын Ярослав Мудрый, родной брат Изяслава. 
		</p>
		<p>
		Однако два <a href="../terms/polotsk_principality.php">Полоцких князя</a>, правившие весь XI век запомнились иначе — 
		<a href="../terms/polotsk_principality.php">Брячеслав</a> "обращался к волхвам и сын его рожден от волхования", а 
		<a href="../figures/charadzej.php">Всеслава</a> летописи описывали как оборотня-воўкалака и потомки дали ему имя колдун-Чарадзей.
		</p>		
		<p>
		Массовое "крещение" языческих капищь в <a href="../terms/polotsk_principality.php">Полоцком княжестве</a> — так большинство иследователей 
		определяет назначение <a href="../terms/dwina_stouns.php">Двинских камней</a> — случилось только в XII веке и имело локальный характер.
		</p>		
		<p>
		На западных белорусских землях христианство также долго соседствовало с язычеством. 
		</br>Одна из первых церквей в Гродно — Каложская церковь — 
		была построена на рубеже XII-XIII вв. <a href="yotving.php">Ятвяги</a> оставались язычниками почти до самой своей ассимиляции в XIV-XVI вв. 
		И, хотя попытки христианизации предпринимались и во времена Гродненского княжества и, позже, во времена  
		<a href="../figures/mindowg.php">Миндовга</a>, 
		язычество было популярно до 1387 года — до "крещения Литвы и Жмуди" <a href="../figures/vitovt.php">Витовтом</a> и <a href="../figures/jagiello.php">Ягайло</a>. 
		</p>
		<p>
		Так, хроника "<a href="../download/chronicles_peter.php">Chronicon terrae Prussiae von Peter von Duisburg</a>", 1326 г.,
		рассказывает о "презрении Бога" князем <a href="../figures/vyten.php">Витенем</a> и разорении им церквей в Польше.
		Эта же хроника называет <a href="../figures/gedimin.php">Гедымина</a> "нечестивым врагом веры и христиан" <small>[Dusb, III, 357]</small> и отмечает, что 
		<a href="../figures/david.php">Давид Городенский</a>, военачальник
		и наместник его, со своим гродненским войском "святыни, церкви, облачение и алтарные сосуды и все, относящееся к богослужению, он бесчеловечно осквернял 
		и расхищал" <small>[Dusb, III, 343]</small>. И убийца Давида пришел к нему, "притворяясь другом язычников" <small>[Dusb, III, 361]</small>
		</p>
		<p>[ <small>
		Что характерно — при мирном присоединении Витенем Полоцка к ВКЛ соборы оставались нетронутыми. Полоцкое княжество вошло в ВКЛ на правах автономии, и Софийский собор, с его библиотекой и архивами,
		 оставался в сохранности до вторжения московских войск.
		</small> ]</p>
		
		

		<p>
		До X века на <a href="../download/blr_people.php">белорусских землях</a> единственной религией было язычество.
		</br>В 986 году <a href="polotsk_principality.php">Полоцкое княжество</a> крестил исландский викинг 
		<a href="../figures/torwald.php">Торвальд Кодранссон</a>.
		В 985 году в Константинополе он получает от Императора Василия II грамоту 
		«полномочного представителя Византии в русских городах Восточной Балтики». 
		 В 986 году Торвальд приезжает в Полоцк и строит здесь церковь и монастырь святого Иоанна Крестителя.
		</p>
		<p>
		<small>[Кстати, "крещение Беларуси" Торвальдсоном, так же как "крещение остальной Руси" Владимиром случилось еще до раскола 
		христианской церкви на католиков (латинян) и православных (ортодоксов) в 1054 году.]
		
		</small>
		</p>
		<p>
		Вероятно, христианство не было тут новой религией — в отличие от Новгорода, Киева, Ростова, где бунты волхвов продолжались до середины XI века.
		</br>В 1030-1050 годах строятся Софийские соборы в Полоцке, Киеве, Новгороде, как напоминание о Софийском Соборе Константинополя.
		</p>
		

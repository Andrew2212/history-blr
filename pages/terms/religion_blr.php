		<h4>Реформация. Протестанты. Лютеране и кальвинисты.</h4>
		<p>
		Происхождение протестантизма связано с Реформацией — широким антикатолическим движением XVI века в Европе.
		</p>
		<p>
		Лютеранство — одно из наиболее старых протестантских течений. С ним связано само возникновение понятия протестантизм, 
		поскольку именно лютеран стали называть протестантами после их протеста в Шпайере.
		</p>
		<p>
		Кальвинизм также порожден Реформацией. 
		Его приверженцы считали Библию единственным источником веры и проповедовали изначальную предначертанность человеческой судьбы. 
		 Но человек должен верить, что Христос спас именно его, и доказывать это своим трудолюбием. 
		</p>
		<p>
		Кальвинисты в ВКЛ образовали шесть округов, состоявших из общин — "гельветских (швейцарских) сборов". В XVI в. в княжестве насчитывалось 158 общин. 
		Первая кальвинистская община появилась в Бресте (1553). 
		</p>
		<p>
		В 1565 году кальвинисты Виленским привилеем были уравнены в правах с католиками и православными. 
		Кальвинизм становился доминирующей религией в княжестве (В Новогрудском воеводстве проживало более 600 шляхетских фамилий — 
		в православии остались только 16)
		</p>
		<p>
		Лидером кальвинизма стал Николай <a href="../figures/radziwilly.php">Радзивилл</a> Черный. 
		Он, опасаясь польского влияния с одной стороны и православного московского — с другой, прилагал все усилия к распространению третьей религии, способной объединить шляхту ВКЛ.
		</p>
		

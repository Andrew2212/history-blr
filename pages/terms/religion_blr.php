		<p>
		В 1389 году <a href="../figures/vitovt.php">Витовтом</a> был издан привилей евреям Гродно, который освобождал от налогов синагогу и кладбище, а также регулировал торговые отношения в городе. 
		С тех пор иудаизм стал неизменной составляющей религиозной картины белорусских земель. Традиция не прерывалась веками, и в XX веке большинство лидеров Израиля были эмигрантами из Беларуси.
		</p>
		<p>
		В 1395 г. хан Тохтамыш потерпел поражжение от Тимура и был принят Витовтом и поселен, вместе с его придворными, в г. Лида. 
		Вслед за ним пришли татары с семьями и собственностью — в страну, 
		где им обещали землю и свободное исповедование мусульманской веры. 
		Татары, которые имели статус <a href="shliahta.php">шляхты</a>, сохранили его и в ВКЛ. На протяжении целого столетия после переселения, 
		они имели возможность получить документы от крымских ханов о своих предках. Так в религиозную картину вплетался ислам.
		</p>		
		
		<p>
		В XV веке межконфессиональное противостояние подпитывало огонь <a href="../events/war_civil_1432.php">Гражданской войны</a> в ВКЛ 1432—1438 годов.
		</p>
		
		<p>
		В 1563 году (после освобождения Полоцка от войск <a href="../terms/ross_empire.php">Московии</a>, уничтоживших библиотеку 
		<a href="../places/sophia_sobor.php">Софийского Собора</a>) <a href="../figures/batory.php">Стефан Баторий</a> призвал в страну иезуитов — "армию папы". 
		Орден оказал большое влияние на образование и просвещение в ВКЛ: Виленская Академия (остававшаяся центром <a href="filomaty.php">национального духа</a> до XIX века), Полоцкий коллегиум (а позже — Академия, был престижным европейским учебным завелдением, 
		его выпускник — <a href="../figures/ryllo.php">Максимилиан Рылло</a> — был первым иследователем остатков древнего Вавилона), сеть школ и коллегиумов по всей стране.
		 Наследие иезуитов — первая аптека (<a href="../places/grodno.php">Гродно</a>), первые башенные часы, типографии (большинство книг XVII века в ВКЛ были напечатаны иезуитами), 
		  принесли барокко в восточную европу (первое барочное здание — Несвижский фарный костел).		
		</p>
		
		<p class="img-float-center">
		<a target="_blank" href="../../images/img-terms/religion_blr/kirkor_unia.jpg">

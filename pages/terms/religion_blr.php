		<h2>Ислам</h2>
		<p>
		В 1395 г. хан Тохтамыш потерпел поражжение от Тимура и был принят Витовтом и поселен, вместе с его придворными, в г. Лида. 
		Вслед за ним пришли татары с семьями и собственностью — в страну, 

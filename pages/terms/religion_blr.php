		<p>
		Значимой фигурой в униатстве был архиепископ Полоцкий <a href="../figures/kunciewicz.php">Иосфат Кунцевич</a>. Он выступал как решительный сторонник единства Западной и Восточной церквей. 
		В 1643 г. папа Урбан VIII признал Кунцевича блаженным. Пий IX в 1867 г. причислил его к святым, провозгласив его патроном для Руси и Польши. 
		В энциклике Ecclesiam Dei Папа римский Пий XI назвал Кунцевича «апостолом единения».
		</p>
		

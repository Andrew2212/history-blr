	<h4>Православие</h4>
	
	<p>
	Термин <a href="../articles/ortodox.php">"православная вера"</a>, неизвестный до конца XIV века, стал вытеснять термин "ортодоксия" или "истинная вера" в документах Московского государства при Иване Грозном.
	К XVII веку понятие "истинная вера" было полностью заменено на "православие" или "русская вера". Униатство в московских документах того периода именовали "белоруская вера".
	</p>
	
	<h4>Старообрядцы</h4>
		<p>
		После церковного раскола в РПЦ в середине XVII века часть староверов переселилась в ВКЛ на белорусские земли.
		 Одним из центров старообрядчества была Ветка. В 1791 году старообрядцы составляли 4% населения всего ВКЛ.
		</p>
	
	<h2>Христианство латинского (римского) обряда
	</br><small>католицизм</small>
	</h2>	
		<p>
		1323 Стремясь к европейскому признанию и ища поддержки в борьбе с Тевтонским Орденом, в. кн. <a href="../figures/gedimin.php">Гедымин</a> пишет письма 
		<small>(<a target="_blank" href="https://archive.org/stream/codexdiplomatic00lithgoog#page/n48/mode/2up">оригинал</a> / 
		<a target="_blank" href="http://www.vostlit.info/Texts/Dokumenty/Litva/XIV/Gedimin/index.phtml?id=2241">перевод</a>)</small> 
		папе римскому Иоанну XXII, властям ганзейских городов, немецким францисканцам и доминиканцам. 
		В письмах Гедимин заявлял о своем желании принять христианскую веру и приглашает рыцарей, лекарей, ремесленников, купцов, земледельцев, а также священников, и сообщает,
		что уже выстроены две церкви — в <a href="../places/wilno.php">Вильне</a> и <a href="../places/nowogrudok.php">Новогрудке</a>.
		</p>
		<p>
		1385 Подписана <a href="../events/unia_krewo.php">Кревская уния</a>(династическая уния с королевством Польша). 
		В. князь <a href="../figures/jagiello.php">Ягайло</a> был крещен по католическому  обряду и становится королем Польши. 
		</p>
		<p>
		1387 Ягайло и <a href="../figures/vitovt.php">Витовт</a> проводят совместное "крещение Литвы и Жмуди".
		Папа Урбан VI  в 1389 году признает статус <a href="../terms/wkl.php">Великого княжества Литовского</a> как католического государства.
		</p>

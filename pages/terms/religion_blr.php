		<p>
		Что характерно — присоединение Полоцка к ВКЛ происходило иначе. Полоцкое княжество вошло в ВКЛ на правах автономии, и Софийский собор, с его библиотекой и архивами,
		 оставался нетронутым до вторжения московских войск.
		</p>
		
		<p>
		Стремясь к европейскому признанию и ища поддержки в борьбе с Тевтонским Орденом, Гедымин пишет письма 
		<small>(<a target="_blank" href="https://archive.org/stream/codexdiplomatic00lithgoog#page/n48/mode/2up">оригинал</a> / 
		<a target="_blank" href="http://www.vostlit.info/Texts/Dokumenty/Litva/XIV/Gedimin/index.phtml?id=2241">перевод</a>)</small> 
		папе римскому Иоанну XXII, властям ганзейских городов, немецким францисканцам и доминиканцам. 
		В письмах Гедимин заявлял о своем желании принять христианскую веру и приглашает рыцарей, лекарей, ремесленников, купцов, земледельцев, а также священников, и сообщает,
		что уже выстроены две церкви — в <a href="../places/wilno.php">Вильне</a> и <a href="../places/nowogrudok.php">Новогрудке</a>.
		</p>
		
		<p>
		Таким образом на белорусские земли пришли две христианские конфессии, что в дальнейшем породит много конфликтов 
		и попыток влияния соседних стран через церковное противостояние. 
		</p>
		
		<p class="img-float-center">
		<a target="_blank" href="../../images/img-terms/religion_blr/blr_catholic.jpg">

		</p>
		Базилиане — на основе монастырей, принявших Брестскую унию, был основан Орден Пресвятейшей Троицы (Ordo Sancti Basilii Magni). 
		Большой упор в деятельности ордена был сделан на воспитание молодёжи, на этом поприще базилиане соревновались с иезуитами.
		Братом Базилианского Ордена был <a href="../figures/simeon.php">Симеон Полоцкий</a> ("hieromonachi Polocensis Ordinis Sancti Basilii Magni")
		<p>
		
		</p>
		
		<p>
		В XVI веке, после <a href="../events/unia_liublin.php">Люблинской унии</a> 1569 года, с 
		образованием <a href="rzeczpospolita.php">Речи Посполитой Двух Народов</a> и усилением влияния католичества, 
		чтобы уравнять в правах представителей всех религий (религий, а не просто конфессий — и это на фоне религиозных войн в Европе, порожденных 
		Реформацией!) был принят акт <a href="../events/confederacia_warshawa.php">Варшавской Конфедерации</a> 1573 г.
		</p>
		<p>
		В 1596 году, для уменьшения межконфессиональных противоречий и снижения влияния Москвы на внутренние дела государства 
		принята <a href="../events/unia_religion_brest.php">Брестская уния</a> — переход православных приходов в греко-католические (униатские).
		</p>
		

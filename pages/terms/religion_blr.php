		<p>
		Религиозное многообразие было одной из причин "<a href="../events/blood_potop.php">Кровавого Потопа</a>" 1654-1667 гг. Наказ московского царя Алексея стрелецкому войску: 
		“Унии не быть, латинству не быть, жидам не быть” был 
		равносилен объявлению геноцида — под определение попадало более 80% населения.
		</p>
		
		<p>
		После церковного раскола в РПЦ в середине XVII века часть староверов переселилась в ВКЛ на белорусские земли.
		 Одним из центров старообрядчества была Ветка. В 1791 году старообрядцы составляли 4% населения всего ВКЛ.
		</p>
		
		<p>
		К концуу XVIII века униаты составляли 40-70% населения ВКЛ, православные 6,5% (до 250 тысяч человек).
		</br>После <a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>, в 1839 униатские приходы были переданы в ведение 
		Московского патриархата РПЦ. Белорусские земли вступили в <a href="sewero_zapadny_kraj.php">серую полосу</a> своей истории. 
		Развитие образования и культуры затормозилось почти на столетие.
		</p>
		

		<h4>I Борисов камень (Полоцк)  — сохранился</h4>
		<p>
		Полоцкий Борисов камень ("Борис", "Борис-Глеб") первоначально находился примерно в 5 километрах от Полоцка, вниз по течению Западной Двины напротив деревни Подкостельцы (вошла в состав современного Новополоцка). Это валун полевого шпата размером примерно 3 метра.
		Первая попытка вытащить камень из реки состоялась в 1889 году и закончилась неудачей. Почти сто лет спустя, в 1981 году камень был всё же поднят из Двины и установлен в Полоцке напротив Софийского собора, где и находится до сих пор.
		</p>
		<h4>II Борисов камень (Дисна) — не сохранился</h4>
		<p>
		Первый из дисненских камней ("Писаник") — серый гранитный валун неправильной формы размером более 3 метров. 
		Находился в 5 километрах от города Дисны, напротив деревни Наковники.
		</p>
		<p>
		С этого камня начиналось изучение Борисовых камней. 
		По-видимому о нём говорил Матей Стрыйковский в своей "Хронике польской…". 
		Первый сохранившийся рисунок камней был снят именно с него в 1818 году и опубликован в "Ученых Записках Императорской Академии Наук".
		</p>
		<p>
		Камень находился почти посредине реки и при спаде воды мешал судоходству и в 1818 году его пытались взорвать. 
		Однако эта попытка закончилась неудачей. Свидетель, историк и археолог  А. М. Сементовский, писал —  
		"Народ, видящий во всём чудо, неудачу взрыва камня выразил словами: <i>"Борис не поддался"</i>. Дальнейшая судьба камня неизвестна.
		</p>

		<h4>III Борисов камень (Дисна) — не сохранился</h4>
		<p>
		Второй дисненский камень находился в двух километрах от первого, напротив деревни Болотки. 
		Это самый большой из Борисовых камней — гранитный валун размерами более четырёх метров. Внутри подножия креста было неясное изображение, возможно, черепа. 
		Надпись была выполнена тщательнее, чем на других камнях и, за исключением нескольких повреждённых мест, читалась легко.
		Судьба камня неизвестна.
		</p>

		<h4>IV Борисов камень (Друя) — сохранился</h4>
		<p>
		Четвёртый Борисов камень был найден А. П. Сапуновым в середине 1890-х в Друе. До этого момента он был практически неизвестен — большую часть года был полностью скрыт под водой. После открытия о нём забыли на долгое время. В 1930-х он был стянут сильным ледоходом вниз по течению, расколот и занесён песком.
		</p>
		<p>
		В 1982-84 проводились поиски Друйского камня, но безуспешо. Жарким летом 2002 Западная Двина обмелела и валун выступил из воды. Камень по частям извлекли из реки и установили на берегу, недалеко от его первоначального расположения. 
		В 2011 камень перенесли с берега Двины на центральную площадь Друи.
		</p>
		

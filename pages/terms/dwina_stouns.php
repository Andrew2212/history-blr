 		<h3>Двинские камни</h3>
		<p class="img-float-left">
		<a href="../../images/img-terms/dwina_stouns/stouns_RB.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Князь Рогволод-Борис и Двинские камни" src="../../images/img-terms/dwina_stouns/stouns_RB_thumb.jpg" height="120">
		</br><small>Культовые камни</small>
		</a>
		</p>
		<p>
		Двинские камни — памятник эпиграфики XII века, огромные (до 4 метров) валуны с выбитым на них крестами и надписями. 
		Часть из них носит название "Борисовы камни".
		</p>
		<p>
		С именем Рогволода-Бориса связывают Двинские камни, имеющие надпись «Господи, помоги рабу своему Борису». На них также высечены буквы: «IС ХС НИКА»
		(«Иисус Христос побеждает»).  
		Остальные камни обычно ассоциируют с именем его сына — Рогволода-Василия. 
		</p>
		<p>
		Массовое "крещение" языческих капищь в Полоцком княжестве — так большинство иследователей определяет назначение Двинских (Борисовых) камней.
		Однако, в связи с раздробленностью Полоцкого княжества на уделы, 
		"крестовый поход" на язычество затронул только саму Полоцкую землю (Витебская обл.).
		</p>
		<p>
		Впервые Двинские камни упомянуты в "Kronika Polska, Litewska, Zmudzka i wszystkiej Rusi" Мацея Стрыйковского, служившего в 1572-74 гг. под началом Александра Гваньини, 
		коменданта Витебского.
		</p>
		<p>
		Кстати — хрестоматийные портреты <a href="../terms/dukes_wkl.php">князей Литовских</a> взяты из работы Гваньини "Sarmatiae Europeae descriptio". Еще одна классическая работа Гваньини — 
		<a target="_blank" href="http://www.vostlit.info/Texts/rus5/Gwagnini/otryv3.phtml?id=3958">"Описание Московии"</a>		
		</p>
		

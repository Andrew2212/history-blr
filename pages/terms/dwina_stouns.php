		<p>
			Рогволод-Борис (1040-1128) — князь Полоцкий, сын <a href="../figures/charadzej.php">Всеслава  Чародея</a>. После смерти отца в 1101 году Борис занял Полоцкий престол. 
			Княжил в период феодальной раздробленности <a href="../terms/polotsk_principality.php">Полоцкого княжества</a> (оно было разделено между 7 сыновьями Всеслава).
		</p>
		<p>
		В отличие от отца — чародея-язычника, <i>"волхованием рожденного"</i> — князь носил два имени: родове и крестильное (Борис).
		С именем Рогволода-Бориса связан город Борисов — <i>"ходил он на ятвяг и, победя их, возвратясь, поставил град во свое имя..."</i> 
		</p>
		<p>
		В его княжение были ограничены права князя в Полоцке, усилилось городское самоуправление. 
		Рогволод-Борис был последним князем, запомнившимся долгим (27 лет) правлением.
		К концу XII  века форму правления в Полоцком княжестве <a href="../download/chronicles.php">"Хроника Литовская и Жмойтская"</a>
		 характеризует как <i>"свободность полоцкую або Венецию"</i>.
		</p>
		<p>
			В XII веке продолжилась вражда <a href="../figures/rogvolod_rogneda.php">Рогволодовичей</a> с Ярославичами — в разгоревшейся войне сын Бориса, Рогволод-Василий вместе с другими полоцкими князьями 
			был пленен и выслан в Византию. Через 10 лет они возвратились, но к этому времени князей в полоцких землях выбирало вече. 
			Подробно свидетельства и причины высылки князей рассмотрены в 
			<a target="_blank" href="../../images/img-terms/dwina_stouns/about_Polotsk_duces.pdf">работе А. В. Рукавишникова</a> (МГУ им Ломоносова). 
		</p>
		<p>
		К середине XII века <a href="../terms/religion_blr.php">христианство</a> стало укореняться на белорусской земле. 
		В этот период писал свои труды <a href="../figures/kirill_tur.php">Кирилл Туровский</a> (1130-1182) — богослов, писатель и проповедник. 
		Племянницей (дочерью?) Рогволода-Бориса была св. <a href="../figures/efrosinia.php">Ефросиния Полоцкая</a> (1101-1167) 
		— монахиня и просветительница, легендарная переписчица Полоцкой летописи.
		</p>
		<p>
		Рогволод-Борис упоминается в "Житии Евфросиньи Полоцкой" как участник основания Спасского монастыря — 
		там хранился <a href="/efrosinia_rood.php">крест Ефросинии Полоцкой</a> и там же храняться мощи св. Ефросинии. 
		В княжение Рогволода-Бориса была построена княжеская резиденция с Борисо-Глебским монастырём около Полоцка в Бельчицах.

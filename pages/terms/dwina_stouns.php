		<h4>Камень Иосафата (Витебск) — не сохранился</h4>
		<p>
		В конце XIX века в Витебске, напротив Успенского собора, примерно в 20 метрах от берега Двины находился обломок камня с глубоко высеченным на нём шестиконечным крестом. Сохранившийся фрагмент имел наибольший размер около полутора метров. Местные жители называли камень "Иосафатовым" .  
		</p>
		<p>
		<a href="../download/kirkor.php">А. Г. Киркор</a> считал, что камень был связан с гибелью здесь известного униатского священника <a href="../figures/kunciewicz.php">Иосафата Кунцевича</a>.
		</p>
		
		<h4>«Кравец» (Высокий Городец) — не сохранился</h4>
		<p>
		Найден в 1889 этнографом и археологом Е. Р. Романовым. Камень представлял собой глыбу красного гранита примерно 3 на 3 метра и высотой около  метра. В надписи угадывался текст: "Господи, помоги рабу своему Борису". 
		</p>
		<p>
		Историк отмечал, что в данной местности камень называли "Кравец" ("Портной"), и приводит предание: 
		<i>"Давно, в незапамятные времена, камень обладал чудною силою шить платье. Стоило принести вечером сукно или другую ткань и сказать: "Степан, сшей мне жупан!" 
		— и наутро получалось готовое платье… Но одна женщина вздумала насмеяться над "Кравцом": принесши сукно, она попросила камень сшить ей ни то, ни сё. 
		Утром берет платье, но оно испорчено: один рукав на надлежащем месте, а другой пришит к поле внизу… С той поры "Кравец" перестал шить, хотя и до сего 
		времени на камне заметны следы бывших когда-то ножниц"</i>.
		</p>
		<p>
		Дальнейшая судьба его неизвестна.
		</p>
		
		<h4>«Святополк-Александр» (Краслава) — не сохранился</h4>
		<p>
		Найден в 1818 году на Западной Двине при производстве взрывных работ для прочистки русла. "Дневные записки  работ по Двине от Дисны до Динабурга" сообщают:
		</p>
		<p>
		"Пятница, 25 октября. Сим числом работа производилась истреблением гряды по средине Двины около местечка Креславки, где ... 
		выбуравлен камень 16 сажень, вышиною в 1,5 сажени (3 метра); в плоской фигуре, на котором выбит щит древних рыцарей, на коем солнце и славянскими литерами надпись: 
		<i>да не убоится душа моя врага моего яко c твердою рукою десницы отросль Святополка Александр</i>,
		 — которому дано было шесть дыр, каждая шесть четвертей и расстрелян в мелкие дребезги."
		</p>
		

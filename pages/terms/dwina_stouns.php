		<h4>«Сулиборь хрьстъ» (Дисна) — сохранился (Москва)</h4>
		<p>
		Камень "Сулиборь хрьстъ" находился недалеко от Дисны — гранитный валун  неправильной формы, примерно полтора метра в поперечнике (самый меньший из всех Двинских камней).
		</p>
		<p>
		 В 1879 был доставлен в Москву, в Археологический музей, позже передан Историческому Музею. В 1920-х перевезён в музей-заповедник "Коломенское", 
		 где и находится до сих пор.
		</p>
		
		<h4>«Воротишин хрьстъ» (Камено) — сохранился</h4>
		<p>
		Камень находится в селе Камено Вилейского района Минской области на берегу Вилии. Это глыба тёмного гранита конусообразной формы высотой около 
		трёх метров. На камне высечен шестиконечный крест и надпись "Воротишин хрьстъ". Как и у Борисовых камней вверху по сторонам креста присутствует надпись "IС ХС НИКА".
		</p>
		<p>
		Сохранилась легенда о том, что "живший здесь крестьянин, в первый день Светлаго Христова Воскресенья, сохою запряженною двумя быками пахал в этом месте свое поле. Господь, желая наказать его за нарушение торжественного праздника, обратил в этот камень крестьянина и быков запряженных в соху."
		</p>
		<p>
		Этот камень - единственный из Двинских или Борисовых камней, сохранившийся на своём первоначальном месте.
		</p>
		
		<h4>«Рогволодов камень» (Дятлово) — не сохранился</h4>
		<p>
		В 1792 году у деревни Дятлово в 18 километрах от Орши был найден монументальный памятник эпиграфики XII века. На камне был высечен шестиконечный крест и надпись: 
		<i>"В лето 6679 месяца мая в 7 день доспен крест сей. Господи помози рабу своему Василию в крещении именем Рогволоду сыну Борисову"</i>.
		</p>
		<p>
		В 1805 году над камнем была построена церковь во имя св. мучеников Бориса и Глеба. В 1930-х церковь была разобрана, а камень взорван.
		</p>
		
		<h4>Камень в Забежье (Витебск) — не сохранился</h4>
		<p>
		А. П. Сапунов в своей книге "Двинские или Борисовы камни" писал о том, что был  найден камень (<i>«5-й Двинской или Борисов камень»</i>), лежащий на средине реки Западной Двины в 5 верстах от Витебска и 2 верстах от Маркова монастыря недалеко от деревни Забежье. На камне глубоко вырезан (глубина более 5 сантиметров) шестиконечный крест, но без всяких надписей.
		</p>
		<p>
		Дальнейшая судьба его неизвестна.
		</p>
		

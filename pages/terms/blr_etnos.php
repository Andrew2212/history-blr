		<h3>IX-XII вв</h3>
		
		<p>
		В этот период на <a href="../figures/torwald.php">пути из вряг в греки</a> возникает 
		<a href="../terms/polotsk_principality.php">Полоцкое княжество</a> — центр славянской культуры на "балтских" землях. 
		Не миграция и не завоевание. Просто смена культурной парадигмы.  
		</p>
		<p>
		На востоке — Полоцкое княжество, на юге — Волынское (позже Турово-Пинское) княжество, на западе — ятвяжские земли. Забавная деталь 
		— дикие <a href="../terms/yotving.php">ятвяги</a> растворились в общем славянском этносе бесследно, а Турово-Пинское <a href="../places/sea_herodotus.php">Полесье</a> 
		(бывшее <a href="../places/sea_herodotus.php">море Геродота)</a> до сих пор сохраняет культурную самобытность. 
		</p>
		

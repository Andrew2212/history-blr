		<h3>XX-XXI вв</h3>
		<p>
		Край, лежащий на перекрестке европейских путей "с запада на восток" и "из варяг в греки", принял и переплавил множество племен 
		— <a href="../articles/dnk.php">исследования генетиков</a> РАМН РФ и НАН РБ показали высокую генетическую однородность белорусского этноса.
		</p>
		<p>
		Свой отпечаток наложила <a href="../terms/religion_blr.php">пестрая религиозная палитра</a> на протяжении веков. Край — не дремучий анклав, "блюдущий чистоту крови". 
		</p>
		<p>
		И не случайно "белорусская идея", через создание <a href="../terms/bnr.php">БНР</a> не позволившая Краю раствориться в новой империи, 
		вышла из <a href="../terms/kraj_tut.php">тутэшай краевасьци</a>.
		</p>
		
		<p>

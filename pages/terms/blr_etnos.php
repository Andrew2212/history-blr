			Именно за этот тысячелетний период возникла традиционная беларуская ментальность, воплощенная в архетипах.
		</p>
		<p>
		Автохтонное население территории <a href="../terms/belarus.php">Беларуси</a> до XIX века не знало ни гнета завоевателей, ни массовых потоков иммиграции. Наследники балтской 
		археологической культуры, принявшие славянскую культурную парадигму, язык и религию — кривичи, дреговичи, ятвяги — сложились в единый белорусский этнос. 
		Немногочисленые волны мигрантов — варяги времен 
		<a href="../terms/polotsk_principality.php">Полоцкого княжества</a>, <a href="../terms/lutizy.php">вильцы-лютичи</a>
		 и <a href="../terms/yotving.php">пруссы</a> короля <a href="../figures/mindowg.php">Миндовга</a>, 
		 <a href="../events/battle_worskla.php">татары Тохтамыша</a>, приглашенные <a href="../figures/vitovt.php">Витовтом</a> — растворились 
		 в многоплеменном котле на перекрестке Европы.  
		</p>
		
		
		<h3>III-VIII вв</h3>
		<p>
		Археологические культуры III-VIII веков на территории Беларуси называют "балтскими". Об этом говорят археологи и лингвисты — старейшие топонимы 
		(гидронимы — названия рек и озер) в большинстве своем имеют балтское происхождение.
		</p>
		<p>
		Из очагов пра-славянской культуры — Пражской в центральной Европе и Пеньковской (Черняховской) на юге — славянская культура проникает на территорию 
		Беларуси. В Летуве беларусов и сегодня называют <a href="gudai.php">Gudai (гуды)</a>, т. е. готами. 
		На <a href="../download/map_16century.php">знаменитой какрте Мюнстера</a> 
		Великое княжество Литовское обозначено, как <i>Litvania-Godinia</i>. 
		</p>
		

		<h3>XIX в</h3>
		<p>
		Беларусь — <a href="../terms/sewero_zapadny_kraj.php">Северо-Западный край</a> Имерии. Стремительно меняется терминология. Термин "литвины" 
		уходит из официального обращения, оставаясь прерогативой "сьвядомых змагароў" — будущих 
		<a href="../terms/filomaty.php">филоматов</a> и <a href="../events/uprising_1830.php">инсургентов 1830 года</a>.
		<i>"Естественно, переродиться я никогда не сумею и надеюсь на Бога, что я — или в Кордильерах, или в Панарах (Вильня) — все равно умру литвином..."</i>, 
		— <a href="../figures/domeyko.php">Игнаци Домейко</a>.
		</p>
		<p>
		В первом <a href="../download/all_peoples_RI.php">"Описании всех народов России"</a>, составленном после 
		Второго <a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>, население восточных белорусских губерний именуется "поляками".
		Позже, в обиход войдет термин "белорусы", ранее обозначавший конфессиональную (<a href="../events/unia_religion_brest.php">униатскую</a>) принадлежность.
		</p>
		<p>
		Стоит вспомнить, что большинство современных европейских наций сформировалось в XIX в. 
		</br><i>"В конце восемнадцатого века... люди, которые жили на территории от Пруссии до Рейнской области и южной Баварии, 
		не считали себя немцами. Даже в двадцатом веке крестьяне некоторых европейских регионов не имели понятия, 
		что они принадлежат к какому-либо этническому сообществу, большему, нежели их локальное сообщество."</i>
		<small>Ф. Знанецкий, польский социолог</small>

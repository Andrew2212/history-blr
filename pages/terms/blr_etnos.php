		<h3>XIII-XVIII вв</h3>
		
		<p>
		К XIII веку Священная Римская Империя окончательно вытеснила полабских славян с западной Балтики. Новые переселнцы на белорусских землях 
		— <a href="../terms/lutizy.php">лютичи-велеты</a> и пруссы <a href="../figures/mindowg.php">Миндовга</a>, — как когда-то варяги, 
		положили начало новому государству — <a href="../terms/wkl.php">Великому княжеству Литовскому</a>. 
		</p>
		<p>
		<i>1248 год "132. В этом же году окрещенные пруссы со своим королем Мендольфом из-за многочисленных тягот, причиненных им крестоносцами, 
		оставив христанскую веру, которую приняли ранее, ушли с некоторыми братьями Ордена крестоносцев к литвинам, смело к ним присоединившись..."
		</i> <small>Monumenta Poloniae Historica</small>
		</p>
		<p>
		В XIV веке все белорусские земли были объединены в единое государство. К XV веку образовался единый белорусский этнос,
		принявший эндоэтноним <a href="litwiny.php">литвины</a>, который в XIX-XX вв. был заменен на <a href="../download/blr_types.php">белорусы</a>.
		</p>
		<p>
		Единственным государственным языком на белорусских землях до конца XVII века был <a href="../terms/blr_mova.php">белорусский язык</a>.

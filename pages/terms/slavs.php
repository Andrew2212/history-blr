		<p>
			Надо пояснить, что термин «волна» условен. Славянизированных пришельцев во всех четырех «волнах», по сравнению с коренным населением, 
			было очень мало. Никаких следов массовых миграций чужих этносов на наши земли не обнаружено. 
			Ни I-II, ни III-IV, ни V-VI веков. Нет следов археологических, нет упоминаний в народном эпосе. 
			</br>Этот факт подтверждает основной тезис: не было ассимиляции аборигенов-балтов пришельцами-славянами. 
			Происходило постепенное, растянувшееся почти на тысячу лет, превращение балтов в славян.
		</p>
		<p>
			Наглядный пример процесса славянизации — судьба булгар. Как известно, группа кочевых тюркоязычных племен во главе с ханом Аспарухом в 681 году пришла с территории Приазовья (через нынешние Молдову и Румынию) на Балканы. Здесь эти тюрки постепенно переняли оседлый образ жизни, усвоили славянский язык, приняли христианство. Так они превратились в славянский народ «болгары». 
			А другая часть булгар, осевшая в бассейне Волги и Камы, 
			в X веке приняла ислам и называется «татары». Сейчас это два разных народа.
		</p>

		
		

		<h3>Славяне</h3>
		<p>
			Для начала, определим, что есть "славяне".
			</br>По мнению ряда ученых (Н. С. Трубецкой, А. А. Бычков, В. Б. Егоров, С. Е. Рассадин, А. А. Клёсов и др.), «славяне» — не этнос, 
			а сходство языка, культуры и верований. 
			</br>Вот что пишет, к примеру, Анатолий Клёсов, крупный современный ученый — биохимик и генетик, кандидат на Нобелевскую премию:
			</br>«Официальная наука», помещая наших предков в VI-VII века нашей эры, недоговаривает, что «славяне» — это только лингвистическое понятие, 
			и оно относится только к языкам славянской группы. (…) 
			Любой толковый историк и лингвист это знает, но на публике продолжают уныло долдонить, 
			что «славяне появились только во второй половине 1-го тысячелетия нашей эры». 
			Заметьте, не язык сформировался, а «славяне появились». То есть как люди, как популяция. Так у историков-лингвистов принято. 
			Иначе окрикнут. Сделают внушение. Оно им надо?»
		</p>
		<p>
		Вероятно, праславянский этнос сложился в ареале <a href="yotving.php">Черняховской археологической культуры</a>, существовавшей с начала III до середины VI века. 
		Это регион между Дунаем на западе и Днепром на востоке, Припятью на севере и Черным морем на юге. 
		Здесь была контактная зона готских (германских), балтских и скифо-сарматских (иранских) племен. 
		Постепенно они объединились в протогосударство во главе с готской племенной знатью и за 250-300 лет мирного взаимодействия выработали 
		«койне» — упрощенный язык «межнационального общения». Это и был праславянский язык.
		</p>


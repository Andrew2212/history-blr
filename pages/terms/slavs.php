			В 370-376 гг. под ударами гуннов население южной части ареала (готского протогосударства) ушло большей частью на Балканы и в Центральную Европу,
			 меньшей частью — на север, в лесные районы северо-западной и северной части нынешней Украины.
		</p>
		<p>
			<i>Первая волна славянизации.</i> Через 200 лет, во второй половине VI века пришло в движение население северной части региона, 
			на этот раз под ударами аваров (обров). Ученые пишут: «В конце 50 - начале 60-х гг. VI века авары обрушились на антов и опустошили их земли». 
			Спасаясь от этих жестоких хищников, массово истреблявших людей на захваченных территориях, анты бежали преимущественно на запад, 
			и отчасти на север, в леса и болота Полесья — южной части нынешней Беларуси. 
			Отметим попутно, что антские племена проживали в северной части ареала Черняховской культуры. 
			Этот исход положил начало славянизации исконных жителей наших земель
		</p>
		<p>
			<i>Вторая волна</i> — в VIII-Х веках. Эти носители славянской культуры (варяги) пришли из Полабья, они двигались по Западной Двине, 
			Нёману, Днепру и их притокам.
		</p>
		<p>
			<i>Третья волна</i> пришлась на конец IX - начало X веков. Ее образовали беженцы из Великой Моравии, погибавшей под ударами германцев (саксонцев) и мадьяров (венгров).
			 Они уходили в сопредельные страны на северо-восток, восток и юго-восток: в Польшу, Чехию, Словакию, Закарпатскую Русь. 
			 Некоторая часть моравов по Припяти и «<a href="../places/sea_herodotus.php">морю Геродота</a>» проникала на земли будущей Беларуси. 
			Моравы несли с собой не только славянский язык, не только развитые ремесла, но и христианство арианского толка.
		</p>
		<p>
			<i>Четвертая волна</i> шла из Галицко-Волынской и Киевской Руси. Это X-XII века. 
			Галичане и киевляне основали Берестье (Брест), Волковыск, Гародню (Гродно), Здитов, Каменец, Кобрин, Новогородок, Острею, 
			Слоним и ряд других градов.
		</p>
		<p>
			Надо пояснить, что термин «волна» условен. Славянизированных пришельцев во всех четырех «волнах», по сравнению с коренным населением, 
			было очень мало. Никаких следов массовых миграций чужих этносов на наши земли не обнаружено. 
			Ни I-II, ни III-IV, ни V-VI веков. Нет следов археологических, нет упоминаний в народном эпосе. 
			</br>Этот факт подтверждает основной тезис: не было ассимиляции аборигенов-балтов пришельцами-славянами. 
			Происходило постепенное, растянувшееся почти на тысячу лет, превращение балтов в славян.
		</p>
		<p>
			Наглядный пример процесса славянизации — судьба булгар. Как известно, группа кочевых тюркоязычных племен во главе с ханом Аспарухом в 681 году пришла с территории Приазовья (через нынешние Молдову и Румынию) на Балканы. Здесь эти тюрки постепенно переняли оседлый образ жизни, усвоили славянский язык, приняли христианство. Так они превратились в славянский народ «болгары». 
			А другая часть булгар, осевшая в бассейне Волги и Камы, 
			в X веке приняла ислам и называется «татары». Сейчас это два разных народа.
		</p>

		
		

		<h4><a href="../times/15_vek.php">XV век</a></h4>
		<p>
		Правление <a href="../figures/vitovt.php">Витовта</a> (1392-1430). 
		</br><a href="../events/grunwald.php">Битва под Грюнвальдом</a> 1410 года — перелом в  противостоянии  с Тевтонским Орденом. 
		Итогом войн с Орденом стало присоединение Самогитии к ВКЛ (1411) и доступ Польши к Балтийскому морю (1466).
		</p>
		<p>
		 При в. князе Витовте наступил "Золотой век" ВКЛ (1410-1569) — время белорусского Ренессанса.
		 </br>Издан <a href="../terms/statut.php">Статут ВКЛ</a>, составлена знаменитая карта <a href="../download/map_radziwill.php">Magni Ducatus Lithvaniae</a> Радзивилла "Сиротки", 
		 издаются труды гуманистов и просветителей (<a href="../figures/skorina.php">Скорина</a>, <a href="../figures/gusowsky.php">Гусовский</a>, 
		 <a href="../figures/tiapinsky.php">Тяпинский</a>, <a href="../figures/budny.php">Будный</a>) — золотое наследие Беларуси. 
		 </br>Завершается формирование <a href="../terms/blr_etnos.php">белорусского этноса</a>.
		</p>
		

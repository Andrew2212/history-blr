		<p>
		В правление (1316-1341) в. князя <a href="../figures/gedimin.php">Гедимина</a> к Великому княжеству Литовскому мирно присоединились Полоцк, Гродно и Берестье, 
		Витебск, Минск, Туров и Пинск. 
		Собраны воедино все белорусские земли, кроме <a href="../places/sea_herodotus.php">Полесья</a>.
		</p>
		
		<p>
		Собраны мирно — нет в хрониках упоминания о битвах и осадах. Полоцкая <a href="../places/sophia_sobor.php">София</a> нетронутой простояла 
		еще 300 лет (до прихода московского войска) — что несравнимо с походом 
		<a href="../figures/david.php">Давида Городенского</a> (наместника Гедимина) на Ревель (Таллин) или Мазовию. 
		<p>
		<p>
		Возможно, Императорское Русское Географическое Общество (<a href="../download/kirkor.php">"Живописная Россия"</a>, 1882) было право, 
		утверждая происхождение Гедыминовичей от кривичских <a href="../figures/rogvolod_rogneda.php">Рогволодовичей</a>
		 — из многих версий эта выглядит наиболее логично..
		</p>
		<p>
		<a href="../places/samogitia.php">Самогития-Жмудь</a> (Samogitia) — <i>"в <a href="../times/13_vek.php">XIII</a> и даже в <a href="../times/14_vek.php">XIV</a> вв. занимала большое пространство, 
		заходя в пределы нынешней Виленской губ... Достоверная история Жмуди начинается с XIII в., со времени появления в соседстве немцев, и наполнена, главным образом, 
		известиями о борьбе с последними, продолжавшейся до начала XV века."</i>
		<small><a target="_blank" href="http://dic.academic.ru/dic.nsf/brokgauz_efron/41009/Жмудь">(Энциклопедический словарь</a> Брокгауза и Ефрона)</small>
		</p>
		<p>
		При этом, со <a href="../places/samogitia.php">жмудинами</a> — народом Самогитии — Гедимин так и не смог договориться даже о союзе в войне с общим врагом — Тевтонским Орденом:
		</br><i>"...И никогда во время правления своего король Littouwin </i><small>
		[правящий из столицы — <a href="../places/nowogrudok.php">Новогрудка</a>]</small><i> 
		не мог договориться с Samaiten, чтобы вместе выступить на войну против братьев </i><small>[рыцарей ордена]</small><i>".</i>
		</p>
		<p>
		На всех картах и во всех <a href="ross_empire.php">титулах</a> Самогития отмечалась отдельно от Литвы.
		Жмудский и Полоцкий земские привилеи, подтверждавшие особый статус этих земель, подтверждались дольше других — до 1574 и 1634 годов соотвественно.
		</p>
		

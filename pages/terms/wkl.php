		<h4><a href="../times/16_vek.php">XVI век</a></h4>
		Окончание "Золотого века" ВКЛ.
		</br><a href="../events/unia_liublin.php">Люблинская уния</a> 1569 года. ВКЛ объединяется с Королевстовм Польским в 
		<a href="../terms/rzeczpospolita.php">Речь Посполитую Двух Народов</a> для противостояния
		внешним угрозам — главным образом, Московской экспансии на запад. По условиям унии <a href="../download/zemie_ruskie.php">Земли Русские</a> отходят Польше.
		</br>Подписан акт <a href="../events/confederacia_warshawa.php">Варшавской конфедерации</a>, положивший начало религиозной свободы в Речи Посполитой (на фоне религиозых войн в Европе).
		</br>Для уменьшения межконфессиональных противоречий и снижения влияния Москвы на внутренние дела государства 
		принята <a href="../events/unia_religion_brest.php">Брестская уния</a> — переход православных приходов в греко-католические (униатские).
		</p>
		
		<h4><a href="../times/17_vek.php">XVII век</a></h4> 
		Кровавый век беспрерывных войн.
		</br>Войны с Московией, Швецией, Османской империей, Войском Запорожским. 
		Ослабление центральной государственной власти в противостоянии со "шляхетской демократией".
		</br><small>[ <a href="../terms/shliahta.php">Шляхта</a> ВКЛ-Беларуси составляла 10-15% населения ]</small>

		<h4><a href="../times/15_vek.php">XV век</a></h4>
		<p>
		1392-1430 Правление <a href="../figures/vitovt.php">Витовта</a>. 
		</br><a href="../events/grunwald.php">Битва под Грюнвальдом</a> 1410 года — перелом в  противостоянии  с Тевтонским Орденом. 
		Итогом войн с Орденом стало присоединение Самогитии к ВКЛ (1411) и доступ Польши к Балтийскому морю (1466).
		</p>
		<p>
		 При Витовте Великое княжество Литовское становится крупнейшим европейским государством. Наступил "Золотой век" ВКЛ (1410-1569) — время белорусского Ренессанса.
		 </p>
		<p>
		Издан <a href="../terms/statut.php">Статут ВКЛ</a>, 
		 издаются труды гуманистов и просветителей (<a href="../figures/skorina.php">Скорина</a>, <a href="../figures/gusowsky.php">Гусовский</a>, 
		 <a href="../figures/tiapinsky.php">Тяпинский</a>, <a href="../figures/budny.php">Будный</a>) — золотое наследие Беларуси. 
		 </br>Завершается формирование <a href="../terms/blr_etnos.php">белорусского этноса</a>.
		</p>
		<p>[ <small>
		В культурологическом плане интересно сравнить первую статью Соборного Уложения (действовало с 1649 по 1832) с первой статьей <a href="../download/statut_wkl.php">Статута ВКЛ</a> 
		(действовал с 1529 по 1840). <i>"Вси обыватели Великого князства Литовского тым одным правом писаным 
		и от нас даным сужоны быти мають" </i>
		( арт. 1, разд. 1,: "О персоне нашой Господарской").
		</small> ]
		</p>
		

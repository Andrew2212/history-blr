		<h4><a href="../times/16_vek.php">XVI век</a></h4>
		<p>
		Окончание "Золотого века" ВКЛ.
		</p>
		<p>
		1487-1537 <a href="../events/wars_1487-1537.php">Войны с Московским царством</a>, определившие современную восточную границу Беларуси
		</p>
		<p>
		1558-1583 <a href="../events/war_livonia.php">Ливонская (Инфлянтская) война</a> с Московским царством, приведшая к заключению Люблинской унии 1569 года.
		</p>
		<p>
		1569 <a href="../events/unia_liublin.php">Люблинская уния</a>. Великое княжество Литовское объединяется с Королевстовм Польским в 
		<a href="../terms/rzeczpospolita.php">Речь Посполитую Двух Народов</a> для противостояния
		внешним угрозам — главным образом, Московской экспансии на запад. По условиям унии <a href="../download/zemie_ruskie.php">Земли Русские</a> отходят Польше.
		</p>
		<p>
		1573 Подписан акт <a href="../events/confederacia_warshawa.php">Варшавской конфедерации</a>, положивший начало религиозной свободы в Речи Посполитой 
		(на фоне религиозных войн в Европе).
		</p>
		<p>
		1596 Принята <a href="../events/unia_religion_brest.php">Брестская церковная уния</a> — слияние исповеданий православного и католического
		с сохранением восточных обрядов. Переход православных приходов в греко-католические (униатские) помог снять межконфессиональные противоречия 
		в стране и уменьшил влияние Москвы на внутренние дела государства.
		</p>
		<p>[ <small>
		В 1595 году был издан самый первый <i>Атлас</i> Герхарда Меркатора (все карты Земли в одной книге). 
		<a href="../download/map_mercator.php">Lithvania Меркатора</a> — первая в истории карта, посвященная только ВКЛ.
		Примечательно, что в то время картографы еще изображали на территории Беларуси <a href="../places/sea_herodotus.php">море Геродота</a> (ныне Полесье).
		</small> ]</p>
		

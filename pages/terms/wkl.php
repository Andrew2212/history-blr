		<p>
		Поэтому Самогития на всех картах и во всех <a href="ross_empire.php">титулах</a> отмечалась отдельно от Литвы. на жмудские земли не распространялся Статут ВКЛ 
		(была отдельная "<a target="_blank" href="../../images/img-places/samogitia/zmud_law.jpg">грамота жмудская</a>").
		 Это разделение было закреплено в <a href="statut.php">Статуте ВКЛ</a> 1566 г. (арт. 9, раздел 3):
		</br><i>
		"... Но на благородность [шляхетство-дворянство] и всякую должность духовную и светскую не может быть избран, 
		даже от Нас Государя ставленник, только издавна предков своих уроженец Великого Княжества Литовского <a href="litwiny.php">Литвин</a> 
		и <a href="../download/zemie_ruskie.php">Русин</a> ..."
		</i>
		</p>
		<p>
		При в. князе (1345-1377) <a href="../figures/olgerd.php">Ольгерде</a> в ВКЛ были собраны все <a href="../download/blr_people.php">белорусские земли</a>. 
		</br>После победы над Золотой Ордой на <a href="../events/sinie_wody.php">Синих Водах</a>, Ольгерд присединил к ВКЛ <a href="../download/zemie_ruskie.php">"земли Русские"</a> (современная Украина).
		</p>
		<p>
		В 1340-1390 года королевство Польша и Великое княжество Литовское воевали за галицко-волынское наследство. 
		Ради прекращения борьбы и объединения против внешних угроз в 1385 году была принята <a href="../events/unia_krewo.php">Кревская уния</a>.
		</p>
		<p>
		Кревская уния  — династический союз, по которому литовский великий князь <a href="../figures/jagiello.php">Ягайло</a> (сын Ольгерда и тверской княжны), вступив в брак с польской королевой Ядвигой, провозглашался польским королём.
		Войска, законодательство и судебная система, а также казна (включая денежную эмиссию) оставались раздельными, сохранялась и граница между государствами с взиманием таможенных сборов.
		</p>
		<p>
		В результате <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> (1381-1392) — борьбы за власть между Ягайло и Витовтом (племянником Ольгерда) — Ягайло признал Витовта великим князем литовским, 
		а тот, в свою очередь, признал Ягайло верховным сюзереном Литвы.
		</p>

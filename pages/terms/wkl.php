		<p>
		1345-1377 При в. князе <a href="../figures/olgerd.php">Ольгерде</a>, сыне Гедимина, в Великое княжество Литовское были собраны все <a href="../download/blr_people.php">белорусские земли</a>. 
		</br>Разбив войска Золотой Ордой в <a href="../events/sinie_wody.php">битве на Синих Водах</a>, Ольгерд присединил к ВКЛ 
		<a href="../download/zemie_ruskie.php">"земли Русские"</a> (современная Украина).
		</br>1377 после смерти Ольгерда новым в. князем становится его сын <a href="../figures/jagiello.php">Ягайло</a>.
		</p>
		<p>
		В 1340-1390 года королевство Польша и Великое княжество Литовское воевали за галицко-волынское наследство. 
		Ради прекращения борьбы и объединения против внешних угроз в 1385 году была принята <a href="../events/unia_krewo.php">Кревская уния</a>.
		</p>
		<p>
		Кревская уния  — династический союз, по которому литовский великий князь Ягайло (сын Ольгерда и тверской княжны), вступив в брак с польской королевой Ядвигой, провозглашался польским королём.
		Войска, законодательство и судебная система, а также казна (включая денежную эмиссию) оставались раздельными, сохранялась и граница между государствами с взиманием таможенных сборов.
		</p>
		<p>
		В результате <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> (1381-1392) — борьбы за власть между Ягайло и Витовтом (племянником Ольгерда) — Ягайло признал Витовта великим князем литовским, 
		а тот, в свою очередь, признал Ягайло верховным сюзереном Литвы.
		</p>

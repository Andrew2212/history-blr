		<p>
			Великое княжество Литовское (ВКЛ) - восточноевропейское государство, 
			существовавшее с середины XIII века по 1795 год на территории современных <a href="belarus.php">Беларуси</a> и 
			<a href="../places/samogitia.php">Летувы</a>, 
			а также частично Украины, России, Латвии, Польши, Эстонии и Молдавии.
		</p>
		
		<p>
			ВКЛ-<a href="../places/litwa.php">Литва</a> (Littaw, Litua, Lithuania) как княжество (государство) зародилось в XIII веке на 
			северо-западных белорусских землях — Lithuania Propria — 
			где жили <i>"ятвяги, литва и мелкие литовские народки"</i> <a href="../download/shafarik.php"><small>(Й.Шафарик)</small></a>, как 
			итог миграции <a href="lutizy.php">полабских славян</a> и <a href="yotving.php">пруссов</a>. 
			</br>Первым князем и единственным королем Литвы был <a href="../figures/mindowg.php">Миндовг</a>. 
		</p>
		<p>
		<i>"132. В этом же году окрещенные пруссы со своим королем Мендольфом из-за многочисленных тягот, причиненных им крестоносцами, 
		оставив христанскую веру, которую приняли ранее, ушли с некоторыми братьями Ордена крестоносцев к литвинам, смело к ним присоединившись..."
		</i>
		</br><small>Monumenta Poloniae Historica, XII-XIV вв., 132-133</small>

		<p>
		Гусария, или крылатые гусары — элитная кавалерия <a href="rzeczpospolita.php">Речи Посполитой</a>, действовавшая на полях сражений 
		с середины XVI века до середины XVIII века. 
		</p>		
		<p>
		Гусария была подразделением народного ауторамента (наёмного войска местной военной традиции, 
		набирали по принципу товариществ). Завербованный товарищ приводил с собой почт 
		(<i>драб</i> — пеший, пехотинец, <i>пахол(и)к</i> — слуга, оруженосец), коней и челядь. 
		</br><small>[ см. <a href="../download/spis_voiska.php">Перепись Войска Литовского</a>, 1567 ]</small>
		</p>		
		<p>		 
		Кавалерия, и самая её боеспособная часть — гусария — являлись основной военной силой Речи Посполитой. 
		Длительное время гусария не имела себе равных в Европе, и её атаки не раз приносили победу 
		Королевству Польскому и <a href="wkl.php">Великому княжеству Литовскому</a> — как в 
		<a href="../events/battle_kircholm.php">битве при Кирхгольме</a>, где 4-х тысячная армия ВКЛ под командованием 
		<a href="../figures/hodkewich.php">Яна Ходкевича</a> разгромило 11-ти тысячное шведское войско.
		</p>
		<p>
		Первыми гусарами были сербы (легкая конница без доспехов), поступившие на службу польского короля в конце XV века. 
		К середине XVI века гусария трансформировалась в тяжелую конницу, близкую западноевропейской рыцарской традиции.
		</p>		
		<p>
		На внешний вид крылатых гусар оказали влияние турецкие "дели" (безумные) — они обычно набирались из балканских народов, 
		подвластных Османской империи. В 1577 году король и в. князь 
		<a href="../figures/batory.php">Стефан Баторий</a> произвёл стандартизацию гусарского вооружения и снаряжения. 
		</p>
		

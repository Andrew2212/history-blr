		<h4>Пруссы </h4>
		<p>

		Пруссы (лат. Borusci) — союз балтских и славянских племён, объединённых общей религией, в IX/X—XVIII веках населявший территорию нынешней Калининградской области России, 
		южной части Клайпедского уезда Литвы и Варминско-Мазурского воеводства Польши.  		 
		</br>Собственно, пруссами вначале назывались славяноязычные (подобно литве и кривичам, видимо, балтского происхождения) жители, 
		а балтоязычные именовались в источниках кимврами. 
		</br>К XIII веку ареал расселения пруссов на западе был ограничен нижним течением Вислы, а на севере — линией водораздела Преголи и Немана.
		</br>Ввиду ускоренной ассимиляции славяноязычных пруссов мазурами, кашубами, словинцами, поляками и другим населением, 
		прибывшим после завоевания Пруссии Тевтонским орденом, с XV века пруссами называлось только балтоязычное население. 
		</br>Слово <i>пруссы </i>— не самоназвание. Версий его происхождения много, но распространённым является предположение, 
		что название «Prus» — готское слово, означающее «конь», на старославянском языке «прус» — кобыла. 
		</br>Из смешения прусского, частично польского населения Восточной Пруссии с немецкоязычными колонистами к началу ХХ в. 
		сложилась особая субэтническая группа — немцы-пруссаки, а временем окончательного исчезновения прусской народности условно можно считать 1709—1711 гг., 
		когда от голода и эпидемии чумы погибло около половины населения древнепрусских земель, включая последних носителей прусского языка.
		

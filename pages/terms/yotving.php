		<h4>Ятвяги</h4>
		<p>
		Еще в 1673 году Феодосий Софонович в своей «Хронике» так писал о ятвягах:
		</br><i>«Ятъвежи были едного народу з литвою и з прусами старыми, з готтов пошли, которых столечное место было Дорогичінъ, 
		а Подляшье все аж до Прус, з Волыня почавши, осевши были, Новгородок Литовскиі и околичниі волости держали».</i>
		</br>Лингвисты полагают, что ятвяжские диалекты были близки диалектам пруссов. Наиболее значительный и ценный памятник ятвяжского языка — рукописный 
		польско-ятвяжский словарь «Роganske gwary z Narewu», найденный в конце 1870-х годов в южной части Беловежской пущи.
		Он включает более 200 лексем, многие из которых раскрывают важные черты быта и особенностей культуры ятвягов. 
		Материал словаря позволяет выявить ряд фонетических и морфологических особенностей ятвяжского языка. 
		Их анализ позволил исследователям определить ятвяжские диалекты как близкие прусскому языку, а также выявил связь их с готскими языками 
		(на основе значительного числа германизмов). 
		Германские племена готов в начале нашей эры жили на южном обережье Балтики от Вислы до Нарева и Нёмана, и с последней четверти II века н. э. 
		эти племена начали постепенно продвигаться в юго-восточном направлении. Таким образом, не будет «натяжкой» суждение о том, что ятвяги — потомки готов.	

	<p>
	Вообще, не надо забывать, что со времен Петра-1 московской РПЦ управлял св. Синод под руководством Обер-прокурора, 
	имевшего статус министра — <i>"ока государева и стряпчего о делах государственных в Синоде"</i>. Должность не была упразднена даже после развала империи.
	Патриарх Тихон (†1925), который благославлял БНР в 1918 году, был единственным за 300 лет (1720-2018) главой РПЦ, не признавшим власть государства над церковью. 
	</p>
	<p>
	В том же 1918 году он писал свое Воззвание к властям (большевикам): 
	<i>"Опомнитесь, безумцы, прекратите ваши кровавые расправы. Ведь то, что творите вы, не только жестокое дело, это поистине дело сатанинское."</i>
	В 1922-1924 Тихон состоит под следствием, а в 1923 году Второй Поместный всероссийский собор (первый обновленческий), 
	 высказался в поддержку советской власти и вынес решение о лишении сана «бывшего патриарха Тихона». 
	 </p>

		<p>
		К началу XIX в., после <a href="../events/razbor_rechi.php">Разделов Речи Посполитой</a>, наш Край впервые в своей истории вошел в состав московского гоударства.
		</p>
		<p>
		В первом <a href="../download/all_peoples_RI.php">"Описании всех народов России"</a> 1793 года, жителей Полоцкой, Минской и Могилевской губерний именовали "поляками".
		Сами жители называли себя поляками, <a href="../terms/litwiny.php">литвинами</a>, рускими, <a href="../terms/kraj_tut.php">тутэйшыми</a>, всячески дистанцируясь от "пришлых великоруссов". 
		</p>
		<p>
		С самого начала оккупации Московский патриархат Русской Православной церкви (РПЦ) выступал как мощный рычаг культурно-политической ассимиляции. 
		Московские православные, 300 лет жившие под Соборным Уложением, и жители ВКЛ, жившие под Статутом, имели разные культурные ценности.
		</p>
		<p>
		Вообще, к началу XIX в. "православных" в ВКЛ <a href="../terms/kraj_tut.php">насчитывалось примерно 6%</a>. Даже после ликвидации 
		<a hrf="../events/unia_religion_brest.php">Брестской унии</a> в 1839 году и перевода униатов в "православие",
		в Виленской губернии белорусов-католиков было больше, чем "новолитовцев" всех вероисповеданий.
		</p>
		

	<p>
		После подавления <a href="../events/uprising_1830.php">восстания 1830</a> и <a href="../events/uprising_1863.php">восстания 1863</a>, 
		роль и возможности РПЦ в культурной ассимиляции возросли. <i>"Весь учебно-педагогический состав усилиями Муравьева сделался русским и православным".</i>
		При этом <i>"сяляне цэлымі вескамі адмаўляліся выконваць абрады праваслаўнай царквы... Далучаныя супраць волі да праваслаўя, сяляне самі сталі задавальняць свае духоўныя патрэбы"</i>.
	</p>
	<p>
	После 1863 года РПЦ <a href="../arch_about/allObjects.php">занимает и перестраивает</a> католические и униатские храмы и монастыри. В нашем Крае появляются первые "золотые цыбулины".
	 Начинается массовое строительство новых "православных храмов" в городах и местечках, переименование улиц и населенных пунтков. 
	Так, в Минске ул. Францысканская стала Губернаторской, Доминиканская — Петро-Павловской и тп.
	Придумываются "православно-народные праздники" — чествование А.Невского и тп.
	</p>
	<p>
	Отказавшихся принимать <a href="../articles/ortodox.php">"исконную православную веру"</a> ждали репрессии — штрафы, тюрьма, 
	ссылка, изъятие детей из семьи (по материалам Национального Архива РБ). 
	</p>

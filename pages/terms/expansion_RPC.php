	<p>
	И о традиционных крестах. Наш классический канон был заложен в <a href="../terms/efrosinia_rood.php">кресте Ефросинии Полоцкой</a> 
	— это четырех- или шести-конечный крест. Такие кресты стояли на могилах, при дорогах, на храмах и <a href="../terms/dwina_stouns.php">Двинских камнях</a>. На гербе Вильни и на щите Погони. 
	Сегодня, вслед за цыбулинами, наш край заполнили восьми-конечные "русские кресты", ставшие внезапно очень популярными в пост-советской РФ.

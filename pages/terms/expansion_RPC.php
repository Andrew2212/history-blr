	<p>
	Экспансия МП РПЦ в нашем Крае начавшись в XIX веке, продолжается и сегодня. "Белорусская православня церковь" (БПЦ) — это просто экзархат (филиал) Московского патриархата.
	В отличие от польской или болгарской <i>автокефальных</i> (независимых) православных церквей. 
	И БПЦ по-прежнему уродует нашу традиционную архитектуру и служит молебны иностранным Невским и Матронам Московским, оставаясь абсолютно инородным телом 
	для нашей истории и культуры.
	</p>
	<p>
	И даже главный храм белорусского экзархата РПЦ — собор Св. Духа в Минске, — это бывший монастырь бернардинок, 
	где лежат мощи <a href="../figures/zofia_sluck.php">св. Софии Слуцкой</a>, католички и жены протестанта, "заступницы православия в Беларуси". 
	</p>
	

	<p>
	И если кто-то думает, что РПЦ в Беларуси "продолжает традиции св. Ефросинии", то он сильно ошибается. Ефросиния молилась за свой Край, <i>восточная</i> граница которого 
	совпадает с <i>восточной</i> границе РБ. Границей, на которой <a href="../events/_events_intro.php">адбылося 90% всех войн</a> в нашей истории.
	</p>
	<p>
	Сегодня католики, униаты, протестанты в Беларуси ведут имшу на мове, молятся за национальных героев и апеллируют к национальной памяти.
	Так, недавно в Минск был привезен и перезахоронен в костеле св. Роха прах Магдалены Радзивил — фундатарки издания первой книга М. Богдановича.  
	Проведены имшы в память юбилея БНР. 
	</p>
	<p>
	Понятно, что ни одна Церковь, как международный Институт, не ставит себе целью "адраджэньне Беларуси". А своей <i>автокефальной Церкви</i> у нас нет. 
	Но только РПЦ-БПЦ проводит агрессивную коммерческо-идеологическую экспансию, направленную на присоединение нашего Края к чужой стране и уничтожение нашего культурного наследия.

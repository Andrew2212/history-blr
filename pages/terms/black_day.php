		<p>
		28-29 октября 1937 года по обвинению в участии в "антисоветской объединенной шпионско-террористической национал-фашистской организации" 
		в подвалах минской тюрьмы НКВД было расстреляно более 100 белорусских писателей, поэтов,  ученых, государственных деятелей.	
		</p>
		<p>
		Характерная деталь. Минск в 20-30-е годы был в полном смысле слова мультикультурным городом. 
		Выходили книги и периодика, ставились пьесы по-беларуски, по-польски, по-еврейски, по-русски, по-литовски. 
		Здесь говорили и творили на всех упомянутых языках. 
		</p>
		<p>
		Словом, Минск напоминал <a href="../places/wilno.php">Вильну</a> с ее средневековыми традициями, место слияния различных культур. 
		Но минский мультикультурализм был полностью уничтожен в 30-е. После войны существовавшая здесь традиция не возродилась. 
		</p>		

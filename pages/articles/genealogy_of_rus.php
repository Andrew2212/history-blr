		<h3>Трипольцы</h3>
		
		<p>
			Попробуем экспонировать это древнекиевское летописное исследование к самому истоку, вглубь тысячелетий. Начнем с предисловия. В конце прошлого века зародилось ДНК-генеалогия с помощью которой можно проследить распространение по планете предков современных наций и народов вплоть до общих прародителей - собственно, уже людей вида homo sapiens…
		</p>
		<p>
			7,6 тыс. лет назад произошел так называемый Черноморский потоп, приведший к увеличению площади Черного моря и появлению Азовского, что создало уникальный мягкий микроклимат, превратив эти изначально плодородные земли в благодатную саванну. Создались идеальные условия для земледелия и скотоводства, чем наши далекие, но уже прямые предки не замедлили воспользоваться, развив Трипольскую Цивилизацию.

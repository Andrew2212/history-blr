		<p>
			Осмелюсь предложить отличный от общепринятого путь из Триполья в Аркаим и Индию. Прямой путь из Украины на восток кажется наиболее логичным только лишь на первый взгляд. Однако, учитывая определяющую роль природных условий в действиях людей 5 тыс. лет назад мы видим, что двигаться земледельцам вглубь континента в зону более засушливого континентального климата воистину алогично.
		</p>
		<p>
			Основным же был - по украинскому балто-черноморскому водоразделу с Прикарпатья и Волыни в Западную Беларусь - огибая слева пост-ледниковое Припятское море-озеро. Далее по этому же водоразделу через центр Беларуси. Затем по Валдайскому балто-каспийскому водоразделу мимо тверских автохтонных пост-ледниковых кроманьонцев…

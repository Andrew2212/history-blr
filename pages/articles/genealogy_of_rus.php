		<h3>Славяне</h3>
		<p>
			Надо отметить, что в последнее время исследователи начинают догадываться кто причина, и где следствие, но все равно избегают называть исток миграции  и носителей подветвей R1a своим именем - трипольцами с территории современной Украины. Это расселение происходило в три этапа… Таким образом, генетический анализ указывает на Украину как на прародину для западных и восточных славян, а также части южных славян…
		</p>
		<p>
			То есть переселение, а значит и развитие этих культур могли осуществляться только из Украины, расходясь веером из центра украинского Триполья, но никак не наоборот. Это украинцы 4,5-5 тыс. лет назад осуществили вторую (после пост-ледниковой) европейскую  волну переселения в Скандинавию и южную Балтику. Из этого логично следует гипотеза создания Стоунхенджа трипольскими переселенцами, которая могла иметь несколько функций.
		</p>
		<p>
			То, что Аркаим был построен потомками трипольцев через 1-1,5 тыс. лет после выхода из Украины не вызывает сомнения. Аркаим, как и вся южноуральская "Гардарика" построены как временная промежуточная стоянка ариев-трипольцев по пути в Индию, Иран и далее на Ближний Восток и просуществовала всего 20-30 лет. Однако их путь туда лежал не напрямую из Украины.

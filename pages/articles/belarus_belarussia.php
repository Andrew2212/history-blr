  		<h1>Беларусь или Белоруссия
		  <br><small>беларуский или белорусский</small>
		  </h1>
<p>
26 лет назад (в 1991 году) БССР стала Республикой <a href="../terms/belarus.php">Беларусь</a>. Однако до сих пор находятся эксперты, считающие правильным написанием "Белоруссия".
</p> 		
<p>
Берег Слоновой Кости, Молдавия, Киргизия, Цейлон. Можно вспомнить еще "отрок, кофий, понеже". Время идет, мир и языковые нормы меняются. 
Можно ностальгировать по "Цейлонскому чаю" или Советскому Союзу. А можно стараться сохранить адекватность окружающему миру. 
</p> 		
<p>
Почему-то для русскоязычного большинства легче оказалось поломать вековой критерий гармотности "кофе — он, мой", чем принять 
правильное написание названия нашей страны. Кыргызстан — пожалуйста. Это легко и по-русски. Беларусь — язык не поворачивается.
Простим Кубе 5 млрд. долга, а Украине за 3 млрд. отключим газ. 
</p> 		
<p>
Когда кто-то, из заставших СССР, по-привычке говорит "Белоруссия" — что ж, это можно понять. Но когда старое название используется из принципа или от ностальгии — 
это грустно. Чем то напоминает "ирокез" на голове пожилого человека, призванный вернуть уходящую потенцию.
<p> 		
  		
  		
<h3>Белорусские нормы и правила</h3>
		
<p>
Верховный Совет БССР 19 сентября 1991 года принял 
<a target="_blank" href="http://www.pravoby.info/docum09/part36/akt36194.htm">закон от 19.09.1991 N 1085-XII</a> 
«О названии Белорусской Советской Социалистической Республики». 
</p>
<p><i>
«Белорусскую Советскую Социалистическую Республику далее называть «Республика Беларусь», а в укороченных и составных названиях — «Беларусь».
Установить, что эти названия транслитеруются на другие языки в соответствии с белорусским звучанием».
</i></p>
<p>
Транслитерация — точная передача знаков одной письменности знаками другой письменности. Т.е. кириллицей везде правильно писать "Беларусь". Латинкой — Belarus. 
Пожелания о транслитерации названия удается выполнять всем, кроме представителей русскоязычного сообщества.
<p>


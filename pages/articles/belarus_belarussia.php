<h3>Российские нормы и правила</h3>

<p>
<i>"НОРМА ЯЗЫКОВАЯ — совокупность языковых средств и правил их употребления, принятая в данном обществе в данную эпоху. Норма противопоставлена системе, понимаемой как присущие тому или иному языку возможности выражения смыслов. Далеко не все из того, что «может» языковая система, 
«разрешается» языковой нормой."</i> <small>Энциклопедия Кругосвет</small>
</p> 
<p>
Федеральный закон Российской Федерации от 1 июня 2005 г. N 53-ФЗ 
<a target="_blank" href="http://www.rg.ru/2005/06/07/yazyk-dok.html">"О государственном языке Российской Федерации"</a> гласит:
</p>	
<p>
<i>"п3. Порядок утверждения норм современного русского литературного языка при его использовании в качестве государственного языка Российской Федерации, правил русской орфографии и пунктуации 
определяется Правительством Российской Федерации."
</i>
</p>	
<p>
<a target="_blank" href="http://docs.cntd.ru/document/1200082595">Общероссийский классификатор стран мира</a> (ОКСМ) — предназначен для идентификации стран мира и используется в процессе обмена информацией при решении задач международных экономических, научных, культурных, спортивных связей и т.д.
Принят и введен в действие постановлением Госстандарта России (федеральный орган исполнительной власти) от 14 декабря 2001 года N 529-ст.
</p>
<p><i>
"Краткое и полное наименование страны — Беларусь, Республика Беларусь"
</i></p>

<h4>Кофе — "оно, моё"</h4> 	
	
<p>
Приказ Министерства образования и науки Российской Федерации (Минобрнауки России) от 8 июня 2009 г. N 195 
<a target="_blank" href="http://www.rg.ru/2009/08/21/russkiy-slovari-dok.html">"Об утверждении списка грамматик, словарей и справочников"</a> 
в своем приложении объявляет <i>Грамматический словарь русского языка: Словоизменение. Зализняк А.А. - М.: "АСТ-ПРЕСС", 2008. - 794 с.</i> носителем 
литературного русского языка. 
</p>
<p>
Словарь объявляет слово "кофе", как "среднего / мужского" рода. 
</br>Анектдот <i>"Адын кофе... Какой грамотный нерусский!... И адын булачка!"</i> 
уже не прокатывает.
</p>


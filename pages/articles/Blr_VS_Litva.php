		<h2>Резюме</h2>
		
		<p>
		Не надо стесняться своей истории. Мы тут не горный анклав, где за 200 колен все переженились со всеми и бдят "чистоту крови" потому, что больше гордиться нечем. Вы (борцы с "Беларусью")
		пытаетесь играть по чужим правилам — как мальчик из хорошей семьи ведтся на гопарское "выйдем-поговорим". 
		Выходит и получает в подворотне по соплям от всей гопоты разом.
		</p>
		<p>
		Если от ВКЛ оторвать все "руское" — "руских" жен всех князей, "рускую" мову и письменность Статутов и 
		<a href="../download/metrica_litwa.php">Метрик Литовских</a>, славянство как основу доминирующего этноса, 
		"руское" имя Погони и "рускую" <a href="../download/chronicles_dubnica.php">клятву Кейстута</a>, "руские" имена (а конкретно белоруские) 
		<a href="../download/spis_voiska.php">Списа войска Литовского</a>, — то останется Самогития-Летува как она есть. 
		</p>
		<p>
		"Беларусь" и "беларусы" — хороший политоним и хорошее название страны (лучшего при вашей жизни не будет). Имеет историю и связь с Краем. При написании на картах
		наших земель всегда шло вместе (пересекалось) с Magni Ducatus. А этноним "литвины" вернется обязательно. Беларус (поляк), беларус (татарин), беларус (литвин).
		</p>
		<p>
		Их гоняли все, кому не лень. Их садили в тюрьмы и сжигали в печах. Они сказали "My life my rules". Сегодня они уважаемые избиратели. 
		Проводят парады в больших городах. Отважные московские джигиты пишут их девиз на своих машинах. 
		</p>
		<p>
		Играйте по своим правилам. Не ведитесь на чужие штампы.
		</p>
		
	
	
				

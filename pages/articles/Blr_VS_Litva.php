		<h2>Холивар</h2>
		<p>
		Но почему-то у многих хороших людей возникает мысль вырвать и стереть Russia Alba из нашей истории. Как империя и большевики пытались стереть 
		Magni Ducatus Lithvania. Само название "Беларусь" вызывает у них ненависть. 
		</p>
		<p>
		Попытка "все упростить" [как и "все поделить"(с)] вызывает недоумение. Сжечь все упоминания о восточной Березине — "границе Литвы и Руси" (западная Березина никогда под этим именем на картах не отражалась).
		Границе, довольно точно соотносящейся со <a href="../download/spis_swidrigailo.php">"Списом городов Свидригайло"</a> 
		времен <a href="../events/war_civil_1432.php">Гражданской войны 1432-38</a>. При этом сегодня Беларусь более моно-этничное государство, 
		чем все ближайшие соседи — впереди только Финляндия.
		</p>
		<p>
		Противников "Беларуси" не смущает даже тот факт, что под этим именем впервые в новейшей истории была провозглашена независимость Края. 
		И не случись БНР — был бы снова 
		<a href="../terms/sewero_zapadny_kraj.php">Северо-Западный край</a> и не было бы повода для дискуссий. Либо было бы принято имя "Кривия" (обсуждалось) и вся "державность и история"
		сосредоточились бы в Витебской области. Было бы круто назваться "Литвой" — но это имя уже 100 лет, как было фактически и юридически было перенесено 
		на <a href="../places/samogitia.php">Самогитию</a>.

		<p>
		п. 1 Для начала, я просто хочу сделать такую монету. Талер с <a href="../terms/blr_gerb.php">Погоней</a>. 
		С гербом, которым гордилась моя страна в 1300-1795 годы. 
		От <a href="../figures/vyten.php">в. князя Витеня</a> до <a href="../events/razbor_rechi.php">разделов Речи Посполитой</a>.
		 Первая монета за 220 лет (ладно, "монетовидный жетон", Ок).
		</p>
		<p> 
		Проходя по гродненской брукаванке, хочу поглаживать пальцами монету со старым гербом. 
		Хочу иметь возможность подарить приехавшим знакомым пристойный сувенир, а не пост-совковый трэш или китч с гербом на всю грудь. 
		И дать такую возможность любому из тех, кому это интересно. 
		</p>
		<p>
		п. 2 Да, хорошо бы замутить свой монетный двор, обеспечить страну приличной сувениркой, чтоб на рыцарских турнирах всадники кидали монеты в толпу, 
		а в руинах турики с металлоискателями разыскивали спрятанные клады. И купить остров. Но сначала п. 1
		</p>
		<p>
		И немного лирики.
		</p>
		<p>
		В обществе просыпается интерес к своей истории. Восстановлен Несвижский замок, строится парк миниатюр крепостей и храмов, 
		энтузиасты проводят экскурсии по старым руинам. 
		И обычное человеческое желание прикоснуться (буквально) и унести с собой нечто, хоть немного аутентичное, натыкается на грустный ответ — "Нету!".
		</p>
		<p>
		<i>"Все идут к истокам, ищут культурную идентичность. Все пытаются определиться и найти свое место через обращение к очень простым формулам 
		своей культурной идентичности. Был создан огромный мировой рынок, где происходит конкуренция.
		</p>

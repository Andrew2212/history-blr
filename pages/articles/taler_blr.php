		<h3>Поиск подрядчика
		</br><small>готово!</small>
		</h3>
		<p class="img-float-left">
		<a href="../../images/img-articles/taler_blr/price.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Беларуский талер с Погоней" src="../../images/img-articles/taler_blr/price_thumb.jpg" height="140">

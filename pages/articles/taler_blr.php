		<p>
		Забавный квест и характерные ответы.
		</p>
		<p>
		Мне сложно это понять. 20 лет назад полстраны работало на оборонку. 10 лет назад можно было без проблем заказать любые фрезерные, граверные, штамповочные работы у заводских умельцев. 
		Сегодня некому сделать приличный значок. Ничего не работает. Грустно.
		</p>
		<p>
		Самые дружелюбные и оперативные — буржуи немецкие. Готовы сделать, подробно отвечают на письма в тот же день, примеры качественных работ, 
		нет проблем с материалом и тп.,
		сами предлагают компромиссные решения (высылка на европейский адрес и тп.)
		Ценник как у белорусов, но на них возьмут 20% коммерческого налога и 30% почтового. Плюс таможенные приключения.
		</p>
		<p>
		Самый кислый ответ — от штамповщиков из Бобруйска. Делать отказываются по идейным соображениям. Доставляет:
		— "Мы можем сделать. Но эти монеты... На них надпись... рисунок... (неразборчивое мычание). 
		Если бы это была "царская россия"... А этот заказ мы делать не будем." 
		Это при том, что заказов не много и парни выставляют на продажу собственное оборудование.
		</p>
		<p>
		Многочисленные "Монетные аттракционы" стремятся все упростить. Их потолок по качеству — кривоватый 2D жетон. Немного не то, что нужно.
		</p>
		<p>
		Есть еще несколько красивых сайтов. Но клиенты в ярости. Постят фотки проваленных заказов на всех доступных ресурсах.
		</p>		
		<p>
		В итоге подрядчик найден, договор на изготовление заключен.
		</p>
		

		<h2>Беларуский талер</h2>
		
		<p class="img-float-left">
		<a href="../../images/img-articles/taler_blr/TalerCard.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Беларуский талер с Погоней" src="../../images/img-articles/taler_blr/TalerCard_thumb.jpg" class="thumbInfoUnSign">
		</a>
		</p>
		
		<p>
		Белорусский талер с Погоней — это <i>первая</i> монета, связывающая современную Беларусь с ее историческим гербом и наследием. 
		Качество его чеканки — выше, чем у современных монет НацБанка РБ. Статус обязывает.
		</p>
		<p>
		Это был проект for fun. Талер с <a href="../terms/blr_gerb.php">Погоней</a>. С гербом, которым гордилась моя страна в 1300-1795 годы. 
		От <a href="../figures/vyten.php">в. князя Витеня</a> до <a href="../events/razbor_rechi.php">разделов Речи Посполитой</a>.
		 Первая монета за 220 лет (ладно, "монетовидный жетон", Ок).
		</p>
		<p>
		Проект задуман и осуществлен в 2016-2017. Первая партия монет отчеканена в рамках проекта на Talaka.by
		<a target="_blank" href="http://www.talaka.by/projects/1468/overview">"Беларуский талер"</a>, вторая — в рамках проекта с Виталием Гурковым
		<a target="_blank" href="https://www.talaka.by/projects/1835/overview">"Погоня — это красиво"</a>
		</p>
		
		<h3>Первая монета</h3>
		<p>
		Первая монета (талер d=24mm). Это не копия исторического оригинала. Просто "вариации на тему".
		Прототипом послужили монеты <a href="../terms/dukes_wkl.php">Сигизмунда II Августа</a> 
		— последнего в. князя Литовского (1544-1572) из династии <a href="../figures/jagellony.php">Ягеллонов</a>.
		 Последние монеты, имевшие на аверсе (или реверсе, если угодно) изображение <a href="../terms/blr_gerb.php">Погони</a>. 
		</p>
		<p>
		Кроме того, на монетах Сигизмунда хорошо закомпонованы Погоня и <a href="../terms/kalumny.php">Калюмны</a> — наш старый символ, не отраженный даже в банкнотах. 
		</p>
		
			

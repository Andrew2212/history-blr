		<h3>Вторая монета</h3>
		<p>
		Второй талер (d=38mm). Прототипом эскиза для проекта послужила Погоня из гербовника Эразма Камина, 1575 год.
		</p>
		<p>
		Надпись по периметру. В ней выбрано наиболее привычное и узнаваемое написание слов. Шрифт латинский, поскольку до разделов Речи Посполитой кириллица 
		ни в нумизматике, ни в картографии нашего Края не использовалась. 
		</p>
		<p>
		Семиотическая (символьная) связь РБ со своим прошлым настолько слаба, 
		что кириллица еще долго будет чужеродной в таком контексте. Да и вообще, современная литературная 
		<a href="../terms/blr_mova.php">беларуская мова</a> сформировалась исключительно на латинке 
		— от Калиновского до Купалы. 
		</p>
		

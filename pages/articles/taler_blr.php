		<p>
		В правление Сигизмунда Августа была заключена <a href="../events/unia_liublin.php">Люблинская уния</a> (1569 год) — Польша и ВКЛ образовали 
		<a href="../terms/rzeczpospolita.php">Речь Посполитую</a>. Следующий в. князь — <a href="../events/confederacia_warshawa.php">Генрих Валуа</a>, 
		— монету, вроде, не чеканил (правил январь-июнь 1574, потом стал королем Франции). А при <a href="../figures/batory.php">Стефане Батории</a>
		(в. князь 1575-1586) на монете чеканили уже полный герб Речи Посполитой. 
		</p>
		<p>
		Да, были еще "боратинки" (арендатор Брестского монетного двора — Тит Ливий <i>Боратини</i>) с Погоней при Яне II Казимире Ваза (1648-1668), возможно, что-то еще. 
		Однако, "Погоня" Сигизмунда Августа, пожалуй, самая стильная своей наивностью, свойственной монетам середины XVI века. 
		</p>
		

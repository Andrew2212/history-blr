		<h2>Prehistory</h2>

		<p>
		Первое, что делает человек, заходя в руины древнего храма — внимательно смотрит под ноги. Помимо практических соображений, в душе живет 
		детское желание что-то найти. Монету. 
		</p>
		<p>
		Логика говорит: "Тут ничего нет". Иррациональное шепчет: "Это место — туннель времени. Эти стены стоят четыреста лет. 
		Надо только хорошо посмотреть!" Это как найти в прибое "куриного бога". Непонятно зачем, но хочется. 

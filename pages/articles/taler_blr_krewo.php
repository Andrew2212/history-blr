		<p>
		Да, были еще "боратинки" (арендатор Брестского монетного двора — Тит Ливий <i>Боратини</i>) с Погоней при Яне II Казимире Ваза (1648-1668), возможно, что-то еще. 
		Однако, "Погоня" Сигизмунда Августа, пожалуй, самая стильная своей наивностью, свойственной монетам середины XVI века. 
		</p>
		<p>
		Кроме того, на монетах Сигизмунда хорошо закомпонованы Погоня и <a href="../terms/kalumny.php">Калюмны</a> — наш старый символ, не отраженный даже в банкнотах. 
		</p>
		<p>
		В правление Сигизмунда Августа была заключена <a href="../events/unia_liublin.php">Люблинская уния</a> (1569 год) — Польша и ВКЛ образовали 
		<a href="../terms/rzeczpospolita.php">Речь Посполитую</a>. Следующий в. князь — <a href="../events/confederacia_warshawa.php">Генрих Валуа</a>, 
		— монету, вроде, не чеканил (правил январь-июнь 1574, потом стал королем Франции). А при <a href="../figures/batory.php">Стефане Батории</a>
		(в. князь 1575-1586) на монете чеканили уже полный герб Речи Посполитой. 
		</p>
		<p>
		<i>"Сигизмунд II не понял бы надпись Belarus — ее тогда не было!"</i>. Он не понял бы даже карту 1739 
		<a href="../download/map_wkl.php">"Regnum Poloniae et Magni Ducatus Lithuaniae"</a>, Matthias Seutter 
		— там Russia Alba и Magni Ducatus Lithuaniae обозначают одно и то же географическое понятие. Мир меняется, знаете ли.
		</p>
		<p>
		Написание "Belarus" через "V" — обычная практика для монет того времени и латинского написания вообще. 
		"Ducatus" и "Sigismund" так же писались черз "v". 
		</p>
		<p>
		Сокращение названия "Magni Ducatus Lithuaniae", как "M. D. Litva" не хуже и не лучше многообразных вариантов сокращений этого названия, 
		применявшихся монетными дворами. Хотя против восприятия надписи как "Беларусь-Литва-одна история" тоже ничего не имею.
		</p>
		

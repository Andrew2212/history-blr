		<p>
		Изделие не копирует существующие или существовавшие денежные знаки. Отличия по материалу и графическому исполнению от монет соответсвующих размеров, 
		находящихся в обращении на территории РБ, а так же то, что себестоимость изделия выше, чем номинал соотвествующих монет 
		— не допускает использования изделия (монетовидного жетона) в мошеннических целях.
		</p>
		
		<h3>
		Немного лирики.
		</h3>
		<p>
		В обществе просыпается интерес к своей истории. Восстановлен Несвижский замок, строится парк миниатюр крепостей и храмов, 
		энтузиасты проводят экскурсии по старым руинам. 
		И обычное человеческое желание прикоснуться (буквально) и унести с собой нечто, хоть немного аутентичное, натыкается на грустный ответ — "Нету!".
		</p>
		<p>
		<i>"Все идут к истокам, ищут культурную идентичность. Все пытаются определиться и найти свое место через обращение к очень простым формулам 
		своей культурной идентичности. Был создан огромный мировой рынок, где происходит конкуренция.
		</p>
		<p>
		Для того чтобы удачно конкурировать, нужно иметь то, что называется "человеческий капитал", а это и есть основы — семья, своя старая культура, 
		традиция,"</i> — Б. Гройс, советский и немецкий искусствовед, профессор философи, писатель, публицист. 
		</p>

		

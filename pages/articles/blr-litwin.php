	<p>
	Казалось бы — да какая разница, как и что называть. Но <i>"исправление вещей начинается с исправления названий"</i>.
	Никто не живет прошлым — живут настоящим, строят будущее (которое быстро становится настоящим). 
	Имя, герб, <a href="../terms/blr_flag.php">флаг</a> — это культурологическая точка отсчета,
	задающая базовые ценности. Именно поэтому в 1995, после избрания первого президента, была проведена <i>очень</i> 

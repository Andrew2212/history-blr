	<p>
	Искусственное противопоставление "литвины-белорусы" порождает у БРСМ-идеологов когнитивный диссонас. Очень хочется возить экскурсии в Несвиж 
	и рассказывать про Статут, Сапегу и культуру, но без терминов "литвин", <a href="../terms/blr_gerb.php">Погоня</a>, ВКЛ — получается корявое фэнтези. 
	<i>"Сапега, Монюшко и Дунин-Марцинкевич — "белорусы"? Да ладно!"</i>
	А принять простую логику, что "литвин" — это старый этноним (самоназвание этноса), а "белорус" — новый политоним (гражданство), религия не позволяет.
	</p>
	<p>
	Это их натужное противопоставление порождает еще одно — абсурдное противопоставление "народного" и "<a href="../terms/shliahta.php">шляхетного"</a> культурного наследия.
	В той же Японии нет конфликта между примитивной крестьянской куклой дарума (неваляшка) и аристократической калиграфией. А на японских крестьянах,
	в общем-то, самурай мог опробовать заточку своего меча (это о равенстве традиционных классов). 
	</p>
	<p>
	И в их "великорусских традициях" не уладывается, что лозунг литвинов "<a href="../places/wilno.php">Вильня</a>-Смоленск-Белосток наш!" 
	— это культурологический посыл для внутренего использования (<i>"Вспомни себя!"</i>)
	и к внешним территориальным претензиям никакого отношения не имеет. 
	</p>
	<p>
	На самом деле "просто белорус" — это просто гражданин РБ (200 лет назад его уверенно <a href="../download/all_peoples_RI.php">называли "поляком"</a>). 
	По национальности и культуре может быть кем угодно (бхуту, например, или никем — таких, увы,большинство).
	А "литвин" — спадкаемца ВКЛ и старых традиций, урожденный <a href="../terms/kraj_tut.php">тутэйшы</a> (что даже не обязательно). 
	И да, абсолютное большинство "литвинов" — белорусы. 
	</p>
	<p>
	И да — литвин, говорящий "по-белорусски", где-то симпатичнее белоруса, говорящего "по-расейски".
	</p>
	
	<p>
		Со времен <a href="../events/uprising_1830.php">восстания 1830 года</a> в ходу были оба термина — Литва и Белая Русь. Без всякого антагонизма употреблялись они (территориально)
		<a href="../figures/chodzko.php">"Товариществом Литовским"</a> и литвинкой из-под Пинска <a href="../figures/skirmunt_helena.php">Хеленой Скирмунт</a> 
		(книгу о ней "З жыцьця літвінкі" написал литвин из-под Солигорска Б. Залеский). 
		Идеи <i>тутэйшай краёвасьці</i> легли в основу <a href="../download/blr_nationalists_1957.php">белорусского национализма</a> 
		(культурно-территориального, не этнического).
	</p>
	

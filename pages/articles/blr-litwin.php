		<p>
		Парадокс. СССР давно развалился, идея вырастить моно-этнос "советских людей" должна была сгинуть вместе с ним. 
		Но глубоко забитым гвоздем она по-прежнему сидит в головах даже не очень старых людей (просьба покликать картинки).
		</p>
		<p>
		Казалось бы — <a href="../terms/belarus.php">Беларусь</a> исторический перекресток дорог "из варяг в греки" и из Европы в Тартарию. Не горное ущелье и не остров в океане. 
		Логично, что по аналогии со всеми соседями, тут нет кристального моно-этноса. Как и у соседей (мазуры-поляки, меря-россияне и тп), тут есть
		татары-белорусы, евреи-белорусы, литвины-белорусы, полешуки-белорусы и тп. Пусть даже и без национальных автономий.
		</p>
		<p>
		Но почему-то от слова <a href="../terms/litwiny.php">литвин</a> у идеологических наследников СССР случается зуд. Возможно, потому, что им дорога советская парадигма
		— <i>"белорусы произошли от скрыжаваньня бульбы и партызан в 45-ом из эмбриона Полоцкого княжества"</i>.
		</p>
		<p>
		Их не смущает мысль о том, что <a href="../terms/blr_etnos.php">белорусский этнос</a> сложился только в XV в., 
		через 100 лет после включения <a href="../terms/polotsk_principality.php">Полоцкого княжества</a> 
		в состав <a href="../terms/wkl.php">Великого княжества Литовского</a>. И если ВКЛ еще можно (но не нужно) с натяжкой 
		называть "белорусским", то Полоцк — только "кривичским". У них аллергия на "старые карты" и "цитаты из документов". 
		Банальное знание истории своей страны для них "вражеское тайное знание", а период XIV-XVIII вв. — Terra Incognita и табу.
		</p>
		<p>
		Что характерно — тезисы о "едином" (и "триедином") моно-этносе на всем пост-советском пространстве процветают только в двух самых совковых 
		государствах — РФ и РБ. И что опять же характерно — и термин "русские" и термин "белорусы" (для обозначения этноса) есть довольно молодой новодел XIX-XX вв.
		И только тут могут жить такие уродливые словоформы, как "русские русские" и "белорусские белорусы" 
		(на фоне вполне корректых "русские татары" и "белорусские поляки").
		</p>
		
		

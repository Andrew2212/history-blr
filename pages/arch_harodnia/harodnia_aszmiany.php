		<p>
		За часам <a href="../events/war_1812.php">расейска-французскай вайны</a> 1812-1814  Ашмяны былi часткова спалены французскімі войскамі, 
		касцёл быў часткова разбураны, у 1822 адбудаваны у стылі класіцызму. 
		Да паўночнага бакавога фасада быў, верагодней за усё, прыбудаваны манастырскі корпус (таму тут і не было вокнаў). Перакрывалі святыню цыліндрычныя 
		скляпенні, над уваходам знаходзіліся хоры на 2-х апорах. Касцёл быў бязвежавы, і толькі ў алтарнай частцы ўзвышалася невялікая вежачка (сигнатурка). 
		Злева перад касцёлам была ўзведзена каменная званіца. 
		</p>
		<p>
		Па здушэньні <a href="../events/uprising_1830.php">Паўстаньня 1830</a> кляштар быў зліквідаваны (1845). Неўзабаве кляштарныя будынкі знішчылі, 
		а касьцёл перарабілі пад склад. У 1920-я ўлады міжваеннай Другой Рэчы Паспалiтай вярнулі касьцёл каталікам, якія аднавілі помнік. 
		Па Другой сусветнай вайне савецкія ўлады адабралі будынак у парафіянаў. 
		</p>
		<p>
		У наш час былы касцёл знаходзіцца ў занядбаным стане, захаваліся толькі парэшткi храма, кляштарныя пабудовы ж былi цалкам разбураны. 
		Нягледзячы на багатую гісторыю і адметную архітэктуру, касцёл францысканцаў у Ашмянах сёння нават не з'яўляецца помнікам архітэктуры. 
		На вяршынi халма, дзе месцiцца былая святыня, ўсталяваны драўляныя крыжы — гэтае месца называюць "кальварыей", цi "Ашмянскай Галгофай".
		</p>
		

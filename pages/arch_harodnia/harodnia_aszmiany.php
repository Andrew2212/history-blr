        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
        Ашмяны. Графiчная рэканструкцыя былога францысканскага касцёла Найсвяцейшай Панны Марыі. Выгляд на перыяд першай паловы XIX ст.
		</p>
		<p>
		Першыя згадкі аб Ашмянах адносяцца да 1341. Расказваючы пра сьмерць <a href="../figures/gedimin.php">Гедыміна</a> і падзел пасадаў паміж яго сынамі, 
		сярод гарадоў, што разам з Вільняй дасталіся Яўнуту, <a href="../download/chronicles.php">"Хроніка літоўская і жамойцкая"</a> называе Ашмяны. 
		Па некаторых даследваньнях Ашмяны ўжо існавалі ў 1040 годзе — пад час паходу Яраслава Мудрага на Літву.
		</p>
		<p>
		У 1398 упершыню згаданы касцёл у Ашмянах, закладзены пад час <a href="../terms/litva_baptism.php">Хрышчэнья Літвы</a>. 
		Каля 1497 у Ашмянах з'явіўся кляштар францысканцаў — Юры Яновіч запісвае фундуш <i>"да Блаславёнай Панны ў Старой Ашмяне да канвента братоў ордэну святога 
		Францыска"</i>. 
		</p>
		<p>
		З пачатку XIV ст. Ашмяны, <i>"будучы месцапрабываньнем княжацкім"</i>, пачалi разбудоўвацца на новым месцы (левабярэжжа Ашмянкі), 
		а першапачатковае паселішча з назвай Старыя Ашмяны (правабярэжжа) была перадана манахам-францысканцам, дзякуючы якiм, гэты раён набыў назву "Францiшканы". 
		</p>
		<p>
		Першапачаткова касцёл i кляштарныя пабудовы былi драўлянымi. Драўляныя будынкі кляштару былi спалены маскавiтамi летам 1659  падчас 
		<a href="../events/blood_potop.php">Крывавага патопа</a> 1654-1667. У час падзей <a href="../events/war_great_north.php">Паўночнай вайны</a> 1700-1720 
		са шведамi у   Ашмяны былi заняты шведскім войскам, а кляштар зноўку быў зруйнаваны. 
		</p>
		<p>
		У 1805 годзе касцёл быў адбудаваны манахамі-францысканцамi і ўяўляў сабой ўжо каменна-драўлянае збудаванне. Згодна з даследваньнямi 
		А. Кушнярэвіча i А. Брыля, ніжняя частка ўсходняй паловы асноўнага аб'ёму была ўзведзена задоўга да пачатку XIX ст. 
		з вялікапамернай цэглы некалькіх фарматаў у гатычнай тэхніцы муроўкі з трохграннай падрэзкай швоў, што маюць таўшчыню 2-3см. 
		Таксама генетычную сувязь з готыкай дэманструюць сляды контрфорсаў, 
		трохгранная алтарная сцяна і вузкае выцягнутае ў вышыню вакно паўцыркульнай формы цэнтральнай грані апсіды. 
		</p>
		<p>
		Ужытая пры ўзвядзенні касцёла цэгла і характар выканання гатычнай тэхнікі муроўкі дазваляюць ніжнюю частку ўсходняй паловы храма датаваць 
		канцом XV ст. 
		</p>
		

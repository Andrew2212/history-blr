		Канстанцінопальскага патрыярхата. Наваградак працягваў заставацца кафедрай праваслаўнага мітрапаліта. На 30-я гады ХVІ ст. тут было ужо 10 цэркваў.
		З XVI ст. у царкве (якая напачатку была праваслаўнай, a пазней уніяцкай) захоўваўся цудоўны абраз Маці Божай Наваградcкай — вядомы многімі цудамі, у тым ліку, па словах Адама Міцкевіча, 
		і яго цудоўным выздараўленнем.

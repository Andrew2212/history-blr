        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
		Гiпатэтычная графiчная рэканструкцыя былога фарнага касцёла — <a href="../places/fara_grodno.php">Фары Вітаўта</a>, — на перыяд пачатку XVIIст. 
		(першапачатковы выгляд мураванай святынi да пазнейшых перабудоў). 
		</p>
		<p>
		Фара Вітаўта заўжды прыцягвала ўвагу краязнаўцаў як важны этап у станаўленні каталіцкай канфесіі ў ВКЛ. 
		Палітычныя амбіцыі <a href="../figures/vitovt.php">Вітаўта</a>, 
		суперніцтва з <a href="../figures/jagiello.php">Ягайлам</a>, імкненне да каралеўскай кароны, арыентацыя ў дзяржаўнай палітыцы на каталіцкае веравызнанне спрыялі заснаванню ім 
		пад час <a href="../terms/litva_baptism.php">Хрышчэньня Літвы</a> першага ў Гораднi касцёла, за якiм замацавалася імя заснавальнiка 
		— Фара Вітаўта ("pharre" (прыход, парафія / нем.). 
		</p>
		<p>
		Першы будынак касцёла быў драўляны, неаднаразова перабудоўваўся, але дадзеныя пра перабудовы касцёла часам не стасуюцца паміж сабою. 
		Вядомая даследчыца гісторыі беларускай архітэктуры А. Квітніцкая вызначыла, што гродзенскі фарны касцёл змураваны з цэглы на сродкі 
		<a href="../figures/batory.php">Стэфана Баторыя</a> 
		прыблізна да 1579-1586 гг. і ў яго ўзвядзенні браў удзел муляр Антоні Дзікрып. 
		</p>
		<p>		
		Звесткі пра архітэктара — аўтара праекта фарнага касцёла, — раней адсутнічалі. 
		Аднак у старажытнай калекцыі чарцяжоў канца ХVІ ст., што паходзяць з Нясвіжа, а цяпер захоўваюцца ў Кіеве, знойдзены план мураванага 
		збудаваньня з надпісам лацінкай: "План касцёла гродзенскага". 
		Высокапрафесійная архітэктурная графіка дае падставы сцвярджаць, што стваральнікам праекта з’яўляецца Джавані Марыя Бернардоні
		— <a href="../terms/jesuity.php">мніх-езуіт</a>, першы архітэктар барока ў Польшчы і <a href="../terms/wkl.php">Вялікім княстве Літўскім</a>. 
		</p>
		<p>
		Першапачатковае аблічча гродзенскага фарнага касцёла можна ўявіць на падставе праектнага плана, а таксама выявы святыні 
		з гравюры Гораднi Тамаша Макоўскага (1600) і больш позніх абмерных чарцяжоў 1803. 
		</p>
		<p>
		На праектным і абмерным чарцяжах паказаны планы трохнававай 8-слуповай святыні даволі значных памераў (58,6 х 23м). 
		Аднак паміж графічнымі матэрыяламі ёсць і пэўныя адрозненні, якія сведчаць, што праект не быў рэалізаваны дакладна. 
		</p>
		<p>
		Касцёл меў структуру "несапраўднай" базылікі (сярэдняя нава была вышэйшай за бакавыя, але 
		не мела верхняга бакавога асвятлення). Усе тры навы звонку былі "апранутыя" ў адзіны суцэльны аб’ём, накрыты высокім клінаватым дахам, 
		што адлюстравана на гравюры Т.Макоўскага. 
		</p>
		<p>
		Шматсхільны дах гранёнай алтарнай часткі быў крыху ніжэйшы за асноўны, у выніку чаго вышынная дынаміка аб’ёма нарастала ад алтара 
		да магутнай шмат’яруснай вежы (з фігурным завяршэннем), якая дамінавала ў кампазіцыі і стварала звыклы для таго часу сілуэт святыні.
		Падобны готыка-рэнесансны тып культавага будынка быў распаўсюджаны ў ХVІ ст. у дойлідстве не толькі у ВКЛ, але i Польшчы, Чэхіі і шэрагу іншых краін
		 Цэнтральнай Еўропы. 
		</p>
		<p>
		 Сцены асноўнага аб’ёма, прэзбітэрыя і ніжняга чацверыка вежы умацаваны контрфорсамі, вырашанымі накшталт пілястраў, 
		 якія ўверсе злучаліся лучковымі аркамі, утвараючы на бакавых фасадах рытмічную рэнесансную аркаду, але з выразнай перавагай вертыкальных 
		 прапорцый у параўнанні з італьянскімі ўзорамі. 
		</p>
		<p> 
		За сваю амаль 400-гадовую гісторыю фара Вiтаўта не раз цярпела ад пажараў. У 1782 г. пажар знішчыў верхнія ярусы вежаў 
		(адлюстравана ў абмерных чарцяжах 1803 г.). 
		</p>
		<p>
		Пасля <a href="../events/razbor_rechi.php">падзелаў Рэчы Паспалітай</a> каталіцкая святыня была перададзена РПЦ пад Сафійскі сабор. 
		У 1804—1807 гг. была праведзена рэканструкцыя ў стылі класіцызму, якая, аднак, істотна не змяніла архітэктуры касцёла. 
		</p>
		<p>
		Магчыма, гэта і не задавальняла <a href="../articles/ortodox.php">артадаксальнае праваслаўе</a>. У 1892 г. святыня гарэла чарговы раз і пасля гэтага ў 1896—1898 гг. 
		акадэмік архітэктуры М. Чагін перабудаваў яе ў псеўдарускім стылі. Над дахам паставілі пяць цыбулепадобных галовак, выразную пластыку фасадаў 
		здрабнілі бясконцыя броўкі, какошнікі, парэбрыкі і іншыя псеўдарускія дэкаратыўныя дэталі. 
		</p>

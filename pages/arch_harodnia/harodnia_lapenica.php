        <p>
        Лапенiца Вялiкая (пасля 194 вёска Грыневiчы, Свiслацкага раёна). Гiпатэтычная графiчная рэканструкцыя былога касцёла Iаана Хрысцiцеля пры кляштары францысканцаў. 
		</p>
		<p>
		Вёска Лапеніца вядомаз XVI ст., калі яна з'яўлялася цэнтрам значных зямельных уладаньняў роду Клачко. 
		У розныя часы Лапенiца належала <a href="../figures/oginskie.php">Агінскім</a>, Булгарыным, Юзэфовічам. Дарэчы пры Агiнcкiх iснавала ўжо дзве Лапенiцы — Малая Лапенiца, дзе жылi сяляне i Вялiкая Лапенiца, дзе месцiлася сядзiба Агiнскiх.
 		</p>
		<p>
		Адным з найбольш вядомых уладальнікаў Лапенiцы быў сын Самуiла Льва Агiнскага — Ян (Яцэк) Агінскі, харунжы Ваўкавыскага павета, дэпутат ад павета на Віленскі Трыбунал, маршалак Ваўкавыскі , подваявода Віленскі і адначасова стараста Крычаўскі. Ваяваў са шведамі. Стаў ваяводам Полацкім і адначасова гетманам польным ВКЛ. У гонар перамогі над туркамі пад Венай у 1683  і ў падзяку за шчаслівае вяртаньне дадому ен ахвяраваў 10 тысяч злотых на будаўніцтва ў Вялікай Лапенице каменнага касцёла і кляштара францысканцаў.
		</p>
		<p>
		 Будаўніцтва мураванага касцёла ў 1690 распачаў сын Яна,  Рыгор (Грыгорый) Агінскі.
		</p>
		<p>
		 Але Рыгор атрымаў у спадчыну ад бацькі не толькі маёнтак, але і ваяўнічы характар — служыў  ротмістрам у арміі ВКЛ, 
		 у <a href="../events/war_great_north.php">Паўночную вайну</a> камандаваў трыма расейскімі палкамі і атрымаў за бітву пад Палтавай тытул Вялікага Гетмана. Быў адным з стваральнікаў 
		 канфедэрацыі супраць Сапегаў пад час <a href="../events/war_civil_1696.php">Грамадзянскай вайны 1696-1702</a>.
		</p>
		<p>
		Ваяўнічыя выправы і эканамічныя цяжкасці не спрыялі хуткаму будаўніцтву — яно расцягнулася на доўгія 102 гады (1690- 1792).
		</p>
		<p>
		Пасьля <a href="../events/uprising_1830.php">Паўстаньня 1830</a>  кляштар францысканцаў быў скасаваны (1835) і маёмасць перададзена ў духоўнае ведамства яго імператарскай вялікасці. 
		Пасьля <a href="../events/uprising_1863.php">Паўстаньня 1863</a> былы касцёл быў перабудаваны ў царкву МП РПЦ (1870). Замест аутэнтычных купалоў былі ўсталяваны праваслаўныя цыбуліны і шчыт над франтальным фасадам. 
		</p>
		

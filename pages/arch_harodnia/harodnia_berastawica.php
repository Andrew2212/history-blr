		Пасьля <a href="../events/uprising_1863.php">Паўстаньня 1863</a> касцел быў перададзены МП РПЦ і прыстасаваны пад праваслаўную царкву — будынак пазбавілі некаторых барокавых дэталяў і  разабралі дзве вежы-званіцы. Менавіта яны ніяк не ўпісаваліся ў фасад будучай царквы. 
		</p>
		<p>
		Калі ў 1866  касцёл аддалі пад царкву, зваліўшы вежы, прыляпіўшы да барочнага будынку псеўдавізантыйскія купалочкі, ачысціўшы памяшканьне ад усялякіх абразоў, канфесіяналаў, алтароў, надгробкаў, каталікі сталі праводзіць набажэнствы ў капліцы на могілках. 
		</p>
		<p>
		За часам міжваеннай Польшчы 1920-1940 святыня была вернута каталiкам. 
		Будынку пашчасцiла захавацца ў добрым стане падчас лiхалецьцяў <a href="../events/world_war_2.php">Другой сусветнай вайны</a>. 
		</p>
		<p>

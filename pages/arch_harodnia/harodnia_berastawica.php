        <p>
		Бераставiца Вялiкая. Графiчная рэканструкцыя былога кармелiцкага касцёла Звеставаньня Панны Марыі (Наведзінаў Найсвяцейшай Панны Марыі) 
		</p>
		<p>
		Першы пісьмовы ўспамін пра Бераставіцу згаданы пад 1506 i змяшчаецца ў прывілеі вялікага князя Аляксандра аб перадачы паселішча за заслугі перад Айчынай 
		ў вечнае карыстанне маршалку гаспадарскаму <a href="../figures/hodkewich.php">Аляксандру Хадкевічу</a>. 
		На <a href="../download/map_radziwill.php">мапе Тамаша Макоўскага</a> ("Мапа Радзівіла Сіроткі", 1613) паселішча пазначана як мястэчка Гарадзенскага павета. 
		</p>
		<p>
		Вядома, што з XVI ст. у Бераставіцы існаваў касцёл (першапачаткова драўляны), спалены пазней маскавiтамi i адбудаваны нанова на месцы згарэлага. Згодна з даследваньнямi Т. Габрусь, вядома, што касцёл асвечаны ў гонар Звеставаньня Панны Марыі біскупам віленскім Бенедыктам Войнам. 
		</p>
		<p>
		Ад 1615 з фундацыі <a href="../figures/hodkewich.php">Гераніма Хадкевіча</a> пачала адбудоўвацца тутэйшая міссія кармелітаў, касцёл якіх да пажару 1741  быў драўляны. Пасля гэтага пажару была не адрамантавана, а нанова ўзведзена мураваная святыня, што цалкам пацвярджае яе архітэктурная стылістыка.
		</p>
		

		<p>
		Генрих Дмоховский (Henryk Dmochowski) 26 октября 1810 - 26 мая 1863 — скульптор, участник восстаний 1830 и 1863 годов.
		</p>
		<p>
		Родился в маёнтке Забалацьце (Миорский-р-н) каля Докшыц. В 1825-28 годах изучал право в Виленском университете. Брал частные уроки скульптуры.
		</p>
		<p>
		Был активным участником <a href="../events/uprising_1830.php">восстания 1830 года</a>. После поражения эмигрировал во Францию  и был приговорен российским судом  к смерти заочно. 
		</p>
		<p>
		Так же, как <a href="../figures/chodzko.php">Михал Ходзько</a>, в 1833 году вступил в союз "Месть народа* (Zemsta Ludu*)" и участвовал в экспедиции полковника Йозефа Заливского — в районе Витебска, затем на Полесье и Волыни.
		</p>
		<p>
		*План "Zemsta Ludu" — небольшие вооруженные отряды эмигрантов должны были проникнуть на земли Речи Посполитой, провозглашая там свободу и равенство, склонить крестьян и горожан к всеобщему восстанию. Над каждыми двумя поветами (уездами) примет командование окружной начальник, над воеводством или губернией - начальник воеводства, а верховная власть над всеми будет в руках Юзефа Зеливского.
		</p>
		<p>
		В 1834 был арестован, осужден и до 1841 находился в заключении в австрийской крепости Куфтайн, где начал заниматься скульптурой. Будучи в тюрьме, он из хлебного мякиша лепил фигурки тех, кто сидел с ним камере. Эти фигурки были переправлены в Англию и демонстрировались на выставке.
		</p>
		<p>
		После освобождения, выехал во Францию, где поступил на учёбу в Парижскую Школу изящных искусств. Учился под руководством Давида д'Анже и Франсуа Рюда, вместе с которым в 1846-1847 работал над созданием саркофага Наполеона I.
		</p>
		<p>
		В 1846 вновь прибыл на территорию Австрийской империи, где участвовал в уличных боях во Львове. Позже  выполнил проект  памятника погибшим повстанцам в виде заостренной арки из острых кос, которую Ц. К. Норвид приветствовал как начало польского национального искусства.
		</p>
		<p>
		В 1852 отплыл в Соединенные Штаты. Под псевдонимом Генри Сандерс в США открыл школу скульптуры, собственную мастерскую, в которой создал портретные медальоны и бюсты Джорджа Вашингтона, Бенджамина Франклина, Томаса Джефферсона, которые и сегодня украшают Капитолий (Вашингтон).
		</p>
		<p>
		К наиболее удачным работам скульптора относятся бюсты <a href="../figures/kostiushko.php">Тадеуша Костюшко</a> (1856) и К. Пулавского (1858) — оба произведения закуплены Конгрессом США.
		</p>
		

		<p>
		После смерти императора Николая I и последовавшей амнистией, вернулся в Вильню, где работал скульптором. Здесь создал намогильный памятник Барбары Радзивилл, фигуры святого Владислава для виленского кафедрального собора, памятник Владиславу Сыракомле. Состоял членом Виленской археологической комиссии.
		</p>
		<p>
		Падчас <a href="../events/uprising_1863.php">паўстаньня Каліноўскага</a> 1863-1864 гадоў Дмахоўскі стаў паўстанцкім камісарам Дзісенскага павету. Ён загінуў 26 траўня 1863 году каля фальварку Парэчча пад Докшыцамі ў адным зь першых баёў з царскімі войскамі. Пахаваны на старажытных могілках каля аднайменнай вёскі (цяпер — Бірулі Докшыцкага раёну).


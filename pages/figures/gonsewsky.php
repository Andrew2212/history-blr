		 Участвовал в битве при Кирхгольме.
		</p>
		<p>
		Был регентом государственной канцелярии при <a href="sapega.php">Льве Сапеге</a>. В 1599 и 1600 годах участвовал в посольствах в Москву.
		</p>
		<p>

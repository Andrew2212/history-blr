		<p>
		1318 Ольгерд женится на дочери витебского князя Марии Ярославне. После смерти тестя Ярослава становится князем Витебским. 
		Был хозяином родового замка в <a href="../places/krevo.php">Крево</a>. 
		</p>
		<p>
		1341 Умер в. князь Гедимин. По завещанию <a href="../terms/wkl.php">княжество Литовское</a> было поделено между семью его сыновьями. 
		В 1345 году Кейстут, по уговору с Ольгердом, занял Вильну (где княжил их брат Евнутий) и передал виленские земли Ольгерду, 
		который принял титул Великого князя.
		</p>
		<p>
		Ольгерд и Кейстут заключили союз.
		Борьбу  с Орденом за <a href="../places/samogitia.php">Самогитию</a> вёл главным образом Кейстут. 
		Ольгерд стремился усилить влияние ВКЛ в Новгороде, Пскове, Смоленске 
		и на <a href="../download/zemie_ruskie.php">Землях Русских</a> (Украина).
		</p>
		<p>
		В войнах за галицко-волынское наследие — серия конфликтов за остатки Галицко-Волынского княжества — Ольгерд вел упорную борьбу с Польшей 
		за Волынь. 
		В итоге, по договору от 1377 уделы Берестейский, Владимирский и Луцкий были признаны за ВКЛ, 
		а Холмская и Белзская (Львовская обл.) земли отошли к Польше.
		</p>

		
		

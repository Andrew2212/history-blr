		<p>
		Ольгерд (1296 — 24 мая 1377) — великий князь литовский (1345-1377), сын <a href="gedimin.php">Гедимина</a> и Евны Полоцкой, 
		брат <a href="../figures/kejstut.php">Кейстута</a>. 
		Завершил собирание <a href="../download/blr_people.php">земель белорусских</a> в единое государство — <a href="../terms/wkl.php">Великое княжество Литовское</a>. 
		</p>
		<p>
		<i>"Ольгерд и Кейстутий были в великой любви, милосце и ласке".</i>
		</br>В течение четырёх лет после смерти Гедимина (1341—1345) централизованная власть в ВКЛ, распавшейся на самостоятельные уделы, 
		фактически отсутствовала.
		В 1345 году Кейстут, по предварительному уговору с братом, занял <a href="../places/wilno.php">Вильну</a>, где сидел младший сын Гедимина, Евнутий, 
		и пригласил на великое княжение Ольгерда. С тех пор до самой смерти Ольгерда братья действовали солидарно.
		</p>
		<p>
		В правление Ольгерда — 32 года — территория страны  увеличилась вдвое. Он разбил войска Золотой Орды 
		в <a href="../events/sinie_wody.php">битве на Синих Водах</a> и присоединил Волынь, Киевщину и Подолье. ВКЛ  стало крупнейшим государством Европы.
		</p>
		<p> 
		<i>«Абы Москва памятала, как копие свое Ольгерд крушил о браму московскую»</i> 
		</br><small><a href="../download/chronicles.php">Хроника Литовская и Жмойтская</a></small>
		</p>
		<p>
		Ольгерд вел успешное противостояние с Москвой — в войне 1368-1372 годов трижды разбивал (1368, 1370, 1372) московскую армию и в 1368 и 1370 
		осаждал Кремль (благодаря чему он ныне часто выступает, как мем). 
		</p>
		<p>
		Ольгерд добивался независимой от Москвы православной митрополии в ВКЛ — в 1354 он <i>"сумел договориться с Константинополем 
		о поставлении в митрополиты на землю Литовскую и Волынскую Романа родом тверитянина"</i> <small>(с) www.church.by</small>. 
		
		Ольгерд построил несколько старейших в <a href="../places/wilno.php">Вильно</a> православных церквей — церковь Св. Николая, 
		Пятницкую (1345), Пречистенскую (1346) и Свято-Троицкую церкви, монастырь для сестры Гедимина.
		</p>
		<p>
		С его именем связывают <a href="../terms/religion_blr.php">икону Остробрамской Богоматери</a>. По одной версии, она была прислана греческим императором Иоанном Палеологом 
		в честь принятия Ольгердом христианства. По другой — образ был привезен им в Вильну после успешного похода в Крым в 1363 году из г. Корсуни (Херсонес).
		</p>
		

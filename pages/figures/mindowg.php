		<p>
		Впервые Миндовг упомянут среди литовских князей в Литовско-Волынском договоре 1219 года:
		</br><i>
		"Божиимъ повелениемъ прислаша князи Литовьскии к великои княгини 
		Романовои и к Даниловои и к Василкови миръ дающи бяхо же имена Литовьскихъ князеи се старшии 
		Живинъбоуд Довьят Довспроункъ брат его Мидогъ брат Давьяловъ Виликаил
		а жемоитскыи князъ Ердивилъ Выконтъ а Роушьковичевъ Кинтибоутъ Вонибоутъ Боутовит Вижеикъ 
		и сын его Вишли Китени Пликосова а се Боулевичи."
		</i>
		</p>
		

		
		<p>
		Поскольку "среди всех северных народов одни лишь славяне были упорнее других и позже других обратились к вере" (Гельмгольд, "Славянская хроника", XII век,  
		<a target="_blank" href="https://archive.org/stream/helmoldipresbyt00pertgoog#page/n6/mode/2up">оригинал</a> / 
		<a target="_blank" href="http://www.vostlit.info/Texts/rus/Gelmold/framegel1.htm">перевод</a>), 
		Миндовг не был ревностным христианином. 
		</p>
		<p>
		1246 В Густынской летописи упоминатся его крещение по православному обряду для принятия княжения в Новогрудке.
		</p>
		<p>
		1251 году Миндовг крещен по латинскому обряду
		</p>
		<p>
		1253 Миндовг и его жена были коронованы папой Иннокентием IV 
		в Новогрудке как литовские король и королева. Новогородским князем становится сын Миндовга — Войшелк.
		</p>
		<p>
		<small>В 1254 году тот же папа, Иннокентий IV, короновал Галицко-Волынского князя, Даниила Романовича, королем Руси (Rex Russiae). 
		Сын князя Даниила — Шварн, станет великим князем Литовским после сына Миндовга — Войшелка. В честь другого сына короля Руси — Льва, 
		был назван город Львов</small>
		</p>
		<p>
		С коронацией Миндовга связывают найденный в <a href="../places/nowogrudok.php">Новогрудке</a> и хранящийся в Эрмитаже "кубок св. Ядвиги".  
		Согласно легенде, эти кубки принадлежали св. Ядвиге Силезской (1174-1243) и использовались для коронации, в них вода превращалась в вино. 
		Сегодня в мире известно 14 таких кубков, и иногда их сравнивают с Чашей Грааля. 
		Британский музей относит чаши этого типа к ста самым важным произведениям общечеловеческой материальной культуры.
		<p>
		

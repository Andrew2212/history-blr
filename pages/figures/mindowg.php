		</br>С 1245 года в течение следующих <i>трех лет</i> Людовик IX собирал церковную десятину и в 1248 году сформировал 15-тысячное войско (в очень централизованной стране).
		</br>Собрать и вооружить такое войско из жмудинов в <a href="../places/samogitia.php">Самогитии</a>-Летуве, где не было городов, устойчивых контактов с внешним миром, примитивные ремесла и культура и совершить 
		поход за 500 км — такое мог только сказочный МиндАУГАС. ] 
		</small>
		</p>
		

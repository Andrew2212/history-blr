			</br><small>Король пруссов и литвинов</small>
		</a>
		</p>
		<p>
		В XII-XIII веках происходит миграция <a href="../terms/yotving.php">пруссов</a> и 
		<a href="../terms/lutizy.php">полабских славян</a> на западные и северо-западные белорусские земли 
		— земли <a href="../terms/yotving.php">ятвягов</a>, которые "были единого народу з литвою и з прусами старыми, з готов пошли".
		</br>К 1248 году под предводительством прусского короля Миндовга формируется <a href="../places/litwa.php">княжество Литовское</a> 
		(Новогрудок, Волковыск, Слоним) со столицей в <a href="../places/nowogrudok.php">Новогрудке</a>.
		</p>
		<p>
		<i>"132. В этом же году окрещенные пруссы со своим королем Мендольфом из-за многочисленных тягот, причиненных им крестоносцами, 
		оставив христанскую веру, которую приняли ранее, ушли с некоторыми братьями Ордена крестоносцев к литвинам, смело к ним присоединившись...
		</p>
		<p>
		133. В этом же году упомянутый Мендольф, собрав множество, до тридцати тысяч, сражающихся: своих пруссов, литвинов и других языческих народов, 
		вторгся в Мазовецкую землю. Там прежде всего он разорил город Плоцк, а затем города и деревни всей Плоцкой земли жестоко опустошил мечом 
		и пожаром, разбоями и грабежом. Напав также на Пруссию, разрушил города, уничтожил почти всю землю Пруссии, 
		и его окрещенные пруссы учинили жестокую резню христианского народа."
		</i>
		</br><small>Monumenta Poloniae Historica, XII-XIV вв., 132-133</small>
		</p>
		<p>
		<small>

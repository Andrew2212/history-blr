		<h3>Войшелк</h3>
		
		<p class="img-float-left">
		<a href="../../images/img-places/nalsen/mendog_voiszalk.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Нальшаны, Дяволтва" src="../../images/img-places/nalsen/mendog_voiszalk_thumb.jpg" height="140">
		</br><small>1262-64 Войшелк 
		</br>и смерть Миндовга</small>
		</a>
		</p>
		<p>
		Войшелк (1223-1267) — в. князь Литовский (1264-67), старший сын Миндовга, отомстивший за смерть отца и присоединивший <a href="../places/nalsen.php">Нальшаны и Дяволтву</a>.
		</p>
		
		<p>
		<i>"
		Еще при жизни отца В. управлял Новогрудским княжеством и жестоко преследовал христиан; 
		но Даниловичи галицкие обратили его в христианство, и он ушел в Холм (или Галич), где принял иноческое пострижение и остался жить в его обители,
		 а позже жил в построенном им монастыре в Новогрудке (Лавришенский монастырь). 
		 </p>
		<p>
		 В 1264 (1265) году Миндовг был убит. Власть захватил Тройнат. 
		 При первом же известии об этом Войшелк ушел в Русь, в Пинский монастырь. Тройнат вскоре был убит; еще раньше погиб брат его, Товтивил. 
		 И собралась старшина для выбора князя. Остановились на Войшалке. В Пинск отправлено было посольство. 
		 Долго отказывался В., указывая на свое положение как монаха, но наконец согласился. 
		 </p>
		<p>
		 Теперь он думал только о мести отцовским врагам. Прибыв в Литву, он велел задушить множество жмудских, ятвяжских и литовских панов. 
		 Сотни литовцев бежали на Волынь и в Псков. Большая часть литовских племен признала В. своим князем. 
		 Усмирив врагов, В. возвратиться в монастырь и, отдав литовский стол зятю своему Шварну Даниловичу, ушел в Угровеск, в монастырь. 
		 </p>
		<p>
		 Лев Данилович, досадовавший на В. за отдачу им княжения не ему, а Шварну, пригласил В. в Владимир-Волынский на совещание о каком-то важном деле, 
		 где на другой же день В. был убит Львом. 
		 </p>
		<p>
		 Длугош дает другие мотивы в объяснение убиения В. Львом: он утверждает, что В. по смерти Даниила бросился на Галицкое королевство и Волынь, 
		 чтобы, выгнав оттуда Льва, присоединить этот край к Литве, за что Лев и отмстил ему во Владимире."</i> 
		 </br><small>Энциклопедический словарь Брокгауза и Ефрона</small>
		</p>
		<p>
		В 1253 году Войшелк заключил мир с галицко-волынскими князьями, выдал замуж за <a href="shwarn.php">Шварна</a> свою сестру и передал Новогородское 
		княжество его брату, Роману Даниловичу. Сам же пошёл в Полонинский монастырь (Волынь), где принял постриг под именем Лавриш.
		</br>После трёх лет жизни в  монастыре отправился в паломничество на гору Афон. 

		</p>
		<p>
		В 1261 году Миндовг снова вернулся в язычество.
		</p>		
		<p>
		В 1263 году Миндовг был убит (Rex Mendoch occiditur). По одним источникам поляками, по другим — литвинами 
		</br>Monumenta Poloniae Historica, 
		<a target="_blank" href="http://polona.pl/item/508894/3/">оригинал</a> /
		 <a target="_blank" href="http://www.vostlit.info/Texts/rus/Chron_Pol_majoris/frametext5.htm">превод</a>
		</p>
		
		
		<p>
		В 1263 году, после убийства Миндовга, Войшелк на короткое время бежал в <a href="../places/pinsk.php">Пинск</a>. Но уже в 1264 году вместе с пинским войском возвратился в Новогородок, где соединился с дружинами новогородских бояр и пошёл устанавливать свою власть в Литве.
		</br>Княжил вместе со Шварном, с помощью которого завоевал Нальшаны и Дзяволтву. В 1267 году оставил власть <a href="shwarn.php">Шварну</a> и ушёл в монастырь на Волыни.
		</p>
		
		<h3>PS.</h3>
		<p>
		Имя Миндовга в исторических документах на разных языках писалось по разному: Мидог, Mendolphus, Mendoch, Mindowe.
		</br>Но <i>Миндаугасом</i> (Mindaugas) он не был даже в своих письмах на латыни при переписке с папой. 
		(См. <a target="_blank" href="https://archive.org/stream/codexdiplomatic00lithgoog#page/n24/mode/2up">Codex Diplomaticus Lithuaniae</a>) 

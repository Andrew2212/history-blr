		<p>
		Ягеллоны — королевская династия, потомки <a href="../figures/jagiello.php">Ягайло</a> и Софии Гольшанской (Друцкой). 
		</br>Правители ВКЛ, Польши, Чехии и Венгрии в XIV-XVI веках. Девять славянских королей.
		</p>		
		<p>
		1385 Заключена <a href="../events/unia_krewo.php">Кревская уния</a> — договор о династическом союзе между 
		<a href="../terms/wkl.php">Великим княжеством Литовским</a> и королевством Польша.
		</p>		
		<p>
		1386 В. князь Ягайло женится королеве Ядвиге и становится королём Польши под именем Владислав II. После смерти Ядвиги, его брак с Софией, княжной Гольшанской и Друцкой, положил начало королевской династии Ягеллонов.
		</p>		
		<p>
		Благодаря Кревской унии образовалась федерация, представлявшая для других народов большую притягательную силу. 		
		</p>		
		<p>
		Чехи, отложившиеся в гуситскую эпоху от Сигизмунда Люксембургского, предлагали корону сначала Ягайло, потом Витовту, но чешско-польская уния не осуществилась вследствие противодействия католического духовенства, опасавшегося распространения в Польше гуситской ереси. Когда умер Сигизмунд и чешский престол занял Альбрехт, гуситы возобновили мысль о чешско-польской унии: чешская корона была предложена второму сыну Ягайло, Казимиру, но план этот снова потерпел неудачу по тем же причинам. 
		</p>		
		<p>
		Вскоре после того Ягеллоны приобрели корону другой страны — Венгрии, когда на престол ее взошел (1440) польский король Владислав III Варненьчик,
		 сын Ягайло и Софии Гольшанской (погиб в бою с турками под Варной в 1444).
		</p>		
		<p>
		В 1471 году Владислав II Ягеллон, сын Казимира IV, внук Ягайло, был приглашён на чешский трон, а в 1490 году он надел ещё и венгерскую корону. 
		Таким образом, в конце XV - начале XVI века под властью Ягеллонов оказалась вся Центральная Европа.
		</p>
		

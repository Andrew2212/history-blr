		</p>		
		<p>
		В Венгрии Людовика II сменил князь Трансильвании Янош I Запольяи, будущий муж Изабеллы Ягеллонки (его двоюродной сестры) — дочери Сигизмунда I Старого. 
		Преемник сына Яноша I Запольяи — князь Трансильванский <a href="../figures/batory.php">Стефан Баторий</a> возьмет в жены Анну Ягеллонку, 
		сестру Изабеллы, и в 1575 году станет королем Польши и в. князем Литовским.  [ <small><i>Очень сложные родственные связи</i>, — ред.</small> ]		

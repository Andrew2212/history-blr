		<p>
		Тышкевичи — род Великого княжества Литовского известный с XV века, носители типичной беларуской фамилии и герба Лелива, "граф на Логойске и Бердичеве". 
		Центром семейной ординации был Логойск. Имели также  владения в местечках  Биржи, Паланга, Бердичев.
   		</p>
		<p>
		* Ординация — форма майората, при которой земельные владения магната (т. н. ордината) после его смерти переходили к старшему сыну как неделимое 
		и неотчуждаемое имущество. Усадьбы в составе ординации нельзя было ни продать, ни заложить. В пределах ординации глава семейства 
		обладал почти суверенными правами, не многим уступая в самостоятельности князьям Священной Римской империи.
		</p>
		<p>
		Уроженцы Логойской ординации были Воеводами Брестскими, Киевскими и Черниговскими, 
		региментариями (командирами посполитого рушения, замещали в. гетмана) войска ВКЛ, войтами Минскими (главой магистрата), 
		старостами Жмудскими.
		</p>

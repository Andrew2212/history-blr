	<p>
	Виленский музей древностей был открыт 1 января 1856 в помещении библиотеки Виленского университета. При музее была создана Виленская археологическая комиссия для  археологических изысканий в Северо-Западном крае.  Археологическая комиссия 10 лет (1855-65).
	</p>
	<p>
	Просуществовал музей только 10 лет. После подавления <a href="../events/uprising_1863.php">Восстания 1863</a> власти начали войну с "польской интригой". Все, связанное с "польской историей" реквизировалось археографической комиссией,  созданной генерал-губернатором М. Н. Муравьевым 
	</p>
	<p>
	Часть коллекции Константин Тышкевич успел передать в музеи Кракова и Варшавы, но большая часть была изъята и передана в Московский Румянцевский музей.
	Оставшиеся экспонаты легли в основу Национального музея Летувы. 
	</p>	
	<p>
	Братья Константин и Евстафий Тышкевичи были почетными членами Академии наук в Петербурге, Академии наук в Швеции, Института археологии в Лондоне, многочисленных научных обществ.
	</p>
	
	<h2>Свислочская гимназия</h2>
	
	<div class="img-float-left">
		<a href="../../images/img-figures/tyszkewiczy/gimnasium.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Винцент Тышкевич и Свислочская гимназия" src="../../images/img-figures/tyszkewiczy/gimnasium_thumb.jpg" class="thumbInfo">
		<p class="img-float-center">
		<small>Винцент Тышкевич
		<br>мест. Свислочь</small>
		</p>
		</a>
	</div>
	
	<p>
	Свіслацкая гімназія — адна з 8 сярэдніх навучальных устаноў адкрытых на тэрыторыі Беларусі і Літвы паводле плану ўтварэньня 
	Віленскай навучальнай акругі. Заснавана намаганьнямі Вінцэнта Тышкевіча. З 2005 — гімназія № 1 імя К. Каліноўскага.
	</p>
	<p>
	Гімназія ў Свіслачы была базавай школай Віленскага ўніверсітэта. Параграф 1 Устава Віленскай навучальнай акругі: 
	<i>"Свислочская гимназия передается в непосредственное ведение университета"</i>. 
	Таму гімназія ў Свіслачы адрознівалася высокім узроўнем навучання і мела вялікую вядомасць. 
	Гімназія была даволі буйной установай — тут навучаліся ад 200 да 300 вучняў. Навучаньне разлічвалася на 7 год.
	</p>
	<p>
	Винцент Тышкевич ( 1757- 1816) — сын первого командира 1-й кавалерийской бригады ВКЛ (основанной после Первого раздела Речи Посполитой), 
	Антония Казимира Тышкевича и графини Терезы Тизенгауз. Был женат на племяннице последнего короля Речи Посполитой, 
	<a href="../figures/poniatowski.php">Станислава Августа Понятовского</a>. 
	Избирался сенатором ВКЛ, Писарем великим литовским , Секретарём великим литовским.
	</p>
	<p>
	Учился в Парижском университете, после смерти отца в 1778 стал хозяином местечка Свислочь. Он хотел превратить местечко Свислочь 
	в город с развитой промышленностью — были организованы лесные промыслы, смолярни, бумажная фабрика.
	</p>
	

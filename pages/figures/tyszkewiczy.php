	<p>
	Винцент Тышкевич обустроил Свислочь, организовав застройкой улицы — построил усадьбу, театр, храм, базарную площадь, разбил парк. 
	Свислочская гимназия, основанная в 1805 при активном его содействии, наряду с Виленским университетом, была гнездом свободной мысли, 
	породившим <a href="../download/filomaty_note.php">филоматов и филаретов</a>. 
	Ее учениками были <a href="../figures/orda.php">Наполеон Орда</a>, <a href="../figures/kalinowski.php">Винцент (Кастусь) Калиновский</a>, Р. Траугутт, В. Гельтман и многие другие достойные люди своего времени. 

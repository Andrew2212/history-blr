	<h2>Музей древностей</h2>
	
	<div class="img-float-left">
		<a href="../../images/img-figures/tyszkewiczy/pathers.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Тышкевичи Евстафий и Константин" src="../../images/img-figures/tyszkewiczy/pathers_thumb.jpg" class="thumbInfo">
		<p class="img-float-center">
		<small>Евстафий и Константин
		<br>Тышкевичи</small>
		</p>
		</a>
	</div>
	
	<p>
	В 1842 году братья Константин (1806-68) и Евстафий (1824-73) Тышкевичи открыли в Логойске первый в Беларуси историко-археологический музей. Братья Тышкевичи были известными археологами, фактически основателями белорусским научной археологии.
	</p>
	<p>
	Они раскопали и изучили несколько сотен древних курганов, исследовали и описали древности почти всей северо-западной возвышенности Минщины. Музей Тышкевичей находился в двухэтажном дворце, построенном в 1814-19. В нумизматической коллекции Тышкевичей насчитывалось 1140 медалей и монет. Среди 3000 книг  его библиотеки было около 500 древних памятников отечественного книгопечатания, а также рукописи, географические карты и грамоты.
	</p>
	<p>
	Из уникальных экспонатов музей имел крест из серебра и золота, пожалованный <a href="../terms/dukes_wkl.php">Сигизмундом I Старым</a> в 1514  
	<a href="../arch_mensk/mensk_hajna.php">костелу местечка Гайна</a> и два меча с портретами <a href="../figures/batory.php">Стефана Батория</a> на эфесах.
	</p>
	<p>
	В музее была собрана большая коллекция оружия и военных доспехов. Была также богатая коллекция рушников, и скатертей, которые ткались на фабрике, основанной Константином Тышкевичем.
	Там же хранились портреты представителей 500-летнего логойского рода Тышкевичей.
	</p>
	<p>
	Логойск был небольшим местечком, всего 2 тыс. жителей. И Евстафий хотел создать публичный музей в Вильно или в Минске.
	Петербург дал разрешение на создание Виленского музея древностей. В основу его легли Логойские коллекции. Нумизматику, геральдику, книги передал
	 В. Сырокомля. Личные произведения, рукописи, редкие исторические вещи пожертвовали 
	 <a href="../download/kirkor.php">А. Киркор</a>, <a href="../figures/chodzko.php">И. Ходько</a>, Т. Нарбут и др.
	</p>
	

		<h3>Евфимий (Ефим) Фёдорович Карский</h3>
		
		<p class="img-float-left">
		<a href="../../images/img-figures/karski/Yefim_Karskiy.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img alt="Ефим Карский" src="../../images/img-figures/karski/Yefim_Karskiy.jpg" height="140" >
			</a>
		</p>
		
		<p class="img-float-right">
		<a href="../../images/img-terms/slavs/blr_etnos.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img alt="Этнографическая карта белорусского племени" src="../../images/img-terms/slavs/blr_etnos_thumb.jpg" height="140"/>
		</a>
		</p>
				
		<p>
		Евфимий (Ефим) Фёдорович Карский (01.01.1860 - 29.04.1931) — филолог-славист, палеограф и этнограф, академик Петербургской Академии наук (1916). 
		Почётный член Витебской губернской учёной архивной комиссии.
		</p>		
		<p>
		Исследователь языка, литературы и культуры белорусского народа и памятников письменности.	
		</p>		
		<p>
		Евфимий Карский родился в селе Лаша Гродненской губернии. По окончании семинарии он поступил в Нежинский историко-филологический институт,
		</p>		
		<p>
		1885 Окончив институт, Карский преподает  во 2-й Виленской гимназии. Собирает этнографический материал, печатается в научных журналах. 
		</p>		
		<p>
		1893 Зачислен преподавателем в Императорскиом Варшавском университете, через год стал экстраординарным профессором кафедры 
		русского и церковнославянского языков. 
		</p>		
		<p>
		1901 Становится лауреатом Ломоносовской премии. Избран членом-корреспондентом Императорской Российской Академии наук. 
		В 1902 году Карский стал деканом историко-филологического факультета.  
		</p>		
		<p>
		1903 Ефим Карский предпринял этнографическую экспедицию по Беларуси, по итогам которой был издан первый том главного труда учёного 
		— работы "Белорусы", заслужившей впоследствии название "энциклопедии белорусоведения". 
		</p>		
		<p>
		1905-1910 Ректор Варшавского университета. 1905-1917 — главный редактор журнала "Русский филологический вестник".  
		</p>		
		<p>
		1916-1922 Становится  действительным членом Академии наук,  профессором Петроградского университета, 
		действительным членом Института белорусской культуры, членом Чешской Академии наук. Работал директором Музея антропологии и этнографии. 
		</p>		
		<p>
		При Советской власти научная карьера Ефима Карского не состоялась. В 1919 был арестован, освобожден. К 1929 году снят с ряда должностей. 
		Умер в 1931 году, похоронен на Смоленском кладбище Петербурга.
		</p>
		
		<h3>Митрофан Викторович Довнар-Запольский</h3>
		
		<p class="img-float-left">
		<a href="../../images/img-figures/karski/Downar-Zapolski.jpg" class="highslide"

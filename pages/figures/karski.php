		</a>
		</p>
		
		<p>
		Митрофан Викторович Довнар-Запольский (14.06.1867 - 30.09.1934) 
		— белорусский историк, этнограф, фольклорист, экономист, основоположник белорусской национальной историографии. 
		Доктор исторических наук (1905), профессор (1902).
		</p>		
		<p>
		Родился в семье служащего из мелкопоместного <a href="../terms/shliahta.php">шляхетского рода</a> герба Побог в Речице (Минская губ.). 
		</p>		
		<p>
		Учился в народном училище ст. Барановичи, 
		школе г. Пловдива (Болгария), народном училище г. Речица, прогимназии г. Мозыря, а также в 1-й городской гимназии Киева, 
		из которой был отчислен весной 1888  "за предосудительное и вредное поведение, выразившееся в чтении найденных 
		у него книг преступного содержания", а именно поэмы Т. Шевченко "Мария", письма к А. Герцену и эмигрантского сборника "Громада".
		</p>		
		<p>
		М. Довнар-Запольский ставит перед собой нелёгкую задачу — создать научное сообщество для расширения знаний о прошлом и настоящем Беларуси, 
		а в дальнейшем открыть белорусский университет.  
		</p>		
		<p>
		1889 Поступает на историко-филологический факультет Киевского университета, который успешно окончил; ученик В. Б. Антоновича.
		</p>		
		<p>
		1896-1898 годах служил в Московском архиве Министерства Юстиции. 
		</p>		
		<p>
		1898-1901 годах работал в Московском, в 1901-1919 - в Киевском университете. Создал в Киеве свою научную школу.
		</p>		
		<p>
		1905 Открывает в Киеве высшие женские вечерние курсы и высшие коммерческие курсы, преобразованные затем в коммерческий институт. 
		Сотрудничал с газетами "Белорусское слово", "Белорусское эхо", "Вольная Беларусь", 
		</p>		
		<p>
		25 марта 1918 года в Минске было объявлено о создании <a href="../terms/bnr.php">Белорусской народной республики</a> (БНР). 
		Довнар-Запольский активно включился в происходящее, и в марте 1918 вошёл в состав комиссии 
		правительства БНР по открытию первого белорусского университета. 
		После разгрома БНР Советской Россией работа по созданию университета в Минске остановилась, многие учёные уехали в эмиграцию. М. Довнар-Запольский издал в это время брошюру 
		«Основы государственности Беларуси». Книга была издана на белорусском, английском, немецком, польском, русском и французском языках,
		 но долгие годы была запрещена и вышла в свет только в 1990 году. 
		</p>		
		<p>
		1925-1926 Заведующий кафедры истории Беларуси в Белорусском государственном университете. С осени 1926 работал в Москве, где скончался 
		в 1934 году. Похоронен на новом Донском кладбище, в центральном зале бывшего главного здания Донского крематория.
		</p>				
								
			

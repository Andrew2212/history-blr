		<p>
		После окончания Гражданской войны остался в БССР.  Выступал с лекциями в БГУ и Белорусском педагогическом техникуме, 
		в 1922 году был избран членом Инбелкульта. 
		</p>
		1921-27 Язэп Лёсік выдаў навуковыя мовазнаўчыя працы: "Практычная граматыка <a href="../terms/blr_mova.php">беларускае мовы</a>", 
		"Беларуская мова: Пачатковая граматыка", 
		"Беларуская мова: Правапіс", "Сінтаксіс беларускае мовы", "У справе рэформы нашае азбукі", "Спрашчэнне правапісу", 
		"Да рэформы беларускага правапісу", "Граматыка беларускае мовы: Фанетыка", "Граматыка беларускае мовы: Марфалогія". 
		Асобныя з іх вытрымалі некалькі перавыданняў. Удзельнік Акадэмічнай канферэнцыі па рэформе беларускага правапісу і азбукі (1926).		
		<p>
		Осенью 1922 года Лёсик был арестован ввиду признания его учебника белорусской грамматики "контрреволюционным".
		</p>
		<p>
		1921-30 Член Інбелкульту (1921-30). Вытокі беларускай нацыянальнай культуры Язэп Лёсік даследаваў у артыкуле "Культурны стан Беларусі 
		к моманту Лютаўскае рэвалюцыі" (1924), дзе прыйшоў да высновы, што "як свядомасць вызначаецца быццём, так культурны стан народа вызначаецца 
		яго эканамічна-грамадзянскімі ўзаемаадносінамі". Ён аўтар артыкулаў і нарысаў па гісторыі Беларусі: 
		"1517-1917 (да юбілею 400-лецця беларускага друку" (1917), "Гісторыя <a href="../terms/wkl.php">Вялікага княства Літоўска-Беларускага</a>", 
		"Памяткі старасвецкай беларускай мовы і пісьменства" (1918), "Памятка" (1919), "Літва-Беларусь" (1921), 
		"<a href="../events/grunwald.php">Бітва пад Грунвальдам</a> у апісанні <a href="../download/chronicles.php">хронікі Быхаўца</a>, Длугаша, Бельскага і інш." (1922)
		</p>		

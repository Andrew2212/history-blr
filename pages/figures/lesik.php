		<p>
		Иосиф (Язеп) Юрьевич Лёсик (18 ноября 1883 - 1 апреля 1940) — белорусский общественный и политический деятель, 
		писатель, публицист, языковед, педагог. Председатель Рады БНР. Академик АН БССР (1928). Родной дядя Якуба Коласа
		</p>		
		<p>
		Родился в крестьянской семье в д. Миколаевщина Столбцовского района. Учился в Молодечненской духовной семинарии. 
		В 1905 году был арестован за участие в революционных беспорядках был пожизненно сослан в Иркутскую губернию.
		</p>		
		<p>
		После Февральской революции вернулся из ссылки в Беларусь и примкнул к Белорусской Социалистической Громаде, 
		став одним из лидеров её правого крыла, редактировал газету "Вольная Беларусь".
		</p>
		<p>
		Стал одним из инициаторов создания <a href="../terms/bnr.php">Белорусской Народной Республики</a> и подписания акта о её независимости.
		</p>
		<p>[ <small>
		1902 Белорусская социалистическая громада (Беларуская сацыялістычная грамада; БСГ) — левая политическая партия начала XX века , ставившая вместе 
		с требованиями социалистических преобразований вопрос о национально-культурной автономии (позднее — собственной государственности) для белорусов. 
		</p>
		<p>
		Является первой белорусской национальной политической партией. БСГ отрицала существование классовой борьбы внутри белорусов, 
		признавая её важность в целом. Подробнее — см. 
		<a target="_blank" href="../../images/img-figures/lesik/BelSocGramada_Korszuk.pdf">Беларуская сацыялістычная грамада 
		і праблемы нацыянальнай дзяржаўнасці Беларусі"</a> Мінск : БДУ, 2006.
		</small> ]</p>		
		<p>
		Возглавлял Исполнительный комитет Великой белорусской Рады. Участник Первого всебелорусского съезда (конгресса). 
		После Октябрьской революции публично выступал против большевиков и разгона конгресса.
		</p>
		

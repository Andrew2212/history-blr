		<p>
		Путь "из варяг в греки" — водный (морской и речной) путь из Балтийского моря через Восточную Европу в Византию. 
		Начало функционирования пути "из варяг в греки" относят к самому началу X века. Наибольшее значение путь имел в X - XII вв.
		</p>
		<p>
		Из разных возможных маршрутов самым коротким был Балтика-Двина-Днепр, проходящий из Балтийского моря по Западной Двине через города Рига, Полоцк и Смоленск 
		(в 5 раз короче, чем путь через Ладогу, описанный в ПВЛ). Возможно, часть пути на границе водоразделов Немана и Припяти,
		 которую сегодня можно пройти лишь "волоком", проходила по <a href="../places/sea_herodotus.php">Морю Геродота</a>, 
		отмечавшееся на картах до XVI века. В XVIII веке это путь, разорванный на месте бывшего моря, замкнул 
		<a href="../places/oginski_channel.php">Огинский канал</a>.

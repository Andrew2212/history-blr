		<p>
		Торвальд, сын Кодрана, Торвальд Вандроўнік (950, Исландия - 1002, Полоцк, Беларусь) 
		— легендарный христианский миссионер, путешественник, основатель монастыря святого Иоанна Крестителя в Полоцке.
		 Его походы описываются в исландской "Пряди о Торвальде Путешественнике", которая содержится в "Саге о крещении", "Саге об Олаве Трюггвасоне" 
		и "Книге с Плоского острова". 
		</p>
		<p>
		В X-XII в. связи между княжествами на "пути из варяг в греки" и Скандинавией были очень сильны. 
		Так, Вальдемар I Великий, король Дании — сын киевской княжны Ингеборги, дочери Киевского и Новгородского князя Мстислава. 
		Был женат на Софье, княжне Минской. Женой Владимира Мономаха была Гита Уэссекская, принцесса английская. Великий князь Киевский Ярослав Мудрый, 
		сын <a href="../figures/rogvolod_rogneda.php">Рогнеды</a> — был женат на Ингигерде, шведской принцессе.
		</p>
		<p>
		981 Торвальд вместе с епископом Фридреком отправляются с проповеднической миссией в Исландию. Ими была открыта первая в Исландии христианская церковь. 
		Однако сородичи-язычники отнеслись к затее Торвальда с насмешкой. Разгневанный Торвальд убил двоих недоброжелателей. 
		Проповедники вынуждены были спешно покинуть Исландию.
		</p>
		<p>
		985 Торвальд через Иерусалим прибывает в Миклагард (Константинополь). Он удостаивается аудиенции императора Василия II и патриарха 
		Константинопольского Николая II Хрисоверга. Император даровал Торвальду грамоту, согласно которой он назначался полномочным посланником Византии 
		к «руским князьям в страны Восточной Балтики» (вероятно, подразумевались <a href="../terms/polotsk_principality.php">Полоцк</a>, Новгород и Псков).
		</p>
		<p>
		986 году Торвальд вместе со своим соратником Стевниром Торгильссоном, двигаясь через Кенугард (Киев) по Непру (Днепру), прибыл в Полоцк. 
		Им была построена церковь и открыт монастырь святого Иоанна Крестителя. Умер Торвальд в Полоцке, предположительно после 1002 года.
		</p>

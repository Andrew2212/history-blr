		<p>
		Судзиловский Николай Константинович  (псевдоним Николас Руссель; 15 декабря 1850 - 30 апреля 1930) — 
		учёный-этнограф, географ, химик и биолог; революционер-народник, один из первых участников "хождения в народ". 
		Деятель революционного движения в Российской Империи, Швейцарии, Англии, Франции, Болгарии, США, Японии, Китае. 
		Один из зачинателей социалистического движения Румынии, сенатор Территории Гавайи (1900), президент сената Территории Гавайи (1901-02).
		</p>
		<p>
		Николай Судзиловский родился в Могилёве, в обедневшей <a href="../terms/shliahta.php">шляхетской</a> семье (маентак в деревне Фастов Мстиславского уезда). 
		Поступил  на юридический факультет Петербургского университета. После участия в студенческих выступлениях (против закона об усилении полицейского надзора) был вынужден перевестись на медицинский факультет Киевского университета 
		(в других вузах участникам беспорядков учиться было запрещено). После неудачной попытки (1874) усторить побег политзаключенных вынужден бежать из 
		<a href="../terms/ross_empire.php">Российской Империи</a>.
		</p>
		<p>
		1875-92 Европейская эмиграция. Работал в больнице святого Георгия (Лондон). Окончил университет в Бухаресте. Под псевдонимом Николас Руссель принимал участие в восстании против турок в Болгарии.  
		Был среди организаторов социалистического движения в Румынии. 
		</p>
		<p>
		1892 Судзиловский-Руссель приезжает на Гавайские острова. Был владельцем кофейной плантации, занимался врачебной практикой. 
		Организует "партию самоуправления гавайи", пытается проводить радикальные демократические реформы.
		</p>
		<p>
		1900 Николай Судзиловский и ряд его сторонников проходят в сенат гавайских островов, а в 1901 
		Н. К. Судзиловский-Руссель был избран первым президентом Сената Гавайских островов.
		</p>
		

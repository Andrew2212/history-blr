		<p>
		[ <small>
		В XVIII веке на Гавайских островах существовали четыре полугосударственных объединения. После продолжительных междоусобиц королю Камеамеа I удалось в 1810 году при помощи европейцев объединить острова и основать династию, которая правила на Гавайях последующие 85 лет.
		</p>
		<p>
		В 1891 на престол Гавайев вступила королева Лилиуокалани (1836-1917). Она попыталась восстановить реальную власть гавайского монарха, практически сведённую конституцией к нулю.  Сторонники присоединения Гавайев к США организовали государственный переворот и сместили королеву.
		</p>
		<p>
		Правительство США предлагало вернуть Лилиуокалани корону на условиях амнистии политзаключенных. Королева условия отвергла, и 4 июля 1894 года была провозглашена Республика Гавайи, в 1898 году вошедшая в состав США.
		</small> ]
		</p>
		
		<p>
		Последние годы жизни Николай Судзиловский провёл на Филиппинах и в Китае. 
		Владел 8 европейскими, китайским и японским языками. Он является первооткрывателем телец Русселя, названных в его честь. 
		Открыл ряд островов центральной части Тихого океана, оставил ценные географические описания Гавайев и Филиппин. 
		Был членом Американского общества генетиков, нескольких научных обществ Японии и Китая, занимался этнографией, энтомологией, химией, 
		биологией, агрономией.
		</p>
		<p>
		Советское правительство с 1921 года выплачивало ему, как персональному пенсионеру 
		Всесоюзного общества политкаторжан (он сотрудничал в органе последнего — "Каторга и ссылка"), пенсию, но в СССР Судзиловский не вернулся.
		</p>
		

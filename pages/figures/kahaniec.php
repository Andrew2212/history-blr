		<p>
		Гийом Аполлинер (Вильгельм Альберт Владимир Александр Аполлинарий Вонж-Костровицкий; 26.08.1880 - 9.11.1918) 
		— французский поэт, один из наиболее влиятельных деятелей европейского авангарда начала XX века.
		</p>
		<p>
		Родовая усадьба Костровицких герба Вонж находилась под Новогрудком. 
		Вильгельм родился в Риме, детство провел в Италии, учился в коллежах Монако, Канна и Ниццы, позже переехал с матерью в Париж.
		</p>
		<p><i>
		*Герб "Вонж" — итальянского происхождения. Один из его вариантов является гербом Пружан. 
		Это герб герцогов миланских Висконти (правивили в Милане в XIV  веке и потому он запечатлен на эмблеме Альфа-Ромео) и Сфорца. 
		Из рода последних происходила Бона, королева польская и великая княгиня литовская. 
		После заключения брака с Сигизмундом I Старым она получила во владение Пружаны. 
		В 1589 г. ее дочь Анна Ягеллонка пожаловала городу магдебургское право и вместе с ним герб с родовым символом своей матери.
		</i></p>
		<p>
		В качестве литературного псевдонима Костровицкий выбрал французские варианты двух своих имён — Вильгельм (Guillaume) и Аполлинарий (Apollinaire, это имя носил также его дед). 
		</p>
		

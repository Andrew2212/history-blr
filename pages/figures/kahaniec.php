		<p>
		В 1910-е Гийом Аполлинер работает публицистом, критиком, редактором в парижских изданиях, писал о современной живописи: 
		"Художники-кубисты" (Les peintres cubistes, 1913). 
		</p>
		<p>
		Поддерживал дружеские отношения с художниками Пабло Пикассо, Андре Дереном, Франсисом Пикабия, Морисом де Вламинком и Анри Руссо. 
		</p>
		<p>
		Его манифест "Новый дух" 1917 года на ближайшие двадцать лет определил развитие молодой музыки Франции. 
		В одной из своих пьес Аполлинер ввел в обиход термин "сюрреализм" (Направление в литературе и искусстве, сложившееся в 1920-х годах. 
		Отличается использованием аллюзий и парадоксальных сочетаний форм).
		</p>
		
		

		<p>
		Карусь Каганец (Казимир Рафаил Костровицкий, 10.02.1868 - 20.05.1918) 
		— белорусский поэт, художник, общественный деятель. Родственник известного французского поэта Гийома Аполлинера.
		</p>
		<p>
		Родился в Тобольске в семье Карла Костровицкого, шляхцича гербу "Байбуза", сосланного за участие в 
		<a href="../events/uprising_1863.php">восстании Калиновского</a> 1863 года 
		 в Сибирь. В 1874 году семья вернулась на родину. Учился в Минского городском училище, в Московском училище живописи, ваяния и зодчества. 
		</p>
		<p>
		Начал заниматься литературной деятельностью в 1893 году. 
		Імя ў псеўданіме "Карусь Каганец" узята ў памяць бацькі. Карлаў у Беларусі заўсёды звалі Карусямі. 
		Прозвішча — даніна тагачаснай модзе на гучныя, але народныя назвіскі. Каганец — прымітыўная сялянская свяцільня. 
		Паэт збіраўся свяціць народу. І свяціў, пакуль мог.
		</p>
		<p>
		В 1902-1903 участвовал в создании БСГ. Дважды был арестован. В 1910-1911 году 
		сидел в Минской тюрьме вместе с Якубом Коласом. 
		</p>
		<p><i>
		*Беларуская сацыялістычная грамада — левая политическая партия (1902-1918), ставившая вместе с требованиями социалистических преобразований вопрос о национально-культурной автономии (позднее - собственной государственности) для белорусов.
		</i></p>
		<p>
		Каганец сотрудничал с газетой "Наша ніва", издал учебник "Беларускі лемантар або першая навука чытаньня". Его водевиль "Модны шляхцюк" стал одним из первых произведений белорусской драматургии. 
		</p>
		
		
		
		

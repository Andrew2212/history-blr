		<p>
		Рыцари Ордена, видя в Давыде Городенском своего наиболее опасного врага и не имея сил уничтожить его, напали на его дом. 
		В марте 1324 года три немецких рыцаря, имея при себе более шестисот воинов, тайно прошли к поместью Давыда и сожгли его, 
		возможно, убив при этом его семью.
		</p>
		<p>
		В ответ на разгром имения князь Давыд нанёс поражение рыцарям в Мазовии, а в 1326 году он с небольшим количеством воинов 
		(1.200 всадников и отряд союзного польского войска) совершает поход на Бранденбург.  
		Соединенное войско дошло до Франкфурта, проникнув в самый центр Германии.
		</p>
		<p>
		Во время этого похода на Бранденбургскую марку (маркграфство) Давид погиб. И, хотя был язычником 

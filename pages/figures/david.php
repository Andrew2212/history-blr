		<p>
		Давид Городенский (1283–1326) — государственный деятель и полководец <a href="../terms/wkl.php">Великого княжества Литовского</a>, 
		один из главных военачальников великого князя литовского <a href="gedimin.php">Гедимина</a>, каштелян гродненский. 
		Не потерпел ни одного поражения в битвах с рыцарями Тевтонского Ордена.
		</p>
		<p>
		Крестоносцы пытались захватить Гродненский замок в 1305, 1306 и 1311 гг., 
		но не смогли, хотя численность вражеских отрядов порой достигала 6 тыс. человек.
		В правление же Давида (1312-26), несмотря на то, что все масштабные походы <a href="../terms/litwiny.php">литвинов</a>
		 начинались именно из <a href="../places/grodno.php">Гродно</a>,
		 рыцари Ордена не отваживались нападать на город. 
		</p>
		<p>
		Происхождение Давида неизвестно. В псковских летописях он называется князем. Возможно, был сыном князя псковского Довмонта.		
		Впервые упоминается в <a href="../download/chronicles_peter.php">Chronicon terrae Prussiae</a> под 1314 годом как руководитель 
		обороны <a href="../places/nowogrudok.php">Новогрудка</a> во время осады города тевтонцами.

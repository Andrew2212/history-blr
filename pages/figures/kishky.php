		<h4>Пётр Кишка</h4>
		<p>
		Пётр Кишка, сын Станислава — воевода полоцкий (1521-32), каштелян трокский 
		и староста жемайтский (1532-34). Был одним из самых богатых магнатов ВКЛ.
		</br>В 1522 и 1526 годах возглавлял посольства в Московию.
		</p>
		<h4>Ян Кишка</h4>
		<p>
		Ян Кишка (1552—1592) — староста жемайтский (1579—1589), виленский каштелян (1588—1591), брестский воевода.
		</br>Учился в Базеле, в Цюрихе, затем в Риме, Неаполе и Болонье.
		</br>В <a href="../events/war_livonia.php">Ливонскую войну</a> во главе своего отряда численностью 500 человек участвовал в походе короля 
		<a href="../figures/batory.php">Стефана Батория</a> на Великие Луки. 
		За оказанную помощь был возведен королем в 1579 в ранг сенаторa.
		</p>
		<p>
		Был сторонником арианского учения. В Ивье основал арианскую академию, в Венгруве на Подляшье печатню. Видным представителем ариан был 
		того времени <a href="../figures/budny.php">Сымон Будный</a> (1530-93), издававший свои труды под покровительством 
		<a href="../figures/radziwilly.php">Радзивила</a> Николая Черного.
		</p>
		<h4>Януш Кишка</h4>

			 Аднак, пры вялізных багаццях, Кішкі ні разу не былі ні ваяводамі віленскімі, ні канцлерамі. 
			 Частка праўнукаў Станіслава Кішка — нашчадкі яго ўнука Станіслава Пятровіча, мела тытул <i>графаў на Іўі і Лоску, баронаў на Цеханаўцы</i>. 
		</p>		
		<p>
			Кішкі здаўна католікі, у часы Рэфармацыі, разам з большасцю магнатаў сталі пратэстантамі. 
			Іўе было цэнтрам радыкальнай пратэстанцкай плыні — арыянства. У пачатку XVII ст. Кішкі вярнуліся да каталіцтва			 			
		</p>

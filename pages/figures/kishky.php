		Станислав Кишка, сын Петра Струмилы — лидский наместник (1492), <a href="../terms/weliki_marshalok.php">великий маршалок Литовский</a> (1493—99), 
		воевода смоленский (1500—03), <a href="../terms/weliki_hetman.php">великий гетман литовский</a> (1503—1507), староста Гродно (1508-14).
		</p>

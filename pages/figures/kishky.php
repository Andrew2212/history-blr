		Януш Кишка (1586-1653) — староста парнавский (1610), полоцкий воевода (1621—1654), польный гетман литовский (1635—1646), великий гетман литовский (1646—1654).
		</br>В 1604 участвовал в походе литовских войск гетмана <a href="../figures/hodkewich.php">Яна Ходкевича</a> в Ливонию. 
		В 1605 во время войны со Швецией участвовал в <a href="../events/battle_kircholm.php">Кирхгольмском сражении</a>, в 1609 — в Балтакменской и Парнавской битвах, 
		после чего король Сигизмунд Ваза назначил его старостой Парнавским.
		Участвовал в Русско-польской войне 1605—1618 годов, во взятии литовскими войсками Смоленска в 1611 и в походе Ходкевича на Москву. 
		Затем с 1621 служил полоцким воеводой.

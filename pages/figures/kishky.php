		<p>
			— происходивший из Подляшья <a href="../terms/shliahta.php">шляхетский</a> магнатский род <a href="../terms/wkl.php">Великого княжества Литовского</a>, который угас в середине XVII века.
			 Владели обширными поместьями, 70 городами и 400 селами. Родовой герб "Dabrowa".
			 Династия вела род от подляшского шляхтича Петра Струмилы, прозванного Кишка, дрогичинского старосты, жившего в XV веке.

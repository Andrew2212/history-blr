		<h2>Подляшье</h2>
		<div class="col-md-12">
		<p class="img-float-left">
		<a href="../../images/img-figures/kishky/herb_podlasie.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img src="../../images/img-figures/kishky/herb_podlasie_thumb.jpg" height="140" >
			</br><small>Герб Подляшья</small>
		</a>
		</p>
		<p>
		Подляшье — историко-географическая область на белорусско-польском пограничье между Холмщиной (на юге) и рекой Нарев (на севере).
		</p>
		<p>
		Изначально входившее в Галицко-Волынское княжество, с XIV в. до второй половины XVI в. находились во владениях Великого Княжества Литовского. В конце XV в. появляется само название "Подляшье", а в начале XVI в. было образовано Подлясское воеводство, закрепившее данное название.
		</p>
		<p>
		Обычно выделяются две части Подляшья — северная и южная, граница между которыми проходит по реке Буг.
		</p>
		<p>
		После <a href="../events/unia_liublin.php">Люблинской унии 1569</a> Южное Подляшье перешло в Королевство Польское, а Северное в составе Брестского воеводства осталось 
		в пределах Великого княжества Литовского до 1795 г. 
		После <a href="../events/unia_religion_brest.php">Брестской унии 1596</a> православное население Подляшья перешло в грекокатолическую 
		(униатскую) церковь.
		</p>
		<p>		
		Сегодня административным центром Подляшского воеводства является Белосток. Город является культурным центром белорусского меньшинства в Польше и одним из основных центров Польской православной церкви. В ряде гмин (округов) воеводства вторым официальным языком является белорусский.
		</p>
		<p>
		<a href="../terms/blr_mova.php">Белорусский язык</a> преподаётся в 23 школах, 14 гимназиях и 2 лицеях. В Белостокском университете существует кафедра белорусской культуры. Выходит газета "Ніва", журнал "Czasopis".
		</p>
		<p>
		Примерно с середины 50-х годов в регионе действует Белорусское Общественно-Культурное Общество. В 1980 была создана Белорусская Ассоциация Студентов (БАС). Организация проводит ежегодный популярный фестиваль рок-музыки БАСовище.
		</p>

		<h2>Роман Александрович Скирмунт
		</br><small>элита Империи и Премьер-Министр БНР</small></h2>
		
		<p class=" img-float-left">
		<a href="../../images/img-terms/kraj_tut/Skirmunt_R_A.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img alt="Роман Скирмунт" src="../../images/img-terms/kraj_tut/Skirmunt_R_A_thumb.jpg" height="140" >
			</br><small>Роман Скирмунт</small>
			</a>
		</p>
		
		
		<p>
		 Роман Александрович Скирмунт (25 апреля 1868—1939)) — родился в имении Скирмунтов Поречье близ Пинска. 
		 Окончил Рижскую классическую гимназию. Высшее образование получил в Варшавском университете (юриспруденция) и в Венском университете (философия).
		</p>
		<p>
		1907-1917 заместитель Президента минского Общества сельского хозяйства (Президентом был 
		<a href="../figures/woinillovicz.php">Эдвард Войнилович</a> — фундатор минского Красного костела), 
		депутат 1-й Государственной думы России, член русского Государственного Совета. 
		</p>
		<p>
		Поддерживал идею <a href="../terms/kraj_tut.php">"тутэйшай краёвасьці"</a> — автономии и самоуправления края.		
		</p>
		<p>
		<i>
			"Кожны з нас, мужык або шляхціч, на зямлі Літвы ці Беларусі мае аднолькавае права з годнасцю звацца літоўцам, палякам альбо беларусам. 
			Кожнаму з нас належыць зямля пад нагамі, неба над галавой і вялікая мінуўшчына краю. І гэтыя правы, прынятыя ад гісторыі, 
			сённяшні дзень перадае будучыні. 
		</i>
		</p>
		<p>
		<i>	
			Толькі краёвая ідэя, якая тут, на месцы, ёсць для нас цэнтрам цяжару і крыніцай дзейнасці, 
			запальвае сэрцы здабываннем больш шчаслівай будучыні для гэтай роднай зямлі, якая трапіла ў асаблівую небяспеку. ".
		</i>
		</br><small>
		<a target="_blank" href="../../images/img-terms/kraj_tut/Skirmuntt_Nasza-tutejszosc_blr.pdf">
		"Nasza tutejszosc"
		</a>, Skirmuntt Konstancja, 1905</small>		
		</p>
		<p>
		1915-1917 возглавлял националистический Белорусский национальный комитет в Минске, был членом Минского белорусского народного представительства. 
		</p>
		<p>
		1918 Работал в комитете подготовки первого Общебелорусского съезда и был вторым главой его Исполнительного Комитета. Член Рады и Премьер-министр 
		<a href="../terms/bnr.php">Белорусской Народной Республики</a>.
		</p>
		<p>
		После разгрома БНР и раздела беларуси по Рижскому договору жил в Западной Беларуси (Польша). Помогал издателям белорусской прессы. В 1930-1935 годах был сенатором Польши.
		</p>
		

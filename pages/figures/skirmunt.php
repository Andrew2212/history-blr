		<p>
			Скирмунт — <a href="../terms/shliahta.php">род шляхетский</a>, восходит к XVI в. Одна ветвь, герба Дуб, внесена в VI ч. род. кн. Минской губ. 
			Другая, герба Пржияцель, внесёна в VI и I части род. кн. Гродненской, Виленской и Ковенской губ.
		</p>
		<p>
		Династия промышленников-предпринимателей, преобразившая Пинское <a href="../places/sea_herodotus.php">Полесье</a>. Основателем династии стал Александр Скирмунт.
		Его отец, Симон (Семен) Скирмунт  в конце XVIII в. приобрел у Михала <a href="../figures/oginskie.php">Огинского</a> в 1782 г. поместья Молодово и Поречье недалеко от Пинска.
		</p>
		<p>
		За три года в Молодово был построен дворец в стиле ампир. Француженка Р.Бели, посетившая Пинщину, в своих воспоминаниях отозвалась о дворце так: 
		<i>"Он — как мечта при свете луны под музыку Моцарта. Колонны подобны на лилии, поддерживающие гирлянды, фасад, как снежное изваяние, 
		развертывает легкие и полные грации мотивы"</i>
		</p>
		<p>
		Сын Симеона, Александр Скирмунт строит в Молодово суконную фабрику и сахарный завод — первый в белорусских губерниях. И получает патент на свое изобретение, позволяющее ускорить операцию по выпариванию сахара из сиропа с 4 часов до 5 минут.
		</p>
		<p>
		Суконная фабрика работала на хорошем европейском уровне — серебряная медаль на Мануфактурной выставке 1865 г. в Москве, 
		золотая медаль на московской Всероссийской художественно-промышленной выставке 1882 г., 
		серебряная медаль Всероссийской промышленной и художественной выставки 1896 г. в Нижнем Новгороде, 
		награды всемирных выставок в Париже (1867 и 1878 гг.) и Вене (1873 г.).
		</p>
		<p>
		<i>"Когда Александр Скирмунт умер, его на руках несли из Поречья в Молодово (10 километров). О чём-то это говорит... 
		Знаете, при Скирмунтах Поречье было модернизированным, промышленным селом... Никогда не слышала ничего плохого о Скирмунтах, никогда."</i>
		<small>директор Народного литературного музея А. Сидорук</small>
		</p>
		

		<p>
		В конце XIX в. родовые имения оказались разделенными между шестью сыновьями и пятью дочерьми Александра Скирмунта, 
		которые успешно продолжали преображать Полесье. Как результат, Скирмунты превратились в одну из наиболее богатых и влиятельных в Беларуси династий.

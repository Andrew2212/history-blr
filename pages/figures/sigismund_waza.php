		<h3>Война с Московским царством  1609-1618. Взятие Москвы</h3>
		<p>
		<a href="../events/war_moscow_1610.php">Война с Московским царством  1609-1618</a>. Речь Посполитая вступила в войну после заключения царём Василием Шуйским союза со Шведским королевством. 
		Московское войско было разгромлено в <a href="../events/battle_klushyn.php">битве под Клушино</a>, войска Сигизмунда заняли Москву, царь Василий был взят в плен. 
		Московское правительство (Семибоярщина) признало сына Жигимонта — Владислава — царём и чеканило от имени "Владислава Жигимонтовича" монету.
		</p>
		<p>
			В ходе войны <a href="../places/smolensk.php">Смоленск</a> был возвращен в состав ВКЛ. И до XVIII века <i>"смоленское дворянство именовало себя
		 <a href="../terms/shliahta.php">шляхетством</a>, 
		смоленские шляхтичи предпочитали читать польские книги и брать себе жён из Польши. При Анне Иоанновне польские книги на Смоленщине были запрещены, 
		за владение ими били кнутом и ссылали в Сибирь; запрещены были также браки с польками; 
		однако смоляне жён из <a href="../terms/ross_empire.php">России</a> по прежнему не брали, предпочитая браки в своей среде."</i> 
		<small>(Из "Записок" Л. Н. Энгельгардта)</small>
		</p>
		

		<h3>Война со Швецией 1600-1611</h3>
		
		<p>
		<a href="../events/war_1600.php">Война 1600-1611 годов</a> — война за Эстонию, наследие Ордена Меченосцев. 
		Другой причиной войны была попытка Сигизмунда вернуть себе шведский трон.
		Командующим войсками был назначен <a href="../figures/hodkewich.php">Ян Кароль Ходкевич</a>. 
		Его подвигам посвящена поэма <a href="../download/carolomachia.php">"Кароломахия"</a> минского воеводы 
		<a href="../figures/zawisza.php">Христофора Завиши</a>.
		</p>
		<p>
		Уже в должности великого гетмана Литовского Ходкевич воевал в 1611-1618 с Московским царством. Участвовал в осаде московского Кремля 
		и помогал его удерживать после взятия, сражался с ополчением Минина-Пожарского, освобождал Смоленское воеводство от московских войск.
		</p>
		

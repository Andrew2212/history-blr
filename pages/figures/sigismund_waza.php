		<p>
		Сигизмунд III Ваза (20 июня 1566 - 30 апреля 1632) — король польский и великий князь литовский (1587-1632), 
		король шведский (1592-1599) — сын шведского короля Юхана III Вазы и Катерины Ягеллонки 
		(дочь Боны Сфорца — жены Сигизмунда I Старого, дочки миланского герцога Джана Галеаццо Сфорца).
		</p>
		<p>
			В Швеции династия Ваза правила в 1523-1654 годах. Титул короля —  
			Med Guds Nade, Sveriges, Gotes och Vendes Konung (Божьей милостью Король Шведов, Готов и Вендов). В Речи Посполитой Ваза правили с 1587 по 1668 год. 
			Название династии происходит от герба Vase (vase, risvase, stormvase — вязанка, фашина) в семейном гербе.
		</p>
		<p>
		Как потомок <a href="../figures/jagellony.php">Ягеллонов</a>, Сигизмунд был избран польским королём и великим князем Литовским после 
		смерти <a href="../figures/batory.php">Стефана Батория</a>. 
		Приглашение последнего Ягеллона и наследника шведской короны на трон должно было уладить со Швецией территориальные проблемы на севере страны.
		</p>
		<p>
		Будучи избран на престол <a href="../terms/rzeczpospolita.php">Речи Посполитой</a>, Сигизмунд стремился объединить под своей властью Речь Посполитую и Швецию. 
		На короткое время ему это удалось. В 1592 году он объединил под личной унией оба государства, но в 1595 году шведский парламент избрал герцога 
		Седерманландского регентом Швеции вместо отсутствующего короля. 
		</p>
		

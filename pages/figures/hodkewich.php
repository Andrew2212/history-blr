		<p>
			<b>Иван Федорович Ходкевич</b> (1420-85) — <a href="../terms/weliki_hetman.php">гетман великий Литовский</a>, воевода Витебский, воевода Киевский. 
		</p>
		<p>
			<b>Григорий Александрович Ходкевич</b> (1513-72) — гетман великий Литовский. 
			</br>Был сторонником самостоятельности ВКЛ и противником <a href="../events/unia_liublin.php">Люблинской унии</a>. Протестуя против объединения с Польшей в Речь Посполитую, 
			в 1569 отказался от всех государственных и административных должностей. 
			Участвовал в <a href="../events/war_livonia.php">Ливонской войне</a>. 
			Герой <a href="../events/battle_ula.php">битвы при Чашниках</a>: <i>"все это случилось с помощью Божией и благодаря содействие пана  Ходкевича"</i> 
			<small>из письма Радзивилла Рыжего</small>.
		</p>
		<p>
			Основал типографию при православном монастыре в местечке Заблудово Гродненского повета, где первопечатники Иван Фёдоров и Пётр Мстиславец, 
			бежавшие от преследований из Москвы, печатали свое "Евангелие учительное" с гербом Ходкевичей. 
			В Москве было оно признано сочинением папистическим и царским указом его было приказано уничтожать.
		</p>
		<p>
			<b>Иероним Александрович Ходкевич</b> (1500-61) — военный и политический деятель ВКЛ, каштелян трокский и виленский , 
			староста жемайтский, ошмянский, шкловский. Основатель быховской линии рода Ходкевичей. 
			Отец Яна и дед Яна Кароля, брат Григория. Участник Ливонской войны с Московией.
		</p>
		<p>
			В 1555 году Сигизмунд II Август послал его с дипломатической миссией к папе римскому Павлу IV и императору Фердинанду, 
			который даровал ему и потомкам титул графа Священной Римской империи в знак признания его ораторского таланта.
		</p>
		<p>
			<b>Ян Иеронимович Ходкевич</b> (1530-79) — граф шкловский, маршалок великий Литовский, староста жемайтский. 
			Воспитывался в лютеранском окружении, учился в Кёнигсберге, Лейпциге и Виттенберге. Участник Ливонской войны, получил Ливонию в свое управление. 
			Сопротивлялся принятию Люблинской унии с Польшей. 
			Выступал против Ивана Грозного и поддерживл <a href="../terms/dukes_wkl.php">Генриха Валуа</a> как претендента на престол Речи Посполитой.
		</p>
		<p>
			<b>Иероним Юрьевич Ходкевич</b> (1560-17) — государственный и военный деятель Великого княжества Литовского. 
			Конюший великий литовский, воевода мстиславский , каштелян виленский , староста берестейский. 
			Участник Ливонской воины с Московией  и <a href="../events/war_1600.php">войны 1600-1611</a> со Швецией.
		</p>
		

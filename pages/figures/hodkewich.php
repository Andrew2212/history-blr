			Ходкевичи — <a href="../terms/shliahta.php">шляхетский</a> род Великого княжества Литовского собственного герба "Ходкевич", 
			носители тардиционной <a href="../articles/blr_family_names.php">белорусской фамилии</a>.
		</p>
		<p> 
			Происходили из киевского боярства, их появление среди правящей шляхты связывают с именем великого князя 
			<a href="svidrigailo.php">Свидригайло</a>. 
			Род Ходкевичей дал <a href="../terms/wkl.php">Великому княжеству Литовскому</a> целую плеяду воинов и государственных деятелей.
		</p>
		

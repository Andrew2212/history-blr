		<h4>Ян Кароль Ходкевич (1560-1621)
		</br><small>
		граф на Шклове, Новой Мыши и Быхове, пан на Мельце и Краснике
		</small></h4>
		<p>
			 — легендарный полководец, гетман великий литовский, генеральный староста жмудский, воевода виленский. 
		</br>Герой белорусского эпоса — поэмы <a href="../download/carolomachia.php">"Кароломахия"</a> 
		 Христофора <a href="zawisza.php">Завиши</a> (воеводы минского). 	
		</p>
		<p>
		 
		В войне 1600-1611 со Швецией успешно командовал войсками Речи Посполитой. 
		В 1605 году Ходкевич с отрядом в 4 тысячи воинов одержал победу в <a href="../events/battle_kircholm.php">битве под Кирхгольмом</a> 
		над шведским 11-тысячным корпусом.		 
		Поздравительные письма ему писали папа римский Павел V, католические государи Европы, турецкий султан Ахмед I 
		и персидский шах Аббас I.
		</p>
		<p>
		 Командовал войсками в <a href="../events/war_moscow_1610.php">войне 1611-1618</a> с Московским царством. 
		 Участвовал в осаде московского Кремля и помогал его удерживать после взятия, 
		 воевал с ополчением Минина-Пожарского, освобождал <a href="../places/smolensk.php">Смоленское воеводство</a> от московских войск.
		</p>
		<p>  
		 Во время войны с Османской империей 1620-21 Ходкевич принял командование войсками Речи Посполитой 
		 после разгрома армии С. Жолкевского под Цецорой. Свою последнюю 
		 великую победу он одержал в 40-дневной <a href="../events/battle_hotin.php">битве под Хотином</a> с турками.  
		</p>
		

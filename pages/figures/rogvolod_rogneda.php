		<p>
		Рогволод (920-978) — первый полоцкий князь, названный по имени в исторических источниках. 
		До сих пор остается почти мистической фигурой — сведений о нем и его происхождении мало: <i>"пришел из-за моря и держал власть свою в Полоцке"</i>. 
		</p>
		<p>
		Большинство историков принимают его скандинавское происхождение и возводят к нему Полоцкую княжескую династию, 
		обособляя ее от Киево-Новгородских Рюриковичей.
		</p>
		<p>
		<i>"Особое тяготение варягов к Полоцку"</i> подчеркивал А. Н. Веселовский в своей работе 
		"<a target="_blank" href="../../images/img-terms/polotsk_principality/Veselovskij_Rus_in_Wilkinasaga.djvu">Русские и вильтины в саге о Тидреке Бернском</a> (Wilkinasaga)". 
		Так же, как и <i>"необщерусскую его политическую ориентацию — у одних на руках половцы, у других Литва и Ливь"</i>. 		
		</p>
		<p>
		Повесть Временных лет, год 6488 (980) — <i>"И послал [Владимир] к Рогволоду в Полоцк сказать: "Хочу дочь твою взять себе в жены". Тот же спросил у дочери своей: "Хочешь ли за Владимира?". Она ответила: "Не хочу разуть сына рабыни, но хочу за Ярополка". Этот Рогволод пришел из-за моря и держал власть свою в Полоцке, а Туры держал власть в Турове, по нему и прозвались туровцы… И напал Владимир на Полоцк, 
		и убил Рогволода и двух его сыновей, а дочь его взял в жены."</i>		
		</p>
		<p>
		<i>"И с тех пор взимают меч Рогволожи внуки против Ярославовых внуков."</i>
		</br>Дети от брака Владимира и Рогнеды положили начало родам Изяславичей ("Рогволожи внуки" — князья Полоцкие) от старшего Изяслава и Ярославичей 
		("Рюриковичи" — князья Киевские, Новгородские, Ростовские) от Ярослава Мудрого, младшего сына. 
		</p>
		<p>
		<i>"В вековой распре Ярославичей и Изяславичей есть что-то расовое"</i> — отмечал профессор Веселовский. 
		Полоцкие князья не участвовали в 
		<a target="_blank" href="https://ru.wikipedia.org/wiki/Съезды_русских_князей">
		съездах руских князей</a> (Ярославичей). Единственный раз, после <a href="../events/battle_nemiga.php">битвы на Немиге</a>  
		(князь <a href="../figures/charadzej.php">Всеслав-Чародей</a> vs Ярославичи) 
		киевский князь Изяслав приглашал Всеслава на съезд и <i>«крест целовал: "Приди к нам, не сотворим тебе зла"»</i>. Там Всеслав был пленен, 
		затем освобожден восставшими горожанами и киевским вече посажен на Киевское княжение.
		</p>
		<p>
		От Изяслава — внука Рогволода выводило происхождение <a href="../terms/dukes_wkl.php">Гедиминовичей</a> Русское Императорское Географическое Общество в 
		своем труде <a href="../download/kirkor.php">"Живописная Россия"</a>, 1882
		</p>

		

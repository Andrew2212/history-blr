		<p>
		Николай Гусовский (1470-1533) — создатель белорусского эпоса "Песня о зубре", поэт раннего Ренессанса, живший и творивший на землях 
		<a href="../terms/wkl.php">Великого княжества Литовского</a>, Италии и Королевства Польского.
		</p>
		<p>
		Эта эпоха дала Беларуси целую плеяду гуманистов и просветителей — Н. Гусовсий, <a href="skorina.php">Ф. Скорина</a>, 
		<a href="tiapinsky.php">В. Тяпинский</a>, <a href="budny.php">С. Будный</a>. 
		</p>
		<p>
		Гусовский родился в семье охотника, вырос и видел охоту на грозного зверя в деревне Уссово (потом слилась с деревней Королевщина), 
		расположенной на берегу реки Уса, притока Немана, в прежней Литовской, а нынче Налибокской пуще.
		</p>
		<p>
		Был католическим священником, сан принял около 1518 года. Некоторое время исполнял должность публичного нотариуса апостольской канцелярии 
		для Великого княжества Литовского. 
		В 1520-1522 годах Николай Гусовский в составе дипломатической миссии (к папе римскому Льву Х) попал в Рим.
		</p>
		<p>
		Николай <a href="../figures/radziwilly.php">Радзивилл</a>, виленский воевода, предполагал направить в подарок Льву X чучело зубра, 
		в связи с этим Николай Гусовский должен был написать некое "приложение" к этому необычному подарку —
		 поэму "Carmen de statura feritate ac venatione Bisontis" ("Песня про облик, дикость зубра и охоту на него"), 
		 которая ныне более известна как "Песня про зубра". Но Лев X умер и подарок не был ему доставлен.
		 </p>
		<p>
		 "Песня про зубра" была издана в 1523 году и посвящена королеве Боне, которая 
		 дала средства на печать этого произведения. Поэму характеризуют как произведение о родном крае и народе, размышление о судьбе Родины, о её месте на путях истории.
		</p>
		

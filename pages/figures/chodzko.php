		<h2>Ян Борейко Ходзько</h2>
		
		<p class="img-float-left" >
		<a href="../../images/img-figures/chodzko/jan.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Ян Борейко Ходзько" src="../../images/img-figures/chodzko/jan_thumb.jpg" class="thumbInfo">
		</a>
		</p>
		
		<p>
		Ян Борейко Ходзько (Jan Borejko Chodzko)  24.06.1777 - 10.11.1851 — писатель, общественный деятель, великий магистр масонской ложи 
		"Северный факел" в Минске, двоюродный брат Леонарда Ходзько, дядя Игнаци Ходзько и отец Александра, Михаила и Иосифа Ходзько.
		</p>
		<p>
		Родился в имении Кривичи Вилейского уезда. Учился в Кривичской школе ордена тринитариев, продолжил обучение в Вильне.
		Был визитатором школ Виленской и Минской губерний. В <a href="../events/war_1812.php">войне 1812</a> , 
		будучи главой Временного совета Минска, приветствовал приход Наполеона. После поражения французов вынужден был покинуть 
		родину и возвратился только после амнистии. 
		</p>
		<p>
		По  инициативе Яна Ходько  была создана масонская ложа "Северный факел" в Минске (1816), в которой он был Великим магистром. 
		</p>
		<p>
		В 1817 году в Минске отдельной книгой была издана его комедия "Освобожденная Литва, или переход через Неман". 
		</p>
		<p>
		Его книга "Пан Ян из Свислочи, странствующий торговец"(" Pan Jan ze Swisloczy", 1821) стала первым в своем жанре краеведческим описанием 
		Края, и была признана школьным комитетом Виленского университета классическим произведением.
		</p>
		<p>
		В 1826 арестовывался по  делу <a href="../terms/filomaty.php">филоматов</a>. 
		Принимал участие в <a href="../events/uprising_1830.php">восстании 1830</a>, после которого был сослан на Урал на 4 года. 
		</p>
		<p>
		Умер в 1851 и похоронен в Заславле.
		</p>
		
		<h2>Игнаци Ходзько</h2>
		
		<p class="img-float-left" >

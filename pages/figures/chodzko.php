		<p>
		Игнацы Ходзько (Ignacy Chodzko) 29.09. 1794, местечко Костеневичи, Вилейского района - 1861, Девятни, Сморгонский район — писатель; племянник Яна Ходзько.
		</p>
		<p>
		Адам Киркор в книге <a href="../download/kirkor.php">"Живописная Россия: … Литовское и Белорусское Полесье" </a> (1882) про него писал:.
		</p>
		<i>
		<p>
		Игнатий Ходзько — знаменитейший из польских повествователей. Его Obrazy litewskie снискали ему громкую известность. Друг и родственник (по жене) Одынца и Корсака, он, под их влиянием, сделался горячим сторонником романтизма, хотя прежде, под влиянием дяди своего Яна, придерживался классицизма. 
		</p>
		<p>
		Игнатий Ходзько в полном смысле слова <a href="../terms/litwiny.php">Литвин</a>. Литва у него во всем как живая; с легкостью и какою-то особенною задушевностью, он изображает живыми красками домашнюю жизнь, обряды, обычаи, то вдруг воскрешает в памяти давно минувшие деяния, предания, рисует исторические личности, и все это с такою естественностью, с таким неподражаемым умением приковывать внимание читателя, что невольно очаровывает его. 
		</p>
		<p>
		Некоторые же из его сочинений, как, например, "Дворики на Антоколи" (Вильня), "Дом моего деда" и др., даже в будущем будут иметь высокое значение, как верные этнографические снимки быта и обычаев.
		</p>
		</i>
		
		<h2>Александр Ходзько</h2>
		<p class="img-float-left" >
		<a href="../../images/img-figures/chodzko/alexander.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Александр Ходзько" src="../../images/img-figures/chodzko/alexander_thumb.jpg" class="thumbInfo">
		</a>
		</p>
		<p class="img-float-right" >
		<a href="../../images/img-figures/chodzko/chodzkaFilamati.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Александр Ходзько" src="../../images/img-figures/chodzko/chodzkaFilamati_thumb.jpg" class="thumbInfo">
		</a>
		</p>
		<p>
		Александр Ходзько (Aleksander Chodzko) 30.08. 1804 - 27.12. 1891 — поэт и востоковед; брат Михала Ходзько и Иосифа Ходзько.
		</p>
		<p>
		Родился в имении Кривичи Вилейского уезда. После гимназии поступил на филологическое отделение Виленского университета (1820-1823). 
		Там познакомился с Адамом Мицкевичем, <a href="../figures/volovicz.php">Михаилом Воловичем</a>, 
		<a href="../figures/domeyko.php">Игнаци Домейко</a>. 
		В 1923 был арестован за участие в тайном студенческом <a href="../terms/filomaty.php">обществе филоматов</a>.
		</p>
		<p>
		Материалы следствия по делу филоматов и именные списки участников были собраны в докладной на имя имератора 
		<a href="../download/filomaty_note.php">"К истории тайных обществ и кружков"</a>, 1824.
		</p>
		<p>
		После освобождения был выслан в Санкт-Петербург; учился в Институте востоковедения (1824-31). Был консулом Российской империи в Персии.
		</p>
		<p>
		Александра Ходзько ввел  в польскоязычную литературу восточные мотивы — в сборник "Poezji" (1829) вошли восточная поэма "Дерар" , 
		переводы новогреческих песен и персидской поэзии, баллады, основанные на народных мотивах. 
		Адам Мицкевич посвятил ему стихотворение-импровизацию "Александру Ходзьку".
		</p>
		<p>
		<i>
		"В польской поэзии за Мицкевичем следует второй замечательный поэт — А. Ходзько. Они вдвоем воспевали Восток, 
		однако у каждого из них свой самобытный взгляд."</i> 1835   П. Дубровский
		</p>
		<p>
		Научное значение сохраняют работы на английском и французском языках о персидской литературе.
		</p>
		
		<h2>Михал Ходзько</h2>
		<p>
		Михал Ходзько (Michal Chodzko) 1808-1879 — поэт и переводчик; брат дипломата и поэта Александра Ходзько, двоюродный брат писателя Игнацы Ходзько.
		</p>
		<p>
		Учился в Виленском университете. Участвовал в восстании 1830 года. После поражения эмигрировал во Францию. Переводил с английского и немецкого языков, сотрудник многочисленных периодических изданий, редактор эмигрантской газеты. Чалец  Общества Литовского и Зем ель русских земель.
		</p>
		<p>
		В 1833 году Михаил Ходзько вступил в союз "Месть народа* (Zemsta Ludu*)"  и участвовал в экспедиции полковника Йозефа Заливского.
		</p>
		<p>
		*План "Zemsta Ludu" — небольшие вооруженные отряды эмигрантов должны были проникнуть на земли Речи Посполитой, провозглашая там свободу и равенство, склонить крестьян и горожан к всеобщему восстанию. Над каждыми двумя поветами (уездами) примет командование окружной начальник, над воеводством или губернией - начальник воеводства, а верховная власть над всеми будет в руках Юзефа Зеливского.
 		</p>
		<p>
		В этой партизанской вылазке он занимал должность начальника третьего округа (Пинский и Слуцкий уезды Минской губернии). В 1848 году был одним из командиров польских легионов в Италии
		</p>
		<p>
		После вылазки 1833 года написал поэму "Десять картин из экспедиции в Польшу 1833 года". Перевёл на польский язык поэмы Байрона "Манфред" и "Мазепа".
		</p>
		<p>
		В переводе на белорусский язык стихи Мхаила Ходзько были опубликованы в сборнике "Раса нябёсаў на зямлі тутэйшай: Беларуская польскамоўная паэзія ХІХ стагоддзя: Вершы" (Мн., 1998).
		</p>
		
		<h2>Язэп (Іосіф) Ходзько </h2>
		<p class="img-float-left" >
		<a href="../../images/img-figures/chodzko/jozef.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Ходзько Язэп (Іосіф)" src="../../images/img-figures/chodzko/jozef_thumb.jpg" class="thumbInfo">
		</a>
		</p>
		<p>
		Иосиф Ходзько (Jozef Chodzko) 1800-1881 — генерал-лейтенант Генерального штаба российской армии, военный геодезист, географ и картограф, учёный-исследователь с мировым именем, инициатор и главный исполнитель Большой Кавказской триангуляции, названной его именем. Член-учредитель и Руководитель Кавказского Отдела Императорского Русского географического общества;  
		Кавказский Отдел ИРГО назначил Премию его имени за лучшее географическое сочинение по географии Кавказа.
		</p>
		<p>
		Родился в местечке Кривичи на реке Сервечь, в  семье Яна Ходзько. 
		</p>
		<p>
		Поступил в  Виленский университет, где близко сошелся с Адамом Мицкевичем, Томашем Заном и вступил 
		в <a href="../terms/filomaty.php">Общество филоматов</a>. 
		После разгрома организации , чтобы избежать ареста и ссылки, вынужден был поступить на военную службу.
		</p>
		<p>
		Наполеоновские войны в Европе показали слабую обеспеченность точными топографическими картами. Император согласился с предложением российской топографической службы о необходимости новейших топогеодезических измерений.
		В 1816 было начато первое масштабное в России геодезическое измерение - триангуляция Виленской губернии. 
		</p>
		<p>
		До 1831 Ходзько был одним из самых деятельных сотрудников экспедиции  по производству геодезических и астрономических определений триангуляции  вначале Виленской, а затем Гродненской, Минской и Курляндской губерниях.
		За Кавказскую триангуляцию в 1868 году ему была присуждена высшая награда Императорского Русского географического общества — Большая золотая Константиновская медаль. Комиссия  дала такой отзыв об его трудах:
		</p>

		<p>
		Праведная София Юрьевна, княгиня Слуцкая, княгиня Радзивилл (1 мая 1585 - 19 марта 1612) — последняя княгиня Слуцкая и Копыльская, 
		жена подчашего литовского Януша Радзивилла, последняя представительница княжеского рода Олельковичей-Слуцких.
		</p>
		<p>
		Святая, чтимая белорусской православной церковью. Канонизирована в лике Праведных.
		</br>Даты памяти: 1 апреля (19 марта по ст.ст.), 3-я неделя по Пятидесятнице (Собор белорусских Святых).
		</p>
		<p>
		"Праведный" — святой, который, живя в миру, вел святую и праведную жизнь. Праведная София Слуцкая считается заступницей православия в Великом княжестве Литовском. 		
		Мощи святой находятся в Свято-Духовом кафедральном соборе Минска (главный храм белорусского экзархата РПЦ).		
		</p>
		<p>
		Олельковичи-Слуцкие — княжеский род, ветвь наследников в. князя <a href="../figures/olgerd.php">Ольгерда</a>. Представители рода в XV—XVI веках 
		занимали привилегированное положение в <a href="../terms/wkl.php">Великом княжестве Литовском</a>. 
		В Слуцке при дворе Олельковичей существовала школа и с 1581 типография. 
		Княжна Александра Олелько-Слуцкая была женой в. гетмана <a href="../figures/ostrogski.php">Константина Острожского</a> и матерью 
		<a href="../figures/ostrogski.php">Константина-Василия Острожского</a>. Отец княжны Софии — князь Юрий Слуцкий — фундатор
		"Слуцкого евангелия" (1582), памятника белорусской письменности конца XVI века, подаренного им Слуцкому Свято-Троицкому монастырю
		</p>
		

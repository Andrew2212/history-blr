		<p>
		В два года княжна потеряла родителей и стала самой богатой невестой в Речи Посполитой. Опеку над ней взяли Ходкевичи, 
		родственники по женской линии. Младенчество и детство княжна провела в Берестье и Вильне.
		</p>		
		<p>
		В  1594 опекун княжны Юрий Ходкевич, находившийся в денежном долгу у Радзивиллов, договорился отдать Софью, замуж за за Януша Радзивилла. 
		Был назначен день бракосочетания — 6 февраля 1600 года, сразу после достижении Софией совершеннолетия (15-летия).
		</p>		
		<p>
		В то время в Литовском Трибунале проходила тяжба между Радзивиллами и Ходкевичами по поводу имения Копысь. 
		Ко дню свадьбы претензии достигли апогея — Копыльское княжество входило в часть приданого. Обе стороны готовились к войне. 
		Войска Радзивиллов приступили к Вильне. Послы короля, как и униатский митрополит Ипатий Потей не смогли примирить стороны. 
		</p>		
		<p>
		София дает согласие на брак, когда войско Радзивиллов стояло в самой Вильне, где в "каменице" ждали сражения Ходкевичи. 
		Армии и артиллерии в конфликте предполагалось задействовать больше, чем в <a href="../events/battle_kircholm.php">битве под Кирхгольмом</a>.
		Позже Ходкевичи примирились 
		с Радзивиллами и возникавшие споры уже навсегда решались только мирным путем.
		</p>
		<p>
		Венчание состоялось 11 октября 1600 года в одном из соборов Бреста.
		</p>
		

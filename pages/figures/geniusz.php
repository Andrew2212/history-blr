		<h3>"Споведзь"
		</br><small>короткие цитаты</small></h3>
		<i>
		<p>
		Да мяне падышоў Вітушка, сказаў, што завязе мяне да Арсеньневай. Калі апынуліся ля ягонай машыны, такога нейкага «казла», 
		ён спыніўся й кажа: «Ларыса Геніюш, я ненавіджу немцаў, я толькі й толькі беларус, але нам трэба здабыць яе для народу, гэту зямлю нашу,
		 і трэба нам усё дзеля гэтага выкарыстаць... Я мусяў сказаць вам гэта». — «А я й так гэта ведала»
		</p>
		<p>
		Мы паехалі да Кушаляў. Натальля Арсеньнева была сама ўдома, мы прывіталіся. Я памылася. Раптам увалілася ў хату поўна моладзі, яны хацелі, каб я пабыла з імі, 
		у іхняй кампаніі гэты вечар. Вечарам прыйшоў Кушаль. Мала ў жыцьці я сустракала так шляхотных, бескарысных патрыётаў, якім быў Кушаль. Ён, як і Вітушка, жыў і дыхаў толькі тым,
		 што мог нешта рабіць для Беларусі.
		</p>
		<p>
		Мы былі ў такім становішчы, што нам нельга было абмінуць найменшай магчымасьці, гдзе можна было б заявіць аб тым, што мы людзі й зямля гэтая 
		— наша зямля! Аб сабе з нас мала хто думаў, думалі аб народзе, аб справе. 
		</p>
		<p>
		«Малая розьніца між немцамі й рускімі для нас», — адказаў мне Кушаль, калі я ганіла немцаў. «Яны нелюдзі, але й тыя нічога добрага, акрамя лагероў і сьмерці, 
		пакуль што не прынясьлі нам»...
		</p>
		</i>
		

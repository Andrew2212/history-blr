		<i>
		<p>
		У Прагу пачалі зьяжджацца ўцекачы з Беларусі, з Украіны. Гэта былі пераважна гарачыя патрыёты, якія з болем ад'яжджалі ў сьвет, і толькі таму, 
		што беспэрспэктыўнасьць беларускай нацыянальнай працы пры саветах была ім ведамая даўно.		
		</p>
		<p>
		Чэхі былі амаль стопрацэнтна супраць немцаў і прагна чакалі рускіх, вельмі ружова рысуючы сваю будучыню пры іх. У нас наконт гэтага ніякіх ілюзыяў не было, 
		горкай практыкай лягла на маю сям'ю хваленая рознымі агентамі ўсходняя культура...
		Пазьней скажа мне нейкі журналіст, які забрыў на дапрос у знамянітую «амэрыканку», што якая розьніца — з чэскім мы грамадзянствам ці з іншым, гэта толькі 
		пытаньне пару гадоў, калі ўсё гэта будзе — рускае... Адна дзяржава... 
		</p>
		<p>
		Колькі іх ужо перавярнулася, тыранаў на нашай зямлі, і кожны крута загадваў найстрашнейшую нагласьць: любіць сябе! «Собака бьющую руку лижет». 
		О, не, не, не, мы нашчадкі Міколы Гусоўскага, мы племя Скарыны, браты Кастуся!..
		</p>
		<p>
		Усе мы былі для саветаў нявольнай ненавіснай масаю. Працаваў хто з немцамі, ці толькі ў Бога верыў, ці толькі хацеў гаварыць на роднай мове, ці здуру расказаў 
		страшны сон пра Сталіна - усе разглядаліся аднолькава, за выняткам тых, незалежна ад іх віны, якія верай і праўдаю кляліся ў вернасьці «бацьку Сталіну» 
		і ў лютай нянавісьці да ўсяго іншага сьвету. Такія былі пераважна лагернымі прыдуркамі, і іх рукамі часта нас нямала мучылі. 
		Прыдуркамі, безумоўна, былі пераважна расейкі, але здараліся і іншыя...
		</p>
		<p>
		Я была лагернай энцыкляпэдыяй, і да мяне часта зьвярталіся ў спорах. Стаімо раз шэрай, ад сьвежага сьнегу прамоклай грамадкай пад сьцяною нейкага інцінскага «палаца», прад намі праходзяць інцінскія жыхаркі, 
		едуць студабэкеры. Дзяўчаты з Прыбалтыкі і ўкраінкі пачынаюць крытыкаваць іх уборы, негустоўныя, прэтэнсыяльныя ў гэтым нялюдзкім клімаце. 
		Расейкі тут і сьцяліся з імі, што ў іх лепш, як у Эўропе! «I машыны?» — пытаюцца ўкраінкі. «Да, и машины». — «Та, пані Ларысу, скажыце вы: е ў іх маркі своіх машынів?» 
		Кажу: «Е». Тут мае дзяўчаткі й скісьлі... «А яка ж у іх марка?» — «Яка? А «чорны воран»,— кажу. Ну й пачаўся хохат.
		</p>
		<p>
		Нічога не было для мяне немагчымага ў жыцьці. Я ўмела выйсьці з любой бяды, бо кожнаму змагла лагічна даказаць сваю праўду. Я не магла ратавацца толькі ад саветаў,
		 бо тут была адна толькі магчымасьць: прадаць сумленьне, чорнае назваць белым, адрачыся ад Хрыста і ўпасьці на твар перад Сталіным. 
		 Не, не, не!!! Даруй, малы пакінуты юнак, даруй, мой сын... Хачу, каб некалі ты зразумеў мяне, каб дараваў свае мучэньні. 
		 Не было ў мяне выбару, сын, — Бацькаўшчына і яе інтарэсы важней за цябе, за мяне, за нашага тату...
		</p>
		</i>
		

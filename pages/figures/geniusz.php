		<h1>Лариса Гениюш</h1>
		
		<p>
		В ее судьбе и творчестве тесно увязано все, что Советская власть стремилась вычеркнуть из нашей истории 
		— <a href="../terms/bnr.php">БНР</a> и <a href="../figures/kalinowski.php">Калиновский</a>, 
		ГУЛАГ и жаўнеры Монте-Касино, Беларуская Краевая Абарона и Белорусская Освободительная Армия. 
		В своих воспоминаниях она тепло отзывалась об <a href="../figures/tarashkevicz.php">Олехновиче</a> и Витушко, Арсеньевой и Кушеле.
		</p>
		<p>
		Памятник Ларисе Гениюш в Зельве — это, пожалуй, единственный памятник в РБ с символикой БНР 
		— <a href="../terms/blr_flag.php">БЧБ-флагом</a> и <a href="../terms/blr_gerb.php">Погоней</a>.
		</p>
		

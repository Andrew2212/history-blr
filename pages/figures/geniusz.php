		1948 Органы МГБ арестовывают Ларису и Янку. Оба находятся в тюрьмах Чехословакии, Львова, с октября 1948 года в тюрьме в Минске. 
		Здесь Ларису допрашивает министр госбезопасности БССР Лаврентий Цанава.
		</p>		
		<p>
		1949 года Ларису и Янку Гениюшей приговаривают к 25 годам лишения свободы в лагерях ГУЛАГ (Мордовия, Коми). 
		</p>		
		<p>
		1956 После смерти Сталина Гениюши выходят на свободу (срок пакараньня быў зьменьшаны). Не реабилитированы (считаются врагами народа) до сих пор. 		
		</p>		
		<p>
		Трагична судьба ее семьи: в Казахстане умерла мать и две сестры. Брат Аркадий погиб 27 июля 1944 в битве под Монте Кассино (армия Андерса) в Италии. 
		Другой брат, Ростислав, погиб в апреле 1945 года под Берлином в армии Берлинга (1-я польская армия).

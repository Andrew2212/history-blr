		<p>
		Лариса Антоновна Гениюш, в девичестве Миклашевич (9 августа 1910 - 7 апреля 1983) — белорусская поэтесса, писательница и общественный деятель.
		</p>		
		<p>
		Родилась в имении Жлобовцы Волпинской волости Гродненского уезда (Волковысский район) в семье зажиточного крестьянина-землевладельца.  
		</p>		
		<p>
		1935 Выходит замуж за Янку Гениюша, который учился в Карловом университете в Праге, а потом работал врачем. После рождения сына выезжает к мужу в Прагу. 
		</p>		
		<p>
		1939 Красная армия вступила на территорию Западной Белоруссии. Отец Ларисы, Антон Миклашевич, был расстрелян, мать и две сестры сосланы в Казахстан.
		Лариса Гениюш публикует свои первые стихи в газете белорусских эмигрантов-националистов "Раніца". 
		Первый сборник "Ад родных ніў" выйдет в 1942 году. 
		</p>		
		<p>
		1943 Лариса Гениюш становится Генеральным секретарем Рады БНР в эмиграции. Она сохраняет и упорядочивает архив Белорусской Народной Республики, 
		помогает белорусским эмигрантам, политическим беженцам и военнопленным. Позже, в тюрьме НКВД, ее  будут допрашивать, чтобы получить сведения об архиве.
		</p>
		

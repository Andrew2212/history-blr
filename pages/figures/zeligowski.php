		<p>
		Люциан Мечислав Рафаил Желиговский (Lucjan Mieczyslaw Rafal Zeligowski, 19.10.1865 - 09.07.1947) — 
		генерал и политический деятель, глава Срединной Литвы.
		</p>
		<p>
		Родился в шляхетской семье участника <a href="../events/uprising_1863.php">восстания 1863 года</a> Густава Желиговского в Ошмянском повете. 
		По окончании Рижского военного училища служил в российской армии. Участвовал в Русско-японской войне 1904-1905. 
		Во время <a href="../events/world_war_1.php">Первой мировой войны</a> в звании полковника командовал пехотным полком. 
		</p>
		<p>
		После Февральской революции Люциан Желиговский стал одним из организаторов польских национальных частей. 
		В польской армии стал сначала командующим Литовско-белорусского фронта, затем — командиром пехотной дивизии (1919) во время советско-польской войны. 
		</p>
		<p>
		С октября 1920 генерал  Люциан Желиговский — командир 1-й Литовско-белорусской дивизии. По его приказу дивизия заняла 
		<a href="../places/wilno.php">Вильню</a> (9 октября 1920) и часть Юго-восточной Литвы. На занятых территориях было образовано 
		самостоятельное государство, формально независимое от Польши — <a href="../terms/lit_bel.php">Срединная Литва</a>. 
		Генерал Желиговский намеревался восстановить <a href="../terms/wkl.php">Великое Княжество Литовское</a>.
		</p>
		

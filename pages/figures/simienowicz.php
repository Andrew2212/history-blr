		К. Семенович уходит в отставку и возвращается в Нидерланды.
		В Амстердаме, при покровительстве эрцгерцога Леопольда-Вильгельма Габсбурга, пишет и издает в 1650 г. свою главную книгу 
		— трактат “Аrtis mаgnае аrtillеriае, pаrs primа” («Великое искусство артиллерии, часть первая») на латинском языке, 
		в которой была описана многоступенчатая ракета, треугольное крыло и ракетная система залпового огня. 
		Книга сразу же была переведена на многие европейские языки, и на два столетия оставалась самой популярной научной работой по артиллерии.

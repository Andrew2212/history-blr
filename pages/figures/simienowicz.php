		в сражении Речи Посполитой с татарами под Ахматовым 30 января 1644 г.
		</p>
		<p>
		Вскоре К. Семенович выехал в Нидерланды, где в ходе Тридцатилетней войны (1618-48 гг.) участвовал в осаде многих городов. 
		В 1646 г. король Речи Посполитой  Владислав IV Ваза отозвал его из Нидерландов и назначил на службу в коронную артиллерию 
		Польского Королевства на должность инженера артиллерии.
		</p>

		Учился, скорее всего, в Виленской академии, но точных подтверждений нет. 
		Не подлежит сомнению тот факт, что познания К. Семеновича были в духе эпохи Возрождения, требовавшей универсальных гениев: 
		кроме физики, химии, гидравлики, оптики, акустики и так далее, он «изучил много искусств, как свободных, так и механических»: 
		изобразительное искусство, скульптуру, граверное и литейное искусство. 

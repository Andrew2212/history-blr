		<h3>Софийский собор и язычество</h3>
		<p>
		Забавная деталь. Полоцкий Софийский собор (1030-1050) строили два князя-язычника 
		— Брячеслав, что <i>"обращался к волхвам и сын его рожден от волхования"</i>, и его сын Всеслав, которого летописи описывали как оборотня-воўкалака 
		и кому потомки дали имя колдун-Чарадзей.
		</p>
		<p>
		Первым князем Полоцким, носившим крестильное имя, был сын Всеслава — <a href="../terms/dwina_stouns.php">Рогволод-Борис</a>. 
		С его именем связаны <a href="../terms/dwina_stouns.php">Двинские камни</a> — следы массового крещения языческих капищь.
		</p>
		<p>
		В первой половине XI века были построены три Софийских собора — в Киеве, Полоцке и Новгороде. 
		Почти два десятка Киево-Новгородских князей того периода стали канонизированными святыми. 
		</p>
		<p>
		В Полоцком княжестве до середины XII века был один святой — креститель Полоцка <a href="../figures/torwald.php">Торвальд Кодранссон</a>. 
		Почитание внучки Всеслава — Ефросинии Полоцкой (1101-1167), — началось только в конце XII века.		
		</p>
		

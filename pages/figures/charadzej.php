		<h3>Отношения с соседями и битва на Немиге</h3>
		<p>
		Всеслав Чародей продолжил традицию противостояния Ярославичей и <a href="../figures/rogvolod_rogneda.php">Рогволодовичей</a> 
		— в 1065 году держал в осаде Псков и разграбил Новгород. 
		Против него выступили совместно правившие трое Ярославичей — Изяслав Киевский, Святослав Черниговский и Всеволод Переяславский. Они направились на Минск. 
		Войска Ярославичей и Всеслава сошлись на реке Немиге.
		</p>
		<p>
		1067 <a href="../events/battle_nemiga.php">Битва на Немиге</a>. Эпичное сражение, вызывавшее трепет у летописцев. 
		Первое упоминание о Минске и Дудутках. 
		По итогам битвы Всеслав Чародей был приглашен на переговоры, пленен, освобожден киевским вече и избран киевским князем.
		</p>
		<p>
		Про результаты этой битвы Полоцкая (считается утраченной) и Киевская летописи рассказывают по-разному. 
		Так, Киевская летопись говорит о поражении Всеслава и его бегстве, тогда как Полоцкая предлагала другую версию: 
		о больших потерях с обеих сторон и отступлении Ярославовичей, которые спустя несколько месяцев встретились с Всеславом и просили о мире.
		</p>
		<p>
		Княжение Всеслава в Киеве было недолгим — в том же году он вернулся в Полоцк, не желая участвовать в чуждой ему войне Киева с Польшей (между Полоцким княжеством и 
		Польшей лежали земли <a href="../terms/yotving.php">ятвягов</a> и волынские поселения и общих границ они не имели).
		</p>
		
		

		<p>
		Хелена Скирмунт (05.11.1827-01.02.1874) — художница и скульптарка з роду Скирмунтов, племянница <a href="../figures/orda.php">Наполеона Орды</a>.
		</p>
		<p>
		Род Скирмунтов  известен на Полесье с XIV века. Начиная с  Симона Скирмунта  (1747-1835) 
		—  маршалка брестского воеводства и депутата сейма, — род Скирмунтов становится предпринимательской династией. 
		К 1860-ым   работает сахарный и винокуренный заводы, налажена мелиоративная система в пойме Ясельды, начато воспроизводство леса. 
		Продукция суконной фабрики Скирмунтов на Парижской и Московской выставках отмечена серебряной и золотой медалями.
		Последним видным представителем рода был <a href="../figures/skirmunt.php">Роман Скирмунт</a> — Премьер-министр БНР.
		</p>
		<p>
		Живописи Хелена Скирмунт училась в Вильне у Винцента Дмаховского — "Клода Лоррена виленских окраин". 
		В 1844-45 путешествовала по Европе  и продолжала обучение живописи и скульптуре. 
		</p>
		<p>
		Большинство ее работ были утрачены в во время войны и сохранились только в фотокаталоге 1933 года. 
		Дневник Хелены лег в основу книги "Z zycia Litwinki 1827-1874" Бронислава Залеского. Цитаты приведены по изданию <a target="_blank" href="https://www.facebook.com/Xypcik.khursik/">выдавецтва Віктара Хурсіка &copy;</a> 2009.
		</p>
		

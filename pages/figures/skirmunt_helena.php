	<h2>Бронислав Залеский "Z zycia Litwinki 1827-1874"</h2>
	
	<div class="img-float-left">
		<a href="../../images/img-figures/skirmunt_helena/bronislaw.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Бронислав Залеский" src="../../images/img-figures/skirmunt_helena/bronislaw_thumb.jpg" class="thumbInfo">
		</a>
	</div>
	<p>
	Бронислав Залеский (1819-1880) — политический деятель, журналист, издатель, историк и художник, писал под  псевдонимом  "Литвин" (Licwin).
	</p>
	<p>
	Подробная биография Хлены Скирмунт "З жыцьця літвінкі" была написана Залеским на основе ее писем и дневника.
	Книга, с подробностями передающая дух времени, во многом созвучна з "Каласы пад сярпом тваім" Караткевіча.
	</p>
	<p>
	Печальный парадокс. Книги, написанные на польском "тутэйшыми литвинами" в РБ считаются иностранными, а написанные на русском иностранцами, 
	никакого отношения к нашему Краю не имеющими — называются "нашей литературой".
	</p>
	<p>
	<i>"Сапраўднае дзіця і кветка тамашняга грамадства пані Скірмунт нарадзілася ў адным з аддаленых куткоў Літвы і ўсім сэрцам зраслася з той зямлей. 
	Гэта абсолютна наша сучасніца жыла ва ўмовах агульнага вясковага жыцця і спазнала ўсе змены нашага грамадства. 
	Яна прайшла праз страту маемасці, турэмнае зняволенне, высылку, пры гэтым адной толькі думкай i пачуццем пераўзыходіла шматлікіх сваіх сучаснікаў"</i>, 
	- так описывал он свою современницу.
	</p>
	<p>
	Оригинальное издание "Z zycia Litwinki 1827-1874" 1876 года можно посмотреть <a target="_blank" href="http://www.dbc.wroc.pl/dlibra/docmetadata?id=11020&from=publication">ТУТ</a>
	</p>
	<p>
	Сучаснае выданьне перакладу на беларускую мову ажыцьцявіла <a target="_blank" href="https://www.facebook.com/Xypcik.khursik/">выдавецтва Віктара Хурсіка &copy;</a> ў 2009 годзе.
	</p>
	<p>
	1819 Залеский родился в <a href="../terms/shliahta.php">шляхетской</a> семье в местечке Вызна Минской губернии. В 1837 году поступил в Дерптский (Тарту) университет, 
	но за участие в студенческих суполках, был исключен,  арестован и выслан в Чернигов. В 1842 году окончил Харьковский университет.
	</p>
	<p>
	По возвращении поселился в Вильне. В 1846 году последовал второй арест —  и он был  отправлен рядовым в Оренбургский корпус, 
	в составе которого участвовал в войне в Хиве и Коканде (Узбекистан). Здесь он подружился с  Сигизмундом Сераковским (будущим лидером восстания 1863 года) и Тарасом Шевченко.
	</p>
	<p>
	Вернувшись на  родину, Бронислав Залеский служил в редакционных комиссиях по подготовке крестьянской реформы. 
	В 1860 году получил заграничный отпуск и более в Российскую империю не возвратился.
	</p>
	<p>
	В эмиграции  Залеский поддерживал связи с революционными организациями — ему, в частности, была поручена покупка оружия для повстанцев 
	<a href="../terms/sewero_zapadny_kraj.php">Северо-Западного края</a>. 
	С 1866 года он был секретарём Историко-литературного общества (Towarzystwo Historyczno-Literackie).
	</p>
	<p>
	С 1868 года директор Польской библиотеки в Париже, член Академии знаний (предшественницы Польской АН), автор и издатель биографических и мемуарных работ. 
	Умер в 1880 на юге Франции, в Ментоне.
	</p>
		
		
		
	

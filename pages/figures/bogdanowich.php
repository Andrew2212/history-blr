		<p>
		Богданович, Максим Адамович (9 декабря 1891, Минск - 25 мая 1917, Ялта) — белорусский поэт, публицист, литературовед, переводчик; 
		классик белорусской литературы, один из создателей современного литературного 
		<a href="../terms/blr_mova.php">белорусского языка</a>.
		</p>
		<p>
		Отец поэта — Адам Богданович — учитель минского городского училища, впоследствии белорусский этнограф, фольклорист, историк культуры. 
		Мать, Мария Мякота, <a href="../terms/shliahta.php">шляхетского рода</a> герба "Курч", умерла, когда сыну исполнилось пять лет. 
		</p>
		
		<p>
		В ноябре 1896 года Адам Богданович с детьми переехал по службе в Нижний Новгород. Здесь у него завязались дружеские отношения с Максимом Горьким, с которым они вскоре породнились, женившись на сёстрах Волжиных. 
		Горький часто бывал у них дома, он оказал влияние на любовь мальчика к литературе.
		</p>
		<p>
		Окончив в 1911 году Нижегородскую гимназию, Максим Богданович посещает <a href="../places/wilno.php">Вильню</a>, знакомится с <a href="../figures/lastowski.php">Вацлавом Ластовским</a>, 
		<a href="../figures/luckiewicz_anton.php">Антоном</a> и <a href="../figures/luckiewicz_ivan.php">Иваном Луцкевичами</a> 
		и другими деятелями белорусского Возрождения. 
		Под впечатлением от посещения <a href="../terms/blr_museum.php">музея братьев Луцкевичей</a> с коллекциями древних раритетов он пишет стихотворение "Слуцкие ткачихи".
		</p>
		<p>
		</p>
		<p>
		В том же году Максим Богданович намеревался поступить на Филологический факультет Петербургского университета, но из-за недостатка средств 
		и сырого климата столицы, возвращается в Ярославль к отцу, поступив в Демидовский юридический лицей.
		</p>
		<p>
		В 1909-13 годах поэт переводит на белорусский язык стихи Овидия, Горация, французского поэта Поля Верлена. Кроме того, в этот период Максим Богданович занимается разработкой концепции истории развития белорусской литературы от древности до начала XX столетия (статьи "Глубины и слои", "Краткая история белорусской письменности до XVI столетия", "За сто лет. Очерк истории белорусского письменности", "Новый период в истории белорусской литературы").
		</p>
		<p>
		В Вильне, в начале 1914 года в типографии Мартина Кухты при денежной поддержке Ивана Луцкевича и 
		Марии Магдалены <a href="../figures/radziwilly.php">Радзивилл</a> тиражом 2000 экземпляров издан единственный прижизненный сборник произведений Максима Богдановича "Венок" (на титуле обозначен 1913 год).
		</p>
		<p>
		В благодарность к княгине на титульном листе книги было постановлено поместить знак лебедя — отсылка 
		к гербу <a href="../figures/zawisza.php">Завишей</a>, к которым принадлежала Мария Магдалена .
		</p>
		

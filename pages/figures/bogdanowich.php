		<p>
		Летом 1916 года, закончив лицей, Максим Богданович вернулся в Минск. Хотя он был уже тяжело болен, но много работает в Минской губернском 
		продовольственном комитете и в Белорусском комитете помощи жертвам войны, а свободное время отдает литературному 
		творчеству и организации молодежных кружков, которым старается придать общественно-просветительный и национально-революционный характер.
		</p>
		<p>
		В это время Максим Богданович написал такие знаменитые произведения, как "Потерянный лебедь" и "Погоня", ставшее национальным гимном.
		</p>
		

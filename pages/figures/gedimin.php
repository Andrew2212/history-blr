<p>
По поводу генеалогии Гедиминовичей.
</p>
<p>
Некоторые современные историки, оспаривая выводы Императорского географического общества (хотя и не имея доступа к его архивам — с Полоцкой летописью после 
Татищева уже никто не работал), считают Гедимина потомком <a href="../places/samogitia.php">жмудинов</a>, которые 
<i>"уже давно сидели на княжеских престолах уделов Полоцкого княжества — оно было ослаблено и туда приглашались/назначались князья 
из сильной <a href="../places/samogitia.php">Летувы</a> (Жмуди), поэтому присоединение Полоцких земель происходило добровольно и мирно"</i>
</p>
<p>
Сразу возникает такой вопрос. 
</br>Насколько вероятно приглашение (мирное — никакого завоевания не было) на княжеский престол в христианский центр вождей аборигенов-язычников
</p>
<p>
[ <small>
<i>"Самогиты носят плохую одежду и притом в огромном большинстве случаев пепельного цвета. 
Жизнь свою проводят они в низких и притом очень длинных хижинах; посредине в них поддерживается огонь, у которого сидит отец семейства 
и видит скот и всю свою домашнюю утварь. Ибо у них в обычае держать скот, без всякой перегородки, под той же кровлей, 
под которой живут сами. Более знатные употребляют также рога буйволов в качестве кубков. ...Землю взрывают они не железом, а деревом... 
Собираясь пахать, они обычно несут с собою очень много поленьев, которыми роют землю"
</i>
 </br>С.Герберштейн,<a target="_blank" href="http://www.vostlit.info/haupt-Dateien/index-Dateien/G.phtml?id=2044"> "Записки о Московии"</a>, 
 XVI век, о современных ему жмудинах. (В XIII веке было еще печальнее) </small>]
</p>
<p>
 и чем руководствовались жители, предпочтя их выходцам из соседних (Волынь, Киев, Смоленск, Новгород, Мазовия) княжеств, которые
 </br>- представляют мощное государственное образование
 </br> - ближе по культуре
 </br>- ближе по языку
 </br> - династически родственны
 </br>- живут в городах, знают письменность и подобие законов
</p>
<p>
И это при том, что в то время — время княжеско-вечевого строя — неугодных правителей довольно часто просто изгоняли.
</p>
<p>
 <a href="mindowg.php">Миндовг</a> был принят на ятвяжских землях — но он был король <a href="../terms/yotving.php">пруссов</a>, а "Ятъвежи были едного народу з литвою и з прусами старыми". 
 Он пришел из даже более цивилизованного края (сравни печати Миндовга, немецких/полабских князей и польские/литовские/русские).
 </p>
<p>
 Был (наверно) Рюрик — но он был варягом — представителем передовой культуры, религии и военного дела. 
</br>Был Чингиз Хан. Но очень долго воевал, пока его аборигены привели его к власти. 
</p>
<p>
А насчет "непривычных" имен... Единственный в. князь Литовский того периода, чья родословная точно известна — сын короля Руси Даниила Галицкого и Анны Смоленской. 
Звали его <a href="shwarn.php">Шварн</a>.
</p>
<p>
 Поэтому на сайте мы приводим версию Императорского Русского Географического общества, приведенную в труде <a href="../download/kirkor.php">"Живописная Россия"</a> под руководством П.П.Семенова-Тяньшаньского. 
 </p>


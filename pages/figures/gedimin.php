		Папе Римскому Иоанну XXII, ганзейским городам, францисканцам и доминиканцам (1323-1324) заявлял о своём желании 
		принять христианскую веру и приглашал в Литву рыцарей, ремесленников, купцов, земледельцев, священников.
		</p>
		<p>
		Был женат на Евне Полоцкой. Из семи сыновей Гедимина пятеро были христианами, все его дочери также были выданы замуж за христиан.
		</p>
		<p>
		После смерти князя Гедимина Великое княжество Литовское было разделено между семью его сыновьями и братом Воином. 
		</br>Забавно повторилась история — после смерти <a href="charadzej.php">Всеслава Чародея</a> Полоцкое княжество также было поделено между семью его сыновьями.
		</p>
		<p>

		<p>
		Казимир Малевич (23.02.1879-15.05.1935)— художник-авангардист, педагог, теоретик искусства, философ. Основоположник супрематизма — одного из 
		наиболее ранних проявлений абстрактного искусства новейшего времени.
		</p>
		<p>
		Носитель тредиционной <a href="../articles/blr_family_names.php">белорусской фамилии</a>, основавший в Витебске группу 
		"Утвердителей Нового Искусства" (УНОВИС), которая в 20-е годы продвигала супрематизм и сделала своим символом "Черный квадрат". 
		Благодаря ему и <a href="../figures/shagal.php">Марку Шагалу</a>, в искусствоведческий язык вошло словосочетание "витебская школа".  
		</p>
		<p>
		В Витебске же был написан главный теоретико-философский труд Малевича — «Супрематизм. Мир как беспредметность или вечный покой».
		</p>

		<p>
		1765-69 Тадеуш учится в созданной Станиславом Понятовским Рыцарской школе в Варшаве — фактически военной академии. 
		Особое внимание уделял изучению военной архитектуры и фортификации. 
		За успехи в обучении Костюшко получает королевскую стипендию и отправляется  в Париж для продолжения образования. 
		Учится в Королевской  академии живописи и скульптуры и посещает Военную академию. 

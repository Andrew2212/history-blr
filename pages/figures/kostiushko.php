		<h3>Речь Посполитая
		</br><small>Восстание 1794 года</small>
		</h3>
		
		<p>
		1772 <a href="../events/razbor_rechi.php">Первый  раздел Речи Посполитой</a> — аннексия части страны Пруссией, Россией и Австрией.
		</p>
		<p>
		1784 Костюшко возвращается в Речь Посполитую в своей маентак Сехновичи.
		</p>		
		<p>
		1787-91 Четырехлетний Сейм, принявший <a href="../events/constitution_3may.php">Конституцию 3 мая</a>, объявил об увеличении численности и реорганизации армии для защиты границ федерации. 
		Тадеуш Костюшко подает заявку на вступление в армию и получает должность генерал-майора, командует войсками на Волыни. 
		</p>		
		<p>
		1792 Создана Тарговицкая конфедерация — движение магнатов под покровительством России, направленное против реформ. На помощь конфедератам в Речь Посполитую 
		вторгаются российские войска. Тадеуш Костюшко командует войсками в битве под Дубенкой (18 июля) — одном из ключевых сражений войны 1792 года —  битве против пятикратно превосходящих 
		сил врага (5 тыс. у Костюшко против 25 тыс. российских). После битвы король польский и в. князь литовский 
		<a href="../figures/poniatowski.php">Станислав Август Понятовский</a> повысил 
		Тадеуша Костюшко до звания генерал-лейтенанта и наградил орденом Белого орла.  
		</p>
		<p>
		Однако по итогам войны Станислав Август присоединился к Тарговицкой конфедерации и приказал прекратить военные действия против русских. 
		</p>

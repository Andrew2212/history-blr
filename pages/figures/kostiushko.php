		<h4>1794 <a href="../events/uprising_1794.php">Восстание Костюшко</a></h4>
		<p>
		Узнав о том, что российский гарнизон покинул Краков, Костюшко прибыл в город в ночь на 23 марта 1794 года, 
		а на следующее утро на Главной площади объявил «Акт восстания граждан». Он был объявлен диктатором республики и принес публичную присягу. 
		Акт восстания провозглашал Тадеуша Костюшко верховным главнокомандующим национальными вооруженными силами и предоставлял ему всю полноту власти в стране.
		</p>
		<p>		
		7 мая Костюшко издает Поланецкий Универсал — акт, провозглашавший опеку правительства над крестьянами и объявлявший их лично свободными. Универсал позволял крестьянам вступать в повстанческую армию.
		</p>
		<p>
		В битве под Мацеевицами (10 октября) Тадеуш Костюшко был тяжело ранен и попал в плен.
		</p>
		

		<p>
		Восстание было подавлено. 25 октября русский генерал А. Суворов занял Варшаву и принял подписанную капитуляцию. 
		Через год, 24 октября 1795 года, после Третьего раздела, Речь Посполитая была полностью разделена между Россией, Австрией и Пруссией.
		</p>
		<p>
		1796 Вскоре после смерти Екатерины II Тадеуш Костюшко был освобожден и в 1798 отбыл в Филадельфию (США). Во всех приходских церквях 
		<a href="../download/west_russland.php">Западного края</a> во время воскресной службы зачитывалось распоряжение, согласно которому в случае появления его на российской земле Костюшко, он должен был быть арестован.
		</p>
		
		<h4>После восстания</h4>

		<p>
		1798 С приходом к власти во Франции Наполеона Бонапарта и началом <a href="../events/war_1812.php">Наполеоновских войн</a> Тадеуш Костюшко возвращается в Европу. 
		старался употребить все свое влияние на восстановление независимости Речи Посполитой 
		и призывал к освобождению крестьян от феодальной зависимости с предоставлением им земли.
		</p>
		<p>
		1799 Костюшко встречается с Наполеоном по вопросам восстановления Речи Посполитой и формирования польских легионов, 
		 однако взаимопонимания достичь не удалось.
		 </p>
		<p>
		 1800 Костюшко анонимно издает брошюру "Могут ли поляки добиться независимости" (Czy Polacy moga sie wybic na niepodleglosc).По его мнению, в борьбе за независимость не стоит опираться на чужую помощь. Освобождение должно свершиться путём народного восстания, но для этого необходимо освобождение крестьян. Народная армия большей частью будет плохо вооружена, однако 
		 <i>"нет оружия, которое могло бы противостоять косе, и нет в Европе войска, которого нельзя бы победить"</i>. 
		 Он считал, что наиболее подходящей формой войны является партизанская. 
		 Спустя годы, уже после поражения <a href="../events/uprising_1830.php">восстания 1830 года</a>, брошюра многократно переиздавалась.
		 </p>
		<p>
		1805 Когда французские войска подошли к границам Польши, Костюшко написал Наполеону письмо с требованиями гарантий установления шляхетской демократии, 
		которые тот проигнорировал.  Костюшко решил не появляться в Варшавском герцогстве и отказался присоединиться к армии новообразованного государства, 
		воевавшей на стороне Наполеона.
		</p>
		<p>
		1814 После поражения Наполеона он направил русскому царю письмо, в котором просил объявить всеобщую амнистию полякам, 
		провозгласить в Польше и <a href="../terms/wkl.php">Великом княжестве Литовском</a> конституционную монархию и, наконец, через десять лет отменить крепостное право там, 
		где оно было введено, наделить всех крестьян землёй. Однако, ни созданное Царство Польское, ни 
		<a href="../terms/sewero_zapadny_kraj.php">Северо-Западный край</a> не опрадали его надежд.
		</p>
		<p>
		1817 Анджей Тадеуш Бонавентура Костюшко умер в возрасте 71 года. 
		Своим завещанием он освободил крестьян в Сехновичах от введённой российскими властями крепостной зависимости.
		</p>

		
		<h3>Дань уважения</h3>
		<h4>Варшава</h4>
		<p>
		 Памятник Тадеушу Костюшко — точная копия памятника в Вашингтоне (1910), установленная спустя ровно 100 лет (в 2010).
		</p>
		
		<h4>Краков</h4>
		<p>
		Общественность Кракова, где был похоронен Тадеуш Костюшко, требовала от городских властей установки памятника. 
		В июле 1820 года Сенат Вольного города Кракова принял решение построить курган Тадеуша. В течение трёх лет в строительстве участвовали многочисленные добровольцы.
		</p>
		<h4>Австралия</h4>		
		<p>
		Гора Костюшко — высочайшая вершина Австралийского континента. Расположена на юге штата Новый Южный Уэльс, 
		близ границы этого штата со штатом Виктория на территории одноимённого национального парка.
		</p>
		

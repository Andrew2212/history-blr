		<h3>США
		</br><small>Война за независимость</small>
		</h3>		
		<p>
		1776 После завершения учебы Костюшко переехал в Северную Америку, где в звании полковника Континентальной армии принял участие в войне за 
		независимость США. В качестве военного инженера он разрабатывал укрепления и руководил их строительством, 
		в том числе легендарного форта в Вест-Пойнте (ныне Военная академия США). В 1783 году, в знак признания его заслуг, Континентальный конгресс повысил его в бригадные генералы.
		</p>		
		<p>
		Конгресс предоставил Костюшко американское гражданство. Он был принят в почётное Общество Цинцинната, куда входили известные участники войны за независимость. Джордж Вашингтон наградил его парой пистолетов, на которых была выгравирована надпись Ex pluribus unum ("Из многих одному").
		</p>
		

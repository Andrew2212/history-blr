		<p>
		Тадеуш Костюшко (04.02.1746 - 15.10.1817) — военный и политический деятель Речи Посполитой и США, участник Войны за независимость США, 
		организатор восстания 1794 года, национальный герой Польши, Беларуси, Летувы и США, почётный гражданин Франции.

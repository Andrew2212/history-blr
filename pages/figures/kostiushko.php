		женатого на дочери одного из князей Гольшанских и получившего от 
		<a href="../terms/dukes_wkl.php">Жигимонта I Старого</a> маентак Сехновичи (Малыя Сяхновічы — вёска ў Жабінкаўскім раёне Берасьцейскай вобласьці), 

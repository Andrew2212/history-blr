		<p>
		Сымон Будный (1530-13.10.1593) — социнианский проповедник XVI века, гуманист, просветитель, церковный реформатор.
		</p>
		<p> 
		Напечатал Новый Завет с комментариями и замечаниями, что стало первой во всемирной литературе попыткой радикальной 
		рационалистической критики Евангелия. 
		Поддерживал концепцию ограниченной, просвещённой монархии. Один из первых идеологов развития белорусской культуры на родном языке.
		</p>
		
		</div>
		
		<p>[ <small>
		Социнианство или антитринитарное учение получило свое название по имени Фауста Социна — итальянского теолога, 
		жившего и работавшего в Кракове. Возникло и было распространено в Речи Посполитой. 
		</small>
		</p>
		<p><small>
		Социниане в основание своего учения кладут только Священное Писание, учат, что оно написано по внушению Св. Духа. Отрицают догмат о Троице.
		</br>Они отрицают первородный грех, а также наклонность человека только ко злу: он свободен поступать дурно или хорошо. 
		Они считали, что Иисус явился затем, чтобы примирить не Бога с людьми, а людей с Богом. 
		Он умер крестной смертью, чтобы своей смертью запечатлеть истину проповедуемого Им учения, а никак не с 
		целью искупить первородный грех и умилостивить гнев Божий. Относительно воскресения из мертвых они учили, что оно будет духовное 
		и тела в нем участия не примут. Крещение детей они также допускали как древний обряд, но необходимости его не признавали.
		</small> ]
		</p>
		
		<p>
		Сымон Будный закончил Краковскую академию, учился в Италии, Швейцарии. По приглашению <a href="radziwilly.php">Радзивилла Черного</a> 
		стал пастором протестанской общины в Клецке, был магистром в <a href="../arch_mensk/mensk_kleck.php">Фарном костеле св. Троицы</a>. 
		В <a href="../places/nesviz.php">Несвиже</a> издавал свои сочинения на польском, латинском и белорусском языках. 
		Его деятельность поддерживал будущий канцлер ВКЛ <a href="volovicz.php">Астафий Волович</a>. Именно Волович приказал перевести из Вильни 
		в новую типографию шрифты самого <a href="skorina.php">Скарыны</a>.
		</p>
		<p>
		Одна из его работ "Катехизис, то есть наука стародавная христианская, от святого писма, для простых людей языка руского, в пытаниах и отказах събрана" (Несвиж, 1562) переиздавалась трижды: в Стокгольме в  1628 и в Минске в 2005 и  2012 гг.
		</p>
		<p>
		В посвящении к "Катехизису" он обращается к князьям Радзивиллам: 
		</br><i>"Абы ваши княжацские милости не только в чужоземских языках кохали, але бы ся тежь и того здавна славного языка словенского размиловати 
		и оным ся бавити рачили. Слушная бо речь ест, абы ваши милости того народу язык миловати рачили, в котором давные предки 
		и их княжацские милости панов отци ваших милости славне преднейшие преложеньства несуть".
		</i>
		</p>
		

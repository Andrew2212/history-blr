		В 1627 году, после своего возвращения в Киев, принимает униатство. Осенью 1627 года по инициативе Смотрицкого в Киеве был созван собор, на котором он обещал подготовить к изданию свой катехизис, но просил сперва позволить ему опубликовать свои размышления об отличиях между православной и католической церквями. В феврале 1628 года на соборе в г. Городке на Волыни Смотрицкий утверждал, что Западная и Восточная церкви в основных положениях не расходятся, так что возможно их примирение.

		<p>
		Мелетий (в миру - Максим Герасимович Смотрицкий, 1577-1633) — публицист, ученый филолог, церковный и общественный деятель, 
		автор "Грамматики", архиепископ Полоцкий.
		</p>
		<p>
		Ярый противник <a href="../events/unia_religion_brest.php">Брестской Унии</a>, к концу жизни перешедший в униатство.

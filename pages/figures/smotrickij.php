		<p>
		[ <small><i>Переход в Униатство — "Гэта неправільная фармуліроўка. Праваслаўныя заставаліся праваслаўнымі, проста біскупы дамовіліся, што цяпер яны пераходзяць 
		(ці вяртаюцца ў каталіцкім разуменні) у еднасць з Рымам."</i> — поправил редакцию представитель Рыма-каталіцкага Касцёла ў Беларусі.
		</small> ]
		</p>
		<p>
		Родился в семье писателя-полемиста Герасима Смотрицкого, знатока церковнославянского языка, участника редактирования и издания 
		"Острожской Библии" <a href="../figures/fedorow.php">Ивана Федоровым</a>.
		</p>
		<p>
		Учился в иезуитской Виленской академии, в протестантских Лейпцигском, Виттенбергском и Нюрнбергском университетах. Вернувшись, поселился у князя Соломерецкого под Минском, часто ездил в Минск, боролся против унии, вследствие чего многие униаты вернулись к православию и в Минске было основано православное братство.
		</p>
		<p>
		Под псевдонимом Теофил Ортолог в 1610 году напечатал свой знаменитейший труд "Плач", в котором критиковал как католических, так и православных иерархов.
		</p>
		<p>
		В 1618-19 годах вышел главный филологический труд "Ґрамматіки Славенския правилное Cvнтаґма" — основа церковнославянской грамматической науки на следующие 
		два века, выдержавшая множество переизданий и переводов. "Грамматика" состоит из нескольких частей: орфография, этимология, синтаксис, просодия. Она установливала 
		системы падежей, спряжения глаголов и др. Смотрицким было выдвинуто 5 ступеней обучения: <i>«зри, внимай, разумей, рассмотряй, памятуй»</i>.
		</p>
		<p>
		В 1620-1621 в борьбе с Унией  патриарх Феофан стремился назначить архиепископами сторонников РПЦ. Смотрицкого Феофан поставил архиепископом Полоцким, епископом Витебским и Мстиславским. 
		Однако никакой реальной церковной власти тот не получил: все названные кафедры с 1618 года занимал униат <a href="../figures/kunciewicz.php">Иосафат Кунцевич</a>.
		</p>
		<p>
		После гибели Кунцевича Мелетий, обвиненный в духовном сообщничестве с убийцами Иосафата, отправляется на Восток (1624-26). 
		Он побывал в Константинополе, посетил Египет и Палестину.
		</p>
		

		<p>
		Язеп Нарцизович Дроздович (13.10.1888 - 15.09.1954) — белорусский живописец, график, скульптор, писатель, фольклорист, этнограф и археолог.
		</p>
		
		<p>
		Родился на хуторе Пуньки Глубокского района в бедной шляхетской семье.
		Работал учителем рисования в женской гимназии в Минске. Одновременно, как иллюстратор, он активно сотрудничал с журналами и газетами.
		</p>
		
		<p>
		Во время своих путешествий уже по советской Беларуси он обошел сотни деревень, где разговаривал с местными жителями, писал ковры,
		 записывал народные песни, собирал и обрабатывал для словарей лексику народного языка.
		</p>
		
		<p><i>
		"Жизнь. Минутка вечности. Хотелось бы поработать и для наук, и для чистого искусства. 
		Да нет чем обеспечить время на это. А вот и подсчет всего  того, что смог сделать на протяжении последнего лета: 
		настенные ковры, карандашные и резные по дереву портреты для людей, и свободными от  работы вечерами около пол сотни лекций  
		по астрономии для не знающих, что такое небо, звезды, солнце и месяц."
		</i></p>
		<p><i>
		"С той поры как начал интересоваться астрономией — перечитал горы астрономической литературы, 
		а во сне блуждаю по удивительным загадочным странам; ... 
		Рассматривая книгу Фламариона "Популярная астрономия", как на чудо отыскал ту местность, которую видел во сне"
		</i></p>
		

		<p>
		Домейко Игнаци (31.07.1802-23.01.1889) — выдающийся геолог, минералог, географ и этнолог, ректор Чилийского университета и член многих научных обществ, 
		один из самых известных воспитанников Виленского университета и национальный герой Чили, Беларуси, Польши. 
		</p>		
		<p>
		Родился имении Медвядка Новогрудского уезда Минской губернии. 
		Начальное образование получил в школе монахов-пиаров в Щучине. Окончил физико-математический факультет Виленского университета, 
		изучал биологию, химию, математику, литературу, историю, иностранные языки.
		</p>
		В 1820 году стал членом тайной студенческой организации филоматов. Принимал участие в деятельности дочерних организаций филоматов — общества филадельфистов, 
		лучезарных и других, заведовал библиотекой филоматов.
		</p>
		<p>[ <small>
		<a href="../terms/filomaty.php">Филоматы</a>. Осенью 1817 г. по инициативе студентов университета — <a href="../figures/mickiewicz.php">Адама Мицкевича</a> из Новогрудка, 
		Томаша Зана из-под Молодечно и Яна Чечота из-под Кореличей
		— в <a href="../places/wilno.php">Вильно</a> было создано тайное Общество филоматов (“любителей наук”), одной из целей которого провозглашалась общественная работа 
		на благо угнетенной родины. Это были молодые люди 18-24 лет (1795-1802 г. р.). Самым юным участником общества был 
		Игнаци Домейко.
		</small> ]
		</p>		
		<p>
		Игнатий Домейко принял участие в национально-освободительном <a href="../events/uprising_1830.php">восстании 1830-31</a>. в Польше, Беларуси и Литве против царской России. 
		После поражения восстания вместе с вместе с другими повстанцами отступил в Пруссию.		
		</p>
		<p>
		[ <small>
		<i>"Домейко в дневниковых записях о восстании 1830-1831 практически в каждом случае не забывает указать национальную принадлежность 
		того или иного повстанца. Он четко выделял «коронных» (поляков), «<a href="../places/samogitia.php">жмудинов</a>» (современных литовцев) и «<a href="../terms/litwiny.php">наших литвинов</a>» 
		(современных белорусов)."
		</i> (с) <a target="_blank" href="http://archives.gov.by/index.php?id=162773">Архивы Беларуси</a>
		</small> ]
		</p>
		

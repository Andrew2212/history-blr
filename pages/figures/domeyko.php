		<p>
		После окончания ректорства Игнатию Домейко в свои 82 года удалось осуществить мечту всей жизни в Чили — съездить на родину. 
		В Беларуси, в памятной с детства усадьбе Жирбутовщина в Лидском уезде жила его дочь Анита (Анна).
		</p>		
		<p>
		Из письма Мицкевичу: <i>"Естественно, переродиться я никогда не сумею и надеюсь на Бога, что я — или в Кордильерах, или в Панарах (Вильня) 
		— все равно умру литвином..."</i>.
		</p>		
		<p>
		Умер он в Сантьяго-де-Чили 23 января 1889 года. На похоронах президент страны объявил Игнатия Домейко национальным героем. 
		В его честь выбита памятная медаль, в Сантьяго установлен величественный памятник с надписью: "Гранде Эдукадор" ("Великий Просветитель"). 
		Именем Домейко названа малая планета (астероид 2784), город, порт Пуэрто-Домейко, местечкo Пуэбло-Домейко, 
		вулканическая гряда в Андах (Кордильеро де Домейко). В его честь назван минерал (домейкит), головоногий моллюск Nautilus Domeycus, 
		фиалка Viola Domeykiano, сорт азалии, семейство растений Domeykiaceae, кактус Maihueniopsis domeykoensis, улицы в Сантьяго-де-Чили, 
		Вальпараисо и ещё в восьми городах Чили.
		</p>		
			
		
		

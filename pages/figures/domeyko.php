		<p>
		1832-37 Вынужденный оставаться за границей,  жил в Париже, принимал участие в общественной деятельности белорусско-польско-литовской эмиграции.
		Был помощником и консультантом Адама Мицкевича при создании самой крупной и значительной поэмы великого поэта "Пан Тадеуш" (послужил прототипом одного из героев - эконома Жеготы).
		 Посещал лекции в Сорбонне, изучал химию и геологические науки в Парижской Горной школе.
		</p>		
		<p>
		Поворотным в его жизни стало письмо Мицкевича, где он сообщал о возможности работать по контракту на 6 лет в Чили —
		  стране на другом полушарии земли. Срок этот растянется на 46 лет и Чили станет для него второй родиной.
		</p>		
		В Кокимбо Игнатий Ипполитович создал школу по подготовке специалистов, организовал лабораторию для анализов руды, собрал коллекцию минералов, 
		организовал метеорологическую службу, провел шесть экспедиций в Анды и Кордильеры. 
		Результатом стали открытия месторождений серебра, меди, золота, каменного угля, известной во всем мире чилийской селитры, 
		организация их добычи. 
		</p>
		

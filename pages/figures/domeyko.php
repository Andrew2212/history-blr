		<p>
		В 1844 г. он совершил путешествие на юг Чили, в страну индейцев-аравканов, 300 лет боровшихся с конкистадорами. 
		Написанная потом книга "Аравкания и ее жители" имела большой успех, переводилась на многие языки.
		</p>		
		<p>
		Для молодой Чилийской республики, только два десятилетия тому назад освободившейся от испанской колониальной зависимости и 
		строившей новую жизнь, Игнатий Домейко стал настоящим авторитетом. Поэтому чилийские власти попросили его не уезжать из страны.
		</p>		
		<p>
		С 1846 г. судьба Игнатия Домейко была связана с национальным университетом в столичном Сантьяго. 
		После Всемирной выставки в Париже 1867 г., где он  представлял промышленность Чили, его избрали ректором университета. 
		Эту должность он занимал 16 лет. Его учебник "Минералогия" на протяжении нескольких десятилетий использовался во многих 
		латиноамериканских странах.
		</p>		
		<p>
		Чилийские газеты писали: <i>"Пан Домейко не просто ученый, он — апостол науки. В стране нет более популярного и уважаемого имени, 
		чем имя Домейко"</i>. 
		Профессору Домейко была назначена самая высокая пенсия в стране. Такой не получали даже генералы, сражавшиеся за независимость Чили.
		</p>
		

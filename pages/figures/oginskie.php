			Михаил Казимир Огинский (1728-1800) — <a href="../terms/weliki_hetman.php">гетман великий литовский</a>, поэт, писатель, композитор и драматург. 
			</br>Чашник великий литовский 1744—48, писарь польный литовский 1748—64, воевода виленский 1764—68, генерал-майор войск ВКЛ. 
			</br>Инициатор строительства <a href="../places/oginski_channel.php">Огинского канала</a> — обновления <a href="../figures/torwald.php">пути из варяг в греки</a>.

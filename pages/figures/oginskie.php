			Григорий Антоний Огинский (1654—1709) — <a href="../terms/weliki_hetman.php">гетман великий литовский</a>. 
			</br>Чашник великий литовский 1684—87, хорунжий великий литовский в 1687—98, 
			староста жемайтский с 1698 и одновременно гетман польный литовский с 1703. Староста мстиславский и тельшяйский.
		</p>

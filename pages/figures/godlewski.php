		<p>
		Винцент Годлевский (16 ноября 1888 - 24 декабря 1942) — католический священник, руководитель националистического белорусского подполья 
		в годы немецкой оккупации, идеолог белорусской национальной идеи. Один из крупнейших деятелей Западной Белоруссии в 1930-х гг.
		</p>		
		<p>
		Родился в деревне Щуричи Волковысского уезда. Скончыў Віленскую каталіцкую духоўную семінарыю (1912) і Пецярбургскую 
		каталіцкую духоўную акадэмію (1916). Пасвячоны ў святары ў 1914. 
		<p>
		Пастырскую дзейнасць распачаў у 1916. Служыў вікарыем пры кафедральным касцёле ў Менску. Пасля Лютаўскай рэвалюцыі 1917 г. 
		прымаў удзел у дзейнасці беларускіх культурна-адраджэнскіх, рэлігійных і палітычных арганізацый.
		</p>		
		<p>
		У сакавіку 1917 абраны ў Беларускі нацыянальны камітэт, удзельнік Усебеларускага з'езда 1917 (будынак, у якім праходзіў кангрэс быў акружаны бальшавіцкімі атрадамі, а дэлегаты былі разагнаныя).
		 Пасля абвяшчэння <a href="../terms/bnr.php">БНР</a> уваходзіў у склад яе Рады,  працаваў ў камісіі па бежанскіх справах пры Радзе БНР.
		</p>		
		<p>
		Адзін з заснавальнікаў і лідараў Беларускай хрысціянскай дэмакратыі, рэдагаваў газету "Крыніца". Выкладаў у Нясвіжскай беларускай семінарыі. 
		На пачатку 1920-х гадоў пераехаў на Віленшчыну. У 1925 і 1927 арыштаваны польскімі ўладамі за нацыяналістычную дзйнасьць, 
		у 1927 асуджаны на 2 гады турмы.
		</p>		
		<p>
		1939 после раздела Польши между Германией и СССР жил в <a href="../places/wilno.php">Вильно</a>, занимался переводом на белорусский язык Библии и Нового Завета.
		 Был инициатором создания Белорусского национального фронта, также издавал газету "Беларускі Фронт".
		</p>		
		<p>
		С началом Второй мировой войны занимался образовательной деятельностью на должности главного школьного инспектора. 
		Возможность свободно передвигаться по оккупированной Беларуси использовал для создания белорусского национального сопротивления немецким 
		и советским войскам. 
		</p>
		

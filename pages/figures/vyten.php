		<p>
		Витень — великий князь Литовский (1293-1316), старший брат <a href="gedimini.php">Гедимина</a>, основателя династии Гедыминовичей. 
		</p>
		<p>
		Первый князь <a href="../places/litwa.php">Литвы</a> — княжества на <a href="../terms/yotving.php">ятвяжских</a> землях, 
		снованного <a href="mindowg.php">Миндовгом</a> — при котором к ВКЛ начали мирно присоединяться земли наследства 
		<a href="rogvolod_rogneda.php">Рогволодовичей</a> — Полоцкого княжества. До него Литва расширялась войной и только в сторону "ничейной" 
		<a href="../places/samogitia.php">Самогитии</a>.
		</p>
		<p>
		В правление Витеня были к <a href="../terms/wkl.php">ВКЛ</a> мирно присоединен Полоцк — 
		Полоцкая <a href="../places/sophia_sobor.php">София</a> нетронутой простояла 
		еще 300 лет (до прихода московского войска) — что несравнимо с походами Витеня на Польшу, описанными в 
		<a href="../download/chronicles_peter.php">Chronicon terrae Prussiae</a> Петером  из Дуйсбурга в 1326 г. 
		</p>
		<p>
		Им были отвоеваны у Галицко-Волынского княжества и присоединены к ВКЛ Брест и Подляшье. 
		</p>
		<p>
		За время своего правления Витень вел постоянные войны с Тевтонским орденом и <a href="../places/samogitia.php">жмудинами</a>, 
		стремясь сохранить целостность ВКЛ. В 1298 году Витень заключает союз с архиепископом 
		Риги и вместе с рижанами в битве на реке Трейдере (ныне Гауя) разбивает войско Ливонского Ордена. 
		Он также смог подавить восстание жмудских князей, которые выступали за союз с крестоносцами.
		</p>
		<p>
		По сообщению Густынской летописи, именно Витень установил герб ВКЛ — <a href="../terms/blr_gerb.php">Погоня</a>: 
		</br><i>«Витен нача княжити над Литвою, измысли себе герб и всему князству Литовскому: рыцер збройны на коне з мечем, еже ныне наричут Погоня».</i>
		</p>
		

		
		
		
		<h3>XIII-XIV век</h3>
	
		<p>
		В княжестве Литовском 
		к 1266-69 году, после смерти Войшалка(сына <a href="../figures/mindowg.php">Миндовга</a>) и <a href="../figures/shwarn.php">Шварна</a> 
		(зятя Миндовга и сына короля Руси Даниила Галицкого), 
		княжеская (королевская) династия обрывается.
		</p>
		<p>
		В Пруссии начинает доминировать Тевтонский Орден. В Ливонии папской буллой утвержен Орден Меченосцев (Ливонский Орден). 
		Основание Ливонским Орденом на Двине города Динабург (Даугавпилс) 
		в 1275 году снижает роль <a href="../terms/polotsk_principality.php">Полоцкого княжества</a> в транзитной торговле. 
		Установившаяся граница с Латгалией (Латвией) существует по сей день. 
		</p>
		<p>		
		<i>Anarchie</i>  или <i>sans duc.</i> (<i>анархия, без князя</i>; франц.) — так характеризовали в старых учебниках период с 1223 года 
		в Полоцком княжестве и с 1267 в княжестве Литовском. 
		Конец этого периода связывали с княжением детей Лютувера (Lutuver) — в 1307 князя Воина в Полоцке 
		и в 1291 князя Витеня в Литве. 

		<h3>Битвы под Лоевом</h3>
		<p>
		<a href="../events/battle_loew.php">Битвы под Лоевом</a> — сражения войск ВКЛ с Войском Запорожским во время восстания Хмельницкого. 
		В 1649 году Януш Радзивилл во главе литовского войска выступил на подавление восстания Хмельницкого на юге Великого княжество Литовского 
		и на коронных землях на Украине. В июле 1649 года во главе 7-тысячной армии разгромил в битве под Лоевом 20-тысячное повстанческое 
		войско Михаила Кричевского. В 1651 году снова под Лоевом разбил казацкие Мартына Небабы, затем вторгся на Украину и захватил Киев, 
		выбив оттуда казаков.
		</p>
		

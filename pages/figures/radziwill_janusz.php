		<p>
		Януш <a href="../figures/radziwilly.php">Радзивилл</a> (2 декабря 1612-1655) — воевода виленский (1653-1655), 
		<a href="../terms/weliki_hetman.php">великий гетман литовский</a> (1654-1655), 
		князь Священной Римской империи, внук Николая Радзивилла Рыжего.
		</p>
		<p>
		Владел обширными и многочисленными имениями. Ему принадлежали Биржи, Дубинки, Кейданы, Вижуны, Папели и Яшуны на территории Летувы, 
		Любча в Новогрудском повете, Дойлиды и Заблудув в Гродненском повете, Невель в Витебском повете, Себеж в Полоцком повете. 
		</p>
		<p>
		Януш Радзивилл — племянник <a href="../figures/zofia_sluck.php">святой Софии Слуцкой</a>, католической покровительницы православия в Беларуси, 
		чьи мощи лежат в минском Кафедральном соборе (главный храм белорусского экзархата РПЦ) — бывшем католическом монастыре бернардинок. 
		</p>
		<p>
		По имени Януша Радзивилла, как первого владельца, названа <a href="../download/letopis_radziwil.php">Радзивилловская летопись</a> 
		— список XV века "Повести временных лет" (летопись начала XII века), иллюстрированный 617-тью миниатюрами.
		</p>
		<p>
		Закончил кальвинистскую Слуцкую гимназию, продолжил своё образование в Лейпцигском, Альтдорфском и Лейденском университетах, 
		посетил Францию и Англию. В 1632 году король Владислав IV Ваза назначил Януша Радзивилла послом Речи Посполитой в Англии и Нидерландах.
		</p>
		<p>
		В 1646 году Януш Радзивилл стал гетманом польным литовским, в том же году получил должность старосты генерального жмудского. 
		Из-за старости гетмана великого литовского <a href="../figures/kishky.php">Януша Кишки</a> Януш Радзивилл стал фактическим 
		командующим армии <a href="../terms/wkl.php">Великого княжества Литовского</a>.
		</p>
		

<p>
		Во время "Потопа" шла еще и Северная война 1655-60 гг. между Речью Посполитой и Швецией. Незадолго до падения Вильни группировка магнатов и шляхты ВКЛ во главе с великим гетманом Янушем Радзивиллом и его племянником Богуславом в поисках выхода из трагической ситуации обратилась к шведскому королю Карлу X с просьбой о покровительстве Швеции над ВКЛ.
		</p>
		<p>
		17 августа 1655 года в военном лагере возле Кейданов была подписана Кейданская декларация. Под этим документом подписались почти 550 шляхтичей и священников. Они представляли войско ВКЛ, но подписывались от имени всего Великого Княжества. 
		</p>
		<p>
		Согласно декларации  ВКЛ разрывало унию с Польским королевством и вступало в конфедеративный союз со Швецией. Шведский король становился еще и великим князем литовским. Он обязывался уважать шляхетские вольности и привилегии, покровительствовать костёлу. В ВКЛ сохранялись свои органы власти, законы и суды. Положение всех религиозных конфессий сохранялось без изменений. Войско ВКЛ передавалось в подчинение шведскому королю, но не должно было воевать с польским войском. После освобождения территории ВКЛ от московских оккупантов все имения следовало вернуть прежним владельцам.
		</p>
		<p>
		По оценке современных беларуских историков, Кейданская декларация явилась компромиссом между Я. Радзивиллом и В. Гонсевским, протестантами и католиками, шляхтой Жамойтии и верховной властью ВКЛ. Они пошли на уступки друг другу ради того, чтобы спасти от захвата Москвой хотя бы остатки государства.
		</p>
		<p>
		Польша восприняла декларацию как государственную измену. Ведь по условиям Люблинской унии ВКЛ не имело права вступать в сепаратные дипломатические отношения и заключать международные соглашения. Но Статут ВКЛ 1588 года трактовал Великое Княжество как независимое государство, имеющее право вести собственную внешнюю политику.
		</p>
		<p>
		Шведский парламент отказался утвердить Кейданскую декларацию - его не устраивал отказ войска ВКЛ воевать против Польши, требование начать войну против Московии, принцип равенства ВКЛ и Шведского королевства. Поэтому переговоры продолжались.
		</p>
		<p>
		За два месяца, прошедших с момента объявления Кейданской декларации политическое положение ВКЛ и Польши резко изменилось.
		</p>
		<p>
		Шведский корпус (свыше 10 тыс. чел.) занял северную часть ВКЛ. Шведы требовали рекрутов, взымали подымный налог, реквизировали лошадей и фураж. Это вызывало сопротивление шляхты и крестьянства, которые перешли в некоторых местах к партизанской борьбе. Часть войска ВКЛ покинула лагерь в Кейданах, перешла в Вербилово и создала конфедерацию, против Радзивиллов. 
		</p>
		<p>
		В Польше шведские войска 8 сентября заняли Варшаву, а 25 сентября - Краков. К октябрю более двух третей территории Польши оказались под властью шведского военного командования и назначенной им администрации.
		</p>
		<p>
		Теперь шведские представители могли навязать Радзивиллам свои условия. Вопреки желанию последних, ВКЛ становилось протекторатом Швеции, а не равноправным партнером.
		</p>
		<p>
		Верховную власть в ВКЛ должен был осуществлять наместник короля. Только король (а не вальный сейм) имел право объявлять войну и заключать мир. Но все юридические и имущественные права шляхты сохранялись, равно как права и имущество римско-католической, униатской и православной церквей.
		</p>
		<p>
		20 октября 1655 года в Кейданах был подписан акт унии между ВКЛ и Швецией. Документ подписали 1172 представителя ВКЛ, в том числе 75 урадников (государственных чиновников), много офицеров и священников, большая группа лютеранских баронов из Ливонии. Однако большинство магнатов и шляхты ВКЛ не поддержали новую унию. В первую очередь их не устраивали ликвидация выборов короля и требование воевать против Польши.
		</p>
		<p>
		Вскоре Януш Радзивилл умер в Тыкоцине (31 декабря 1655 г.), судя по обстоятельствам смерти - в результате отравления. 
		</p>


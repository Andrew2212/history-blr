		<p>
		Кейданская уния — соглашение между  магнатами Великого княжества Литовского и Королем Швеции Карлом X Густавом, подписанное по инициативе Януша Радзивилла в 1655 году во время "Кровавого Потопа" 1654-1667.  
		</p>
		<p>
		Ее целью было положить конец польско-литовского союза и выделить из Речи Посполитой Великое княжество Литовское, управляемое Радзивиллами.
		</p>
		<p>
		По оценке современных беларуских историков, Кейданская декларация явилась компромиссом между магнатскими родами разных конфессий, шляхтой Жамойтии и верховной властью ВКЛ ради того, чтобы спасти от захвата Москвой хотя бы часть государства.
		</p>
		<p>
		Согласно предварительной декларации ВКЛ разрывало унию с Польским королевством и вступало в конфедеративный союз со Швецией, сохраняя свои органы власти, законы и суды. Войско ВКЛ передавалось в подчинение шведскому королю, но не должно было воевать с польским войском.  
		</p>
		<p>
		Из-за тяжелого положения на фронтах войны условия подписанного договора изменились.
		</p>
		<p>
		Верховную власть в ВКЛ должен был осуществлять наместник короля. Только король (а не вальный сейм) имел право объявлять войну и заключать мир. Все юридические и имущественные права шляхты сохранялись, равно как права и имущество всех религиозных конфессий.
		</p>
		<p>
		Большинство магнатов и шляхты ВКЛ не поддержали новую унию. В первую очередь их не устраивали ликвидация выборов короля и требование Швеции воевать против Польши.
		</p>
		<p>
		Фактически, уния прекратила свое действие со смертью Януша Радзивилла.
		</p>
		

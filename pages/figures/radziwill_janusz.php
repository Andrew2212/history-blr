		<p>
		В политическом отношении был сторонником идеи независимости ВКЛ. Еще в юности в Вильне в присутствии монарха бросил польским сенаторам: 
		"Придёт время, - поляки к дверям не попадут: через окна их выбрасывать будем". С 1649 года пытался наладить контакты с протестантской Швецией.
		</p>
		<h3>Кровавый Потоп</h3>
		<p>
		<a href="../events/blood_potop.php">Кровавый Потоп</a> — война с Москвой 1654-1667 годов. Самая кровопролитная война в истории Беларуси.
		Как великий гетман Литовский, Януш Радзивилл участвовал в первых боях под Шкловом и Шепелевичами, был ранен, едва не попал в плен. 
		Местный житель провёл его через болота в Борисов, откуда Януш отступил в Минск, где собирал шляхетские отряды для продолжения борьбы.
		</p>
		
		<p>
		1655 Русские войска оккупировали большую часть ВКЛ. Януш Радзивилл руководил обороной Вильны, после падения города 
		бежал в Кейданы, где 20 октября заключил со шведским королём Карлом X Густавом Кейданскую унию — соглашение, по которому ВКЛ выходило 
		из состава Речи Посполитой и вступало в федеративный союз со Швецией. В его планах было создание самостоятельного Литовского княжества 
		с ним же самим во главе. 
		</p>
		<p>
		Литовские магнаты под руководством воеводы витебского <a href="../figures/sapegi.php">Павла Яна Сапеги</a> отказались поддерживать Януша. 
		Они отступили в <a href="../pages/figures/kishky.php">Подляшье</a> и стали лагерем в Вербилово. Здесь Павел Сапега и Винцент Гонсевский заявили о создании конфедерации 
		против Радзивилла и Швеции.
		</p>
		<p>
		В ноябре 1655 года гетман великий литовский Януш Радзивилл с небольшим войском (до 2 тыс. чел) укрепился в своем замке в Тыкоцине 
		(Подляшье), который вскоре осадили войска Павла Яна Сапеги. Шведский король не оказал военной помощи своему союзнику. 
		В ночь с 30 на 31 декабря 1655 года Януш Радзивилл внезапно скончался.
		</p>
		

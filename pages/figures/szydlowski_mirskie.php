		<h3>Святополк-Мирские и Мирский замок</h3>
		<p class="img-float-left" >
		<a href="../../images/img-figures/szydlowski_mirskie/herb_bialynia.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Сымон Шидловский и Святополк-Мирские" src="../../images/img-figures/szydlowski_mirskie/herb_bialynia_thumb.jpg" height="140" >
		</br><small>Герб "Бялыня"</small>
		</a>
		</p>
		<p class="img-float-right">
		<a href="../../images/img-figures/szydlowski_mirskie/map_Radzivill_Mir.jpg" class="highslide"
        onclick="return hs.expand(this)">
		<img alt="Сымон Шидловский и Святополк-Мирские" src="../../images/img-figures/szydlowski_mirskie/map_Radzivill_Mir_thumb.jpg" height="140" >
		</br><small>Мир на карте Радзивилла</small>
		</a>
		</p>
		<p>
		Святополк-Мирские — <a href="../terms/shliahta.php">шляхетский род</a> Мирские, герба "Бялыня" род. Известен с XVI века — некрупные землевладельцы Браславского повета. 
		Фамилия происходит от белоруского местечка Миоры. В начальный период никто из них не титуловался князем и не пользовался родовым прозванием Святополк.
		</p>
		<p>
		В период <a href="../terms/shliahta.php">"разбора шляхты"</a> — документирования дворянских родов 
		<a href="../download/west_russland.php">Западного края</a> Российской империи — добились признания княжеского достоинства 
		и использования прозвания Святополк, на основе родства с князьями Святополк-Четвертинскими (природными князьями 
		<a href="../terms/wkl.php">ВКЛ</a>  турово-пинской ветви Рюриковичей) 
		и начинают именоваться Святополк-Мирскими.
		</p>
		<p>
		1891 кн. Николай Святополк-Мирский, атаман Войска Донского и Генерал-от-Кавалерии, приобрел у Марии Гогенлоэ-Шиллингфюрст 
		(дочь Льва Витгенштейна и Стефании Радзивилл) имение Замирье с руинами замка. 
		</p>
		<p>
		Сам замок был заложен в 1522 году Юрием Ильиничем герба "Корчак" — старостой Лидским, Брестским и Ковенским, маршалком надворным Литовским — 
		и достроен его двоюродным братом, Николаем Радзивиллом "Сироткой", который пожаловал местечку Мир неполное 
		<a href="../terms/magdeburg_law.php">Магдебургское право</a>. 
		</p>
		<p>
		Николай Радзивилл Сиротка, сын Николая Радзивилла Черного — перестроил <a href="../places/nesviz.php">Несвиж</a>, основал иезуитский коллегиум и несколько монастырей. 
		По его инициативе в Мирском замке был построен трёхэтажный дворец вдоль северной и восточной стен. Был фундатором знаменитой 
		<a href="../download/map_radziwill.php">карты ВКЛ 1630</a>
		</p>
		<p>
		Во владении <a href="../figures/radziwilly.php">Радзивиллов</a> Мир оставался до 1840, когда Мирское графство было выделено из состава Несвижской ординации 
		и перешло к Витгенштейнам в качестве приданого Стефании Радзивилл.
		</p>
		<p>
		Мария Гогенлоэ-Шиллинсфюрст, дочь Стефании Радзивилл, получила наследство, состоявшее из 171 имения в Минской, Виленской, Ковенской 
		и Витебской губерниях. Её муж, Хлодвиг Гогенлоэ с 1894 был  канцлером Германии. По российскому закону иностранцы, имевшие владения 
		в Российской империи, должны были либо принять российское подданство, либо продать владения. Мирские земли были проданы Святополк-Мирским. 
		</p>
		

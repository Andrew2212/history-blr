		<p>
		Одним из мотивов приобретения Святополк-Мирскими этого наследия Радзивиллов было совпадение названия местечка со второй частью фамилии. 
		Под руководством Николая Святополк-Мирского был вырыт пруд, заложен винодельный завод, построен двухэтажный дворец в парке. 
		Князь умер в имении Мир и там же был похоронен.
		</p>
		

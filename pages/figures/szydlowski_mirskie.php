		<h1>Сымон Шидловский и Святополк-Мирские
		</br><small>легендарный хорунжий и последние хозяева Мирского замка</small></h1>
		
		<h3>Сымон Шидловский и Витебская хоругва</h3>
		<div class="col-md-12">
		
		<p class="img-float-left" >

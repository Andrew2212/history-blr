		<p>
		Сымон Визунас Шидловский (Szymon Wizunas Szydlowski) — паводле няпэўных даных (wedlug niepewnych danych) потомок 
		<a href="../terms/shliahta.php">шляхетского рода</a> Святополк-Мирских.
		Его портрет в крестьянской сермяге с хоругвой Витебской земли стал символом соединения традиций простого народа и аристократии.
		</p>
		<p>
		Закончил военную академию в Санкт-Петербурге, много путешествовал. В Санкт-Петербурге был знаком и вел переписку с будущей румынской королевой 
		и поэтессой Елизаветой Нойвидской. Служил адъютантом генерала Трепова. Цвет армейской элиты Российской империи.
		</p>
		<p>
		Конец военной карьеры произошел в 1861 году, когда Сымон Шидловский принял участие 
		в юбилейных торжествах <a href="../events/unia_gorodlo.php">Городельской унии</a> под Витебском. Он был избран хорунжим Витебской земли. Во время церемонии ему была вручена Витебская хоругвь, 
		доставленная из костёла в городке Сураж. На знамени был изображен образ Богоматери в окружении гербов Польши (орел), 
		Литвы (<a href="../terms/blr_gerb.php">Погоня</a>) и Руси (Архангел Михаил).
		</p>
		<p>
		Сымон Визунас Шидловский был участником <a href="../events/uprising_1863.php">восстания 1863</a> 
		(восстание <a href="../figures/kalinowski.php">Калиновского</a>). Он вынес из храма уже знакомую ему хоругвь и не расставался с ней всю свою жизнь. 
		В сражении у города Августов получил пулевое ранение в правую руку, после которого потерял навсегда способность владеть ею. 
		Раненый был спрятан у простых крестьян в лесной избушки, где лечился, скрываясь от царских войск.
		</p>
		<p>
		Эмигрировав после восстания, Сымон Шидловский поселился во Львове (Австрийская империя, Австро-Венгрия). 
		Он организовал госпиталь и приют для тяжелораненых повстанцев и передал на него все свои сбережения. 
		Витебскую хоругвь Шидловский передал Львовской ассоциации ветеранов 1863 года.
		</p>
		<p>
		В 1906 году, когда Шимон Шидловский умер, на Львовском Лычаковском кладбище ему был поставлен памятник, 		
		На постаменте выбиты слова: <i>«Из нашего праха восстанут мстители!»</i>
		На 2-х гранитных таблицах под памятником помещены <a target="_blank" href="http://archives.gov.by/index.php?id=166384">списки участников восстания 1863</a>.
		</p>
		
		

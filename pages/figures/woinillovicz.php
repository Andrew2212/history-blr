		<p>
		1918-1920 Это было время, когда на руинах <a href="../terms/sewero_zapadny_kraj.php">Северо-Западного края</a> 
		идею национального самоопределения 
		— в различных его вариантах: от унии с Польшей до полной независимости — 
		поддерживали все слои населения, от крестьян до польскоязычной шляхты. 
		</p>		
		<p>
		Эдвард Войнилович участвовал в заседаниях Рады <a href="../terms/bnr.php">БНР</a>. 
		Был сторонником независимой <a href="../terms/belarus.php">Беларуси</a> в союзе с Польшей, выступал против раздела Беларуси между Польшей и большевиками. 
		Так же, как и <a href="../figures/skirmunt.php">Роман Скирмунт</a> (наследник промышленной династии и заместитель Войниловича 
		в Минском Обществе Сельского Хозяйства), поддерживал идею <a href="../terms/kraj_tut.php">тутэйшай краевасьци</a>.
		</p>
		<p>
		Границы молодой Беларуси менялись стремительно — БНР, ССРБ, ЛитБел, Срединная Литва, БССР. Газеты включали Эдварды Войниловича 
		в предполагаемый состав Белорусской Речи Посполитой <a href="../figures/bulak.php">Булак-Балаховича</a>.

		<p>
		<i>
			"Войниловичи не пришли ни с Востока, ни с Запада — они коренные, местные, кость от кости, кровь от крови того народа, 
			который когда-то хоронил своих предков в этих курганах (сегодня — на сельских кладбищах) 
			и родную белорусскую землю сохой пашет".
		</i>
		</br><small>"Воспоминания", Э. Войнилович</small>		
		</p>
		<p>
		Войниловичи — <a href="../terms/shliahta.php">шляхетский</a> род "гербу ўласнага Вайнiловiч". Является одним из древнейших шляхетских родов Великого княжества Литовского. 
		Тесно связан с Ваньковичами и Монюшко. 
		<i>
		"Для точности могу отметить, что это был род местный, белорусский. Собственный герб — измененная Сырокомля." 
		</i><small>"Воспоминания", Э. Войнилович</small>
		</p>
		<p>
		В течение многих столетий Войниловичи занимали скромные должности мечников, подкомориев, хорунжих, 
		а после разделов — должности маршалков шляхты Слуцкого уезда.
		</p>
		<p>
		Последним представителем рода был Эдвард Войнилович (13.10.1847 - 16.06.1928). 
		</br>Родился в Слепянке (Минск). 
		Учился в Петербургском императорском технологическом институте. Стажировался на заводах Германии и Бельги, работал инженером 
		на Путиловском заводе в Петербурге. Был почётным мировым судьей Слуцкого уезда, вице-председателем Минского Общества Сельского Хозяйства. 
		 Трижды избирался в российскую Государственную Думу. Был членом Государственного Совета России от Минской губернии. 
		Был выдвинут Столыпиным на должность вице-министра сельского хозяйства Российской империи (отказался).
		</p>
		<p>
		Войнилович был фундатором строительства Красного Костела (костел св. Симеона и св. Елены) — в память своих рано умерших детей. 
		</p>
		<p>
		После себя Войнилович оставил <a href="../download/blr_nationalists_1957.php">"Воспоминания"</a> — 
		<i>
		"Все, что во мне переболело и мною пережитое, приношу в жертву Богу. Возможно, на весах последнего Суда это перевесит в сторону моего 
		оправдания… буду как можно больше рассказывать и меньше осуждать, ибо чем дальше в прошлое уходят действия и события, 
		тем объективнее они видятся. Пусть рассудит время."
		</i>
		</p>
		

		<p>
		Стефан Баторий (27 сентября 1533 - 12 декабря 1586, Гродно) — король польский и <a href="../terms/dukes_wkl.php">великий князь литовский</a> 1575-86. 
		Сын трансильванского воеводы, князь Трансильвании 1571-76. Герб Батория — три когтя/зуба дракона, которого по преданию убил его предок.
		</p>
		<p>
		На польском королевском троне стал преемником <a href="../events/confederacia_warshawa.php">Генриха Валуа</a>, 
		женившись на Анне Ягеллонке (от брака с которой Генрих отказался в пользу шансов занять французский трон). 
		Вслед за Генрихом, так же как и все последующие короли, подписал <a href="../events/confederacia_warshawa.php">"Генриховы артикулы"</a>, 
		подтверждавшие верховенство <a href="../terms/shliahta.php">"шляхетской демократии"</a>.

		<p>
		Правление Стефана Батория пришлось на годы <a href="../events/war_livonia.php">Ливонской войны</a> с Московским царством. 
		При нем был освобожден Полоцк, войска дошли до Пскова и Старой Русы, 
		были ликвидированы все завоевания Ивана Грозного в Ливонии. Территория Ливонской конфедерации была разделена между Речью Посполитой, Швецией и Данией.
		</p>
		<p>
		Король управлял страной без знания языков своих подданных (пользовался латынью). Однако в 
		<a target="_blank" href="http://niab.by/unikdocs/">Национальном архиве</a> РБ сохранилась грамота Батория 
		на <a href="../terms/blr_mova.php">старобелорусском</a> (государственном языке ВКЛ). 
		Что характерно — она адресована справцу <i>венгерских</i> войск.
		</p>
		

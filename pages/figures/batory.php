		<p>
		Стефан Баторий много сделал для образовательной системы. Активно поддерживал <a href="../terms/jesuity.php">иезуитов</a>, чей образовательный стандарт был лучшим в Европе. 
		При нем  были основаны иезуитские коллегиумы в Люблине, Полоцке, Риге, Калише,  Несвиже, Львове и Дерпте. 
		Его привилеем Виленская иезитская коллегия преобразована в Академию (Университет). 
		Для основания коллегиумов в Гродно и Бресте не хватило кадровых ресурсов у провинции ордена и времени жизни у короля.
		</p>
		<p>
		В 1582 Стефан Баторий ввел в <a href="../terms/rzeczpospolita.php">Речи Посполитой</a> григорианский календарь, которым мы пользуемся сегодня 
		(в России он был введен в 1918 году). 
		</p>
		<p>
		В связи с нацеленностью внешней политики на восток Баторий стремился развивать инфраструктуру государственного управления 
		в границах ВКЛ, планировал перенести столицу Речи Посполитой в <a href="../places/grodno.php">Гродно</a>, 
		где перестроил королевский замок, реконструировал <a href="../places/fara_grodno.php">Фару Витовта</a> и провел последние годы жизни. 
		</p>
		

		<p>
		Василий Тяпинский (1530-1600) — гуманист-просветитель, писатель, книгопечатник и переводчик <a href="../terms/wkl.php">ВКЛ</a>.
		</br>Родился в семье <a href="../terms/shliahta.php">шляхтича</a> господарского полоцкого повета (Тяпино, Чашникский район), 
		«кровнородственной» роду Слушка, герба "Остоя". 
		</p>		
		<p>
		Участвовал в Инфлянтской войне c Московским царством, 
		служил у подканцлера <a href="volovicz.php">Остафия Воловича</a>, который поддерживал реформационное движение и книгоиздательскую деятельность 
		в Великом Княжестве Литовском.
		</p>		
		<p>
		По своим взглядам Тяпинский был близок к <a href="budny.php">Сымону Будному</a>. Вместе с ним он принимал участие в протестантских съездах. 
		Воевал, путешествовал, сблизился с радикально настроенными протестантами — социниами (антитринитариями).		
		</p>		
		<p>
		<small>[ Антитринитарии  - приверженцы унитарианской системы верований в христианстве, не принимающие догмат о Троице. 
		Антитринитарная идеология была объявлена ересью на Никейском соборе в 325 году. ]
		</small>
		</p>		
		<p>
		Тяпинский отстаивал позицию, заключающуюся в том, что владеть имениями и землей, а также участвовать 
		в справедливых войнах против нашествий и угрозы тирании — это не грех и не противоречит Библии, как считали крайне левые антитринитарии.
		</p>		
		<p>
		С возмущением он писал о необразованности и ренегатстве православного духовенства, которое отказывалось от родного языка, 
		считая его слишком грубым для храма, в то время как само плохо владело принятым в богослужении церковнославянским.
		</p>		
		<p>
		Главным делом жизни Василя Тяпинского стало просвещение народа.
		</p>		
		<p>
		Ради этого он взялся за перевод на <a href="../terms/blr_mova.php">белорусский язык</a> Евангелия и на собственные деньги основал в своем имении 
		Тяпино (теперь Чашникский район) типографию. Эта трудная и кропотливая работа требовала больших средств, 
		которых просветитель не имел. 
		По его признанию, он смог перевести и напечатать только тексты Евангелий от Матфея и Марка и частично от Луки. 
		Просветитель намеревался издать и Катехизис, однако этот план остался нереализованным.
		</p>
		

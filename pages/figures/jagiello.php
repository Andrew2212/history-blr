		<h4>Хронология событий</h4>

		<p>
		1377 После смерти в. кн. Ольгерда, по его завещанию, Ягайло становится новым великим князем. Поскольку он не был ни младшим братом, 
		ни старшим сыном умершего князя, нарушение династического наследования вызвало борьбу за власть в ВКЛ.
		</p>
		<p>
		1381-84 Первый этап <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> за власть в ВКЛ — Ягайло vs Витовт и Кейстут. 
		Кейстут, ненадолго взявший власть (1381-82), погибает в плену в <a href="../places/krevo.php">Кревском замке</a>. Ягайло остается в. князем Литовским. 
		</p>
		<p>
		1385 Заключена <a href="../events/unia_krewo.php">Кревская уния</a> — соглашение о династическом союзе ВКЛ и Польши через брак в. кн. Ягайло с королевой Ядвигой. 
		Ягайло, оставаясь в. князем Литовским, становится королем Польши. 
		</p>
		<p>
		1386 Ягайло прибыл в Краков, 15 февраля был крещён в Вавеле под именем Владислава и 18 февраля обвенчан с тринадцатилетней Ядвигой.
		</p>
		<p>
		1387 Ягайло возвращается в Вильню и совместно с Витовтом проводит активное <a href="../terms/litva_baptism.php">крещение Литвы</a> в католическую веру.
		</p>
		<p>
		1389-92 Второй этап <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны</a> — Ягайло vs Витовт.
		Витовт, будучи наместником Ягайло в Литве, стремится стать независимым от Короны князем ВКЛ.
		</p>		
		<p>
		1392 Островское соглашение — итог Гражданской войны. Витовт признан Великим князем Литовским и приносит присягу на верность Ягайло.
		</p>
		<p>
		Отношения между ВКЛ и короной будут в дальнейшем регулироваться различными соглашениями и униями. 
		И в 1569 завершатся <a href="../events/unia_liublin.php">Люблинской унией</a>, объединившей
		страны в единое федеративное государство — <a href="../terms/rzeczpospolita.php">Речь Посполитую</a>.
		</p>
		

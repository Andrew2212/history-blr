		<a href="../terms/wkl.php">Великого княжества Литовского</a> становится Витовт.		
		</p>
		<p>
		При Ягайло на белорусские земли пришло <a href="../terms/magdeburg_law.php">Магдебургское право</a> — основа городского самоуправления. Первыми городами были Вильня (1387), Брест (1390), Гродно (1391).	

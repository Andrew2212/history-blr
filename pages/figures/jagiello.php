		племянник <a href="../figures/kejstut.php">Кейстута</a>, двоюродный брат <a href="../figures/vitovt.php">Витовта</a>. 
		</p>
		<p>
		Его правление ознаменовалось <a href="../events/war_civil_1381-84_1389-92.php">Гражданской войной 1381-92</a> за власть в ВКЛ 
		(Ягайло vs Кейстут и Витовт), заключением династического союза с Польшей — <a href="../events/unia_krewo.php">Кревской унией</a>, 
		многочисленными союзами и войнами с Тевтонским Орденом. После 
		<a href="../events/grunwald.php">Грюнвальдской битвы</a> с Орденом к ВКЛ в 1411 году была окончательно присоединена 
		<a href="../places/samogitia.php">Самогития</a>. 
		</p>
		<p>
		В ВКЛ Ягайло княжил в общей сложности 8 лет. 

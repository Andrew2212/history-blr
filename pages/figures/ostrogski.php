		<p>
		Великий гетман был опорой греческой церкви в ВКЛ — милости к православным, по заявлению Жигимонта I Старого, делались ради Константина Ивановича. 
		Острожский сделался центром, вокруг которого группировались все сильные православные магнатские роды Беларуси и Волыни: кн. Вишневецкие, Сангушки, Дубровицкие, Мстиславский, Дашковы, Солтаны, Гулевичи и др.
		</p>
		<p>
		При его поддержке митрополит Киевский, Галицкий и всея Руси Иосиф II Солтан  получил от короля Сигизмунда I грамоту, 
		которая гарантировала независимость греческого духовенства от светской власти.
		</p>
		<p>
		Константин Острожский был фундатором Троицкой и Пречистенской церквей в Вильне. С его именем так же связывают Михайловскую церковь в Сынковичах 
		(по времени постройки и архитектурному сходству с Троицкой церковью).
		</p>
		

		при заключении <a href="../events/unia_religion_brest.php">Брестской Унии</a>. Заботился о развитии просвещения, издавая книги, учреждая школы, оказывая покровительство учёным. 
		Основал Острожскую типографию. В ней работали первопечатники Иван Федоров и Петр Мстиславец, 

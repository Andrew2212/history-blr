		<h1>Князья Острожские
		</br><small>защитники великого княжества и фундаторы православия</small>
		</h1>
		
		<p>
		О происхождении рода Острожских есть две версии. 
		По первой, они происходили от князей Галицких, по второй версии — были потомками Турово-Пинских князей.
		</p>
		
		<h3>Острожский Константин Иванович
		</br><small>герой битвы под Оршей</small>
		</h3>
		

		<p>
		Князь Константин Константинович Острожский (также Василий-Константин; 1526-1608) — глава рода Острожских, староста Владимирский 
		и маршалок Волынской земли (1550-1608), воевода киевский (1559-1608), покровитель православной веры. 
		</p>
		<p>
		Детство и юность провёл в Турове. Сын великого гетмана литовского князя Константина Ивановича Острожского 
		и княжны Александры из рода Олельковичей-Слуцких, потомков великого князя <a href="../figures/olgerd.php">Ольгерда</a>. 
		Последняя представительница рода — св. Праведная <a href="../figures/zofia_sluck.php">София Слуцкая</a> — София Юрьевна, княгиня Слуцкая (1585-1612), жена Януша <a href="../figures/radziwilly.php">Радзивилла</a>. 
		Католичка, канонизирована православными в лике Праведных. 
		Ее мощи хранятся в Свято-Духовом кафедральном соборе Минска.
		</p>
		

		<p>
		Князь Острожский Константин Иванович (1460 - 10 августа 1530) — литовский полководец из православного рода Острожских, староста брацлавский, винницкий и звенигородский 
		(1497-1500, 1507-1516, 1518-1530), староста луцкий и маршалок Волынской земли (1507-1522), каштелян виленский (1511-1522), 
		воевода трокский (1522-1530), <a href="../terms/weliki_hetman.php">гетман великий литовский</a> (1497-1500, 1507-1530).
		</p>
		<p>
		Великий полководец <a href="../events/wars_1487-1537.php">войн с Московией 1487-1537</a>, определивших современную восточную границу 
		<a href="../terms/belarus.php">Беларуси</a>. 
		Прославился победами над татарами. Пленных татар поселял в предместьях своих городов — татарских слободах.
		</p>
		<p>
		В награду за заслуги князя Константина Ивановича в борьбе с Москвой и татарами Жигимонт I Старый издал универсал о назначении его паном виленским 
		— Острожский вошёл в круг высшей знати ВКЛ.
		</p>
		<p>
		В войне с Москвой 1500-1503 Константин Острожский командовал войсками в битве на реке Ведрошь. 
		14 июля 1500 года Литовское войско потерпело крупнейшее поражение со времен битвы на Ворскле. 
		Острожский вместе со многими военачальниками попал в плен.
 		</p>
		<p>
		В 1506 году, через 6 лет пленения, князь согласился служить русскому государю (по Карамзину — <i>"под угрозой темницы"</i>). 
		В 1507 под предлогом осмотра вверенных ему войск Острожский выехал из Москвы и бежал в Литву.  Ему были возвращены его прежние старостства 
		и должность маршалка волынской земли, благодаря чему Острожский сделался главным военным и гражданским начальником всей Волыни. 
		Он снова был утверждён великим гетманои Литовским.
		</p>
		<p>
		В памяти остался как победитель в <a href="../events/orsha_battle.php">битве под Оршей</a> — сражении 8 сентября 1514 года в ходе войны 
		с Московией 1512-1522 годов, в котором 30-тысячное войско ВКЛ воевало разгромило 80-тысячную московскую армию 
		и на 250 лет остановило московскую экспансию.
		</p>
		

  Першы пісьмовы ўпамін пра Мазыр датуецца 1155 г., калі ён перайшоў ад Кіеўскага да Чарнігаўскага княства. 
Працяглы час Мазыр знаходзіўся ў складзе Тураўскага княства, князі і епіскапы якога называліся тураўскімі і 
мазырскімі. З сярэдзiны XIV ст. Мазыр далучыўся да <a href="../terms/wkl.php">Вялікага Княства Літоўскага</a>. 
	</p>

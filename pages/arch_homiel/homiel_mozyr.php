        <p>
		Мазыр. Графiчная рэканструкцыя цыстэрыянскага кляштара і касцёла Унебаўзяцця Панны Марыі на перыяд пачатку мiнулага стагоддзя, калi 
		касцёл ужо пераабсталяваны пад праваслаўную царкву царскімі ўладамі.

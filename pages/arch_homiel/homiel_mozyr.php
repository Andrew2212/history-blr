Пасля пераходу Кіеўскага 
ваяводства да Каралеўства Польскага па вынiках <a href="../events/unia_liublin.php">Люблінскай уніі</a> 1569г. 
і з ініцыятывы <a href="../terms/shliahta.php">мясцовай шляхты</a> 
Мазырскі павет далучыўся да Менскага ваяводства. 28 студзеня 1577г. <a href="../figures/batory.php">Стэфан Баторы</a> надаў Мазыру Магдэбургскае 
права і герб.
	</p>
	<p>
  Манахі-цыстэрцыянцы з’явіліся тут яшчэ ў пачатку XVIII стагоддзя. Мужчынскі кляштар гэтага ордэна у 
прадмесці Мазыра — Кімбараўцы, быў заснаваны ў 1711г. мазырскім маршалкам павятовым (па iншых звестках - 
навагародскiм каштэлянам) Антоніем Аскеркай.
Пазней кляштар атрымліваў вялікія ахвяраванні не толькі ад прадстаўнікоў 
роду Аскерак, але і ад каралёў Рэчы Паспалітай.
	</p>
	<p>
Цыстэрыянцы — Ордэн, заснаваны ў 1098 годзе бенедыкцінам Раберам Малемскім. 
Назва ад першага манастыра ордэна — Цыстэрцыум. Статут ордэна 1119 года прадпісваў фізічную працу, аскетычны лад жыцця.
	</p>
	<p>
	Пасьля <a href="../events/razbor_rechi.php">Падзелаў Рэчы Паспалітай</a> і <a href="../events/uprising_1863.php">паўстаньня 1863 года</a> 
 "... у Мазырскім павеце ў 1864 г. скасаваны Кiмбараўскi рыма-каталіцкі мужчынскі кляштар і 
зачынены існаваўшы пры iм касцёл", — з паведамленьня начальніку <a href="../terms/sewero_zapadny_kraj.php">Паўночна-Заходняга краю</a>.   
	</p>
	<p>
Пабудовы з былым касцёлам былі малапрыдатнымі для 
размяшчэння ваенных і грамадзянскіх аб'ектаў i з 1893г. былi прыстасаваны пад праваслаўную царкву, якая 
дзейнiчала да 1917г. Пазней тут месцiлася запалкавая фабрыка. А ў апошнія гады ў старадаўніх будынках 
працавала мэблевая фабрыка. Аднак яна збанкрутавала i напрыканцы 2014г. мазырскія ўлады прапанавалі Касцёлу 
прыняць будынак. 
	</p>


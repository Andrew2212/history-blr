		<i>
		<p>
		 "По прибытии в Литву король Владислав (Ягайло) собирает в Вильно съезд в день Пепла [20 февраля]. Туда съехались по повелению короля 
		его братья, князья Скиргайло Трокский, <a href="../figures/vitovt.php">Витовт Гродненский</a>, Владимир Киевский, Корибут Новгородский, 
		и большое число рыцарей и простого люда; 
		</p>
		<p>
		и в течение многих дней Владислав, король польский, при содействии католических князей, прибывших с ним, прилагал много стараний к тому, 
		чтобы рыцари и простые люди, отвергнув ложных богов, которым они, обманутые тщетой языческих заблуждений, до тех пор поклонялись, 
		согласились почитать единого истинного бога и поклоняться ему и исповедовать христианскую веру."
		</p>
		</i>
		
		<p>
		1389  По распоряжению князя  <a href="../figures/vitovt.php">Витовта</a> в центре города  был построен деревянный костел. 
		За ним на долгие годы прочно закрепилось имя "Фара Витовта" (ад нямецкага Pfarre, Pfarrei, Pfarrkirche або парафія, парафіяльны касцёл). 
		</p>		
		<p>
		В 1494 г. король польский и великий князь литовский <a href="../terms/dukes_wkl.php">Александр</a> несколько обновил костел, 
		а в 1551 г. по распоряжению королевы Боны храм был отремонтирован.
		</p>		
		<p>
		1586 <a href="../figures/batory.php">Стефан Баторий</a>, который сделал Гродно своей постоянной резиденцией, называл его самым прекрасным костелом на землях ВКЛ. 
		По  его распоряжению деревянное здание перестроили по проекту архитектора Санти Гуччи. 
		</p>
		

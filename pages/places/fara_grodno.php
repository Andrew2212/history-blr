		<p>
		В 1919 Гродно отошло к Польше и храм был передан католикам. После пожаров 1920 и 1935 храм перестраивается. К исходному проекту вернуться не удалось — вместо ренессансного костела ему придали черты готического храма. 
		В 20-30 годах храм был гарнизонным костелом.
		</p>
		

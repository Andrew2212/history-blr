		Она имеет ряд привилегий, по сравнению с рядовыми костелами: в ней, например, при определённых условиях, может быть дарована полная индульгенция.
 		В Беларуси почётный статус присвоен трём храмам: церкви Вознесения Девы Марии в Будславе, собору Святого Франциска Ксаверия в Гродно и собору Вознесения Девы Марии в Пинске

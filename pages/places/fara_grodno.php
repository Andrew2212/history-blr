		<p>
		1804 После включения Гродно в состав Российской империи, город стал губернским, в нем появилось много православных чиновников 
		и членов их семей, расквартировался значительный военный гарнизон. Городские власти столкнулись с проблемой отсутствия 
		православных храмов. Высочайшим повелением было решено восстановить поврежденный храм и передать его православным. 
		Здание реконструировано в классическом стиле и переосвящено в собор св. Софии. 
		</br>1870 проведена очередная реконсктрукция после пожара.		
		</p>		
		<p>
		9 мая 1892 г. собор охватил разрушительный пожар. Храм был перестроен в псевдо-русском стиле.
		</p>
		

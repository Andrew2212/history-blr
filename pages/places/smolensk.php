		<p>
		Древний город впервые упоминается в «Повести временных лет» под 862 годом как центр племенного союза кривичей на пути "из варяг в греки".
		</p>
		<p>
		XII век — время расцвета княжества. Смоленск активно торгует с Ригой и Готландом. Город занимал площадь 115 га, на которой располагалось около 8 тыс. домов с населением около 40 тыс. человек. 
		</p>		
		<p>
		В конце XIII века смоленские князья завязывают тесные отношения с <a href="../terms/wkl.php">ВКЛ</a>. Князь Святослав Иванович 
		оказывал военную помощь <a href="../figures/olgerd.php">Ольгерду</a> в походах на Москву (1368, 1370) 
		</p>		
		
		</div>
		
		<h4>XIV-XVI века 
		</br><small>формирование белорусского этноса</small></h4> 
		<p>
		Кривичи были одной из основных составляющих <a href="../terms/blr_etnos.php">белорусского этноса</a>, складывавшегося в границах единого государства — ВКЛ.
		</p>		
		<p>
		1395 год — в правление <a href="../figures/vitovt.php">Витовта</a> Смоленское княжество входит в состав Великого княжества Литовского.
		</br>1410 год — Смоленские хоругви под командованием князя Лугвения Мстиславского принимают участие в 
		<a href="../events/grunwald.php">Грюнвальдской битве</a>.
		</br>1508 год — Смоленск становится центром Смоленского воеводства ВКЛ. 
		Воеводство существовало с 1508 по <a href="../events/razbor_rechi.php">1793</a> год, (в 1514-1613 и 1654-1793 находясь под властью Мосвквы, существовало номинально)
		</p>
		

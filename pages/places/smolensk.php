		<h4>XVI-XVIII века 
		</br><small>шляхетский город в Московском царстве</small></h4> 		
		<p>
		
		</p>		
		<p>
		1513-1514 год — московский князь Василий III трижды осаждает город и захватывает его.
		</br>1569 год — по <a href="../events/unia_liublin.php">Люблинской унии</a> ВКЛ и Польша образуют Речь Посполитую
		</br>1611 год — <a href="../terms/dukes_wkl.php">Великий князь Литовский</a> и король Польши 
		<a href="../figures/sigismund_waza.php">Сигизмунд III Ваза</a> занимает Смоленск и вновь присоединяет его к ВКЛ.
		</br>1611 год — город получает <a href="../terms/magdeburg_law.php">Магдебургское право</a> и герб "в червленом поле архангел Михаил над поверженным змеем"
		</br>1654 год — <a href="../events/blood_potop.php">Кровавый потоп</a> на землях белорусских. Смоленск отходит к царству Московскому.
		</p>
		<p>
		После присоединения к Московии белорусская этно-культурная общность еще долго оставалась трендом — смоленское дворянство именовало себя
		 <a href="../terms/shliahta.php">шляхетством</a>, 
		смоленские шляхтичи предпочитали читать польские книги и брать себе жён из Польши. При Анне Иоанновне польские книги на Смоленщине были запрещены, 
		за владение ими били кнутом и ссылали в Сибирь; запрещены были также браки с польками; 
		однако смоляне жён из <a href="../terms/ross_empire.php">России</a> по прежнему не брали, предпочитая браки в своей среде. 
		<small>(Из "Записок" Л. Н. Энгельгардта)</small>
		</p>
		

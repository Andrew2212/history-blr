		<h4>XIX-XX века 
		</br><small>русификация</small></h4> 		
		<p>
		<i>"Весьма понятно, что после присоединения Смоленской губернии русские цари употребили усилия на то, чтобы изгладить этот польский элемент."</i>
		</br><small>Центральный Статистический Комитет Министерства Внутренних Дел, 1868</small>
		</p>
		<p>
		Усилия по русификации нашли свое отражение в статистике. 
		Если в 1862 году к "белорусскому населению" в Смоленской губернии относили 54%, то в 1904 году — только 6%. 
		</p>
		<p>
		Однако до самого распада Империи Смоленская губерния относилась к Белорусско-Литовскому краю и все три этнографические экспедиции 
		Императорского Русского Географического общества на протяжении 40 лет включали ее в <a href="../download/blr_people.php">ареал белорусского этноса</a>.
		</p>
		<p>
		После крушения Империи Смоленск пытались включить в свой состав и <a href="../terms/bnr.php">БНР</a> и БССР. Кстати — первой столицей БССР в 1919 году был именно Смоленск.
		</p>
		

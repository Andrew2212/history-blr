		<p>
		В XVI-XVII вв. работает арианская школа, где изучаются древние языки, богословие и естественные науки. В 1562 году в городе открывается Несвижская 
		типография, в которой в 1562-1571 гг. печатали книги просветители <a href="../figures/budny.php">Симон Будный</a> и <a href="../figures/tiapinsky.php">Василий Тяпинский</a>. 
		Несвиж также является родоначальником театрального искусства <a href="../terms/belarus.php">Беларуси</a> - первый стационарный театр "Комедихауз" открыт именно здесь.
		</p>
		<p>
		В XVIII в. в городе действовали придворная капелла, крупнейший в Европе театр балета (открыт в 1740 г.), кадетский корпус и школа флотских офицеров в Альбе (пригороде Несвижа) для войска Радзивилла. В 1764 и 1768 гг. Несвиж вновь оккупируется российскими войсками в результате противостояния Екатерины II и Радзивилла Пане-Коханку. После второго раздела Речи Посполитой в 1793 г. город входит в состав Российской империи.
		</p>
		

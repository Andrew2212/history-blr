		<p>
		Несвиж появляется в летописях в связи с передачей местечка от Великого князя литовского Казимира Ягеллончика к Миколаю Яну Немировичу в 1446 году..
		</p>
		<p>
		В 1492 году великий князь Александр передал город Петру Кишке. Таким образом, Несвиж перешёл во владение 
		к известному роду <a href="../figures/kishky.php">Кишек</a>, одна из представительниц которого, Анна, в 1513 году вышла замуж за 
		Яна Радзивилла "Бородатого", который получил город во владение как приданое невесты ("по кудели"). 
		В 1533 году Несвиж окончательно переходит к <a href="../figures/radziwilly.php">Радзивиллам</a>.
		</p>
		<p>
		Привилей на <a href="../terms/magdeburg_law.php">Магдебургское право</a> Несвижу в 1586 году подписал в <a href="grodno.php">Гродно</a> король <a href="../terms/rzeczpospolita.php">Речи Посполитой</a> и 
		великий князь литовский <a href="../figures/batory.php">Стефан Баторий</a>. 
		Несвижская ратуша является древнейшей среди построек городского самоуправления, сохранившихся на территории Беларуси.
		</p>
		<p>
		Бурный расцвет города начался в XVI в. и связан в основном с именем первого ордината Николая Христофора Радзивилла Сиротки 
		— сына Николая Радзивилла Чёрного. 
		Получив в наследство от родителя деревянный Несвиж, он проводит огромную работу по его реформированию — постройки заменяются каменными строениями, 
		хаотичность средневекового города реформируется в регулярную квартальную систему, сохранившуюся до настоящего времени.
		</p>
		<p>
		Вернувшись из путешествия по Средиземноморью и странам Ближнего Востока, Сиротка воспринимает дух перемен и начинает переустройство родового гнезда. 
		Воплощая свои идеи в жизнь, на первом этапе он освобождает мещан от многих феодальных повинностей, ослабляет налоговое бремя и превращает город 
		в типично европейский, привлекая в него торговцев и ремесленников. Город быстро развивается - в нём открывается школа, строятся баня, 
		парикмахерская и госпиталь. Начинают действовать ткацкий и портняжный, слесарный и скорняжный цеха. Чуть позже, уже в XVIII веке, 
		налаживается мануфактурное производство и мастерская художественного литья. В 1583 году начинается строительство Несвижского замка.
		</p>
		

		1229-1247 "Через Нальшаны на Литву" (durh Nalsen kein Litowen) — походы Ливонского Ордена ("Ливонская рифмованная хроника" XIII в)
		</p>
		<p>
		1285 год, Вяйсеяй (Wiesieje, Veisiejai):
		</br><i>"Pielas w Xieztwie Nalszczanskiem, na zamku swoim, "Wiesieje" zwanym, obchodzil wesele corki, za maz idacej za jakiegos panka bogatego; 

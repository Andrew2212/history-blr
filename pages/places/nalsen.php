		T. IV  
		</p>
		<p>
		1368 год, Велье (чуть южнее Пскова). <i>«В лето 6876 рать Немецкая у Велья из Налесья была…»</i> Псковская летопись.
		</p>
		<p>
		XIII век, «Описание земель» (Descriptiones Terrarium):
		</br><i>
		"Без меча там (в Samoita, Самогитии) никогда не проповедовали... 
		Lectaui (Литвины), Ietuesi (Ятвяги) и Nalsani (Нальшаны) с легкостью крестятся, потому что христианские няньки их выращивают с колыбели. 
		Так что мы сможем в безопасности среди них пребывать."  
		</i>
		</p>
		

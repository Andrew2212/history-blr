		<h1>Нальшаны и Дяволтва
		</br><small>Nalsen, Nalexe, Nalez, Нальша, Нальщанская земля, Налесье (Залесье), Завилейская Литва</small>
		</h1>
		<p>
		Нальшаны и Дяволтва — неявно очерченная в хрониках территория вассального Литве княжества между 
		<a href="../places/litwa.php">Литвой</a> и <a href="../places/samogitia.php">Самогитией</a>.
		До похода <a href="../figures/mindowg.php">Войшелка</a> (сына <a href="../figures/mindowg.php">Миндовга</a>)</a> 
		"на Нальшаны и Дяволтву" (1264) упоминалась, как отдельная административная единица.
		После — только как территория.
		</p>
		
		
		<h3>Довмонт и Гердень
		</br><small>князья Нальшан и Дяволтвы</small>
		</h3>
		<p>
		Вторая половина XIII в. Времена правления <a href="../figures/mindowg.php">Миндовга</a> (1236-1263). 
		
		</p>
		<p>
		Дяволтовско-Нальщанское княжество чаще всего связывают с именами двух князей — Герденя и Довмонта. 
		</p>
		<p>
		Гердень  (Gerdine de Nailse) упоминается в грамоте Миндовга от 1260 года. Также упоминается под 1263 годом, 
		когда выступил посредником при заключении договора между ливонским магистром и Ригой с одной стороны и Полоцком и Витебском с другой.
		</p>
		<p>
		Довмонт — князь Нальшанский, — по некоторым источникам, был сыном Миндовга, братом Войшелка, по другим источникам был старшим братом Тройденя. 
		 Довмонт вместе с Тренятой составляет заговор против первого короля Литвы Миндовга. Осенью 1263 года заговорщикам удается осуществить убийство Миндовга 
		 и посадить на престол Великого княжества Треняту. Войшелк, сын убитого великого князя, разбил и изгнал заговорщиков. 
		</p>
		<p>
		Довмонт бежал в Псков и принял крещение под именем Тимофей. Татищев описывает его набеги на земли Герденя — противостояние Нальшан и Псковского княжества отражено
		во многих источниках <i>"И пришли ко Двине реке и стали на берегу..."</i>. 
		</p>
		<p>
		1267 Довмонт  организовал поход на Нальшаны ("земли Литовские") с псковичами и новгородцами, во время которого Гердень был убит.
		</p>
		

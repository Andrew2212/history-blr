		<p>
		Канал Огинского, Днепровско-Неманский канал — канал в Беларуси, построенный в 1767-83, связывает реки Ясельда (бассейн Припяти) и Щара 
		(бассейн Немана), соединяя Балтийское море с Чёрным. Назван в честь Михаила Казимира <a href="../figures/oginskie.php">Огинского</a>, инициатора строительства.
		</p>		
		<p>
		Расположен на месте бывшего <a href="../places/sea_herodotus.php">моря Геродота</a>, свяывавшего некогда бассейны рек на <a href="../figures/torwald.php">"пути из варяг в греки"</a>. 
		После того, как в 1821 году Пруссия 
		заблокировала транзит в Балтику по Неману, был построен <a href="august_channel.php">Августовский канал</a>, связавший Неман с Вислой. 
		Воднотранспортная система Огинского и  Августовского каналов — значимый памятник гидротехники XVIII-XIX веков.
		</p>		
		<p>
		Длина Огинского канала около 55 км. Частью канала являются озера Выгонощанское и Вульковское. 
		На канале было 2 пристани — Телеханы и Огинская. Первоначально он назывался Телеханский канал.
		</p>		
		<p>
 		В 1783 году канал был сдан в эксплуатацию. В XIX веке ежедневно курсировали пароходы: Пинск-Телеханы, Пинск-Слоним (раз в два дня).
		  Кроме того, судоходство по Ясельде и Огинскому каналу до озера Выгонощанское осуществлялось конной и людской тягой. 
		  Благодаря каналу начался активный рост прилегающих деревень и селений.
		</p>

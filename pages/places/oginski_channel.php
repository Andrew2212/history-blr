		</p>		
		<p>
		1945 После <a href="../events/world_war_2.php">Второй Мировой войны</a> планировалось восстановление водной системы, со статусом "Союзного значения", поскольку это был бы кратчайший путь 
		из Каспийского и Чёрного морей в Балтийское. Однако в 1960-е  были взорваны оставшиеся шлюзы и плотины.  

		Канал сильно пострадал в <a href="../events/world_war_1.php">Первую мировую войну</a>. Все гидротехнические сооружения были взорваны и сожжены.
		</p>		
		<p>
		После присоединения Западной Беларуси к Польше польские власти обновили и модернизировали канал. 
		Он успешно использовался до 1939 года. В основном для лесосплава, но ходили здесь в обе стороны и пассажирские пароходы 
		с паровыми двигателями.
		</p>		
		<p>

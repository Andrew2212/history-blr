		</p>
		
		<p>
		До второй половины XIX века жмудинами называли всех летувисов, не разделяя их на жемайтов и аукштайтов.
		</br>Слово "аукштайт" до XIX века 
		в хрониках не встречается. В 
		"<a href="../download/chronicles_peter.php">Chronicon terrae Prussiae von Peter von Dusburg</a>" 
		1326 г., на которую ссылаются многие, упоминается только Аукштайтия (местность): 
		<i>"Он [тевтон] вел многочисленные сражения на судах, одно в Аукштайтии, земле короля литовского"</i>. 
		</p>
		<p>

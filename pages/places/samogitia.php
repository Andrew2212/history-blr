		<h3>Samogitia. Летува (Lietuva). Жмудь</h3>
		
		<p class="img-float-left">
		<a target="_blank" href="../../images/img-terms/ross_empire/ross_empire_gerb.jpg">
		<img src="../../images/img-places/samogitia/samogitia_gerb.jpg" width="200">
		</br><small>Герб Самогитии "Медведь", с 1584 г.</small>
		</a>
		</p>
		
		<p>
		<i>"Жмудь — в <a href="../times/13_vek.php">XIII</a> и даже в <a href="../times/14_vek.php">XIV</a> вв. занимала большое пространство, заходя в пределы нынешней Виленской губ.
		</i> <small>[до самых границ <a href="../download/blr_people.php">белорусских земель</a>]</small><i>"
		</p>
		<p> 
		Достоверная история Ж. начинается с XIII в., со времени появления в соседстве немцев, и наполнена, главным образом, 
		известиями о борьбе с последними, продолжавшейся до начала XV в...
		 была усердною хранительницею национальных преданий и обычаев и ревностно отстаивала их против всех попыток великих князей литовских ввести какие-либо нововведения... 
		 </p>
		<p>
		 После 1410 г. Ж. избавляется от вторжений немцев. С этого времени ее владельцы, <a href="../figures/vitovt.php">Витовт</a> и 
		 <a href="../figures/jagiello.php">Ягайло</a>, начинают усиленно насаждать здесь христианство, 
		 которое вследствие сопротивления жителей утверждается окончательно только в XVI в., иезуитами."
		</i> 
		</br><small><a target="_blank" href="http://dic.academic.ru/dic.nsf/brokgauz_efron/41009/Жмудь">Энциклопедический словарь</a> Брокгауза и Ефрона</small>	
		</p>
		
		<p>
		До второй половины XIX века жмудинами называли всех летувисов, не разделяя их на жемайтов и аукштайтов.
		</br>Слово "аукштайт" до XIX века 
		в хрониках не встречается. В 
		"<a target="_blank" href="http://books.google.de/books?id=YX8OAAAAYAAJ&printsec=frontcover&hl=ru#v=onepage&q&f=false">
		Chronicon terrae Prussiae von Peter von Dusburg</a>", на которую ссылаются многие, упоминается только Аукштайтия (местность): 
		"Он [тевтон] вел многочисленные сражения на судах, одно в Аукштайтии, земле короля литовского". 
		</p>
		<p>
		А всех жмудинов (Samaiten) Петер из Дуйсбурга противопоставляет <a href="../terms/litwiny.php">литвинам</a> (Littouwin):
		</br>[III, 259] <i>"...И никогда во время правления своего король Littouwin </i><small>
		[правящий из столицы — <a href="../places/nowogrudok.php">Новогрудка</a>]</small><i> 
		не мог договориться с Samaiten, чтобы вместе выступить на войну против братьев </i><small>[рыцарей ордена]</small><i>".</i>
		</br><small>см. <a target="_blank" href="../../images/img-places/samogitia/peter_text.jpg">оригинал</a> и 
		<a target="_blank" href="http://www.vostlit.info/Texts/rus6/Dusburg/frametext33.htm">перевод</a></small> 
		</p>
		
		<p>
		<i>"Производство глиняных сосудов постепенно становилось ремеслом, но довольно долго находилось на более низком уровне, 
		чем в соседних славянских землях; посуда однообразна по форме и орнаментике. 
		</br>Лишь во второй половине XIII - начале XIV века, когда в поселениях городского типа нашел применение быстро вращающийся тяжелый гончарный круг, 
		в этом деле появились значительные сдвиги. Отсутствие городов тормозило развитие специализации среди ремесленников; 
		здесь еще пока не применялись такие известные соседям достижения, как производство кирпича и возведение каменных построек, 
		изготовление стеклянной глазури".</i><small> "История Литовской ССР" (Вильнюс, 1978, с. 21)</small>
		</p>
		
		<p>
	 	В XV веке для них была выдана "<a target="_blank" href="../../images/img-places/samogitia/zmud_law.jpg">грамота жмудская</a>". 
		 После этого фамилии на -УС,-АС (Adamkus, Sauliunas)
		можно иногда всетретиь в среди имен <a href="../terms/shliahta.php">шляхты</a> и <a href="../download/spis_voiska.php">списке войска</a>. 
		 Однако, поскольку тягловые крестьне на военную службу не призывлись, 
		их там <a target="_blank"  href="../../images/img-places/samogitia/voisko_zmud.jpg">меньше 5%</a>
		</p>
		<p>
		На земли жмудские не распространялся <a href="../terms/statut.php">Статут ВКЛ</a>. Второстепенное значение жмудинов в истории ВКЛ 
		стало особенно заметно после образования в XX веке государства Летува (Lietuva), когда <a href="../places/wilno.php">Вильно</a>, Ковно, Троки, Вилькомир и еще более 400 населенных пунктов были переименованы, как 
		"основанные не жмудинами и имеющими не жмудские имена". 
		
		</p>
		<p>
		Историческим гербом Жмуди был "Медведь". В 1935 году премьер-министр Летувы 
		Тубалис официально заявил в сойме 
		о «нелитовском» (то есть не жмудском) происхождении «Погони» 
		и сказал, что начата работа по созданию оригинального герба Республики Летува, 
		на основе исторического герба «Медведь». Этот процесс был прерван Второй Мировой войной.
		</p>
		

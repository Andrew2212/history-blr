    	<p>
    	Пры "савецкай уладзе" у 1930-я храм быў адабраны у вернікаў. З успамінаў мясцовага жыхара (А.А.Рэвякі), хата якога знаходзілася непадалёк ад адпаведнага храма : 
		"У мясцовых жыхароў была вельмі моцная рэлігійнасць. Пры закрыцці тутэйшай Уваскрасенскай царквы ў гады ваяўнічага атэізму 30-х годоў жыхары баранілі храм 
		і нават скінулі са званіцы аднаго з актывістаў-бязбожнікаў. Царкоўныя абразы і кнігі вернікі абаранілі і захоўвалі ў сваіх хатах."
		</p>
		<p>
		 У пачатку вайны (лета-восень 1941 г.) на Рыжкаўскім ускрайку фашысты зрабілі габрэйскае гета. Людзей размяшчалі не толькі па хатах, а і проста на прыдняпроўскім лузе побач з будынкам Уваскрасенскай царквы. 
		 Царкоўны будынак быў адзіным прытулкам для гэтых змучаных людзей у непагадзь. Пад час вайны, нямецкія акупанты, таксама выкарыстоўвалі будынак царквы як майстэрню па рамонту зброі і іншых гаспадарчых патрэб". 
		</p>
		<p> 
		 У пасляваенны час мураваны будынак Уваскрасенскай царквы са званіцняй па-ранейшаму ўзвышаўся над наваколлем. Шклоўскія старажылы сцвярджаюць, што ў 50-х гадах на будынку храма яшчэ знаходзілася нават шыльда, якая паведамляла аб архітэктурнай каштоўнасці пабудовы. Але дзесці ў пачатку 60-х гадоў царкву ўзарвалі. (выкарыстаны асобныя фрагменты з артыкула Грудзіна А.П., Крыніца: НАША СЛОВА № 18 (1065) 2.05.2012г.) 
		 </p>
		<p>
		У 2003г. на месцы былога храма праводзіліся археалагічныя даследванні навукоўцамі з Акадэміі навук Беларусі пад кіраўніцтвам прафесара, доктара гістарычных навук В. Ляўко. Курганы, што на месцы былога храма, як i курганы былога касцёла і кляштара дамініканцаў, што знаходзяцца непадалёк, як i рвы, на якiх некалі мясціўся старажытны замак - Усе гэтыя мясціны патрабуюць далейшых навуковых даследванняў.
    	</p>
    	

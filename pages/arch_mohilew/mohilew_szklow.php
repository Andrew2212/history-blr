        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
		Шклоў — магдэбурскае месца гістарычнай Аршаншчыны, колішняя сталіца графства. <a href="../figures/hodkewich.php">Ян Караль Хадкевіч</a> меў тытул "граф на Шклове і Мышы пан Віленскій".
		</p>
		<p>
		Першы пісьмовы ўпамін пра Шклоў змяшчаецца ў "Запісках" З. Гербэрштэйна і датуецца 1520г.
		 Даўняе паселішча знаходзілася на месцы сучасных вёсак Стары Шклоў і Хоцімка. У выніку ваенных спусташэнняў адбыўся перанос Шклова на цяперашняе месца. 
		 Але за сваю гісторыю Шклоў некалькі разоў "пераязджаў" з месца на месца. Адносна нядаўна лiчылася, што існавалі толькі Стары і Новы Шклоў. 
		 Вось толькi на cтарашклоўскiм гарадзішчы самыя познія знаходкі датаваліся сярэдзінай XVI ст.. а ў самім жа горадзе не было знойдзена ні адной рэчы старажытнее XVIII стагоддзя. 
		</p>
		<p>
		 Белае пляма знiкла пасля таго, як у вёсцы Рыжкавічы археолагi знайшлi замчышча былога месца Шклоўскага, ля пабудаванага Хадкевiчамi замка. 
		 Драўляная Уваскрасенская царква доўгі час была галоўным шклоўскім храмам. Пасля пажару 1690 года "згарэла з усімі скарбамі, кнігамі, начыненнем, званамі, 
		 нічагусенькі не засталося..." 
		 </p>
		<p>
		 У 1701 г. "Шклоўская царква змуравана і дакончана панам Базылём Аўсяевічам, шклоўскім купцом, ягоным коштам, толькі падобна, не без дапамогі іншых". 
		 Яе ўзвялі ў традыцыях магілёўскай школы дойлідcтва, так званае магілёўскае барока. 
		 </p>
		<p>
		 У выніку першага <a href="../events/razbor_rechi.php">падзелу Рэчы Паспалітай</a> (1772г.) 
		 Шклоў апынуўся ў складзе Расейскай імпэрыі. Гістарычныя крыніцы гэтага перыяду называюць царкву як Уваскрасенскую, пры якой дзейнічае адпаведна Уваскрасенскі манастыр.
		  У 19 стагоддзі гэты духоўны прыстанак у Шклове вядомы тым, што ігуменам Шклоўскага Уваскрасенскага манастыра двойчы прызначаўся знакаміты Арэст - Летапісец. 
		  "Запіскі ігумена Арэста" знаходзяцца ў адным шэрагу з такімі вядомымі артэфактамі па гісторыі Магілёўшчыны, як Хроніка Быхаўца, Баркулабаўская хроніка, 
		  "Магілёўская хроніка" і іншыя.
		</p>

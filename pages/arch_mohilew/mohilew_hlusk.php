    	Кляштар бернардзiнцаў скасаваны у 1832г., але касцёл застаўся парафiяльным. Пасля рэформы 1861 г. Глуск - цэнтр воласці Бабруйскага павета, 
		у 1864г. - горад, які налічваў каля 3 тысяч жыхароў, касцёл, 2 царквы і 5 сiнагог. У 1935 годзе пры "савецкай" уладзе касцёл, як пазней 
		і праваслаўны храм, быў адабраны ў вернiкаў, а ўсе яго багатае ўбранне было знiшчана i расцягнута. 
		</p>
		<p>
		Назаўсёды зніклі гравюры мастака еўрапейскага маштабу Аляксандра Тарасевіча, якi ў Глуску нарадзіўся і тварыў У 17-м стагоддзі. 
		Былы касцёл ператварыўся ў склад стружкавай арцелі, з адпаведным да яго стаўленнем, дзе неўзабаве адбыўся пажар. 
		Падчас Другой сусветнай вайны немцы ў былым касцёле зрабілі склад боепрыпасаў, а пры адступленні ў 1944г. склад ўзарвалі. 
		</p>
		<p>
		Судзячы па выяве на пасляваеннай карцiне I. Булгакава, у вынiку выбухаў абрынулicя купал i скляпеннi над алтарнай часткай. 
		Пазней парэшткi касцёла былі утылiзаваны на будматэрыялы.
		Зараз тут месцiцца гарадскі стадыён. Да нашых дзён захаваліся замкавыя валы і рэшткі бастыёнаў. 
		З 1996 года некалькi cезонаў археалагічныя даследаванні сістэматычна праводзiлiся пад кіраўніцтвам І. Ганецкай

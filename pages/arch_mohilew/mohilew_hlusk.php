        <p>
        Глуск (Глуск Пагарэлы, зараз в.Гарадок) згадваецца ў пiсьмовых крынiцах XIV-XV ст. як вотчына старажытнага княжацкага роду Гальшанскiх. 
		Пра час пабудовы замка i заснавання Глуска-Дубровiцкага сведчыць каралеўскi прывiлей ад 20 снежня 1522 г. 
		</p>
		<p>
		Падчас вайны 1654-1667 гг. у пачатку сакавiка 1655 г. атрад казакаў Iвана Залатарэнкi разбурыў Глуск. Вельмі шмат тады загінула жыхароў. 
		Дарэчы, археолагі знайшлі іх пахаванні, размешчаныя проста на тэрыторыі замка, пад валамі. Моцна пацярпелы горад канстытуцыяй сойма ад 19.05.1655 г. быў вызвалены ад падаткаў. 
		Працы па аднаўленні і рэканструкцыі Глускага замка пасля вайны ажыццяўляў ягоны гаспадар князь Аляксандр Гіляры Палубінскі. 
		Была ўзведзена фактычна новая магутная фартэцыя з дзвюма мураванымi вежамi, землянымi валамi, пяццю бастыёнамi i ровам, запоўненым вадой. 
		</p>
		<p>
		Цiкава, што вывез Палубінскі для Глуска 80 гармат з... Берасця! (бо з’яўляўся адміністратарам берасцейскай каралеўскай эканоміі). 
		Берасцейскі замак, які пераўтвараўся ў бастыённую фартэцыю таксама ў другой палове XVII стагоддзя, па канфігурацыі такі ж, як і Глускі. 
		Аляксандр Палубiнскi у 1662г. фундаваў сродкi на пабудову драўлянага касцёла св. Антонiя кляштару бернардзiнцаў. 
		</p>
		<p>
		У 1667 г. сойм пацвердзiў выдзяленне А. Палубiнскiм "месца на касцёл, кляштар i сад" айцам бернардзiнцам у Глуску-Дубровiцкiм. 
		У тым жа годзе быў пабудаваны новы мураваны касцёл (Беззаганнага Зачацця Найсвяцейшай Дзевы Марыі). 
		З 1744г. тут дзейнiчае Глуская парафiя (ў Бабруйскiм дэканаце). З 1781г. створана парафiяльная школа (у 1790г. было 15 вучняў). 
		У выніку другога падзелу Рэчы Паспалітай (1793г.) Глуск апынуўся ў складзе Расейскай імпэрыі, у Бабруйскім павеце.
		</p>

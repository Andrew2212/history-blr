        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
		Мсціслаў. Гiпатэтычная графiчная рэканструкцыя былога езуiцкага касцёла Св. Міхаіла Арханёла. 
		</p>
		<p>
		Летапісны заснавальнік горада, князь смаленскі Расціслаў Мсціслававіч (унук Уладзіміра Манамаха), назваў яго ў гонар свайго бацькi Мсціслава Вялікага. Першы пісьмовы ўспамін змяшчаецца ў грамаце князя смаленскага Расціслава, якая датуецца 1136-1150. Але старажытнае паселішча ўзнікла на Замкавай гары шчэ ў ХII ст. 
		</p>
		<p>
		 З сярэдзіны XІV ст. Мсціслаў далучыўся да <a href="../terms/wkl.php">Вялікага Княства Літоўскага</a> за часам <a href="../figures/olgerd.php">в.князя Альгерда</a>.
		</p>
		<p>
		ХVII ст. пачалося з <a href="../events/war_moscow_1610.php">вайны з Масквой 1609-1618</a>, 
		у выніку якой быў заняты Крэмаль і вазвёрнуты Смаленск. За часам вайны у Мсціслаўскім крае з'яўляюцца каталіцкія манахі, якiя прымаюцца за пабудову касцёлаў, кляштароў і школ 
		(да 1600 г.  тут быў толькі адзін касцёл, у Сакольне). Цягам  ХVII ст. у Магілёўшчыне было пабудавана 26 касцёлаў, 
		4 з іх былі ўзведзены ў Мсціславе. Ад 16.08.1634  кароль і в. князь <a href="../terms/dukes_wkl.php">Уладзіслаў Ваза</a> надаў Мсціславу Магдэбургскае права і герб.
		</p>
		<p>
		Першы ў горадзе касцёл Св. Мікалая быў пабудаваны езуітамі ў 1614  пры спрыяньні старасты Пятра з <a href="../figures/pacy.php">роду Пацаў</a>. 
		Зацвярджэньне рэзідэнцыі езуітаў у Мсціславе адбылося на сойме 1690 года; 
		тады ж <a href="../terms/shliahta.php">Мсцiслаўская шляхта</a> заклікала закласьці ў горадзе місіянерскі дом і школу. 
		</p>
		<p>
		Цягам <a href="../events/war_great_north.php">Паўночнай вайны 1700-1721</a>, ў 1708 годзе,  шведскія войскі зруйнавалі горад. Місія езуiтау таксама была разрабавана і спалена. 
		</p>
		<p>
		Пасля вайны горад пачаў адбудову. Будаўніцтва касцёла пад тытулам Св. Міхаіла Арханёла, Св. Барталамея і Св. Ксаверыя пачалося ў 1730 г, 
		скончана ў 1749. 
		</p>
		

			<p>
			За время Второй Мировой Войны Германия и ее союзники потеряли около 8 млн. солдат и 9 млн. гражданских.
			</p>
			<p>
			Потери СССР оцениваются, как 8,6 млн. солдат и 13,7 млн гражданских. 

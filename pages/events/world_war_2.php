		<p>
		Вторая мировая война (1 сентября 1939 - 2 сентября 1945) 
		— война двух мировых военно-политических коалиций, ставшая крупнейшим вооружённым конфликтом в истории человечества. 
		</p>
		<p>
		В ней участвовало 62 государства из 73 существовавших на тот момент (80 % населения земного шара). 
		Боевые действия велись на территории трёх континентов и в водах четырёх океанов. 
		Это единственный конфликт, в котором было применено ядерное оружие.
		</p>
		<h3>1939-1941</h3>
		<p>
		1 сентября 1939 — начало войны. 
		Войска Германии нападением на Польшу с запада начали боевые действия в Европе.
		</p>
		<p>
		3 сентября Англия, Франция, Австралия и Новая Зеландия объявили Германии войну.
		</p>
		<p>
		17 сентября 1939 года в Польшу с востока вторглись войска CCCP.
		</p>
		<p>
		22 сентября 1939 года во время передачи занятого Германией Бреста советской стороне, в Бресте был проведен совместный парад войск вермахта и СССР.
		</p>
		<p>
		28 сентября 1939 года между СССР и Германией был подписан "договор о дружбе и границе".
		</p>
		<p>
		30 ноября 1939 года СССР вторгается в Финляндию. До 12 марта 1940 года идет Советско-Финская война.
		</p>
		

		<h3>1941-1945</h3>
		<p>
		К 1941 году СССР и Германия установили общую границу, приблизительно соотвествующую договоренности.
		</p>
		<p>
		22 июня 1941 года Германия напала на СССР. С этого дня советская историография ведет начало Великой Отечественной Войны.
		</p>
		<p>
		Боевые действия велись на пяти "театрах военных действий":
		Западноевропейский, Восточноевропейский, Средиземноморский, Африканский, Тихоокеанский.
		</p>
		<p>
		7 мая 1945 года был подписан "Акт о безоговорочной капитуляции Германских вооружённых сил"
		</p>
		<p>
		8 мая 1945 года капитуляция Германии вступила в силу. Эта дата в Европе стала праздноваться как День Победы
		</p>
		<p>
		9 мая — эта дата в СССР стала праздноваться как День Победы.
		</p>
		
		<h4>1945 май-сентябрь</h4>
		<p>
		Продолжалась война с Японией — участницей Берлинского пакта (договор об установлении нового мирового порядка, подписанный Германией, Италией 
		и Японией в 1940). К этому времени войну Японии объявили около 60 стран. 
		Однако, несмотря на сложившуюся обстановку, японцы не собирались капитулировать и объявили о ведении войны до победного конца.
		</p>
		<p>
		6 августа 1945 года США сброили на Хиросиму атомную бомбу «Little Boy». 
		</p>
		<p>
		8 августа 1945 года СССР объявил войну Японии.
		</p>
		<p>
		9 августа 1945 года США сбросили на Нагасаки атомную бомбу «Fat Man». 
		</p>
		<p>
		2 сентября 1945 года Япония на борту американского линкора «Миссури» подписала Акт о капитуляции. 
		</p>
		
		

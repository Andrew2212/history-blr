		<h3>Ляховичи</h3>
		<p>
		— город на реке Ведьме в Брестской области. Известен с XV века в ВКЛ как центр одноимённой волости.
 		</p>		
		<p>
		 В 1572 г Сигизмунд II Август передал городок кастеляну виленскому Яну Иеронимовичу Ходкевичу. 
		 Его сын, гетман великий литовский <a href="../figures/hodkewich.php">Ян Кароль Ходкевич</a> выстроил на месте небольшого деревянного новый каменный замок бастионного типа 
		 по самым современным тогдашним европейским образцам.  
		 В 1635 году замок и город перешли во владение Сапег.
		 </br>После второго раздела <a href="../terms/rzeczpospolita.php">Речи Посполитой</a> замок достался Российской империи и в дальнейшем постепенно разрушался.

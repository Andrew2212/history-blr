		<p>
		Ливонская (Инфлянтская) война (1558-1583) — попытка Московского царя Ивана IV Грозного захватить Ливонию. В войне участвовали Ливонская конфедерация, 
		Московское царство, <a href="../terms/wkl.php">Великое княжество Литовское</a>, Шведское и Датское королевства. 
		Боевые действия велись в основном на территории современных Эстонии, Латвии, <a href="../terms/belarus.php">Беларуси</a> и Северо-Западной России.
		</p>
		<p>
		1558 Нападение <a href="../terms/ross_empire.php">Московского царства</a> на Ливонию и захват Нарвы, Дерпта и ряда других городов.
		</p>
		<p>
		1561 По Виленским договорам Ливонская конфедерация была ликвидирована, часть её территории преобразовывалась в вассальное 
		 по отношению к ВКЛ герцогство Курляндия и Семигалия, 
		 другая же непосредственно вошла в состав Великого княжества Литовского.
		 </p>
		<p>
		С этого времени война велась между ВКЛ и Московским царством.
		</p>
		<p> 
		1563 Московским войском был захвачен Полоцк — крупнейший и богатейший городом ВКЛ. Разорение города стало концом величия Полоцка, 
		после которого он больше никогда не возродился. 
		Число пленных жителей составило от 15 000 до 60 000 человек, большинство из них были проданы в рабство.
		</p>
		<p>
		1564 год. 27 января в <a href="battle_ula.php">битве на Уле</a> 6-ти тысячное войско Николая 
		<a href="../figures/radziwilly.php">Радзивилла</a> Рыжего и Григория <a href="../figures/hodkewich.php">Ходкевича</a> 
		рагромило 20-ти тысячную московскую армию. Московский поход на белорусские земли был остановлен.
		</p>
		<p>
		1569 В правление последнего <a href="../figures/jagellony.php">Ягеллона</a> — <a href="../terms/dukes_wkl.php">Сигизмунда II Августа</a> 
		— ВКЛ по <a href="../events/unia_liublin.php">Люблинской унии</a> объединилось с Королевством Польским 
		в единую Речь Посполитую для защиты от внешней агрессии.
		</p>
		<p>
		1575 Великим князем Литовским и королем Польши становится <a href="../figures/batory.php">Стефан Баторий</a>.
		</p>
		

		<p>
		1579 год. 30 августа после 20-ти дневной осады войска Речи Посполитой освободили Полоцк. Войска Стефана Батория дошли до Пскова и осадили город. 
		Шведы взяли Нарву и осадили Орешек.
		</p>
		<p>
		1582 Война закончилась подписанием Ям-Запольского (1582 год) и Плюсского (1583 год) перемирий. Московия лишалась всех завоеваний, 
		сделанных в результате войны, а также земель на границе с Речью Посполитой и приморских балтийских городов (Копорья, Яма, Ивангорода).

		<h2>Хронология событий</h2>
		<p>		
		1558 московские войска вторглись в Ливонию
		</p>
		<p>
		1561 По Виленским договорам Ливония становится протекторатом и получает защиту ВКЛ. Первым губернатором (1566-78) 
		был Ян Иероним <a href="../figures/hodkewich.php">Ходкевич</a>. 
		</p>
		<p>
		1563 Московскими войсками захвачен Полоцк.
		</p>
		<p>
		1569 Заключена Люблинская уния — образована Речь Посполитая.
		</p>
		<p>
		1583 Московское царство проиграло войну в правление <a href="../figures/batory.php">Стефана Батория</a>.
		</p>
		<p>
		 Московия лишалась всех завоеваний, сделанных в результате войны, а также земель на границе с Речью Посполитой и приморских балтийских 
		 городов (Копорья, Яма, Ивангорода). Территория Ливонской конфедерации была разделена между Речью Посполитой, Швецией и Данией.	
		 </p>
		<p>
		Великое княжество Литовское в союзе с королевством Польша образовали новое конфедеративное государство.	 
		</p>
		
		
		

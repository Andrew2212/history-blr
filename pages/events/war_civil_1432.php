		Знать Великого княжества Литовского в одностороннем порядке после смерти Витовта в 1430 г. провозгласила великим князем Свидригайло, что было нарушением условий Городельской унии, по которой великого князя должен был утвердить король польский. Чтобы увеличить свою популярность среди православного  населения и знати, Свидригайло предоставил равные права католикам и православным. Затем великий князь попросил у императора Сигизмунда королевскую корону, предназначенную Витовту. 

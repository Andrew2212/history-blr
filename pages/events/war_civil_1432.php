		Польская шляхта была возмущена и потребовала от Свидригайло присягнуть на верность Ягайло. Свидригайло ответил отказом, разорвав тем самым унию с Польшей и начав реализовывать программу, направленную на восстановление политической независимости Великого княжества Литовского

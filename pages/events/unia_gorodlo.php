		Городельская уния 2 октября 1413 года — договор великого князя литовского <a href="../figures/vitovt.php">Витовта</a> 
		с польским королем <a href="../figures/jagiello.php">Владиславом Ягайло</a>, 
		подписанный в Городло на реке Западный Буг. В местечке над Бугом собрались не только монархи, но и верхи обеих держав. 

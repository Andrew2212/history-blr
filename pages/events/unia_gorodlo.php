		(намеченная на 1430 год коронация не состоялась из-за его смерти).
		</p>
		<p>
		Подтверждалось само объединение государств. При этом впервые была четко признана самостоятельность Великого Княжества Литовского: 
		оно сохраняло право на существование и после смерти Витовта. Вводился наследственный институт 
		<a href="../terms/dukes_wkl.php">в. князя литовского</a>, что отменяло положения Виленско-Радомскиой унии.
		</p>
		<p>
		Великого князя можно было выбрать только после согласования с Ягайлой и его радой. В свою очередь поляки брали обязательство не избирать 
		короля после Ягайлы, не согласовав кандидатуры с великим князем и его боярством.
		</p>
		<p>
		Для более тесного сближения двух государств 47 боярских родов Великого Княжества Литовского по "акту об адопции" обменялись гербами с польской шляхтой. 
		С этим событием связывают начало современной <a href="../download/blr_heraldic.php">белорусской геральдики</a>.
		 В ВКЛ также вводилось одинаковое с Польшей административное деление на воеводства.
		</p>
	
		<h3>Гражданская война и Трокский привилей </h3>
		<p>
		Однако право на эти должности и все привилегии, гарантированные Городельской унией, касались только католиков. Одной из причин открыто 
		католического характера Городельской унии было стремление Витовта приобрести симпатии на Западе, лишить западной поддержки Орден. 
		</p>
		<p>
		Чисто православные земли — Витебская, Полоцкая, Смоленская — не участвовали в Городельском съезде, и акты унии их не касались. 
		Они оставались в государстве автономными территориями.
		</p>
		<p>
		Акты Городельской унии сделали католичество привилегированной религией. Конфессиональное противостояние широко пользовалось противниками 
		в <a href="../events/war_civil_1432.php">Гражданской войне 1432-38</a> (<a href="../figures/svidrigailo.php">Свидригайло</a> vs Жигимонт) и ими же оно было ликвидировано. 
		</p>
		<p>		
		Так, чтобы привлечь к себе православных сторонников Свидригайло, 6 мая 1434 года Жигимонт издал Трокский Привилей, 
		уравнявший их в правх с католиками 
		<i>"что бы не было никакого разлада и чтобы они пользовались равными милостями"</i>.
		</p>
		

		

		<p>
		1430-1500 Границы <a href="../terms/wkl.php">Великого княжества Литовского</a> после смерти <a href="../figures/vitovt.php">Витовта</a> (1430) и до распада Золотой Орды оставались почти неизменными. 
		Однако после смерти хана Ахмата (1481) экспансия <a href="../terms/ross_empire.php">Московского княжества</a> на запад привела к серии войн, определивших современную восточную 
		границу <a href="../terms/belarus.php">Беларуси</a>.
		</p>
		

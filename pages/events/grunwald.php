		<p>
		<h3>Общий ход сражения</h3>
		— армии противников расположились друг от друга на расстоянии в 20 стадиев (3700 метров);
		</br>— армия ВКЛ вступает в бой первой, в то время как польские отряды Ягайло оставляет в резерве;
		</br>— армия Витовта несёт большие потери, поскольку против них выставлены лучшие рыцари Ордена;
		</br>— накал битвы спадает вследствие усталости воинов;
		</br>— Ягайло вводит в бой резервные польские хоругви и обращает в бегство крестоносцев;
		</br>— в битве гибнет цвет Ордена, включая великого магистра, членов верховного капитула и комтуров. 
		</p>
		<p>
		Завершил Великую войну Торуньский мир 1 февраля 1411 года: Орден терял Самогитию в пользу Великого княжества, Добжиньскую землю в пользу Польши и выплачивал контрибуцию. 
		Вскоре после этого, в ходе Тринадцатилетней войны, Орден был окончательно разгромлен Польшей  уже без участия ВКЛ и признал свою вассальную зависимость от нее.
		</p>
		

		Возник из-за конфликта интересов — ВКЛ стремилось получить 
		<a href="../places/samogitia.php">Самогитию</a>, Польша желала  получить  земли, утраченные по итогам предыдущей войны и выход к морю, Орден хотел доминировать в Прибалтике.
		</p>

		<p>
		Восстание 1830-1831 годов — национально-освободительное восстание против власти Российской империи 
		на территории Царства Польского, Литвы, Беларуси и Правобережной Украины.
		</p>
		
		<p>
		В годы Наполеоновских войн на землях Польских было было создано Герцогство Варшавское 
		 (позже царство Польское), Наполеон поддерживал создание Временного правительства ВКЛ. 
		 Наполеоновские войны вызвали подъем патриотических настроений на землях Речи Посполитой и в Российской империи (Восстание декабристов 1825 года).
		</p>
		<p>[ <small>
		Царство Польское. Великое Герцогство Варшавское — государство, образованное в 1807 году по Тильзитскому миру из польских территорий, 
		отошедших во время Разделов Речи Посполитой к Пруссии и Австрийской империи. Герцогство Варшавское являлось протекторатом наполеоновской 
		Франции и просуществовало до 1813 года, когда большая часть герцогства была присоединена к Российской империи как автономное Царство Польское.
		</small> ]
		</p>
		<p>
		Осознание утраты государственности, которое подпитывалось системой образования, ориентированного на польско-литвинскую культуру 
		(в Виленском университете, Полоцкой иезуитской Академии, большинстве школ преподавание велось на польском ), формировало патриотические 
		настроения в среде <a href="../terms/shliahta.php">местной шляхты</a>, вызывало протест против российского господства, порождало стремление к борьбе 
		за восстановление Речи Посполитой.
		</p>
		<p>
		Создавались патриотические сообщества — <a href="../terms/filomaty.php">филоматы</a> в Вильно, Патриотическое Общество в Польше. В Царстве Польском, имевшем свой сейм и войско,  
		движение охватывало многих армейских офицеров, шляхту, студенчество.  
		</p>
		

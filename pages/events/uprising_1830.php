		<h1>1830 Ноябрьское восстание</h1>
		
		<p>
		<i>
		"Идеи, которые лежат в основании нашего современного мира — меритократия, равенство перед законом, права собственности, 
		религиозная терпимость, современное секулярное образование, здоровые финансы, и так далее — были взяты под защиту, 
		консолидированы, кодифицированы и географически распространены Наполеоном. К ним он добавил рациональную и эффективную местную администрацию, конец деревенского бандитизма, поощрение наук и искусств, 
		отмену феодализма и крупнейшую кодификацию законов со времён падения Римской империи."
		</i>
		</br><small>A. Roberts, Visiting Professor at the Department of War Studies, King's College London</small>

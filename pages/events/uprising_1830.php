		<p>
		Июльская революция 1830 года во Франции выступила катализатором. В царстве Польском 29 ноября 1830 года вспыхнуло восстание. 
		В короткий срок вся его территория была освобождена от русской власти. 
		</p>
		<p>[ <small>
		Июльская революция или Французская революция 1830 года —  Вторая французская революция,— восстание в июле 1830 года во Франции, 
		приведшее к свержению Карла Х и возведению на престол Луи-Филиппа. Либеральная буржуазия объединилась с нижними слоями общества, 
		которые впервые с 1795 года вновь получили возможность влиять на политику. Она ознаменовала торжество принципа народного 
		суверенитета над принципом божественного права короля, а также установление либерального режима и окончательное торжество 
		буржуазии над земельной аристократией.
		</small>
		</p>		
		<p><small>
		Июльская революция имела воздействие на всю Европу. Либеральные течения повсеместно обрели уверенность и решимость. 
		В государствах Германского союза начались беспорядки, вызвавшие пересмотр действующих конституций. 
		Волнения начались и в итальянских государствах, в том числе и в Папской области. Наибольший эффект Июльская революция произвела 
		на территории Польши, вызвав восстание 1830 года.
		</small> ]
		</p>
		<p>
		В Беларуси и Литве действовало около 30 повстанческих отрядов, насчитывавших 12 тыс. человек. 
		Среди инсургентов были такие люди как <a href="../figures/orda.php">Наполеон Орда</a>, <a href="../figures/domeyko.php">Игнаци Домейко</a>, Эмилия Плятер. 
		Председателем Временного правительства Литвы был Тадеуш Тышкевич — <i>"граф на Логойске и Бердичеве"</i>.		

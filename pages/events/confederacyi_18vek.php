		<h1>Шляхетские конфедерации XVIII века
		</br><small>временные политические союзы <a href="../terms/shliahta.php">шляхты</a></small>
		</h1>
		<p>
		В  <a href="../times/18_vek.php">XVIII веке</a> страна, пережившая <a href="blood_potop.php">"Кровавый Потоп"</a>
		 и <a href="war_civil_1696.php">гражданскую войну</a>, увязнувшая в <a href="war_great_north.php">Великой Северной войне</a>,
		 испытывала кризис государственной власти. 
		 </br>Борьба "шляхетской демократии" с королевской властью, межконфессиональные противоречия и 
		 личные интересы магнатских родов (чья власть на землях <a href="../terms/wkl.php">ВКЛ</a> была сопоставима с королевской) порождали анархию.
		 Для поиска путей сохранения государства собирались шляхетские конфедерации. 
		</p>
		
		<h4><small>1696-1702</small> Адбываецца <a href="../events/war_civil_1696.php">Гражданская война ВКЛ</a></h4>
		
		<h4><small>1700-1721</small> Адбываецца <a href="../events/war_great_north.php">Великая Северная война</a></h4>
		
		<h3><small>1715</small> Тарногродская конфедерация</h3> 
		<p>
		— объединение польской шляхты, выступившей против насаждаемого Августом II абсолютизма. 
		Не желая усиления власти Августа II в Речи Посполитой, эту конфедерацию поддержал русский царь Пётр I. 
		Деятельность конфедерации была направлена лично против короля, который пытался использовать введённые в 
		Речь Посполитую из Саксонии немецкие войска с целью дальнейшего укрепления своей власти.
		</p>
		
		<h3><small>1716</small> Виленская конфедерация</h3>
		<p> 
		— объединение великолитовской шляхты, аналогичное Тарногродской конфедерации польской шляхты.
		</p>
		
		<h3><small>1767</small> Слуцкая конфедерация</h3>
		<p>  
		— союз протестантской (кальнивинистов, лютеран) и православной шляхты ВКЛ, созданной с целью уравнения в правах 
		с католической шляхтой. Российская императрица Екатерина II приняла конфедерацию под «императорскую протекцию» и прислала ей в поддержку 20-тысячную армию.
		</p>
		
		<h3><small>1767</small> Торуньская конфедерация</h3> 
		<p> 
		— создана в один день со Слуцкой конфедерацией на территории Польши, также с целью поддержания прав диссидентов в Речи Посполитой.
		</p>
		
		<h3><small>1767</small> Радомская конфедерация</h3>
		<p>  
		— создана в Речи Посполитой против попыток короля 
		Станислава Понятовского и его окружения усилить центральную власть. Создавалась при поддержке российского посла 
		Н. В. Репнина и под защитой российских войск. Усилила влияние России в Речи Посполитой.
		</p>
		
		<h3><small>1768</small> Барская конфедерация</h3> 
		<p> 
		— создана в Речи Посполитой против в противовес Слуцкой, Торуньской и Радомской конфедерациям 
		для защиты внутренней и внешней самостоятельности Речи Посполитой от давления России и против советников короля, 
		желавших ограничить власть магнатов.
		</p>
		<p>
		Военные события Барской конфедерации на территории ВКЛ проходили под руководством <a href="../figures/radziwilly.php">Кароля Радзивилла</a> (1768), 
		<a href="../figures/sapegi.php">Сапеги</a> и <a href="../figures/pacy.php">Паца</a> (1769), 
		<a href="../figures/oginskie.php">Огинского</a> и Красинского (1770-1771). Российскими войсками командовал А. Суворов.  
		</p>
		<p>
		В марте 1772 был разоружен в Речицком повете последний партизанский отряд. 5455 участников конфедерации, 70 % которых составляли крестьяне, 
		были сосланы в Сибирь на каторгу. 
		</p>
		<p>
		Барская конфедерация существовала 4 года и была разгромлена российскими войсками и верными королю отрядами Речи Посполитой.
	    </p>

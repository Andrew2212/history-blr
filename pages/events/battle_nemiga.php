		</p>
		<p>
		На Немиге, на фасаде Центра Моды, расположен барельеф Solidarity ("Солидарность"). Довольно неожиданный декор над местом модных показов.
		По замыслу, "барельеф должен олицетворять собой идеи космополитизма".
		</p>
		<p>
		Однако, глядя на него, сами собой всплывают строки <a href="../terms/vajacki_marsz.php">Ваяцкага марша</a>: "Мы выйдзем шчыльнымі радамі..." 
		Суровые люди явно гражданского вида, безродные космополиты с винтовками, они плечом к плечу выступают из неясных глубин 
		пад цяжкім штандарам неба. Что характерно — нешматлікая збоя прыхавана ў натоўпе и в складках небесного знамени. 
		И два обреза из четырех прячут за спинами женщины.
		</p>
		<p>
		Адразу прыгадваецца <a href="../events/war_green_oak.php">рух "Зеленого дуба"</a> и конь бледный из "Воспоминаний террориста" Бориса Савинкова, :
		 <i>"Сроков знать не дано... Да, не мы измерим наш грех. Но и не мы измерим нашу малую жертву. И когда Он снял третью печать, 
		 я слышал третье животное, говорящее: иди и смотри. Я взглянул, и вот, конь вороной, и на нем всадник, имеющий меру в руке своей".
		 </i>
		 </p>

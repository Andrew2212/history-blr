		Битва на Немиге 3 марта 1067 года — сражение между войсками полоцкого князя <a href="../figures/charadzej.php">Всеслава Чародея</a> из династии 
		<a href="../figures/rogvolod_rogneda.php">Рогволодовичей</a> 

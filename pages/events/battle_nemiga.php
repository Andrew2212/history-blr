		<h2>Solidarity
		</br><small><i>"Мы выйдзем шчыльнымі радамі..."</i></small>
		</h2>
		
		<p class="img-float-left" >
		<a href="../../images/img-terms/vajacki_marsz/solidarity.jpg" class="highslide"

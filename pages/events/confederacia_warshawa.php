	<h4></br>Генриховы артикулы</h4>
	
	<p>
		Для того, чтобы акт Варшавской конфедерации получил силу закона, необходимо было, чтобы его подтвердил будущий король. 
		Учитывая, что Генрих Валуа и его мама Екатерина Медичи прославились "Варфоломеевой ночью" (массовая резня гугенотов католиками) — это было непросто.
	</p>
	<p>
		По настоянию диссидентов (некатоликов) были составлены 
		<a target="_blank" href="https://pl.wikisource.org/wiki/Literae_confirmationis_articulorum_Henrico_Regi_antea_oblatorum">Генриховы артикулы</a>, 
		которыми ограничивалась власть короля и обеспечивалась религиозная свобода, а нерушимость этих статей подтверждалась клятвой:
		<i> "В случае же, если мы (от чего, Боже, сохрани!) не выполним этих статей или условий, или учиним что-либо вопреки им, 
		законам и вольностям, то всех жителей королевства и великого княжества объявляем свободными от должного нам повиновения и верности".
		</i>
		</br>[ <small>&sect;17 </i>...A ieslibysmy (czego Boze uchoway) co przeciw prawom, wolnosciom, artykulom, kondycyom wykroczyli, abo czego nie wypelnili: 
		tedy obywatele Koronne oboyga narodu, od posluszenstwa y wiary Nam powinney, wolne czyniemy."</i></small> ]
	</p>
	<p>
		Все епископы, кроме краковского Франциска Красинского, были против ее принятия акта конфедерации и религиозных свобод. 
		И когда католики торжествовали по случаю избрания Генриха Валуа, диссиденты, в числе которых был киевский воевода 
		<a href="../figures/ostrogski.php">Константин Острожский</a> с православной шляхтой, удалились с поля избрания. Междоусобица грозила кровопролитием, пугавшим всех. 
		И противники пришли к компромиссу: диссиденты согласились на избрание Генриха, а католики приняли целиком Генриховы статьи .
	</p>
	<p>
		Кстати — именно известие об избрании Генриха королем Польши положило конец осаде Ла-Рошели и спасло трех мушкетеров.
	</p>
	

		<p>
		Варшавская конфедерация 28 января 1573 года заложила основы религиозной терпимости знати и свободных людей в Речи Посполитой.
		 Она была принята в смутное время "бескоролевья" — в 1572 умер <a href="../terms/dukes_wkl.php">Сигизмунд III Август</a>; 
		 с ним прекратилась династия Ягеллонов. <a href="../terms/dukes_wkl.php">Генрих Валуа</a> будет коронован только в 1574.
		 </p>
		 <p>
		Варшавская конфедерация имела целью преодолеть проблемы, которые возникли после заключения <a href="unia_liublin.php">Люблинской унии</a>, 
		и образования <a href="../terms/rzeczpospolita.php">Речи Посполитой</a>, имевшей единого короля (а в тот момент не имевшей и его), 
		но не имевшей общей церкви. 
		И если в Польше доминировали католики, то <a href="../terms/religion_blr.php">религиозная палитра</a> ВКЛ была довольно пестрой. На восточных землях преобладало православные, на западных — католицизм. 
		На севере и западе распространялись идеи Реформации. Со времен Витовта в ВКЛ проживала большая иудейская община.
		</p>
		 </p>
		 Акт варшавской конфедерации является первым подобным документом в Европе. 
		 Хотя акт и не предотвратил всех конфликтов на основе религии, но сделал Речь Посполитую намного более безопасным и 
		 толерантным местом, чем большая часть Европы, где в то время бушевали религиозные войны.
		</p>
	
		<p>
		Среди подписавших Акт конфедерации были Остафий <a href="../figures/volovicz.php">Волович</a> — подканцлер Литовский, 
		Павел <a href="../figures/pacy.php">Пац</a> — каштелян Витебский. 
		Одна из идентифицированных печатей под Актом принадлежит Николаю <a href="../figures/radziwilly.php">Радзивиллу</a> Рыжему 
		[<small> R. D. G., po bokach N. D. - "Nicolaus Radziwill Dei Gratia Dux"</small> ]
		</p>
		

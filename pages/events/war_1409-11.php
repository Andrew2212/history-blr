		<p>
		Великая война 1409-1411 — война между <a href="../terms/wkl.php">Великим княжеством Литовским</a> в союзе с Королевством Польским с одной стороны и 
		рыцарями Тевтонского ордена с другой. На стороне ВКЛ и Польши воевали Мазовецкое и Молдавское княжества и татары Джелал ад-Дина. 
		 </p>
		<p>
		ВКЛ стремилось присоединить <a href="../places/samogitia.php">Самогитию</a> и не дать Ливонскому (Ливония) и Тевтонскому (Пруссия) Орденам соединиться по суше.
		 Немецкие Ордена стремились к экспансии в приграничных польско-литовских землях. 
		</p>
		<p>Война стала итогом затяжного конфликта, тянувшегося со времен <a href="../figures/mindowg.php">Миндовга</a>.

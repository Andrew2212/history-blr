		<p>
		К XIV веку немецкие рыцарские ордена заняли выходы к Балтийскому морю основных транспортных артерий — Немана (Тевтонский Орден) и Западной Двины 
		(Ливонский Орден Меченосцев). Спорной землей оставалась Самогития — треугольник между ВКЛ и Орденами, 
		не имеющий выхода к морю и служивший предметом торга в конфликтных ситуациях.
		</p>
		<p>
		1226 "Золотая булла Римини" — положила начало экспансии Тевтонского Ордена в Прибалтике. Это разрешение, которое император Священной Римской империи 
		Фридрих II выдал Тевтонскому ордену для свободы действий в Пруссии. 
		Булла предоставила автономию (фактически, независимость) рыцарям Ордена, передав им в собственность все осуществлённые 
		или потенциальные завоевания на землях <a href="../terms/yotving.php">пруссов</a>. Булла императора была подписана в итальянском городе Римини и была скреплена золотой печатью. 
		Постепенно вся Пруссия попала под власть Тевтонского ордена.
 		</p>
		<p>
		 В 1237 году Тевтонский орден вобрал в себя остатки военного братства рыцарей Меченосцев (рыцарей Христа), 
		 и Ливонский Орден стал называться Ливонским ландмайстерством Тевтонского Ордена. 
		 Таким образом, к концу XIII века Орден, фактически став государством, продолжает политику экспансии на Восток. 
		 В 1309 году столицей Тевтонских рыцарей стал город Мариенбург (нем. "Замок Марии"; польск. Мальборк).
		</p>
		<p>
		В XIV веке орден совершил свыше сотни походов в пределы Литвы.
		</p>
		<p>
		Во время <a href="../events/war_civil_1381-84_1389-92.php">гражданской войны 1381-1384</a> (<a href="../figures/jagiello.php">Ягайло</a> vs <a href="../figures/vitovt.php">Витовт</a>) 
		Ордену была передена Самогития, как плата за поддержку в конфликте. 
		</p>
		<p>
		После занятия Ягайло польского престола по условиям <a href="unia_krewo.php">Кревской униии</a>, и после урегулирования отношений между 

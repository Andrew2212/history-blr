		<h3>Даты</h3>
		<p>
		28 июня 1914 Гаврило Принцип, боснийский серб, студент, член организации Млада Босна, убивает в Сараево наследника австрийского престола эрцгерцога Франца Фердинанда.
		</p>
		<p>
		28 июля 1914  Австро-Венгрия объявила войну Сербии — начало Первой Мировой войны.
		</p>
		<p>
		Война в 1914 году разворачивалась на нескольких театрах военных действий — французском и русском, а также на Балканах (в Сербии), 
		на Кавказе и Ближнем Востоке , в колониях- Африке, Китае, Океании. В 1914 году все участники войны собирались закончить войну 
		за несколько месяцев путём решительного наступления. Никто не ожидал, что война примет затяжной характер.
		</p>
		<p>
		14 сентября 1917 Крах Российской империи — Временное правительство провозгласило страну республикой.
		</p>
		<p>
		3 марта 1918 заключен Брестский мир — сепаратный мирный договор, подписанный представителями Советской России с одной стороны 
		и Центральных держав — с другой. Ознаменовал поражение и выход Советской России из Первой мировой войны.
		Страны Антанты приняли решение о поддержке сил, не признавших власть нового режима.
		</p>
		<p>
		9 ноября 1918 года в результате Ноябрьской революции в Германии свергнут последний германский император и король Пруссии Вильгельм II.
		</p>
		<p>
		11 ноября 1918 Подписано Компьенское перемирие между Антантой и Германией. Союзники Германии по Четверному союзу капитулировали ещё раньше. 
		Был дан 101 залп артиллерийского салюта наций, возвестившего окончание Первой мировой войны.
		</p>
		<p>
		28 июня 1919 Подписан Версальский мирный договор, официально завершивший Первую мировую войну 1914-1918 годов.
		</p>
		

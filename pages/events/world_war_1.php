		<p>
		Первая мировая война (28 июля 1914 - 11 ноября 1918) — один из самых широкомасштабных вооружённых конфликтов в истории человечества.
		</p>
		<p>
		Это название утвердилось в историографии только после начала Второй мировой войны в 1939 году. 
		В межвоенный период употреблялось название "Великая война"
		</p>
		<p>		
		В результате войны прекратили своё существование четыре империи: Российская, Австро-Венгерская, Османская и Германская 
		(хотя возникшая вместо кайзеровской Германии Веймарская республика формально продолжала именоваться Германской империей).
		</p>
		<p>
		Страны-участницы потеряли убитыми более 10 млн солдат и около 12 млн мирных жителей, около 55 млн человек были ранены.
		</p>
		
		<h3>Противники</h3>
		<h4>Тройственный союз - Центральные державы</h4>
		<p>
		Тройственный союз — военно-политический блок Германии, Австро-Венгрии и Италии, сложившийся в 1879-1882 годы, который положил начало разделу Европы на враждебные лагеря.
		</p>
		<p>
		В 1915 году Италия вышла из Тройственного союза и вступила в войну на стороне его противников.
		</p>
		<p>
		Османская империя и Болгария присоединились к Германии и Австро-Венгрии уже в ходе войны. Османская империя вступила в войну в октябре 1914, 
		Болгария — в октябре 1915 года. 
		Сложившийся военно-политический блок именовался "Центральные державы" (Четверной союз)
		</p>
		<h4>Антанта</h4>
		<p>
		Антанта (фр. entente - согласие) — военно-политический блок России, Англии и Франции, создан в качестве противовеса "Тройственному союзу"; сложился в 1904-1907 годах и завершил размежевание великих держав накануне Первой мировой войны. 
		</p>
		

		<p>
		 1767 Екатерина II инициирует принятие  "кардинальных прав", которые ликвидировали результаты начатых ранее прогрессивных реформ 1764 года. 
		</p>
		<p>
		 1768 Российские войска участвуют в подавлении <a href="confederacyi_18vek.php">Барской конфедерации</a>, 
		 недовольной вмешательством России.
		</p>
		<p>
		1772 Первый Раздел. Россия, Австрия и Пруссия подписали в Вене конвенцию о разделе Речи Посполитой. Их войска одновременно вошли на территорию 
		страны и заняли области, распределённые между ними по соглашению. 
		</p>
		<p>
		Не имея возможности отстоять целостность страны вооруженным путем, <a href="../terms/shliahta.php">шляхта</a> пыталась не допустить утверждения его юридически. 
		Совместно с другими шляхтичами, среди которых были Самуэль Корсак (Навогрудок) и Станислав Богушевич (Минск), 
		<a href="../figures/rejtan.php">Тадеуш Рейтан</a> пытался сорвать сейм, чтобы не допустить утверждения Первого раздела Речи Посполитой. 
		Когда все формальные юридические средства были исчерпаны, Тадеуш Рейтан, добиваясь от Сейма протеста против Раздела, 
		лёг перед выходом со словами: "Убейте меня, не убивайте Отчизну!"
		</p>
		<p>
		1791 Принятие <a href="constitution_3may.php">Конституции 3 мая</a> повлекло за собой вмешательство со стороны России, которая опасалась восстановления Речи Посполитой в границах 1772 года.
		</p>
		<p>
		1793 Второй Раздел. Пруссия и Россия подписали конвенцию о втором разделе Речи Посполитой. 
		</p>
		<p>
		После второго раздела Российской Императорской АН 
		было составлено первое <a href="../download/all_peoples_RI.php">"Описание всех обитающих в Российском государстве народов"</a>.
		<i>"Народ польский"</i>, обитающий в захваченых восточных воеводствах, был объявлен "отторгнутой и воссоединенной частью Российского народа"
		(термин <i>"русский народ"</i> был еще неизвестен). Пока еще нет причитаний о "горькой доле мужика-белоруса" 
		— <i>"Разного скота и птицы почти каждый поселянин имеет для хозяйственного своего обихода достаточное количество"</i>.
		</p>

		<p>
		Разделы <a href="../terms/rzeczpospolita.php">Речи Посполитой</a> — раздел территории государства между Прусским королевством, 
		<a href="../terms/ross_empire.php">Российской империей</a> и Австрийской монархией в конце XVIII века (1772-1795).
		</p>
		<p>
		Первый раздел произошёл в 1772 году, второй — 23 января 1793 года, третий — 24 октября 1795 года.
		</p>
		
		<p>
		В XVIII век в Речи Посполитой был веком магнатской анархии. Безграничная <a href="../terms/shliahta.php">шляхетская демократия</a> привела к ослаблению центральной, 
		королевской власти и усилению магнатских кланов. 
		Право Liberum veto — позволявшее любому депутату сейма прекратить обсуждение вопроса и работу сейма вообще, — сильно затрудняло проведение реформ. 
		 Абсолютная монархия (сложившаяся в Московии с XVII в.) на наши земли пришла только с российской оккупацией в XIX в.
		</p>
		<p>
		XVIII век начался <a href="war_civil_1696.php">гражданской войной</a> между Сапегами и магнатской конфедерацией за перераспределение власти в ВКЛ (король в ней даже не участвовал), 
		продолжился вооруженной борьбой <a href="confederacyi_18vek.php">шляхетских конфедераций</a>, попыткой утвердить 
		новую <a href="constitution_3may.php">Конституцию 3 мая</a> и закончился разделами государства.
		</p>
		<p>
		К середине XVIII века Речь Посполитая уже не была в полной мере независимой. Российские императоры оказывали 
		непосредственное влияние на ее внутренние дела, подкрепляя политическое давление размещенными в стране войсками. 
 		</p>
 		

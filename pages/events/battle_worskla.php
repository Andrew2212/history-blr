		<p>
		В 1398 году хан Тохтамыш, потомок Чингисхана, боровшийся за объединение Золотой Орды под своей единоличной властью, столкнулся с сильным и опасным противником — ханом Тимуром Кутлугом. 
		Потерпев поражение, Тохтамыш бежал вместе с семьёй к Витовту, был принят и поселен в г. Лида.
		 </p>
		<p>
		Вслед за ним пришли татары с семьями и собственностью — в страну, где им обещали землю и свободное исповедование мусульманской веры. 
		Татары, которые имели статус <a href="../terms/shliahta.php">шляхты</a>, сохранили его и в ВКЛ. На протяжении целого столетия после переселения, 
		они имели возможность получить документы от крымских ханов о своих предках.
		</p>

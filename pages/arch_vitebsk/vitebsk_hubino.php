		</p>
		<p>
		Цягам  <a href="../events/uprising_1830.php">Паўстаньня 1830</a> францысканскія кляштары актыўна падтрымлівалі інсургентаў — там захоўваецца зброя для паўстанцаў, знаходзяць прытулак параненыя. Пасьля здушэньня паўстаньня царская ўлада скасавала дзейнасьць Ордэна ў межах імпэрыі. Частка маемасьці францішканцаў была перададзена каталіцкім касцёлам.
		</p>
		<p>

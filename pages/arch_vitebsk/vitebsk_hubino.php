		На пачатку XX ст. у Губіне шматлікіх манахаў замяшчае адзіны ксёндз. Парафія раскінулася вельмі шырока, да яе далучана некалькі скасаваных парафій: Чашнікі, Кублічы, Бела-Камень. Налічвала 1815 вернiкаў. 
		</p>

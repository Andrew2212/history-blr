		у Лепельскім павеце Віцебскай губэрні. Па здушэньні <a href="../events/uprising_1830.php">Паўстаньня 1830</a>  кляштар францішканаў быў скасаваны і ў 1834 тут збудавалі царкву Сьвятога Мікалая. 
		</p>
		<p>

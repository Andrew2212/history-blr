		У касьцёле было 3 алтары, галоўны зь якіх у імя Марыі Панны. Зьвяртаў на сябе ўвагу бочны алтар Найсьвяцейшай Панны Марыі Вострабрамскай. 
		Пры плябаніі існавала бібліятэка і багаты архіў айцоў францішканаў.

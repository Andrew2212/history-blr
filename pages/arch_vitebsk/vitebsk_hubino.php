        </p>
		<p>
		Губіна — даўняе мястэчка гістарычнай Полаччыны. Знаходзілася ў валоданьні роду Пакашаў, тут пэўны час месьцілася іхная рэзыдэнцыя. 
		У 1714 полацкі стольнік Ян Пакаш збудаваў у Губіне касьцёл Сьвятога Антонія, пры якім дзейнічаў кляштар францішканаў. Пазьней у якасьці пасагу мясьціна перайшла да роду Сяляваў.

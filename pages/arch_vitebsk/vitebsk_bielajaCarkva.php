        <p>
		Белая Царква. Графiчная рэканструкцыя Tроiцкай царквы Троiцкага (Белацаркоўскага) манастыра. Выгляд на пачатак XXст. па апошняй перабудове.
		</p>
		<p>
		Белая Царква — вёска на беразе возера Чарэйскае (раней возера Галаўля). Верагодна, атрымала сваю назву ад белага колеру атынкаваных сцен манастырскай царквы, 
		пабудаванай  канцлерам ВКЛ <a href="../figures/sapega.php">Львом Сапегай</a> у 1599
		 (па некаторым iншым звесткам закладзенай яшчэ па фундацыі Багдана Сапегі напрыканцы XV ст.). 
		 Троіцкая царква была добрым прыкладам архітэктуры позняга барока 
		</p>
		<p>
		Драўляны манастыр пры царкве Св.Троiцы на беразе возера быў заснаваны Смаленскім біскупам (ад 1475 — мiтрапалiт Кiеўскi i ўсяе Русi) Мiсаiлам яшчэ у 1454.
		Калі ў 1495  Багдан Сапега стаў уладальнікам Чарэйскага княства, уладыка Місаіл  
		перадаў яму правы патранату над Чарэйскім манастыром св. Тройцы з перадачай гэтага права ў <a href="../figures/sapegi.php">род князёў Сапегаў</a>. 
		</p>
		<p>
		Чарэйскі манастыр быў мужчынска-жаночым, што характэрна для глыбокай даўніны. Пазней стаў мужчынскім, але калі гэта адбылося, дакладна невядома. 
		</p>
		<p>
		Напрыканцы XVI ст. Чарэйскі манастыр "спусцеў", i менавiта з імем <a href="../terms/weliki_kancler.php">вялікага канцлера ВКЛ</a> звязана яго ўзнаўленне. 
		Атрымаўшы Чарэю, Леў Сапега "дзеля большай хвалы Божья, да душеўнаму свайму збавеньню, на памяць сабе, продком i родзiчом" 
		пабудаваў на востраве другі ужо мураваны манастыр і царкву пры ім (закладзены ў 1597-98 ).
		</p>
		<p>
		Асабістым унёскам Льва Сапегі ў новабудаваны манастыр былі напрастольны срэбраны крыж (да рэвалюцыйных падзей 1917 захоўваўся ў Магiлёўскiм царкоўна-археалагiчным музеi), плашчаніца, цудатворны вобраз Черейской маці Божай.
		</p>
		<p>
		У манастырскім запісе ад 20.01.1599  Леў Сапега абавязваўся скончыць пачатыя будынкі царквы і манастырскага корпуса, 
		пакінуць за сабой і нашчадкамі права патранату над абодвума манастырамі і не ўводзіць там <i>"акрамя грэчаскай"</i> аніякай іншай веры, 
		а насельнікам манастыра даў права выбіраць са свайго асяродьдзя толькі такога ігумена, які будзе <i>"закону гречэскага"</i> 
		(<a href="../articles/ortodox.php">тэрмін <i>"праваслаўе"</i></a> існаваў толькі ў Маскоўскім царстве і тычыўся толькі маскоўскай царквы). 
		</p>
		<p>
		Але ўжо ў 1601  першый жа настаяцель ігумен Ісаія прыняў <a href="../events/unia_religion_brest.php">Унію</a> і перавёў Троiцкi манастыр ва уніяцкую веру, 
		засяродзіўшы ў сваіх руках кіраванне Чарэйскiм манастыром i Ануфрыеўскiм (каля Мсцiслава). 
		</p>
		<p>
		З цягам часу Чарэйскі манастыр прыйшоў у заняпад. У 1804 годзе, напрыклад, у Троiцкiм манастыры былі толькі 4 манахі і 1 клірык. 
		Ад 1834 манастыр скасаваны, а царква засталася прыхадской.
		</p>


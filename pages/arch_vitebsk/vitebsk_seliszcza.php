        <p>
		Селiшча. Графiчная рэканструкцыя касцёла Св. Веранiкi былога бернардзiнскага кляштара. Выгляд на пачатак XXст.
		</p>
		<p>
		Селiшча — вёска Ушацкага раёна на беразе аднаймённага возера. Першы пісьмовы ўпамін пра Селішча як двор, уладаньне Я. Корсака датуецца 1552 годам 
		(рэвізія Полацкага ваяводства).
		У 1674г. харунжым ВКЛ Адамам Слушкам тут быў заснаваны драўляны кляштарны комплекс ксяндзоў-бернардынцаў. З 1680 маёнтак Селiшчы належыў  Корсакам-Сяліцкім, потым  Францкевічам. 
		</p>
		<p>
		У першай траціне XVIII ст. Селішчам валодалі полацкi падсудак Язафат Антоні Сялява з жонкай Элеанорай, на ахвяраваннi якiх, 
		а таксама на грошы падчашніка і ратмістра полацкага Яна Язэпа Рагозы з 1726 на беразе возера бернардынцы пачалi узводзiць "аграмадныя" мураваныя касцёл i кляштарны комплекс, будаўнiцтва якога расцягнулася да пачатку XIX cт. 
		</p>
		<p>
		Касцёл — гэта трохнававая базіліка з трансептам і паўцыркульнай 2-яруснай апсідай на высокім цокалi. 
		Ён мае план у верхнiм сячэннi ў выглядзе лацiнскага крыжа (без купала). Да касцёла з паўночнага боку далучаўся 2-павярховы будынак кляштарнага корпуса пад высокім вальмавым дахам.
		</p>
		<p>
		Па сваёй архiтэктонiцы святыня паўтарае славуты полацкi касцёл езуiтаў: звонку прамавугольную, а знутры — паўкруглую алтарную частку. 
		У iнтэр'еры было 5 алтароў. Высокімі мастацкімі якасьцямі вылучаўся алтар Св. Антонія са скульптурнай групай у завяршэньні (анёл, які топча д'ябла) паміж 6 карынфскімі калонамі. 
		</p>
		<p>
		У галоўным алтары быў змешчаны прывезены з Кублішчынскага касцёла абраз Божай Маці ў сярэбранай рызе, перад якім меліся ўкленчаныя выявы фундатараў храма. 
		Над нартэксам на магутнай арцы месціліся хоры, на якіх стаяў старажытны барочны арган, што ўражваў вернiкаў пышнасцю аздобы. 
		</p>
		

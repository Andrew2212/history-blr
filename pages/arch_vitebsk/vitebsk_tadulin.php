        <p>
		Тадулiн (Янавічы, Слабада) Графiчная рэканструкцыя Успенскай царквы былога базылiянскага кляштара (пазней праваслаўнага).
		</p>
		<p>
		Базыліянскі мужчынскі Свята-Успенскі Тадулінскі кляштар быў заснаваны у 1743 годзе на ўзвышшы Вымнянскага возера 
		(на той час Тадулiнскага). Фундаваны на сродкi князя Тадэвуша-Франціска <a href="../figures/oginskie.php">Агінскага</a> 
		(вялікага пісара ВКЛ, каштэляна Віцебскага, Ашмянскага і Троцкага, ваяводы Троцкага). 
		</p>
		<p> 
		Мураваны храм у стылi Вiленскага барока быў пабудаваны (1769-74) на месцы былой драўлянай царквы Успеньня Прасвятой Багародзіцы. 
		Асвячэння царквы здзейсніў Полацкі Арцыбіскуп Іраклій Лісоўскі. 
		</p>
		<p>
		Галоўнымi манастырскiмi святынямi былi цудатворны абраз Прасвятой Багародзіцы, падараваны князямi Агінскiмi, i абраз Свяціцеля Мікалая Цудатворца, 
		якi перанеслi ў Тадулина пры сваім перасяленні ў 1799 г. манахі-базыльяне з вiцебскага Успенскага сабора, пасля таго як у 1799г. 
		апошнi быў перададены РПЦ. 
		</p>
		<p>
		Адначасова манастыру адышлі ўсе маёнткі віцебскага базыльянскага манастыра. 
		Увесь унiяцкi перыяд свайго iснаваньня, манастыр быў вельмі багаты. У яго маёмасцi знаходзiлася 5 фальваркаў, 28 вёсак. 
		</p>
		<p>
		Пасьля <a href="../events/razbor_rechi.php">Падзелаў Рэчы Пастпалітай</a> 1772-1795 
		і скасаваньня <a href="../events/unia_religion_brest.php">Берасьцейска уніі</a> (1839),
		манастыр быў перададзены ў ведамства РПЦ.
		Была дабудавана (1841) цёплая царква, якая месцiлася ў мураваным двухпавярховым манастырскім корпусе, даданы драўляны купал з цыбулінай (1878-79). 
		</p>
		<p>
		
		Да сярэдзiны XIX ст. гаспадарчыя справы манастыра прыйшлі ў стан заняпаду — ен быў месцам высылалі духавенства. Сюды адпраўлялi няздатных выконваць службовыя даручэнні, а таксама састарэлых, хворых манахаў. 
		У манастыры не было анi школы, анi бальніцы, увогуле аніякай дабрачыннай ўстановы. 
		</p>
		<p>
		I ў 1888г. быў падпісаны Указ аб пераўтварэнні мужчынскага Тадулiнскага манастыра ў жаночы. Прыбылі манашкі з полацкага 
		Спаса-Ефрасіннеўскага манастыра, якiя з цягам часу адрадзілi манастыр. 
		Былі заснавана трохкласная вучэльня для дзяўчынак і аднакласная прыхадская школа для хлопчыкаў і дзяўчынак, створана бібліятэка.
		</p>
		

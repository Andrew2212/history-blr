    	<p>
		У 1921 г. манастыр закрылі пад падставай неабходнасці аказання дапамогі дзецям з галадаючага Паволжа, у былыя манастырскія памяшканні пасялілі дзяцей-сірот, 
		але Свята-Успенскі храм працягваў ажыццяўляць сваю дзейнасць у якасці парафіяльнага. 
		</p>
		<p>
		У траўні 1922г. з Успенскай царквы спецыяльным ўпаўнаважаным былі канфіскаваныя царкоўныя каштоўнасці, знятыя званы, знішчаныя крыжы, 
		разбураныя надмагільныя пліты пахаванняў, размяшчаныя каля храма. Падчас Другой сусветнай вайны, увосень 1943г. былая святыня пры адступленні немцаў была узарвана. 
		</p>
		<p>
		Пасля вызвалення вёскі Слабада многія жыхары, якія страцілі сваё жытло, знаходзілі прытулак у паўразбураных келлях і пабудовах манастыра. 
		У 1960-я гады з дапамогай тэхнікі манастырскія сцены былі цалкам зруйнаваныя, цэглу выкарыстоўвалі на кладку дарогі да Янавічаў. 
		</p>
		<p>

		Вiцебск быў спалены. Згарэлi i драўляныя пабудовы езуiцкай калегii. Новы мураваны касцёл будаваўся ў 1713-1731 па фундацыi 
		ваяводы Віцебскага Марціяна Міхала <a href="../figures/oginskie.php">Агінскага</a> i яго жонкi Тэрэсы, а таксама на сродкi вiцебскай шляхты. Кансекрацыя святынi адбылася ў 1751г. пасля завяршэньня аздабленьня iнтэр'ера. 
		</p>
		<p>
		У 1762 г. касцёл ізноў пацярпеў ад пажару, але быў адноўлены праз тры гады. Увесь гэты час кiраўнiком будаўнiцтва з'яўляўся Базыль Часновiч. З 1756г. пры калегiуме дзейнiчала семiнарыя для дзяцей збяднелай шляхты, друкарня, тэатр. 
		</p>
		<p>

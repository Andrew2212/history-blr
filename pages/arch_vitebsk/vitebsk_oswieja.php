    	<p>
		Касцёл пад тытулам Найсвяцейшай Тройцы, пабудаваны ў 1782г. 
		(ужо пасля далучэння да Расейскай імпэрыі ў вынiку першага <a href="../events/razbor_rechi.php">падзелу Рэчы Паспалiтай</a>) 
		па праекту архiтэктара Антонiя Парака, быў мураваны, у стылi барока. Перад касцёлам стаяла сажнёвае ўкрыжаванне, насупраць амбоны — алтар з фігурай Пана Езуса. 
		Асвейскі касцёл меў размалёваны звод, у зале было шмат ляпных упрыгожванняў: у алтары стаялі фігуры апосталаў. 
		На той час у асвейскай парафіі налічвалася 3120 вернікаў. Пасля смерці сына Яна Аўгуста - Юзафа, асвейскi маёнтак з 1786 г. пераходзіць да графоў Шадурскiх, адзін з прадстаўнікоў якіх, Уладзіслаў, разарыў гэты маёнтак.	
		</p>
    	<p>
    	У часы Расейскай імперыі статус Асвеi панiжаецца, яна зноўку становiцца мястэчкам, цэнтрам воласці Дрысенскага павета. З 1836г. кляштар скасаваны, касцёл 
		становiцца парафiальным (фарным). Зруйнаваны ўжо пры савецкай уладзе у 1937г. пад падставамi "палохаюцца дзеці", "старыя дурнеюць, спакушаюць моладзь" ... 
		Перад выбухам быў зладжаны пагром: з касцёла цягнулі ўсё, што было магчыма. Усё іншае – харугвы, вянкі, іконы, розныя прыналежнасці – пабілі і папалілі на вогнішчы прама 
		перад касцельным ганкам. За дзень да выбуху апавясцілі жыхароў бліжэйшых дамоў, каб заклеілі шкло на вокнах... Выбух абрынуў толькi дах i скляпеннi, 
		сцены расцягвалi ўжо трактарамi...
		</p>
		<p>
		Да нашага часу тут захаваліся парэшткi палаца Гільзэнаў і Шадурскіх (1782) і шпіталь місіянэраў, якi з'яўляецца адным з самых старых помнікаў архітэктуры ў раёне. 
		Таксама помнiкам з'яўляецца Асвейскі парк з рэшткамі некалі знакамітага палаца. На месцы касцёла ўтварыўся штучны пагорак з цэглы і будаўнічага друза, 
		на якім месцамі бачны рэшткі падмуркаў і сценаў.
    	</p>

        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
		Асвея (Верхнедзвiнскi раёон) Графiчная рэканструкцыя касцёла пад тытулам Найсвяцейшай Тройцы былога кляштара мiсiянераў.
		Місіянеры, лазарысты, Кангрэгацыя місій, вікенційцы (лац.: Congregatio Missions) — каталіцкая мужчынская кангрэгацыя* , заснаваная Св. Вікенціем дэ Полем 
		у XVII стагоддзі. Галоўныя кірункі дзейнасці — пропаведзь Евангельля сярод бедных, каталіцкая адукацыя, місіянерская дзейнасць. 
		(*Кангрэгацыя — хаўрус кляштароў, прыняўшых адзін агульны устаў, мніхскі ордэн.) 
		</p>
		<p>
		Асвея — даўняе мястэчка гістарычнай Полаччыны, размешчана на паўднёвым мысе Асвейскага возера. Першы пісьмовы ўпамін пра Асвею («Асьвей») 
		змяшчаецца ў дамове паміж вялікім князем Аляксандрам і маскоўскім царом Іванам III і датуецца 1503г. У 1505г. Аляксандар перадаў паселішча 
		ваяводу полацкаму Станіславу Глебавічу, пазьней яно перайшло ва ўладанне <a href="../figures/kishky.php">роду Кішкаў</a>. 
		У XVI ст. Асвея — цэнтр воласці Полацкага ваяводства. 
		З 1600 г. многія землі па рэках Сар'янка, Росіца і Заходняя Дзвіна пераходзяць ва ўладанне канцлера Вялікага княства Літоўскага Льва Сапегі, i ў 1606г. 
		ваявода віцебскі Станіслаў Кішка прадаў маёнтак канцлеру <a href="../figures/sapega.php">Льву Сапегу</a>. 
		</p>
		<p>
		Двойчы ў 1616 і 1633 гадах Асвея пацярпела ад маскоўскіх захопнікаў. У сярэдзіне XVII стагоддзя Асвея належала Казіміру Львовічу Сапегу.
		 А з 1656г. – Казіміру Паўлавічу Сапегу. У 1695 годзе мястэчка атрымала статус горада. У XVII—XVIII стагоддзях праз Асвею праходзіў гандлёвы шлях з Масковii ў Інфлянты, 
		 Жамойць, Прусію, двойчы на год (2 траўня і 28 чэрвеня) тут праводзіліся кірмашы. З 1689 па 1758гг. тут знаходзiлася езуiцкая мiсiя.
		</p>
		<p>  
		У канцы першай паловы XVIII стагоддзя маршалак Вялікага княства Літоўскага Іван Сапега саступіў Асвею Ежы Мiкалаю Гiльзену, смаленскаму епіскапу. 
		Гэтыя ўладанні новы гаспадар атрымаў яшчэ перад сваім прысвячэннем у сан епіскапа (16 лютага 1746г. у Варшаве). Польскі гісторык Густаў Монтейфель адзначае, 
		што Ежы Мікалай Гильзен «быў рады пасяліцца ў Асвейскiх уладаннях, бо ўласнай рэзідэнцыі не меў». 
		</p>
		<p>
		Сапраўдны росквіт Асвеі пачынаецца з 1749 г., калі яе ўладальнікам становіцца менскі ваявода Ян Аўгуст Гильзен, аўтар «Кронікі інфлянцкай». 
		Браты Гильзены - Ежы і Ян былi ініцыятарамі будоўлi знакамітага палаца, рамантычнага парка ля яго, фундавалi сродкi на касцёл i кляштар мiсiянераў i запрасiлi сюды 
		сясцёр міласэрнасці, якія заснавалі шпіталь (1759г.). Пасля cмерцi Яна Аўгуста Гильзена (1767г.) яго жонка Канстанцыя Гільзен, выконваючы волю мужа, фундатавала сродкi 
		на будоўлю мураванага касцёла. Мураваны шпiталь быў пабудаваны таксама пад яе патранажам. 
		</p>
		

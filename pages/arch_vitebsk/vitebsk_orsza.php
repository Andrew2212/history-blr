        <p>
		Орша. Графiчная рэканструкцыя былой бажніцы Апекі Маці Божай Базыльянскага кляштара (пазней Царква Покрыва Прасвятой Багародзіцы Пакроўскага манастыра РПЦ). Выгляд на пачатак XX стагоддзя. Колер умоўны. 
		</p>
		<p>
		Вобраз Оршы 200 гадоў звязваўся з выдатнейшым архітэктурным комплексам Базыльянскага кляштара, кампазіцыйнай дамінантай якога была бажніца Апекі Маці Божай.
		</p>
		<p>
		Разам са св. Сафіяй ў Полацку, адбудаванай уніятамі ў 1750 пасьля знішчэньня маскоўскім войскам за часам <a href="../events/war_great_north.php">Паўночнай вайны</a>
		 1700-1721 і <a href="../arch_vitebsk/vitebsk_hlybokae.php">царквой св. Пятра і Паўла</a> ў Беразвеччы, 
		аршанскі храм можа служыць трэцім прыкладам варыяцыі на тэму цэнтрычнага, крыжовага ў плане, трохнававага базыльянскага двухвежавага храма. 
		</p>
		<p>
		У 1758  з ласкі Божай і з ініцыятывы і фундатарства аршанскага старасты Івана Лянскоўскага (паводле даследванняў Міколы Купавы) і з фундацыi былога унiятскага архiрэя Юзафа Ленкевiча, 
		архiепiскапа Смаленскага, якi фундаваў кляштару свае землi i сяло Мiхайлавiчы (згодна з даследваннямi I. М. Слюньковай), пачалося будаўнiцтва мужчынскага базыльянскага кляштара. 
		</p>
		<p>
		Ансамбаль узводзіўся выдатнымі майстрамі і па тым часе даволі хутка: за 16 год ён быў завершаны, аздоблены ўнутры і асвечаны ў 1774 годзе, амаль адначасова з Беразвецкiм.
		</p>
		<p>
		Кляштар размясціўся недалёка ад гістарычнага цэнтра горада, на правым беразе Аршыцы. Гэта сведчыла пра тое, што цэнтр места канчаткова склаўся і быў забудаваны, 
		а таксама пра тое, што горад пашыраў свае межы: у паўднёвым Задняпроўі былі збудаваныя комплексы куцеінскага манастыра, 
		на паўночным прадмесці — місіянерскі кляштар і на паўночным захадзе — комплекс базыльянскага кляштара з бажніцаю. 
 		</p>
 		

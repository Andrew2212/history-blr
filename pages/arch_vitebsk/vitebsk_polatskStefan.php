		<p>
		Пасля пажара 1750г., які знішчыў большасць драўляных пабудоў калегіума, пачалося будаўнiцтва мураваных 
		карпусоў. Яно працягвалася і пасля далучэння горада да Расейскай Імперыіі у вынiку першага  
		<a href="../events/razbor_rechi.php">падзелу Рэчы Паспалiтай</a> (1772), 
		i пасля <a href="../terms/jesuity.php">роспуску ордэна езуітаў</a> "на вечныя часы" папай Клімэнтам ХIV (1773). 
		</p>
		<p>
		Кацярына ІІ узяла езуітаў пад сваю апеку з мэтай выкарыстаньня іх ведаў для імперыі. 
		Езуіты маглі весці актыўную дзейнасць і ажыцьцяўляць свае будўнiцтва. 
		У Спаса-Ефрасіннінскім манастыры яны мелі летнюю сядзібу для кіраўнікоў ордэна, калегіуму і нават студэнтаў. 
		У Спаскай царкве быў зладжаны склеп, у якім хавалі выдатных дзеячоў ордэна. 
		</p>
		<p>
		Падчас прыезду Кацярыны ІІ у Полацк (1780) на шляху ва Украіну, езуіты ўдзельнічалі ва урачыстай сустрэчы царыцы, 
		зладзілі цудоўную ілюмінацыю на гарадскім пляцы і каля драўлянага палаца, дзе яна спынілася. 
		Агляд калегіума і яго фізічнага кабінета з усімі цудамі зрабілі на яе вялікае ўражаньне.
		</p>
		<p> 
		Пасля скасавання Віленскай акадэміі езуітаў цэнтрам ордэна стала Полацкая калегія, дзе знаходзіўся генерал Ордэну альбо яго намеснік. 
		У 1812г. калегія была ператворана ў езуіцкую акадэмію навук з правамі ўніверсытэта і наданьні ёй права кантраляваць 
		не толькі езуіцкія навучальныя установы, але і установы іншіх каталіцкіх абшчын. 
		</p>
		<p>
		Пасля высылкі езуітаў з Расеі манастырскі комплекс быў перададзены ордэну піяраў з умовай захаваць тут навучальную ўстанову. 
		Але пасля <a href="../events/uprising_1830.php">паўстання 1830</a> і піяры вымушаныя былі пакінуць Полацк, 
		і ў 1836г. у будынках полацкага калегіума размясцілася вайсковая навучальная 
		ўстанова — кадэцкі корпус, а касцёл ў лютым 1833г. быў перададзены РПЦ і пераасвянцоны ў царкву св. Мікалая Мірлікійскага, 
		якая пазней стала кафедральным саборам Полацка-Віцебскай епархіі.
		</p>
		

        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
		Полацк. Графiчная рэканструкцыя былога езуiцкага касцёла Св. Стэфана, перабудаванага ў праваслаўны Мікалаеўскі сабор 
		(існуючы дзевяцiпавярховы жылы дом з "вухамi" умоуна не паказаны). 
		</p>
		<p>
		Полацк стаў другiм цэнтрам пачатку дзейнасцi <a href="../terms/jesuity.php">езуiтаў</a> на тэрыторыi Беларусi. 
		Пасля вызвалення горада падчас <a href="../events/war_livonia.php">Лiвонскай вайны 1558-83</a> кароль і в. князь 
		<a href="../figures/batory.php">Стэфан Баторый</a> звярнуўся 
		да рэктара Вiленскай езуiцкай акадэмii Пятра Скаргi з прапановай аб неадкладным стварэннi тут езуiцкай мiсii. 
		</p>
		<p>
		Ужо 2.06.1580г., у Полацку закладваецца ордэнскі дом, у 1582г. фундуецца стварэнне рэзiдэнцыi, якая у 1585г. ператвараецца на калегiю. 
		Для развіцця ордэна дзякуючы Баторыю амаль усе праваслаўныя цэрквы i маснастыры за 
		выключэннем Св. Сафii (у тым ліку нават і Спаскі манастыр) i іх маенткі былі канфіскаваныя і перададзеныя езуітам. 
		</p>
		<p>
		Праваслаўныя жыхары i кальвiнiсты на чале з ваяводай Мiкалаем Дарагастайскiм нават звярталiся 
		за дапамогай ва ўзнаўленьні сваiх правоў да пастановы сейма, але безвынiкова. 
		Езуіты пабудавалі на старажытным гандлевым пляцы драўляны касцел і першы будынак калегіуму. 
		</p>
		<p>
		Пасьля захопу Полацка маскоўскім войскам у часы <a href="../events/blood_potop.php">Крывавага Патопу 1654-67</a>, 
		касцел, як і горад увогуле, быў зруйнаваны, а каталікі вымушаныя былi пакінуць горад. Пасля вызваленьня Полацка 
		 i вяртаньня каталiцкiх абшчын, яны зноў атрымалі адабраныя ў іх землі і пачалi аднаўляць свае храмы. 
		 Па звестках 1726 г. (з даследваньняў I. М. Слюньковай) полацкiя езуiты заснавалi на сваiх землях 15 касцёлаў i 22 унiяцкiя царквы.
		</p>
		<p> 
		Новы мураваны езуiцкi касцёл у Полацку ў гонар Стэфана Баторыя, вызваленцы Полацка, пачалi узводзiць на старых падмурках. 
		Яго будаўнiцтва ў 1733-1735гг. пачалось пад кiраўнiцтвам Казiмiра Мецелакоўскага, якi раней выкладаў у полацкiм калегiуме,
		і каменных спраў майстра, баварца Бенедыкта Месмера. 
		</p>
		<p>
		У 1745г. касцёл быў асвячоны пад тытулам св. Стэфана. Ён з'яўляўся высотнай дамiнантай усёй забудовы горада 
		— вежы уздымалiся на вышыню каля 50 м. 
		На правай вежы быў усталяваны гадзіннік з боем і вялізным цыферблатам віленскага майстра Густава Мудні, на левай — дэкаратыўны цыферблат. 
		Падрабязнае апiсанне касцёла i калегiума па падставе архiўных матэрыялаў складзена Аленай Квiтнiцкай. 
		</p>
		

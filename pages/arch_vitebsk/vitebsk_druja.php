		Асноўная ж частка Друі заставалася за Масальскімі. У 1618  Я. С. Сапега падпісвае прывілей на заснаванне побач з Друяй новага горада пад назвай Сапежын, 
		разлічваючы ператварыць яго ў значны рамесны, гандлёвы і абарончы пункт. Сапежын атрымаў магдэбурскае права. 3 пераходам пад юрысдыкцыю Сапегаў усёй Друі магдэбурскае права пашырылася на іншыя часткі горада.

		Смаляны. Графiчная рэканструкцыя касцёлу Найсвяцейшай Панны Марыі былога кляштару дамiнiканцаў.
		</p>
		<p>
		Смаляны (Смальяны) — вёска на паўночны захад ад Оршы, на рацэ Дзярноўцы. Першы пісьмовы ўспамін пра Смальяны як маёнтак у Аршанскім намесніцтве  датуецца 1484 годам. 
		</p>
		<p>
	    У розныя часы Смальяны знаходзіліся ў валоданні князеў Курбскіх, <a href="../figures/ostrogski.php">Астрожскіх</a>, Сангушкаў. 
		* Сангушкі — валынскі княскі род гербу "Пагоня Літоўская" (у двух чвэрцях гербавага поля<a href="../terms/blr_gerb.php"> Пагоня</a>, у двух — <a href="../terms/kalumny.php">Калюмны</a>), 
		буйныя магнаты Княства Літоўскага. Яны лічыліся сваякамі каралеўскай <a href="../figures/jagellony.php">дынастыі Ягелонаў</a>. 
		У XV-XVI ст., як і іншыя нашчадкі <a href="../figures/gedimin.php">Гедыміна</a> і <a href="../figures/olgerd.php">Альгерда</a>, 
		страцілі правы ўдзельных князёў, аднак здолелі захаваць значныя маёнткі на правах уплывовых магнатаў.
		</p>
		<p>
		Да 1626  князь С. Сангушка-Ковельскі збудаваў тут мураваны замак, які атрымаў ўласнае імя — "Белы Ковель" (ад м. Ковель на Валыні, спадчыны Сангушкаў). 
		</p>
		<p>
		Статус мястэчка Смальяны мелі з 1678 года. У 1680 жонка Геранiма Сангушкi, суражскага старасты i тагачаснага ўладальнiка Смалян, Канстанцыя Сапежанка выдзелiла пляц зямлi памерамi 100x100м памiж рынкавай плошчай i iтальянскiм садам манахам дамiнiканцам, з умовай пабудовы тут касцёла.
 		</p>
		<p>
		У 1729 другi сын Геранiма i Канстанцыi — Павел Кароль Сангушка пасля смерцi сваёй жонкi Марыі Ганны Любамірскай пабудаваў (цi перабудаваў) 
		на гэтым месцы драўляны касцёл і кляштарны будынак. Мураваны жа касцёл быў узведзены ў 1786  ужо ў часы знаходжання ў складзе Расейскай Iмперыi 
		пасля першага <a href="../events/razbor_rechi.php">падзелу Рэчы Паспалітай</a> (1772) з фундацыi сына Паўла Караля — князя Гераніма Сангушкі, 
		валынскага ваяводы i генерала расейскай армii. 
		</p>
		<p>
		Мураваны кляштар пачаў узводзіцца адначасова з касцёлам, аднак будаўніцтва яго было скончана толькі на пачатку XIX ст. 
		Да 1832  у кляштарным будынку жылі манахі-дамініканцы, тут жа месцiлася і невялікая парафіяльная школа.

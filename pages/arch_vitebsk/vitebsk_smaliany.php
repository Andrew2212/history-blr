		Пасля <a href="../events/uprising_1830.php">Паустання 1830</a> кляштар быў свасаваны, а касцёл пераўтвораны ў парафіяльны, у кляштарным жа будынку размясцілася плябанія. Да канцу XIX ст. будынкі былога кляштара прыйшлі ў заняпад. У 1898  на прыватныя ахвяраванні парафiян пабудовы плябаніі былі капітальна адрамантаваны.

		Па меркаваньнi Т.Габрусь па сваёй архiтэктонiцы касцёл шмат у чым паўтарае дамініканскі <a href="../arch_vitebsk/vitebsk_druja.php">касцёл Апекі Маці Божай</a> у Друi. Нягледзячы на багатае мінулае зараз тыповага мястэчка Смаляны, 

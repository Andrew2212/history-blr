		Першае графiчнае адлюстраванне святынi захавалася на гравюры Клецка, выкананай Томашам Макоўскiм на мяжы XVI-XVIIcт., яго дапаўняе план касцёла з "альбома Бернардонi" з надпiсам на лацiнцы "Касцёл месца клецкага пана маршалка" (маецца на ўвазе стрыечны брат Сiроткi ардынат клецкi Альбрыхт Радзiвiл, якi з 1585г. быў маршалкам ВКЛ) - адзiны вядомы на сёняшнi дзень план фарнага Троiцкага касцёла ў Клецку, датуемы 1585-1592гг. 
		</p>
		<p>
		З гэтых выяваў вынiкае, што на той час фарны касцёл у Клецку меў трохчасткавую структуру i складаўся з асноўнага прамавугольнай навы, пяцiграннай выцягнутай апсiды прэсбiтэрыя (з вiмай) i магутнай васьмiграннай вежы над нiжнiм чацверыковым ярусам, завершанай высокiм шатром. Чатыры верхнiя ярусы вежы мелi ў плане аднолькавае сячэнне, але розную адвольную вышыню. Наяўнасць апсiды прэсбiтэрыя i яе форма сведчаць аб каталiцкiм паходжаннi помнiка. 
		</p>
		<p>

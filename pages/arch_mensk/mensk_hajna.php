        <p>
		Упершыню Гайна (на той момант паселішча, якое ўваходзіла ў склад Лагойскага княства) згадваецца гісторыкам Я. Длугашам (XV ст.) 
		сярод уладанняў вялікіх князёў, адна з найстарэйшых рымска-каталіцкіх парафіяў <a href="../terms/wkl.php">Вялікага Княства Літоўскага</a>. Паводле некаторых 
		гістарычных крыніцаў XV—XVI стагодзьдзяў (Стрыйкоўскі, Бельскі, Мехавіта) Гайна была адным з першых 10 паселішчаў Вялікага Княства 
		Літоўскага, у якіх Ягайла з 1386г. засноўваў касцёлы на месцы былых паганскіх капішчаў па час <a href="../terms/litva_baptism.php">хрышчэньня Літвы</a>. 
		Першапачаткова касцёл быу драўляны. 
		</p>

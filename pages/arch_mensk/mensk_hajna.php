		значны маёнтак Кацелі. У 1514г. мясцовы касцёл наведаў вялікі князь Жыгімонт Стары (з Барысава прыйшоў да Гайны, як да найбліжэйшага 
		касцёла) для складання Богу ўдзячнасці, з войскам, з высокімі панамі і палоннымі пасля перамогі над маскоўскімі захопнікамі ў 
		<a href="../events/orsha_battle.php">Бітве пад Воршай 1514</a>.
		</p>
		<p>
		Тры дні кароль тут адпачываў і адбываў жалобнае набажэнства ды пахаванне двух сваіх слуг, герояу аршанскай бiтвы - 
		Станіслава Аарона і Станіслава Ніскоўскага. Ёсць версіі, што многіх воінаў пахавалі тут. У войску ВКЛ было шмат татар (былі 
		паселеныя на Лагойшчыне яшчэ Вітаўтам). Род Барана, род Куніцы, род Арла. І тыя татары, што загінулі пад Воршай, пахаваныя таксама 
		тут. Жыгімонт Стары надаў плябаніі свае каралеўскія гарантыі, значна пашырыў землеўладанні касцёла дзеля таго, каб у знак памяці аб 
		гэтай падзеі святары ў кожны месяц адзін раз спявалі імшу аб Нараджэнні Найсвяцейшай Дзевы Марыі з упамінаннем двух Станіславаў.

		Прэзбiтэрый фланкавалi з абодвух бакоў дзве закрыстыi, адна з якiх была двухпавярховай. У інтэр'еры было ўстаноўлена 5 алтароў. Вельмі шанаваўся абраз "Панна Марыя Гайнаўская". 
		Срэбны крыж італьянскай работы пачатку XVI стагоддзя, падараваны касцёлу Жыгімонтам І Старым, захоўваўся ў зборы Тышкевічаў у Лагойскім палацы. 
		</p>

    	<p>
    	У 1522г. пры касцёле адчынiлаcя школа. Пад 1560г. Гайна ўзгадваецца як мястэчка, цэнтр стараства. З 1779г. пробашч гайнаўскі кс. Тышкевіч фундаваў 
		сродкi на новы мураваны касцёл (стары драўляны ўжо валіўся), пачаў завозіць матэрыялы, але толькi яго наступнік кс. Цыдзік выканаў задуму свайго папярэдніка. 
		У 1788г. адбылася кансекрацыя святынi вікарным біскупам жамойцкім Тадэвушам (Фадзеям) Букаты. 

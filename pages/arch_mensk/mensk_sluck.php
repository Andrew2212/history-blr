        <div class="container">
			
        <h3>Гісторыя і рэканструкцыя</h3>
			
        <p>
        Слуцк. Троiцкi сабор Трайчанскага манастыра. Графiчная рэканструкцыя на перыяд пачатку мiнулага стагоддзя. Колер умоўны. 
		</p>
		<p>
		Слуцкі Троіцкі манастыр (Трайчанскі манастыр) — праваслаўны мужчынскі манастыр, які існаваў на левым беразе ракі Случы ў прадмесці Трайчаны у 15 ст. - 1930г., 
		колішні асяродак праваслаўя на землях <a href="../terms/wkl.php">Вялікага Княства Літоўскага</a>. Час узнікненьня невядомы, упершыню ўзгадваецца ў 1455. 
		</p>
		<p>
		У 1571  настаяцелем манастыра быў архімандрыт Міхаіл Рагоза, будучы кіеўскі мітрапаліт. З 1659  архімандрыты манастыра з'яўляюцца намеснікамі кіеўскага мітрапаліта на тэрыторыі ВКЛ. 
		</p>
		<p>
		У 1582, калі браты Алелькавічы падзялілі Слуцкае княства, манастыр застаўся іх сумесным валоданнем. Захаваўся дакумент, у якім яны абавязаны даваць ахвяраваньні манастыру.
		</p>
		<p>
		Апошняя з княскага роду Алелькавічаў — <a href="../figures/zofia_sluck.php">св. Сафія Слуцкая</a>, дачка каталічкі і праваслаўнага, гадаваўшаяся ў каталіцкай сям'і, абралася шлюбам (з дазволу Папы Рымскага)
		 за кальвіністам Янушам Раздзівілам. Напярэдадні вясельля паміж Хадкевічамі (апекунамі) і Радзівіламі (сватамі) у Вільне ледзь не адбылося вялікая бітва
		 — войска з абодвух бакоў было сьцягнута больш, чым на <a href="../events/battle_kircholm.php">бітву пры Кірхгольме</a>.
 		</p>
		<p>
		Януш Радзівіл, якому ад Алелькавічаў перайшоў Слуцк, як i абяцаў жонцы, апекаваў манастыр. У тэстамэнце ён пісаў: 
		<i>
		"Цэрквы і манастыры рэлігіі старарускай ў валоданнях маіх па-ранейшаму павінны захоўвацца і нашчадкі павінны сачыць, каб не адбылося ў тым аніякай перамены..."
		</i> Таму св. Сафія лічыцца ахоўніцай праваслаўя на Беларусі. 
		</p>
		<p>
		Галоўнай святыняй манастыра быў Троіцкі сабор. Каменны храм замест старажытнага драўлянага пабудаваны ў 1505  намаганьнямі Слуцкай княгіні Анастасіі Алелькавіч (верагодна, гэта дата закладкі ў памяць аб абароне горада ад перакопскiх татараў). 
		</p>
		<p>
		Падчас <a href="../events/blood_potop.php">Крывавага патопу 1654-67</a>  маскоўскія захопнікі і казакі спустошылі горад, Траецкую царкву практычна цалкам знішчылі. 
		Тагачасны ўладальнік Слуцку, князь Багуслаў Радзівіл у 1667 фундаваў сродкi на на будаўніцтва манастыра. 
		Мясцовыя жыхары (пераважна купцы і <a href="../terms/shliahta.php">шляхта</a>) таксама рабілі ўнёскі на аднаўленне святынi (аплацілі камень).
		Да 1678 храм быў амаль адноўлены, вялася толькі рэстаўрацыя алтара.
 		</p>
		<p> 
		У <a href="../events/war_great_north.php">Вялікую Паўночную вайну 1700-21</a> Траецкая царква была ізноў зруйнавана. 
		</p>
		<p>
		У 1732  атрыманы прывілей з дазволам на збор грошай на рамонт храма ад караля Аўгуста II . У 1785-88 царква амаль перабудавана,  другарадныя работы вяліся да 1795, а іканастас скончаны ў 1804. 
		У царкве захоўваліся цудоўны абраз Маці Божай візантыйскага пісьма, Слуцкае Евангелле 1582, старажытныя партрэты князёў Алелькавічаў, вырабы з срэбра, парэшткі св. Сафіі Слуцкай .
		</p>
		<p>
		У <a href="../events/world_war_1.php">Першую Сусветную вайну</a> ў манастыры быў лазарэт. Улетку 1917  будынкі манастыра, дзе жыло 13 манахаў і 13 паслушнікаў, перададзены гімназіі, настаяцель высланы. 
		</p>
		<p>
		У 1926  ў адпаведнасці з пастановай Савета народных камісараў БССР "Аб абвяшчэнні дзяржаўнай уласнасцю помнікаў даўніны і мастацтва, побыту і прыроды" ў Слуцку такімі прызнаваліся 12 аб'ектаў. Сярод іх усе захаваўшыеся на той момант цэрквы (12), уключаючы царкву Трайчанскага манастыра.
 		</p>
		

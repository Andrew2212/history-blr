		<p>
		Але ў 1615  гэтыя будынкі, верагодна, драўляныя, знішчыў пажар. Да названага часу належыць фундацыя, ажыцьцяўлёная стольнікам 
		<a href="../terms/wkl.php">Вялікага Княства Літоўскага</a> і старастам рэчыцкім Пятром Тышкевічам. 

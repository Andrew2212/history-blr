		<p>
		1920 (27 ноября) <a href="../events/uprising_sluzk.php">Слуцки Збройны чын</a> — попытка Часовай Рады Случчыны восстановить 
		независимость края в формате БНР. "Збройны чын" справедливо называют не "восстание", а <i>"беларуска-расейская вайна 1920 году"</i>. 
		В течение месяца пад зброю стали свыше 4000 человек. 
		</p>		
		<p>		
		1921 (18 марта) Рижский мир — итог советско-польской войны — снова разделил Беларусь почти по линии 
		Второго <a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>.
		</p>
		

		<p>		
		1920 (31 июля) Создание <a href="../terms/bssr.php">БССР</a> — в газете «Советская Белорусь» была опубликована "Декларация о независимости БССР".
		</p>
		<p>
		1920 (9 октября) Создание Срединной Литвы Litwa Srodkowa (1920-22) — 1-ая Литовско-Белорусская дивизия генерала Желиговского
		(куда входили Виленский, Минский, Новогрудский, Гродненский стрелковые полки 
		и Литовско-Белорусский полк артилерии) отбила Вильню у Литовской Республики. 
		</p>
		
		<p>
		Генерал Желиговский поддерживал пан-славянские движения и считал Литву центром славянства.
		— <i>"Но не только географически Литва была сердцем славянства. Была им морально. Она, одна из всех славянских народов, могла легко поговорить со всеми. 
		Как с Польшей, так с Россией, так с Украиной. Уклад психичны литовских народов была как бы создан, чтобы примирить всех. У него никогда не было враждебности, 
		ни национальной, ни религиозной, ни культурной."</i>
		</p>
		

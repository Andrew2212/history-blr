		<p>
		1919 Поражение БНР в борьбе с большевиками. 1 января в <a href="../places/smolensk.php">Смоленске</a> была провозглашена Советская Социалистическая Республика Белоруссия, 
		8 января правительство ССРБ переехало из Смоленска в Минск. 27 февраля объявлено о создании Литовско-Белорусской ССР.
		</p>

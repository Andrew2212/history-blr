		<p>
		1990 (27 июля) Принята Декларация о государственном суверенитете Белорусской ССР, 
		</p>
		<p>
		1991 (25 августа) Декларации о суверенитете придан статус конституционного закона. 19 сентября БССР была переименована в «Республику Беларусь». 
		</p>
		<p>
		1991 (8 декабря) В усадьбе Вискули (Беларусь) были подписаны Беловежские соглашения о прекращении существования 
		Союза Советских Социалистических Республик как «субъекта международного права и геополитической реальности» 
		и о создании Содружества Независимых Государств (СНГ)
		</p>
		<p>
		1991 (26 декабря) Совет Республик Верховного Совета СССР принял декларацию о прекращении существования СССР, 
		тем самым официально распустив Союз ССР и его институты власти. СССР прекратил свое существование.

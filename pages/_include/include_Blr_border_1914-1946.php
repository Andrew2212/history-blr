		<p>
		1939-1945 <a href="../events/world_war_2.php">Вторая Мировая война</a> (ВМВ). 
		</p>
		<p>		
		1939 (17 сентября) Польский поход РККА и парад в Бресте — <a href="../terms/sssr.php">Советский союз (СССР)</a> вступил во Вторую Мировую войну. 
		2 ноября издан "Закон СССР от 2.11.1939 о включении Западной Белоруссии в состав СССР и воссоединении её с БССР".
		</p>
		<p>
		1946 По результатам Ялтинской конференции 1945 (подводящей итоги ВМВ), Белостокская область, а также небольшие участки Гродненской 
		и Брестской области были переданы Польской Народной Республике.
		</p>
	

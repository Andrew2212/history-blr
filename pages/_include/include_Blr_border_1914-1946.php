	<h3>Хронология событий 1914-1991 в границах Беларуси</h3>
		<p>
		1914-1918 <a href="../events/world_war_1.php">Первая мировая война</a>. 
		В 1916-18 Линия фронта проходит по границе Второго <a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>.
		</p>
		<p>
		1917 Крах <a href="../terms/ross_empire.php">Российской империи</a> — в результате Февральской революции (переворота) в границах империи объявлена Российская Республика. 
		Октябрьским переворотом (революцией) Временное правительство Республики свергнуто. 
		</p>
		<p>
		1918 (25 марта) В исторических <a href="../download/blr_people.php">границах белорусского этноса</a> 
		объявлена <a href="../terms/bnr.php">Белорусская Народная Республика</a> (БНР).
		</p>
		
		

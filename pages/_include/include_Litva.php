		<p>
		На самом деле вся территория Беларуси находится в зоне балтских гидронимов. И "славянство" — это не гены, а лингво-культурная парадигма. Были мощные очаги 
		славянской культуры — Полоцкое и Туровское княжества. Были волынские славянские анклавы на языческих землях — Гродно и Новогрудок. 
		Еще в XII веке сын <a href="../figures/charadzej.php">Всеслава-Чародея</a> 
		князь <a href="../terms/dwina_stouns.php">Рогволод-Борис</a> в те края <i>"ходил на ятвяг и, победя их, возвратясь, 
		поставил град [Борисов] во свое имя...".</i> 
		</p>		

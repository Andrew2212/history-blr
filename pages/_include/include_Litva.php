		<h3>Забавные тезисы</h3>
	
		<p>
			Несколько забавных рассуждений. 
		</p>		
		<p>
		<i>"Литовцы-аукштайты вышли из северных лесов и всех захватили"</i>. При этом миграция населения, начиная с IV века идет с юга на север.
		</p>		
		<p>
			<i>"От Припяти до Вилии была война <a href="../terms/slavs.php">славяне_VS_балты</a> и в XIII веке славяне вытеснили балтов с исконных мест".</i> 
			Это вообще-то должно быть похоже на исход евреев из Египта. Но хроники такого исхода не фиксируют. 
			Автохтонный этнос на <a href="../download/blr_people.php">белорусских землях</a> не менялся 2000 лет. 

		<p>
			Термин Литва впервые упоминается в 1009 году в связи со смертью миссионера Бруно на "границе Литвы и Руси". 
			Новгородская летопись в 1197 (6706) году пишет о князе, что "посажен на Луках княжичи, от Литвы оплечье Новгороду".
			Как централизованное княжество Литва формируется при <a href="../figures/mindowg.php">Миндовге</a> (1195-1263). 
			К этому времени локализация термина "Литва" в хрониках становится более конкретной. 
		</p>
		<p>
		В своей работе <a href="../download/deutschen_und_dachbarstamme.php">"Die Deutschen und die Nachbarstamme"</a> 1837 года Йон Каспер Цойсс, 
		обобщая труды немецких и польских хронистов, локализует расселение <a href="../terms/litwiny.php">литвинов</a> (Litwany) от реки Святая (на севере) до болот Припяти (на юге).	
		Как наследие <a href="../terms/polotsk_principality.php">Полоцкого княжества</a>, отмечается река Березина — <i>"граница Литвы и Руси"</i>.	
		</p>
				

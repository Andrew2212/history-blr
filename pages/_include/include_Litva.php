		<h3>Мигрирующие и забытые термины</h3>
		
		<p>
		Аукштайтия, Жмудь-Самогития, Нальшаны и Дяволтва. 
		</p>
		<p>
		Аукштайтия — верхняя (горная), — то ли расположена на высоких холмах, то ли выше по течению Немана, чем Жамойть (нижняя).
		До 1920-х ее локализовали у истоков Немана на Ошмянской возвышенности — от Гродно до Браслава. 
		Современная Аукштайтия расположена большей частью в низине и Неман через нее не протекает. Термин Аукштайтия, обозначающий территорию,
		 известен со времен <a href="../download/chronicles_peter.php">Питера из Дуйсбурга</a> (14 век). Термин Аукштайты, обозначающий некий этнос, появился в 19-20 веках
		</p>
		<p>
		<a href="../places/samogitia.php">Жмудь-Самогития</a> — <i>"в XIII и даже в XIV вв. занимала большое пространство, заходя в пределы нынешней Виленской губ."</i> (с) Брокгауз и Ефрон. 
		Сегодня сократилась более, чем вдвое.
		</p>
		<p>
		<a href="../places/nalsen.php">Нальшаны и Дяволтва</a> (Литва Завилейская) — во времена Миндовга вассальные Литве княжества, лежащие между Литвой и Жмудью. К собственно Литве были присоединены сыном Миндовга,
		Войшалком. Занимали северную часть <a href="../terms/lit_bel.php">Литвы срединной</a>. Собственная Литва располагалась южнее.
		</p>
		

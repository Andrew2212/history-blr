		<h3>Литва в Российской империи</h3>
		
		<p>
		После второго <a href="../events/razbor_rechi.php">раздела Речи Посполитой</a>, в России было составлено первое 
		<a href="../download/all_peoples_RI.php">"Описание всех обитающих в Российском государстве народов"</a>.
		Народ, живущий западнее Смоленска, был назван <i>"Поляки — ветвь Славянского народа, один из двух главных народов, составивших настоящий Российский."</i>
		А "древней Литвой" была названа часть ВКЛ, не попавшая в состав РИ после второго раздела РП — от Самогитии до Волыни. После третьего Раздела РП имя "Литва"
		было перенесено на Самогитию. Термин "Жмудь" практически более не использовался. 
		</p>
		

		<p>
		К XVIII веку картография вышла на современный уровень точности (буквальному наложению старых карт на современные мешает только использование разных 
		картографических проекций). Картографы единодушны — на всех картах, от схематичных до очень подробных "Литва" 
		или <a href="../terms/wkl.php">Великое княжество Литовское</a> 
		определяется однозначно. Что и было отражено в геральдике воеводств. Наприме, в подробном
		<a href="../download/atlas_historyczny_sage.php">"Atlas historyczny, genealogiczny, chronologiczny"</a> A. Le Sage, 1799.
		</p>

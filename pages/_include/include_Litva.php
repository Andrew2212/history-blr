	<p>
	Начиная с XVII века,  по хроникам и летописям историки пытались очертить географию "Литвы изначальной". Виюк-Коялович (писатель, историк, иезуит, профессор Виленской академии) 
	описывал ее так — <i>"Все, что между реками Вилия и Припять, Litvania Мозырь, Брест, Новгородок, Гродно, Подляшье"</i>. 
	Литву Миндовга исследователи определяют в треугольнике Слоним-Молодечно-Новогрудок.

		<h4>1597 "Kronika Polska", Marcin Bielski</h4>
		
		<p>
		Марцин Бельский (1495-1575) — польский писатель, историк и поэт. Сделал первый в польской литературе обзор всемирной истории. 
		Его работы существенно повлияли на всю восточнославянскую историографию.
		</p>
		<p>
		Погоня и Калюмны — "гербы Litewskie".
		<i>"Рыцарство Литовское уживает на хоругви по одной стороне Погоню/... по другой три слупа белых на поле червоном..."
		</i>
		</p>

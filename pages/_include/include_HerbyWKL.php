		</a>
		</p>
		<p>
		Впервые гербы в современном понятии возникли во времена Крестовых походов XI-XIII вв. 
		На <a href="blr_people.php">белорусских землях</a> такие гербы появились в XIV-XV вв. 
		По <a href="../events/unia_gorodlo.php">Городельской унии</a> 1413 года бояре 
		<a href="../terms/wkl.php">Великого княжества Литовского</a> вошли в польское шляхетское гербовое братство — "акт об адопции".
		</p>
		<p>
		Первые два автора, представленные ниже, писали свои труды в 1580-х  — во времена <a href="../figures/batory.php">Стефана Батория</a>, 
		через 15 лет после <a href="../events/unia_liublin.php">Люблинской унии</a> 1569 
		и образования <a href="../terms/rzeczpospolita.php">Речи Посполитой Обоих Народов</a>.
		</p>
		<p>
		Третий автор ("Атлас" 1799 г.) работал во времена <a href="../events/razbor_rechi.php">разделов Речи Посполитой</a> 1772-1795. 
		</p>
		<p>
		Т.о. можно видеть, что эти 200 лет гербы воеводств ВКЛ-Беларуси оставались неизменными. Погоня и Калюмны. Их стоит чтить хотя бы за это.
		</p>
		<p>
		Карта "Litvaniae, административоное деление, 1749" взята из альбома <a href="../download/kartografia_wkl.php">"Картография ВКЛ XV-XVIII вв"</a>. 
		</p>
		
		<h4>1584 "Herby rycerstwa polskiego", Bartosz Paprocki</h4>
		<p>
		Бартош Папроцкий (1543-1614) - польский и чешский историк и геральдист, поэт и переводчик. 
		</p>
		<p>
		Bartosz Paprocki о Погоне — "Погоня, которая только князьям великим и княжеству оному належит".
		Использование герба Калюмны он связывал с именем <a href="../figures/vyten.php">Витеня</a> — <i>"Уживал знак рыцарски, трех слупов, как о том руские хроники говорят"</i>
		</p>

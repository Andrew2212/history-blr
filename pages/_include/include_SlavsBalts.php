<h1>Славяне и балты
		</br><small>славяне, как лингво-культурная парадигма</small>
		</h1>
		<p class="img-float-left">
		<a href="../../images/img-terms/blr_etnos/slavic_shafarik.jpg" class="highslide"
        onclick="return hs.expand(this)">
			<img src="../../images/img-terms/blr_etnos/slavic_shafarik_thumb.jpg" height="160"/>
			</br><small>Й. Шафарик о "славянах"</small>
		</a>
		</p>
		<p>
			Для начала, определим, что есть "славяне".
			</br>По мнению ряда ученых (Н. С. Трубецкой, А. А. Бычков, В. Б. Егоров, С. Е. Рассадин, А. А. Клёсов и др.), «славяне» — не этнос, 
			а сходство языка, культуры и верований. 
		</p>
		<p>
			Вот что пишет, к примеру, Анатолий Клёсов, лауреат Государственной премии СССР, член Всемирной академии искусства и науки, биохимик и генетик, 
			кандидат на Нобелевскую премию:
			</br><i>«Официальная наука», помещая наших предков в VI-VII века нашей эры, недоговаривает, что «славяне» — это только лингвистическое понятие, 
			и оно относится только к языкам славянской группы. (…) 
			Любой толковый историк и лингвист это знает, но на публике продолжают уныло долдонить, 
			что «славяне появились только во второй половине 1-го тысячелетия нашей эры». 
			Заметьте, не язык сформировался, а «славяне появились». То есть как люди, как популяция. Так у историков-лингвистов принято. 
			Иначе окрикнут. Сделают внушение. Оно им надо?»</i>
		</p>
		<p>
		Славянство возникло в результате длительного мирного взаимодействия конгломерата разных этносов: 
		<a href="yotving.php">германских</a> (готских), <a href="lutizy.php">балтских</a> и иранских (скифо-сарматских) на определенной территории. 
		В процессе такого взаимодействия выработался общий язык, сложились общие черты духовной и материальной культуры. 
		Сходство языков, элементов духовной и материальной культуры — это и есть суть славянства.
		</p>
		<p>
		Соответственно, славянизация соседей происходила путем передачи языка, элементов духовной и, 
		отчасти, материальной культуры по мере проникновения на их земли групп пришельцев и торговых контактов.
		</p>
		<p>
		Вероятно, праславянский этнос сложился в ареале <a href="yotving.php">Черняховской археологической культуры</a>, существовавшей с начала III до середины VI века. 
		Это регион между Дунаем на западе и Днепром на востоке, Припятью на севере и Черным морем на юге. 
		Здесь была контактная зона готских (германских), балтских и скифо-сарматских (иранских) племен. 
		Постепенно они объединились в протогосударство во главе с готской племенной знатью и за 250-300 лет мирного взаимодействия выработали 
		«койне» — упрощенный язык «межнационального общения». Это и был праславянский язык.
		</p>


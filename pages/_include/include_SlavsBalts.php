		<p>
			<i>Первая волна славянизации.</i> В начале VI века авары при движени из Азии на Балканы опустошили земли Черняховской культуры. 
			Жители бежали преимущественно на запад, и отчасти на север, в леса и болота Полесья — южной части нынешней Беларуси. 
		</p>
		<p>
			<i>Вторая волна.</i> В VIII-ХII вв <a href="../terms/lutizy.php">лютичи-велеты</a> пришли из Полабья, вытесняемые германскими переселенцами. 
			они двигались по Западной Двине, Нёману, Днепру и их притокам.
		</p>
		<p>
			<i>Третья волна.</i> В IX- X вв. появились беженцы из Великой Моравии после вторжения венгров.  
			 Некоторая часть моравов по Припяти и «<a href="../places/sea_herodotus.php">морю Геродота</a>» проникала на земли будущей Беларуси. 
			Моравы несли с собой не только славянский язык и развитые ремесла, но и христианство арианского толка.
		</p>
		<p>
			<i>Четвертая волна</i> шла из Галицко-Волынской и Киевской Руси. Это X-XII века. 
			Волыняне основали Берестье (Брест), Здитов, Каменец, Кобрин, Острею, Слоним и ряд других градов.
		</p>
		

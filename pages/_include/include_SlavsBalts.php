		<h2>От венедов до ВКЛ</h2>
		<p>
		Ну, т.е. на наший землях жили <i>"венеды", "кривичи", "ятвяги", "литвины", "вильцы-лютичи"</i> — а потом все стали <i>"славяне"</i>. 
		И ни переселения народов, ни геноцида при этом не случилось.
		Издание 1837 <a href="../download/deutschen_und_dachbarstamme.php">"Немцы и их соседи"</a> Й. К. Цойсса 
		— труд, обобщивший летописные описания наородов и источник часто встречающихся в цитат.
		Архаичные племенные союзы — древляне, дреговичи, радимичи и пр, — после X в. практически исчезают из летописей, заменяясь иными названиями 
		— "русины", "литвины", "полочане". Лишь <a href="../terms/yotving.php">ятвяги</a> упоминаются до XIX века.
		</p>
		

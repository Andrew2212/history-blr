		<br><a href="../figures/vitovt.php">Витовт</a> — трижды крещен, многочилсенные грамоты на "руской мове". 
		<br><a href="../figures/jagiello.php">Ягайло</a> — славянский правитель объединеных славянских Литвы и Польши, при дворе говоривший на чешском и руском. 
		</p>
		

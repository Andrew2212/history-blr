	<h2>1914</h2>
	<p> 
	<span class="select">28 ліпеня</span> Пачалася <a href="/pages/events/world_war_1.php">Першая Сусветная вайна</a> (28.07. 1914 - 11.11. 1918) паміж Траістым Звязам і Антантай.
	Ад пачатку вайны і да 1915 Стаўка Вярхоўнага Глаўнакамандуючага расейскай арміяй знаходзілася ў Баранавічах, а пасьля ў Магілёве.
	</p>
	<p>
	<i>Траісты Звяз</i> — Германія, Аўстра-Вугоршчына і Італія. Цягам вайны да іх далучыліся Асманская імпэрыя і Балгарыя, а Італія перайшла на бок Антанты. 
	Склаўшыся блок менаваўся "Цэнтральныя дзяржавы".
	<br>
	<i>Антанта</i> ("згода", фр.) — Брытанская імпэря, Расейская імпэрыя і Французская рэспубліка. 
	</p>
	<p>
	Па выніку вайны былі скасаваны чатыры імпэрыі — Расейская, Асманская, Аўстра-Вугоршчына і Германскі Kaiserreich.
	</p>
	

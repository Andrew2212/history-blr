	<h2>1915-1916</h2>
	<p>
	<span class="select">1915</span> "Вялікі адступ" — шэраг параз Расейскай арміі і адступ ад Варшавы амаль да Менску. Лінія фронту, расьсекшая Беларусь з поўначы на поўдзень, пратрымаецца да самага сканчэньня вайны.
	</p>
	<p>
	<span class="selectRed">1915</span> У Вільне ўтвораны Беларускі Народны Камітэт на чале з <a href="/pages/figures/luckiewicz_anton.php">Антонам Луцкевічам</a>, 
	як каардынацыйная ўстанова раз'яднаных нацыянальных грамадзянскіх арганізацый. Падтрымліваў канцэпцыю Канфэдэрацыі ВКЛ.
	</p>
	
	

	<span class="select">8 сьнежня</span> Акрэсьлена Лінія Керзана для ўсталяваньня ўсходняй мяжы Польшчы. Вызначана міністрам замежных спраў Вялікабрытаніі, лордам Дж. Керзанам, і прапанавана Парыжскай мірнай канферэнцыяй. Амаль супадае з сучаснай мяжой. 

	<p>
	<span class="selectRed">27 лістапада (9 сьнежня)</span> бальшавікамі РСДРП(б) у Менску ўтвораны "ОблИсполКом" и "СовНарКом" Совета рабочих, солдатских и крестьянских депутатов Западной области и фронта (пред. А. Мясников и К. Ландер). Беларусь разглядалася толькі як Заходняя частка Расеі.
	</p>
	<p>
	<span class="selectRed">7 (20) сьнежня</span> ў Менску распачаў работу Першы Усебеларускі з'езд (1872 дэлегата). Праходзіў ён у будынку гарадского тэатра, цяпер Нацыянальны тэатр імя Янкі Купалы. 
	З'езд вызначў права беларусаў на самакіраваньне і усталяваньне дэмакратычных форм праўленьня. Прапановы ўдзельнікаў-бальшавікоў падтрымкі не знайшлі.
	</p>
	<p>
	<span class="selectRed">17 (30) сьнежня</span> Першы Усебеларускі з'езд быў разагнаны узброенымі бальшавікамі (як калісьці Часовы Урад у Петраградзе). Вялікая беларуская Рада сыйшла ў падпольле.
	</p>
	

	<h2>1917</h2>
	<p>
	<span class="select">23 лютага (8 сакавіка)</span> Лютаўская рэвалюцыя ў Расейскай імпэрыі — скасаваньне манархіі і стварэньне Часовага Ураду. Спроба пераўтварыць манархію  (што да 1906 года была абсалютнай) у рэспубліку.
	</p>
	<p>
	<span class="selectRed">25-27 сакавіка (7-9 красавіка)</span> ў Менску адбыўся I З'езд беларускіх нацыянальных арганізацый. 
	Быў утвораны Беларускі Нацыянальны Камітэт (БелНацКам) у складзе 18 чалавек на чале з <a href="/pages/figures/skirmunt.php">Раманам Скірмунтам</a>. 
	З'езд выказаўся ў падтрымку Часовага Урада, за беларускую аўтаномію ў складзе Расеі, даручыў БелНацКам падрыхтаваць канстытуцыю "беларускага Краёвага Ураду".
	</p>
	<p>
	<span class="select">3 чэрвеня</span> Першы Усерасійскі з'езд працоўных і салдацкіх дэпутатаў (1090 чалавек). Пачатак фарміраванья сістэмы "Саветаў" (стварэньне ВЦИК — Всероссийского Центрального исполкома  Советов депутатов).
	</p>
	<p>
	<span class="selectRed">8-10 ліпеня</span> ў Менску адбыўся II з'езд нацыянальных партый. Замест скасаванага БелНацКам створана Цэнтральная Рада (з кастрычніка - Вялікая Беларуская Рада). Аснову яе праграмы склалі развіцьцё нацыянальнай культуры, арганізацыя беларускай арміі, аўтаномія Беларусі.
	</p>
	<p>
	<span class="select">26 жніўня-1 верасьня</span> Карнілаўская выправа — няўдалая спроба ўсталяваньня вайсковай дыктатуры, распачатая Вярхоўным галоўнакамандуючым Расейскім Войскам генералам Л. Г. Карнілавым дзеля адхіленьня бальшавікоў ад улады. Кіравалася са Стаўкі Глаўкома ў Магілеве. Параза Карнілава толькі ўзмацніла стан бальшавікоў ва ўладных установах.
	</p>
	<p>
	<span class="selectRed">18 (31) кастрычніка</span> пачынаецца з'езд беларускіх вайскоўцаў Заходняга фронту, які стварае Часовы Выканаўчы камітэт Цэнтральнай беларускай ваеннай Рады на чале з Сымонам Рак-Міхайлоўскім. Установа павінна сфармаваць беларускае войска.
	</p>
	<p>
	<span class="select">25 кастрычніка (7 лістапада)</span> Кастрычніцкі пераварот
	<ul>
		<li><p>
	 	- узброенай вартай разагнаны Часовы Урад Расейскай рэспублікі, улада пераходзіць да бальшавікоў
	 	</p></li>
	 	<li><p>
		- распачаўся Другі Усерасейскі з'езд дэпутатаў, сфарміраваны Савет Народных Камісараў (Совнарком) на чале з В. Леніным. Абвешчаньне РСФСР (Расейскай федэрацыі)
	 	</p></li>
	 	<li><p>
		 - распачалася Грамадзянская вайна ў Рассеі (1917-1922)
		 </p></li>
	 </ul>
	</p>
	

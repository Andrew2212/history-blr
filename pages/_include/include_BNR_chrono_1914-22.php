	<h2>1918</h2>
	<p>
	<span class="select">Студзень.</span> Падоўжвецца падрыхтоўка Берасцейскага міру не чакаючы згоды ад хаўруснікаў па Антанце. 
	Германія лічыць Прыбалтыйскія краіны Прускай спадчынай і ўсталюе жорсткія умовы замірэньня. 
	</p>
	<p>
	<span class="select">15(28) студзеня</span> Дэкрэт аб утварэньні  расейскай арміі — Рабоче-крестьянской Красной армии (РККА)
	</p>
	<p>
	<span class="selectRed">25-28 студзеня</span> ў Вільні склікана Беларуская канфэрэнцыя прадстаўнікоў розных сацыяльных колаў і беларускіх арганізацый з акупіраванай Германіяй тэрыторыі Беларусі.
	</p>
	<p>
	<span class="select">18 лютага</span> Нямецкія войскі парушаюць замірэньне ў Солах. За 5 дзен яны рушылі наперад на 200-300 км. Да 1 сакавіка былі заняты Латвія, Эстонія, Менск, Гомель, Орша, Магілеў, Чарнігаў.
	</p>
	<p>
	На адарваных ад былой Расейскай імпэрыі тэрыторыях адразу пайшоў працэс нацыянальнага самавызначэньня — былі абвешчаны Літоўская рэспубліка (16.02.1918), 
	Эстонская рэспубліка (24.02.1918),  БНР (25.03.1918), Латвійская рэспубліка (18.11.1918).
	</p>
	<p>
	Але, калі краіны Прыбалтыцы разглядаліся Германіяй як безумоўная Пруская спадчына (германскі прынц нават быў запрошаны на пасаду літоўскага караля Міндаўгаса II) і атрымлівалі дапамогу ў фармаваньні войска і ўладных  структураў, наш Край разглядался ў якасьці істоты гандлю на Берасьцейскій перамовах і аніякай падтрымкі не вынаходзіў.
	</p>
	<p>
	<span class="selectRed">21 лютага</span>  Выканаўчы камітэт Рады выдаў Першую устаўную Грамату, абвесьціушы сябе Часовым Урадам на Беларусі. У ей сцвярджалася, што ўрад павінен складацца згодна з волей народа Краіны і адрыналася неабходнасьць заставацца ў складзе Расеі.
	</p>
	<p>
	<span class="select">3 сакавіка</span> Берасцейскі мір. Зацьверджаны сепаратныя (асобна ад Антанты) мірныя пагадненьні Расеі з Германіей, які Антанта ўспрыняла вельмі варожа.
	</p>
	<p>
	<span class="selectRed">9 сакавіка</span> Рада Усебеларускага з'езда выдала Другую Устаўную грамату да народаў Беларусі, паводле якой абвяшчалася Беларуская Народная Рэспубліка (БНР). 
	</p>
	<p>
	<span class="selectRed">18 сакавіка</span> Рада Усебеларускага з'езда была перайменавана ў Раду БНР. У склад Рады БНР былі кааптаваныя 6 прадстаўнікоў Віленскай беларускай Рады.
	</p>
	<p>
	<span class="selectRed">25 сакавіка</span> ў Менску Рада БНР зацьвердзіла Трэцьцю Устаўную Грамату, якая абвяшчала незалежнасьць Рэспублікі.
	</p>
	

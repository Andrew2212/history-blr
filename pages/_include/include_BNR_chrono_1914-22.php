	<h2>1919</h2>
	<p>
	<span class="selectRed">1 студзеня</span> ў <a href="/pages/places/smolensk.php">Смаленску</a> абвешчана стварэньне Советской Социалистической Республики Белоруссия (ССРБ) у складзе РСФСР.
	</p>
	<p>
	<span class="select">1 студзеня</span> Польскае войска <i>Samoobrona kresowe na Litwie i Bialorusi</i> займае <a href="/pages/places/wilno.php">Вільню</a>. 
	</p>
	<p>
	Польшча, меўшая з часоў Царства Польскага ў Расейскай імпэрыі асабістые войска, казну і ўрадавыя установы, хутка сфармавалася як незалежная дзяржава і імкнулася адрадзіцца ў межах Рэчы Паспалітай.
	</p>
	<p>
	<span class="select">28 студзеня</span> Пачалася Савецка-польская вайна (28.01.1919 - 18.03.1921)
	</p>

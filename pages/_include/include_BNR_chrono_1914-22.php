	<span class="selectRed">31 студзеня</span> ССРБ выйшла са складу РСФСР і была перайменавана ў  Социалистическую Советскую Республику Белоруссия (Смаленская губернія засталася ў  РСФСР). Урад ССРБ пераехаў са Смаленска ў Менск.
	</p>
	<p>
	<span class="selectRed">27 лютага</span> ССРБ з'ядналася з Літоўскай Савецкай Рэспублікай у Літоўска-Беларускую ССР.
	</p>
	<p>
	<span class="selectRed">19 ліпеня</span> У сувязі з наступам польскіх войскаў СовНарКом ЛітБел ССР прыняў пастанову аб перадачы ўсіх спраў Менскаму губернскаму РВК. На гэтым гісторыя ЛітБел ССР скончылася.
	</p>

	<h2>Юг. Украина
		</br><small>В едином государстве 1362-1569 (207 лет). В составе Речи Посполитой 1569-1795 (226 лет). В составе РИ 1795-1917 (122 года)</small>
	</h2>
	
		<div class="col-md-12">
	
	<h4>Общая граница XIV-XX вв.</h4>
		<p> 
		В единое государство были объеденены в. князем Ольгердом после <a href="../events/sinie_wody.php">битвы на Синих Водах</a> 1362 года между 
		войсками ВКЛ и Золотой Орды.
		</p>
		<p>
		Естественной границей между Беларусью и Украиной является <a href="../places/sea_herodotus.php">Полесье</a>. 
		Еще на картах XVI века его изображали Сарматским морем — <a href="../places/sea_herodotus.php">морем Геродота</a>, разделявшем 
		<a href="../terms/shliahta.php">сарматов</a> и скифов.
		</br>[ <small>		
		По словам Геродота <i>"Родителями Таргитая (первого человека), как говорят скифы, были Зевс и дочь реки Борисфена (Днепра) богиня Апи"</i>
		</small> ]
		</p>
		<p>
		 Окончательно линия границы 
		сформировалась в 1569 году, когда по Люблинской унии <a href="../download/zemie_ruskie.php">Земли Руские</a> отошли от ВКЛ к Польше.
		</p>
		

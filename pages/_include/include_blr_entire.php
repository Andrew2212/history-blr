		<h2>Северо-восток. Латвия
		</br><small>В едином государстве (протекторат ВКЛ) 1565-1793 (228 лет). В составе РИ 1793-1917 (124 года)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Общая граница XIV-XX вв.</h4>
		<p>
		Со времен основания Динабурга (Даугавпилса) рыцарями-меченосцами (Fratres militiae Christi de Livonia) в 1275 году осталась практически неизменной по сегодняшний день. 
		Курляндское и Задвинское герцогства — Herzogtum Kurland und Semgallen & Ducatus Ultradunensis — с 1565 по 1795 — протекторат ВКЛ и Речи Посполитой. 
		С 1796 — Курляндская губерния Российской империи.	
		</p>
		
		</div>
				
		<h2>Северо-запад. Летува (Lietuva)
		</br><small>В едином государстве (после Грюнвальда) 1411-1917 (506 лет). От Гедимина до Грюнвальда 1341-1411 (70 лет)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Общая граница XIV-XX вв.</h4>
		<p>
		Историческая граница между Самогитией и <a href="../terms/wkl.php">Великим Княжеством Литовским</a>. Практически совпадает с северной границей расселения белорусского этноса и 
		с границей Lietuvos Respublika 1935 года. Эта граница, отмеченная на всех картах, упоминается еще в 
		<a href="../download/chronicles_peter.php">"Chronicon terrae Prussiae"</a> 1326 года. 
		Современный вид граница получила в 1940 году. 	
		</p>
		
		</div>
		
		<div class="col-md-12">
		
		<h4>Войны XIV-XX вв. 
		</br><small>0 (ноль) лет</small>
		</h4>
		<p>
		Формирование этносов и государственности современных белорусов и литовцев происходило в границах одного государства. Государственных войн на 
		границе современных Беларуси и Летувы не велось. В XIV-XV веках в Жмуди-Самогитии были слабо задокументированные восстания против ВКЛ. 
		Виленская область (губерния) по родному языку до 1980-х годов делилась пополам <i>мова/калба</i> и войн на языковой границе 
		в рассматриваемый период не было. 
		</p>
		<p>
		Самая длительная и бескомпромиссная война длиной более 20 лет ведется (сегодня — в сети) между <i>сьвядомыми змагарами</i> 
		и <i>lietuviski nacionalistai</i> за право 
		называть "своим" культурно-историческое наследие общей страны. Скоро это станет национальным спортом. 
		</p>
		

		<h2>Запад. Польша
		</br><small>В едином государстве 1569-1917 (348 лет). В личной унии 1385-1569 (184 года)</small>
		</h2>
		
		<div class="col-md-12">
		
		<h4>Общая граница XIV-XX вв.</h4>
		<p>
		Граница по линии Подляшье-Брест	сформировались к окончанию войн ВКЛ-Польша за Галицко-Волынское наследство (1340-1385), прекращенных по заключению 
		<a href="../events/unia_krewo.php">Кревской Унии</a> 1385 года. Были закреплены после окончания 
		<a href="../events/war_civil_1381-84_1389-92.php">Гражданской войны в ВКЛ</a> <i>Витовт VS Ягайло</i> (1381-92) Островецким соглашением.
		</p>
		<p>
		Граница, отмеченная на <a href="../download/map_mercator.php">карте Меркатора</a> без серьезных изменений существовала до 1917 года. 
		Окончательный вид приобрела в 1945 году, после передачи Подляшья от БССР к Польше.
		</p>
		
		</div>
		
		<div class="col-md-12">
		
		<h4>Войны XIV-XX вв.
		</br><small>2 месяца</small></h4>
		<p>
		После заключения Кревской унии 1385 года войны были невозможны — государства находились в личной унии правителей. Единственная война 
		случилась в правление в. князя <a href="../figures/svidrigailo.php">Свидригайло</a> — мятежного князя, разорвавшего унию с 
		<a href="../figures/jagiello.php">Ягайло</a>. С 1569 года по <a href="../events/unia_liublin.php">Люблинской унии</a> ВКЛ объеденилось с Польшей 
		в единое федеративное государство <a href="../terms/rzeczpospolita.php">Речь Посполитую</a>.
		</p>
		<p>
		Луцкая война — с 25 июня 1431 по 26 августа 1431 года.
		</p>
		

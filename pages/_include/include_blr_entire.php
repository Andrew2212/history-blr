		<h4>Общие герои</h4>
		<p>
		Со времен <a href="../events/unia_liublin.php">Люблинской унии</a> 1569 года польский язык начинает доминировать в <a href="../terms/shliahta.php">шляхетской среде</a>. 
		Тогда же <a href="../terms/blr_mova.php">белорусский язык</a> получает (помимо кириллицы и арабского) еще и
		латинское написание. В 1795 польский становится государственным языком и, наряду с латынью, языком обучения — языком горожан и аристократии (как русский сегодня). С этого 
		времени все польскоязычные уроженцы Беларуси зачисляются в польский пантеон славы. Как 
		<a href="../terms/filomaty.php">филоматы</a> и <a href="../figures/mickiewicz.php">Мицкевич</a> — 
		<a href="../terms/litwiny.php">литвин</a>, никогда не бывший в Польше.
		</p>
		<p>
		Таким образом, очень многие национальные герои Беларуси почитаются западными соседями — 
		<a href="../figures/gusowsky.php">Гусовский</a>, <a href="../figures/kostiushko.php">Костюшко</a>, <a href="../figures/domeyko.php">Домейко</a>, 

		А пасля наступнага <a href="../events/uprising_1863.php">Паўстаньня 1863</a> касцёл быў перададзены Маскоўскаму патрыярхату РПЦ. У 1880-я  храм рэстаўраваўся пасля вялікага пажару i нязначна перабудаваны.
		</p>

		18 лістапада 1905г. ксендз Іваноўскі, які прыбыў з Пінска, здзейсніў набажэнства і асвячэнне храма. Неўзабаве касцёл 
		быў зноўку зачынены пад выглядам яго "дрэннага стану". У 1909г. мінскі губернскі архітэктар Дабралюбаў зрабіў агляд 
		будынка, але нягледзячы на станоўчую ацэнку тэхнічнага стану, касцёл яшчэ доўгі час заставаўся зачыненым, пакуль у 1914г. не быў выкуплены каталікамі. 
		У 1920-х гадах будынак касцёла быў рестаўраваны па праекце архітэктара Пшыбылоўскага. 
		</p>
		<p>
		У 1944г. перад адступленнем нямецкіх войскаў, касцёл быў узарваны. Пасля вайны некаторы час захоўваліся парэшткі брамы-званiцы, 
		кляштарнай мураванай агароджы і інш. будынкаў, але паступова яны былі ўтылізаваныя на будматэрыялы. 
		У цяперашні час з кляштарнага комплексу захаваўся толькі двухпавярховы будынак, якi першапачаткова выкарыстоўваўся як свіран (амбар), 
		а пасля 1920г перапланаваны пад жылы дом ксяндза. У 1948г. будынак рэканструяваны і прыстасаваны пад корпус абласной псіхіятрычнай бальніцы.
    	</p>

		да выканання ўмоў пагаднення. Улетку 1899г. касцёл і кляштарныя будынкі былі прададзеныя на цэглу. 
		Корпус паспелі ўтылiзаваць, а разабраць касцёл перашкодзіла каталіцкая грамада і парафіяне, якія паўсталі на абарону былой святынi. 
		У касцёле паспелi разабраць толькі бабінец і вежачку. Цэгла, як паведамляюць дакуманты, пайшла на стварэнне камянiц у Пінску. 
		</p>
		<p>

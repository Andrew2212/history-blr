        <h2>Гісторыя і рэканструкцыя</h2>
			
        <p>
        Берасце. Касцёл Беззаганнага Зачацця Найсвяцейшай Панны Марыі кляштару бэрнардынак. Графiчная рэканструкцыя на перыяд да 1830г. 
		</p>
		<p>
		Мужчынскі і жаночы бернардинскiя кляштары Берасця складалі адзіны ансамбль, які размяшчаўся ў паўднёвым прадмесці горада — Замухавеччы (Валынскае прадмесце). 
		Берасцейскi кляштарны комплекс — адзiны на тэрыторыі Беларусі выпадак арганізацыі прасторы вакол плошчы ў адзіны ансамбль. Касцёлы абодвух кляштараў знаходзіліся адносна побач, пад невялікім вуглом павернутыя адзін да аднаго. 
		</p>
		<p>
		Жаночы кляштар быў заснаваны з фундыцыi Алены Копцавай (з Дольскiх), насупраць ужо iснуючага на той час мужчынскага. 
		Дата кансекрацыi кляштарнага касцёла — 1750, у гонар Бязгрэшнага Зачацьця Панны Марыi.
		</p>
		<p>
		Па сваёй аб'ёмна-прасторавай кампазiцыi касцёл больш блiзкi да "сармацкага", а не "вiленскага" барока: мае строгi прамавугольны абрыс прэсбiтэрыя, на галоўным фасадзе дзве невысокiя чаверыковыя вежы з двума ярусамi аднолькавага сячэння i вышынi, якiя накрыты нiзкiмi распластанымi купаламi-банькамi.
		Аднак па насычанасцi i характары ордэрнай пластыкi гэты фасад ужо цалкам познебарочны i нагадвае фасад крыху больш ранняга касцёла езуiтаў у Юравiчах (гэтага ж Палескага рэгiёну). 
		Бакавыя навы мураванымi перагародкамi падзялялiся на асобныя неглыбокiя каплiцы з разьбянымi алтарамi (характэрная рыса Палесься). 
		</p>
		<p>
		У 1781г. да заходняй сцяны касцёла быў прыбудаваны двухпавярховы мураваны кляштарны корпус, утвараючы у плане квадратны замкнуты двор-клуатр, у якiм знаходзiўся сад.
		У паўночна-захадняй частцы кляштара, афармляючы фронт плошчы, за агароджай быў зладжаны другі невялікі манастырскі двор, а ў паўднёва-ўсходняй частцы - рэгулярны сад. 
		</p>
		<p>
		Пасля падаўлення <a href="../events/uprising_1830.php">паўстання 1830</a> кляштар быў скасаваны, а пабудовы прыстасаваны пад патрэбы ваеннага ведамства (кадэцкi корпус, якi тут месцiўcя 3 1842 па 1863гг., пазней тут быў змешчаны ваенны шпiталь).
		У  1831 афіцэрамі генеральнага штаба расейскай армii былi праведзены абмеры кляштарных пабудоў, дзякуючы чаму мы дакладна ведаем дакладны выгляд святынi. 
		</p>
		<p>
		Пасля перабудовы барочны кляштарны комплекс быў ператвораны ў будынак казарменнага тыпу з прэтэнзіяй на стыль імперскага класіцызму. 
		</p>
		<p>
		Болей за ўсё кляштар пацярпеў у 1915 пры адступленьні царскіх войскаў з Брэст-Літоўска падчас 
		<a href="../events/world_war_1.php">Першай Сусветнай вайны</a>. Будынак быў цалкам выпалены знутры, абсыпалася нават вапнава-пяшчаная тынкоўка. 
		</p>
		<p>
		Гэта пацягнула за сабой далейшае руйнаванне былога касцёла, і за польскім часам 1920-39 парэшткi сценаў касцёла былі разабраны, а рэшткі комплексу былі прыстасаваныя пад сталовую. У гэтай якасці яны выкарыстоўваўся і пасля другой сусветнай вайны.
 		</p>
		

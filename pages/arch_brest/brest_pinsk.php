		каменныя вежы замест драўляных, фасады ўпрыгожаныя гіпсавымі скульптурамі, а з абодвух бакоў фасаду ўзведзены арыгінальныя 
		крылы ("скрыдлы", падпорныя сценкi), якія з'яўлялiся таксама званіцамі. 
		</p>

		падчас пажару 1799 г. Хутчэй за ўсё, рэканструкцыя былога касцёла была завершаная ў той перыяд, калі будынкі перайшлі ў праваслаўнае ведамства. 
		</p>
		<p>
		У 1919 годзе комплекс зноў вярнуўся да езуітаў. На пачатку 20-х гг. кіраўніцтва ордэна вырашыла правесці рамонтныя работы будынка калегіума: 
		былі пераробленыя франтоны на паўночным, усходнім і заходнім фасадах, адрамантаваныя дымаходы, ліквідаваная прыбудова з боку заходняга фасаду, 
		праведзены атынковачныя работы. Будынак канчаткова набыў тое аблічча, якое мы бачым сёння. 

		<p>
		Пасля гэтых страшных падзей пачалося павольнае аднаўленнне езуіцкай калегii. Будаўнічая дзейнасць езуітаў прывяла да таго, што цэнтр горада 
		перамясціўся на ўсход, і своеасаблівым фокусам радыяльна сфармаваных вулачак стаў не старасцiнскi (былы княскi) замак, як было раней, а 
		комплекс езуiцкай калегii, які актыўна ўвайшоў у гарадскую планіроўку і змяніў яе выгляд. З гэтага часу ён як бы "трымае" горад: маштабы касцёла 
		і калегіума, іх строгая і велічная архітэктура робяць будынак галоўным не толькі на пляцы, але і ва ўсім горадзе. 

		</p>
		<p>
		Але ўсё скончылася раптоўна у 1939г. Пад час <a href="../events/world_war_2.php">падзелу II Рэчы Паспалiтай</a> у вынiку змовы краiн-суседак з усходу i захаду, 
		без супрацiву зайшоўшыя ў горад часткi Чырвонай Армii з гармат прамой наводкай растралялi будынак касцёлу. 
		У вынiку пашкоджанняў абрынуўся дах, адна вежа, сцены касцёла спалены, некаторыя засталiся са скразнымi адтулінамi ад разрываў снарадаў. 
		</p>

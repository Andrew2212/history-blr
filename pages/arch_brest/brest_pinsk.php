		распачатыя езуітамі, на момант закрыцця яшчэ не былі завершаныя. Да 1787 г. будынак пуставаў, а потым яго занялі уніяты, 
		якія пераабсталявалі касцёл пад кафедральны сабор Пiнска-Тураўскай унiяцкай епархii. 
		</p>
		
